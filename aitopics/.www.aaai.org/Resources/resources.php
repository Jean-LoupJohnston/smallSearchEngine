<hr />
</div>
<h3>AAAI Presidential Addresses</h3>
<p>Every other year, the current AAAI President delivers an important speech about AI to the
attendees of the National Conference on Artificial Intelligence. Many of the presidential addresses delivered 
by past presidents were published in <i>AI Magazine</i> and are available at the following location:</p><ul> 
<li><a href="http://www.aaai.org/Library/President/president.php">AAAI Presidential Addresses</a>
</li></ul>
<div class="hr1">
<hr />
</div>
<h3>AAAI Policy Papers</h3> 
<p>AAAI members and officers have created white papers dealing with 
artificial intelligence and governmental policy.</p>
<ul> 
<li><a href="http://www.aaai.org/Library/Reports/reports-library.php">Science Policy</a></li> 
</ul> 
