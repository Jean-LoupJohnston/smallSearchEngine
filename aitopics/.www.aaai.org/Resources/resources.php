<h2 class="backgrnd12"><span class="text1">Welcome to the AI Resources Pages!</span></h2>
<div class="content">
<p>As a service to its members and the international AI community, 
AAAI is pleased to provide links to pages that provide additional information about the science of artificial intelligence.
Some of these resources are provided by AAAI; however many others are pages supported by other AI organizations
<i>(listing does not necessarily indicate affiliation or endorsement 
by AAAI)</i></p>
<div class="hr1">
<hr />
</div>
 
<h3>General Information about AI</h3>
<p>To learn more about the science of artificial intelligence, visit AAAI's <a href="http://aitopics.org">AI Topics page.</a> This page 
is the best starting place for anyone wanting to learn more about the field&mdash;especialy students.</p>
<ul> 
<li><a href="http://aitopics.org">AI Topics Pages</a></li> 
</ul> 
<div class="hr1">
<hr />
</div>
<h3>Events</h3> 
<p>The <a href="../Magazine/calendar.php">Calendar of Events</a>, published quarterly in <i>AI Magazine</i> lists 
upcoming AI conferences, workshops, symposia, and other educational events from around the world.</p>
<ul> 
<li><a href="../Magazine/calendar.php">AAAI Calendar 
of AI-Related Events</a></li> 
</ul> 
<div class="hr1">
<hr />
</div>
<h3>AI-Related Organizations</h3> 
<p>The <a href="../index.html" rel="nofollow">AI International</a> site contains information about AI societies, universities with AI programs, and laboratories conducting AI research.</p>
<ul> 
<li><a href="http://www.aiinternational.org/universities.html" rel="nofollow">Universities with AI Programs 
and Groups</a></li> 
 
<li><a href="http://www.aiinternational.org/societies.html" rel="nofollow">AI Societies and 
Organizations</a></li> 
<li><a href="http://www.aiinternational.org/labs.html" rel="nofollow">Labs (mostly without University affiliation)</a></li>
<li><a href="../index.html" rel="nofollow">AI International Web site</a></li>
</ul> 
