<h2 class="backgrnd12"><span class="text1">AAAI Website Privacy Policy</span></h2>
<div class="content"> 

<p>Your privacy is important to AAAI, and we are committed to respecting it.  This statement describes the personal information practices for AAAI's website, including what type of information is gathered and tracked, how the information is used, and with whom the information is shared.</p>

<h3 class="left">Personal Information</h3>

<p>In general, you can visit the public areas of the AAAI web site without telling us who you are and without revealing any personal information about yourself. (If you use password protected areas of the AAAI web site, you are required to give us your login name and password.)</p>

<p>In using specific features of the site, such as the submitting articles for publication, registering for a conference, joining AAAI, or purchasing a book or technical report, you will be asked to give us personal information, such as your name or e-mail address, that will be needed to (for example) correspond with you, process an order, review a paper, or provide you with a membership subscription. Aside from aggregating data, with all personal information removed, to allow us to check on our own performance, it is our intent that your personal information will be used only to fulfill the request(s) you make of us, and to allow us to contact you if there are any problems with your request.  We will not sell or rent your personal information to third parties unless you specifically agree that it is permissible for us to do so.</p>

<h3 class="left">Information Security and Quality</h3>

<p>We intend to protect the integrity of your personal information. To do this, we have implemented appropriate technical and organizational measures, such as using Secure Sockets Layer (SSL) encryption for transmission of credit card numbers and passwords, to help us keep your information secure, accurate, current, and complete.  We will make a sincere effort to respond in a timely manner to your requests to correct inaccuracies in your personal information, to remove personal information you do not want stored, or to answer any questions you may have about our use of your information.</p>

<h3 class="left">Information Gathered through Cookies and Other Technologies</h3>

<p>In general, you can use AAAI web site in all ways without using the technology known as "cookies."  A cookie is a short identifier, stored on your computer, that a site may use to identify you when you make return visits to it.  The features of AAAI site were designed to work without cookies, so if you turn them off, you may still browse, search, and shop at our site.  Performance may be slower, however, and certain features (such as the "remember my password) will not function.</p>

<p>We may collect anonymous information from visits to the AAAI site to help us provide better customer service and a better customer experience. For example, we keep track of the domains from which people visit, and we measure aggregate user activity on the AAAI web site, but we do so in ways that keep user's personal identity and information anonymous. AAAI or the analytic vendors it selects may use this data to analyze site usage trends and statistics, to help us improve our site.</p>

<p>When we collect personal data from you in connection with a transaction (an order, e-mail request, etc.) we may extract some information about that transaction in an anonymous format and combine it with other anonymous information such as clickstream data. This anonymous information is used and analyzed only at an aggregate level to help us understand trends and patterns in our site's usage.</p>

<h3 class="left">Business Relationships</h3>

<p>AAAI web site may contain links to other Web sites. AAAI is not responsible for the privacy practices or the content of these web sites.</p>

<h3 class="left">Questions Regarding this Statement</h3>

<p>Any questions regarding this statement or our privacy policies should be directed to the AAAI site manager. (Choose webmaster at the contact form.)

By visiting and using AAAI's web site, you are accepting the practices described in this Privacy Policy.  This privacy statement was last updated on 1 June 2003.</p>


