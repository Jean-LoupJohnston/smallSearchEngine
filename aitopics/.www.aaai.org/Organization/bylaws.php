<h3><a name="RTFToC4">Article I. Name
</a></h3>
<p>
The name of this corporation shall be the Association for the Advancement of Artificial Intelligence (AAAI).
</p>
<h3><a name="RTFToC5">Article
II. Purpose
</a></h3>
<p>
This corporation is a nonprofit public benefit corporation and is not organized
for the private gain of any person. It is organized under the California
Nonprofit Corporation Law for scientific and educational purposes in the field
of artificial intelligence to promote research in, and responsible use of, artificial intelligence.
</p>
<h3><a name="RTFToC6">Article
III. Limitations on Corporate Activities
</a></h3>
<h4>
<a name="RTFToC7">Section 1.
</a></h4>
<p>
(a) This corporation is organized and operated exclusively for charitable
purposes within the meaning of Section 501 (c)(3) of the Internal Revenue Code.
Notwithstanding any other provision of these articles, the corporation shall
not carry on any other activities not permitted to be carried on:</p>
<dl>
<dd>(i) by a corporation exempt from Federal Income Tax under Section 501 (c)(3)
of the Internal Revenue Code or</dd><dd>
(ii) by a corporation, contributions to which are deductible under Section 170
(c)(2) of the Internal Revenue Code.</dd><dd>
(iii) No substantial part of the activities of this corporation shall consist
of carrying on propaganda or otherwise attempting to influence legislation, nor
shall this corporation participate in, or intervene in (including the
publishing or distribution of statements), any political campaign on behalf of
any candidate for public office.</dd></dl><p></p>
<h3><a name="RTFToC8">Article
IV. Dedication and Dissolution
</a></h3>
<p>The property of this corporation is irrevocably dedicated to educational and
scientific purposes, and no part of the net income or assets of this
organization shall ever inure to the benefit of any councilor, officer, or
member thereof or to the benefit of any private persons.</p><p>
On the dissolution or winding up of the corporation, its assets remaining after
payment of, or provision for payment of, all debts and liabilities of this
corporation shall be distributed to a nonprofit fund, foundation, or
corporation which is organized and operated exclusively for educational and
scientific purposes and which has established its tax-exempt status under
Internal Revenue Code Section 501 (c) (3).</p>
<h3><a name="RTFToC9">Article
V. Offices
</a></h3>
<h4>
<a name="RTFToC10">Section 1. Principal Office.
</a></h4>
<p>The principal office of the corporation in the State of California shall be
located in the City of Menlo Park, County of San Mateo. The Executive Council
may change the principal office in California from one location to another.</p>
<h4>
<a name="RTFToC11">Section 2. Other Offices.
</a></h4>
<p>The corporation may have such other offices, either within or without the State
of California, as the Executive Council may determine or as the affairs of the
corporation may require from time to time.
</p>
<h3><a name="RTFToC12">Article
VI. Members
</a></h3>
<h4>
<a name="RTFToC13">Section 1. Classes and Privileges.
</a></h4>
<p>The corporation shall have two classes of members: Regular and Student. Student
members have all the rights and privileges of Regular members except that
Student members shall not have voting rights.
</p>
<h4>
<a name="RTFToC14">Section 2. Eligibility.
</a></h4>
<p>The Executive Council shall determine the qualifications for membership in the
corporation.
</p>
<h4>
<a name="RTFToC15">Section 3. Membership.
</a></h4>
<p>A person becomes a member (Regular or Student) upon acceptance of an
application for membership by the corporation and payment of dues.
</p>
<h4>
<a name="RTFToC16">Section 4. Voting Rights.
</a></h4>
<p>Each Regular member in good standing as of the record date shall be entitled to
one vote on any vote of the membership.
</p>
<h4>
<a name="RTFToC17">Section 5. Dues.
</a></h4>
<p>The Executive Council shall establish membership dues and the method and time
of payment thereof. Failure to maintain current dues payments shall result in
automatic termination of membership.
</p>
<h4>
<a name="RTFToC18">Section 6. Termination of Membership.
</a></h4>
<p>The Executive Council, by affirmative vote of two-thirds of all the members of
the Council, may suspend or expel a member for cause after an appropriate
hearing, such hearing to be held only after prior written notice of the
termination to the member to be suspended or expelled, stating the reasons
therefore and after providing a timely opportunity to be heard on the matter,
as required by law.
</p>
<h4>
<a name="RTFToC19">Section 7. Resignation.
</a></h4>
<p>Any member may resign by filing a written resignation with the
Secretary-Treasurer. Resignation shall not relieve any resigning member from
any obligation owed to the corporation and shall not diminish any right of the
corporation to enforce such obligation.
</p>
<h4>
<a name="RTFToC20">Section 8. Reinstatement.
</a></h4>
<p>Upon written request filed with the Secretary-Treasurer by a former member
terminated for cause, the Executive Council, by majority vote, may reinstate
such member.
</p>
<h4>
<a name="RTFToC21">Section 9. Transfer of Membership.
</a></h4>
<p>Membership, or any right arising from it, in this corporation is not
transferable or assignable, either voluntarily or by operation of law.
</p>
<h4>
<a name="RTFToC22">Section 10. Liability.
</a></h4>
<p>No member of this corporation shall be personally liable for any indebtedness,
obligation, or liability of the corporation, and all creditors of the
corporation shall look only to the assets of the corporation for payment.
</p>
<h3><a name="RTFToC23">Article
VII. Meeting of Members
</a></h3>
<h4>
<a name="RTFToC24">Section 1. Annual Meeting.
</a></h4>
<p>The annual meeting of the corporation will take place during its Annual
Conference. In years when there is no Annual Conference, the Annual Meeting
shall be held at a time and place selected by the Executive Council. Notice
stating the place, day, and hour of the meeting shall be delivered either
personally, or by mail, telegram, or computer network, or by announcement in an
official publication of the corporation to each member entitled to vote at such
meeting, not less than ten (10) or more than ninety (90) days before the date
of such meeting. If notice is given by mail, and the notice is not mailed first
class, registered, or certified mail, such notice shall be given not less than
twenty (20) days before such meeting. Such notice shall set forth nominations
for President-Elect and Councilors and those matters which the Council intends
at the time of the notice to present for action by the members.
</p>
<h4>
<a name="RTFToC25">Section 2. Special Meetings.
</a></h4>
<p>Special meetings of the members for any lawful purpose may be called by five
percent (5%) or no more than 500 members, by a majority of the Executive
Council, or by the President.
</p>
<h4>
<a name="RTFToC26">Section 3. Notice of Special Meetings.
</a></h4>
<p>Notice stating the place, day, and hour of any special meeting of members shall
be delivered either personally, or by mail, telegram, or computer network, or
by announcement in an official publication of the corporation to each member
entitled to vote at such meeting, not less than ten (10) or more than ninety
(90) days before the date of such meeting. If notice is given by mail, and the
notice is not mailed first class, registered, or certified mail, such notice
shall be given not less than twenty (20) days before such meeting. The notice
shall state the general nature of the business to be transacted, and no other
business may be transacted.
</p>
<h4>
<a name="RTFToC27">Section 4. Place of Meetings.
</a></h4>
<p>The Executive Council may designate any place, either within or without the
State of California, as the place of meeting for any annual or special meeting
called by the Executive Council or the President. The time and place designated
for any special meeting called by the members must be set with the concurrence
of the Executive Council.
</p>
<h4>
<a name="RTFToC28">Section 5. Quorum.
</a></h4>
<p>Five percent (5%) or more than 500 members of the Regular members shall
constitute a quorum for the transaction of business at a meeting of the
members, including all adjourned or continued meetings, provided, however, that
if any annual or special meeting is actually attended in person by less than
one-third (1/3) of the voting power, the only matters that may be voted on are
those of which notice of their general nature was given pursuant to the
requirements of these Bylaws. The members present at a duly called or held
meeting at which a quorum is present may continue to transact business until
adjournment, even if enough members have withdrawn to leave less than a quorum,
if any action taken (other than adjournment) is approved by at least a majority
of the members required to constitute a quorum.
</p>
<h4>
<a name="RTFToC29">Section 6. Voting.
</a></h4>
<p>(a) <i>Methods of Voting, Procedures.</i> Voting may take place at a meeting of
members or by written ballot circulated prior to that meeting. When a quorum is
present, the affirmative vote of a majority of the voting power represented at
the meeting, entitled to vote, and voting on any matter shall be the act of the
members. Approval by written ballot shall be valid only when (1) the number of
votes cast by ballot (including those ballots that are marked "withhold" or
otherwise indicate that authority to vote is withheld) within the time
specified equals or exceeds the quorum required to be present at a meeting
authorizing the action and (2) the number of approvals equals or exceeds the
number of votes that would be required for approval at a meeting at which the
total number of votes cast was the same as the number of votes cast by written
ballot without a meeting. All voting at meetings shall be by show of hand.</p>
<p>(b) <i>Solicitation of Written Ballots.</i> Prior to the annual meeting and
prior to any other meeting at the option of the Executive Council, the
corporation shall distribute one written ballot to each member entitled to
vote; such ballots shall be mailed or delivered in the manner required for
giving notice of the annual meetings. All solicitations of votes by ballot
shall: (1) indicate the number of responses needed to meet the quorum
requirements, (2) state the percentage of approvals necessary to pass the
measure(s), and (3) specify the time by which the ballot must be received in
order to be counted. Each ballot so distributed shall (1) set forth the
proposed action and (2) provide the members an opportunity to specify approval
or disapproval of each proposal, if more than one proposal is set forth.
</p>
<h4>
<a name="RTFToC30">Section 7. Proxies.
</a></h4>
<p>No person, whether member or not, may vote the proxy of any Regular member.
</p>
<h4>
<a name="RTFToC31">Section 8. Waiver of Notice.
</a></h4>
<p>Attendance by a person at a meeting shall constitute waiver of notice of that
meeting, except when the person objects at the beginning of the meeting to the
transaction of any business due to the inadequacy or illegality of the notice.
Also, attendance at a meeting is not a waiver of any right to object to the
consideration of matters not included in the notice of the meeting, if that
objection is expressly made at the meeting.
</p>
<h4>
<a name="RTFToC32">Section 9. Record Date.
</a></h4>
<p>(a) <i>To be determined by Executive Council.</i> For the purposes of
determining which members are entitled to receive notice of any meeting, to
vote, or to take any other action, the Executive Council may fix, in advance, a
"record date," which shall not be more than ninety (90) or fewer than ten (10)
days before the date of any such meeting, or in the event a written ballot is
to be solicited, not more than sixty (60) days before the day on which the
first written ballot is mailed or solicited. Only members of record on the date
so fixed are entitled to notice, to vote, or take other action, as the case may
be, notwithstanding any transfer of any membership on the books of the
corporation after the record date, except as otherwise provided in the articles
of incorporation, by agreement, or in the California Nonprofit Corporation
Law.</p>
<p>(b) <i>Failure of Council to determine date.</i></p>
<dl><dd>(i) Record date for notices or voting. Unless fixed by the Executive
Council, the record date for determining those members entitled to receive
notice of, or to vote at, a meeting of members, shall be the next business day
preceding the day on which notice is given. In the event a written ballot is to
be solicited, members on the day the first written ballot is mailed or
solicited who are otherwise eligible to vote are entitled to cast written
ballots.</dd>
<dd>(ii) "Record date" means as of close of business. For purposes of this
paragraph (b), a person holding membership as of the close of business on the
record date shall be deemed the member of record.</dd></dl>
<h3><a name="RTFToC33">Article
VIII. Executive Council
</a></h3>
<h4>
<a name="RTFToC34">Section 1. Powers of Councilors.
</a></h4>
<p>(a) <i>General Powers.</i> The business and affairs of the corporation shall be
managed and all corporate powers exercised by or under the direction of its
Executive Council, subject to any limitations in the Articles of Incorporation
or these Bylaws and according to provisions of the California Nonprofit
Corporation Law. The Executive Council may delegate the management of the
activities of the Corporation to any person or persons or committee however
composed, provided that the activities and affairs of the corporation shall be
managed and all corporate powers shall be exercised under the ultimate
direction of the Executive Council. All references in such law to "directors"
and "Board of Directors" shall apply herein to "councilors" and "Executive
Council."</p>
<p>(b) <i>Specific Powers. </i>Without prejudice to these general powers and
subject to the same limitations, the councilors shall have the power to:</p>
<dl><dd>(i) set all dues and qualifications of members;</dd><dd>
(ii) appoint or remove such committees and establish such offices as the
councilors deem necessary; and</dd><dd>
(iii) supervise the preparation and maintenance of procedures and policies for
the corporation and its activities.</dd></dl>
<p></p>
<h4>
<a name="RTFToC35">Section 2. Qualifications.
</a></h4>
<p>Councilors of the Executive Council must be members of the corporation. Not
more than 49% of the persons serving on the Executive Council may at any time
be interested persons. An interested person is (1) any person being compensated
by the corporation for services rendered to it within the previous 12 months,
whether as a full-time or part-time employee, independent contractor, or
otherwise, and (2) any brother, sister, ancestor, descendant, spouse,
brother-in-law, sister-in-law, son-in-law, daughter-in-law, mother-in-law, or
father-in-law of any such person. However, any violation of the provisions of
this paragraph shall not affect the validity or enforceability of any
transaction entered into by the corporation.
</p>
<h4>
<a name="RTFToC36">Section 3. Number and Tenure.
</a></h4>
<p>The Executive Council shall consist of the officers, as identified in Article
X; twelve (12) councilors elected by the membership; and six (6) chairs of the
Nominating, Conference, Symposium, Publications, Fellows, and Finance committees,
which chairs shall serve ex officio and without vote, except that any chair
otherwise entitled to vote as an officer or elected director may do so. Four
councilors shall be elected each year to serve three-year terms.
</p>
<h4>
<a name="RTFToC37">Section 4. Vacancies.
</a></h4>
<p>(a) <i>Events causing vacancy. </i>A vacancy or vacancies in the Executive
Council shall be deemed to exist on the occurrence of the following:</p><dl>
<dd>(i) the death or resignation of any councilor, including a resignation caused
by the election of a councilor as an officer;</dd><dd>
(ii) the declaration by resolution of the Executive Council of a vacancy in
the office of a councilor who has been declared of unsound mind by an order of
court or convicted of a felony or has been found by final order or judgment of
any court to have breached a duty under section 5230 and following of the
California Nonprofit Corporation Law;</dd><dd>
(iii) the increase of the authorized number of councilors;</dd><dd>
(iv) the failure of the councilors, at any meeting of the Executive Council at
which any councilor or councilors are to be elected, to elect the number of
councilors to be elected at such meeting; or</dd><dd>
(v) the removal of a councilor or councilors by the members according to
law.</dd></dl>
<p>(b) <i>Resignations.</i> Except as provided in this paragraph, any councilor
may resign, which resignation shall be effective on giving written notice to
the President, the Secretary-Treasurer, or the Executive Council, unless the
notice specifies a later time for the resignation to become effective. If the
resignation of a councilor is effective at a future time, the Executive Council
may elect a successor to take office as of the date when resignation becomes
effective. No councilor may resign when the corporation would then be left
without a duly elected councilor or councilors in charge of its affairs.</p>
<p>(c) <i>Deemed Resignation.</i> Any councilor who is absent from two consecutive
meetings of the Executive Council may be deemed to have resigned, and the
Executive Council may appoint a successor to fill the vacancy after first
giving notice to the councilor who is deemed to have resigned that such
appointment will be made.</p>
<p>(d)<i> No vacancy on reduction of number of councilors.</i> No reduction of the
authorized number of councilors shall have the effect of removing any councilor
before that councilor's term of office expires.</p>
<p>(e) <i>Filling vacancies. </i>A vacancy occurring among the councilors may be
filled by the affirmative vote of a majority of the remaining councilors. Any
person so selected shall serve the balance of the term of the councilor he or
she replaced or until the next election.
</p>
<h4>
<a name="RTFToC38">Section 5. Compensation.
</a></h4>
<p>Members of the Executive Council shall not receive any salaries for their
services as councilors, but nothing herein contained shall be construed to
preclude any councilor from serving the corporation in any other capacity and
receiving compensation therefor. Members of the Executive Council may be
reimbursed for expenses incurred in connection with their services as
councilors.
</p>
<h3><a name="RTFToC39">Article
IX. Meetings of the Executive Council
</a></h3>
<h4>
<a name="RTFToC40">Section 1. Regular Meetings.
</a></h4>
<p>The regular annual meeting of the Executive Council shall be held during the
corporation's Annual Conference. In years when there is no Annual Conference,
the annual meeting shall be held on the same date as the date selected by the
Executive Council for the annual meeting of the members. The Executive Council
may fix by resolution the time and place, either within or without the State of
California, and the mode of the meeting, including telephone or computer
network connection, for the holding of additional regular meetings of the
Council, without notice other than that resolution.
</p>
<h4>
<a name="RTFToC41">Section 2. Special Meetings.
</a></h4>
<p>(a) <i>How Called: Time, Place, and Mode.</i> Special meetings of the Executive
Council may be called by the President or by one-third of its councilors. The
President or the councilors calling a special meeting may fix the time meeting,
including telephone or by computer network connection, for the holding of any
special meeting of the Council.</p>
<p>(b) <i>Notice. </i>Notice of any special meeting of the Council shall be given
by personal notice, by mail, telephone, telegram, or by computer network,
except that if two-thirds of the Executive Council has been notified before the
specified time period has elasped, the meeting may be convened at any agreed
upon time. Any councilor may waive notice of any meeting. The business to be
transacted at the meeting need not be specified in the notice or waiver
thereof, unless specifically required by statute or by these Bylaws.</p>
<p>(c) <i>Time of Notice. </i>Notice of any special meeting given by mail shall be
by first class mail at least ten (10) days before the time set for the meeting.
Notice given by any other means specified in (b) shall be given at least
forty-eight (48) hours before the time set for the meeting.
</p>
<h4>
<a name="RTFToC42">Section 3. Waiver of Notice.
</a></h4>
<p>Notice of a meeting need not be given to any councilor who signed a waiver of
notice or a written consent to holding the meeting or an approval of the
minutes thereof, whether before or after the meeting, or who attends the
meeting without protesting, prior thereto or at its commencement, the lack of
notice of such councilor. All such waivers, consents, and approvals shall be
filed with the records of the corporation or made part of the minutes of the
meetings.
</p>
<h4>
<a name="RTFToC43">Section 4. Quorum.
</a></h4>
<p>A majority of the number of councilors authorized for the Executive Council
shall constitute a quorum for the transaction of business at any meeting of the
Council. Every act or decision done or made by a majority of the councilors
present at a meeting duly held at which a quorum is present shall be regarded
as the act of the Executive Council, subject to the provisions of the
California Nonprofit Corporation Law and unless a greater number is required by
these Bylaws. A meeting at which a quorum is initially present may continue to
transact business, notwithstanding the withdrawal of councilors, if any action
taken is approved by at least a majority of the required quorum for that
meeting or such greater number as is required by law, the Articles, or these
Bylaws.
</p>
<h4>
<a name="RTFToC44">Section 5. Action without Meeting.
</a></h4>
<p>Any action required or permitted to be taken by the Executive Council may be
taken without a meeting, if all members of the council, individually or
collectively, consent in writing or via the computer network to this action.
Such action by written consent shall have the same force and effect as a vote
of the Executive Council. Such written consent or consents shall be filed with
the minutes of the proceedings of the Council.
</p>
<h4>
<a name="RTFToC45">Section 6. Meetings by Telephone.
</a></h4>
<p>Members of the Executive Council may participate in a meeting through use of
conference telephone or similar communications equipment, including computer
network connection, so long as all members participating in such meetings can
hear one another or otherwise participate in the discussion. Participation in a
meeting pursuant to this subdivision constitutes presence in person at such
meeting.
</p>
<h4>
<a name="RTFToC46">Section 7. Adjournment.
</a></h4>
<p>A majority of the councilors present, whether or not constituting a quorum, may
adjourn any meeting to another time and place.
</p>
<h4>
<a name="RTFToC47">Section 8. Notice of Adjournment.
</a></h4>
<p>Notice of the time and place of holding an adjourned meeting need not be given,
unless the meeting is adjourned for more than twenty-four (24) hours, in which
case personal notice of the time and place shall be given before the time of
the adjourned meeting to the councilors who were not present at the time of the
adjournment.
</p>
<h3><a name="RTFToC48">Article
X. Officers
</a></h3>
<h4>
<a name="RTFToC49">Section 1. Officer Designations and Qualifications.
</a></h4>
<p>The officers of the corporation shall be the President, the President-Elect,
the Past-President, and the Secretary-Treasurer, and such other officers as may
be selected in accordance with the provisions of this Article and Article VIII.
Officers other than described in this Article shall have the authority and
perform the duties prescribed by the Executive Council. All officers must be
members of the corporation. No persons shall hold concurrently more than one of
the offices described in this Article, but one or more of these persons may
hold such other offices as may be established by the Executive Council.
Officers are otherwise deemed qualified for office unless specifically removed
for cause.
</p>
<h4>
<a name="RTFToC50">Section 2. Election and Term.
</a></h4>
<p>The President-Elect shall be elected by the membership for a two-year term. The
President-Elect shall automatically become President upon the expiration of his
or her term of office and serve in that capacity for two years. The President
shall automatically become the Past-President upon the expiration of his or her
term of office and shall serve in that capacity for two years. The
Secretary-Treasurer shall be selected by the President, subject to the approval
of the Executive Council, and shall serve at its pleasure. Each officer shall
hold office until his or her successor has been duly elected or selected.
</p>
<h4>
<a name="RTFToC51">Section 3. Removal.
</a></h4>
<p>Any officer may be removed for cause by a two-thirds vote of the Executive
Council whenever, in its judgment, the best interests of the corporation would
be served thereby. The Strategic Planning Board should be consulted prior to
any action being taken.
</p>
<h4>
<a name="RTFToC52">Section 4. Vacancies.
</a></h4>
<p>A vacancy in any office because of death, resignation, removal,
disqualification, or otherwise (regardless of how the officer was initially
selected) may be filled by the Executive Council by an affirmative two-thirds
vote.
</p>
<h4>
<a name="RTFToC53">Section 5. President.
</a></h4>
<p>The President shall be the principal executive officer of the corporation and
shall, in general, supervise and control the business and affairs of the
corporation. He or she shall preside at all meetings of the members and of the
Executive Council. He or she may sign, with the Secretary or any other proper
officer of the corporation authorized by the Executive Council, any contracts
or other instruments that the Executive Council has authorized to be executed,
except in cases where the signing and execution thereof shall be expressly
delegated by the Executive Council, or by these Bylaws or by statute, to some
other officer of or agent of the corporation; and, in general, he or she shall
perform all duties incident to this office of President and such other duties
as may be prescribed by the Executive Council from time to time. The President
can appoint and dismiss the Secretary-Treasurer and the Chairpersons of the
Standing Committees and may supervise and maintain the personnel policies
subject to the approval of the Executive Council. From time to time, the
President may assign duties to the President-Elect, Past-President, and the
Secretary-Treasurer.
</p>
<h4>
<a name="RTFToC54">Section 6. President-Elect.
</a></h4>
<p>The President-Elect shall perform such duties as the President may assign. In
the event either the President or the Past-President is unable to act, the
President-Elect shall perform the duties of such office and, when so acting,
shall have all the powers of, and be subject to, all the restrictions of such
office until the Executive Council fills the vacant office pursuant to Section 4.
</p>
<h4>
<a name="RTFToC55">Section 7. Past-President.
</a></h4>
<p>The Past-President shall preside over the Nominating Committee, the Fellows
Committee, and the Strategic Planning Board and shall perform such other duties
as the President may assign.
</p>
<h4>
<a name="RTFToC56">Section 8. Secretary-Treasurer.
</a></h4>
<p>The Secretary-Treasurer shall keep or cause to be kept the minutes of the
meetings of the members, meetings of the Executive Committee, and meetings of
committees (Article XII); see that all notices are duly given in accordance
with the provisions of these Bylaws or as required by law; be custodian of all
the corporate records and of the seal of the corporation and see that the seal
of the corporation is affixed to all documents, the execution of which, on
behalf of the corporation under its seal, is duly authorized in accordance with
the provisions of these Bylaws; ensure that a current register of the mailing
address of each member is maintained; have charge and custody of, and be
responsible for, all funds and securities of the corporation, receive and give
receipts for monies due and payable to the corporation from any source
whatsoever, and deposit all such monies in the name of the corporation in such
banks, trust companies, or other depositories as shall be selected by the
Executive Council; cause an audit of the corporation's books and records to be
made each year by an independent accountant, the selection of which shall be
approved by the Executive Council, such audit to be completed within four (4)
months from the close of the corporation's fiscal year; and, in general,
perform all the duties incident to the office of Secretary-Treasurer and such
other duties as, from time to time, may be assigned to him or her by the
President.
</p>
<h4>
<a name="RTFToC57">Section 9. Executive Committee.
</a></h4>
<p>The Executive Committee will be composed of the President, President-Elect,
Past-President, and the Secretary-Treasurer which will serve with vote. This
committee will act as a management council to the President and the Executive
Council on day-to-day operations.
</p>
<h4>
<a name="RTFToC58">Section 10. Strategic Planning Board.
</a></h4>
<p>The Strategic Planning Board shall be responsible for long-range planning for
the corporation. The chair of the Strategic Planning Board shall be the
Past-President. Members of such committee shall include former Past-Presidents
and anyone appointed by the President who serve ex officio and without vote.
</p>
<h3><a name="RTFToC59">Article
XI. Election of Councilors and Officers
</a></h3>
<h4>
<a name="RTFToC60">Section 1. Procedures.
</a></h4>
<p>The Nominating Committee shall nominate one person or more persons for the position of President-Elect and at least as many persons for positions on the Executive Council as there are vacancies on the Council. These nominations shall be included in the notice of the annual meeting given to all members. Ballots shall be circulated to the membership at
least forty-five (45) days before the Annual Meeting. Electronic ballots (for instance, web-based voting systems) may be used as the sole mechanism for the election of Councilors and Officers. Each Regular member may vote for the President-Elect and for as many members of the Executive Council as there are vacancies to be filled. Ballots must be received by the Nominating Committee no later than fifteen (15) days prior to the Annual Meeting. Those four nominees for Executive Council receiving the highest number of votes, including write-in votes, shall be declared elected for three-year terms.
</p>
<h4>
<a name="RTFToC61">Section 2. Nominations by Members.
</a></h4>
<p>Each year the AAAI will include an announcement in the official publication
requesting nominations to the positions of President-Elect and Councilor.
Members representing five percent (5%) of the voting power or at least 500
members may nominate candidates for councilors by a petition, signed by those
members, and delivered to an officer of the corporation at least four (4)
months prior to such election. On timely receipt of a petition signed by the
required number of members, the secretary shall cause the names of the
candidates named on it to be placed on the ballot along with the names of those
candidates named by Nominating Committee.</p>
<h4>Section 3. Period of Incumbency.</h4> 
<p>Each person elected to an office shall
occupy that office from the end of the Annual Meeting of the corporation
immediately following his or her election for the term of office specified and
until a successor has been elected and qualified.
</p>
<h3><a name="RTFToC62">Article
XII. Committees
</a></h3>
<h4>
<a name="RTFToC63">Section 1. Committees of the Executive Council.
</a></h4>
<p>The Executive Council, by resolution adopted by an affirmative majority vote of
the number of councilors in office provided a quorum is present, may designate
one or more committees, each of which shall consist of two or more councilors
of the Executive Council. The President may appoint Chairpersons to all
Standing Committees subject to the approval of the Executive Council. These
committees, to the extent provided by their enabling resolutions and within the
limitations of the law or these Bylaws, shall have and exercise the authority
of the Executive Council in the management of the corporation, except that no
committee, regardless of the resolution, may:</p>
<p>(a) take any final action on matters which, under the California Nonprofit
Corporation Law, also require members' approval or approval of a majority of
all the members;</p>
<p>(b) fill vacancies on the Executive Council or on any committee which has the
authority of the Council;</p>
<p>(c) fix compensation, if any, of the councilors for serving on the Council or
on any committee;</p>
<p>(d) amend or repeal bylaws or adopt the bylaws;</p>
<p>(e) amend or repeal any resolution of the Executive Council which by its
express terms is not so amendable or repealable;</p>
<p>(f) appoint other committees of the Council or the members thereof;</p>
<p>(g) approve any transaction (1) to which the Corporation is a party and in
which one or more councilors has a material financial interest or (2) between
the Corporation or any person in which one or more of its councilors has a
material financial interest. </p>
<p>The designation of any committee and any delegation of authority shall not
operate to relieve the Executive Council or any councilor of any responsibility
imposed by law. Such committees shall serve at the pleasure of the Council. 
</p>
<h4>
<a name="RTFToC64">Section 2. Nominating Committee.
</a></h4>
<p>The Nominating Committee shall be responsible for the conduct of elections in
accordance with the provisions of Article XI. The members of the Nominating
Committee will be the Past-President and the members of the Executive Council
whose terms of office expire at the next annual meeting.
</p>
<h4>
<a name="RTFToC65">Section 3. Finance Committee.
</a></h4>
<p>The Finance Committee shall be responsible for the preparation and review of
the annual budget, supervise financial controls over expenditures, and
establish spending limits. The Secretary-Treasurer shall be a member of this
committee. The Executive Council shall, by majority vote, elect the Finance
Chair and such other members of the Committee as the Executive Council deems
appropriate.
</p>
<h4>
<a name="RTFToC66">Section 4. Conference Committee.
</a></h4>
<p>The Conference Committee shall be responsible for the planning and execution of
the conferences held by the corporation. The Executive Council shall, by
affirmative vote, elect the Conference Chair and such other members of the
Committee as the Executive Council deems appropriate. 
</p>
<h4>
<a name="RTFToC67">Section 5. Symposium Committee.
</a></h4>
<p>The Symposium Committee shall be responsible for the planning and execution of Symposia held by the corporation.  The Executive Council shall, by affirmative vote, elect the Symposium Chair and such other members of the Symposium Committee as the Executive Council deems appropriate.
</p>
<h4>
<a name="RTFToC68">Section 6. Publications Committee.
</a></h4>
<p>The Publications Committee shall be responsible for any newsletter of the
corporation and for such other publications as are established by the Executive
Council. The Executive Council shall, by affirmative vote, elect the
Publication Chair and such other members of the Committee as the Executive
Council deems appropriate.
</p>
<h4>
<a name="RTFToC69">Section 7. Fellows Committee.
</a></h4>
<p>The Fellows Committee shall be responsible for selection of Fellows of the
AAAI. The Past President will act as Chair, and past AAAI Fellows will be
members of the Committee.
</p>
<h4>
<a name="RTFToC70">Section 8. Other Committees.
</a></h4>
<p>Other committees not having and exercising the authority of the Executive
Council may be designated by a resolution adopted by majority of the members of
the Executive Council at a meeting at which a quorum is present. Except as
otherwise provided in such resolution, members of each such committee shall be
members of the corporation, and the Executive Council may nominate a person the
chair of the committee subject to the approval of the Executive Council. The President, with the advice and consent of the Executive Committee, may establish committees to study issues, and to make recommendations to and/or to execute decisions of the Executive Council.
</p>
<h4>
<a name="RTFToC71">Section 9. Qualifications and Term.
</a></h4>
<p>Each member of any committee shall be a member of the corporation, unless
otherwise provided in the resolution of the Executive Council establishing such
a committee. Each member of the committee shall be appointed for a specific
term and shall serve until the end of that term or until a successor is
appointed, unless the committee is dissolved, such member is removed or resigns
from such committee, or such member shall cease to qualify as a member thereof.
</p>
<h4>
<a name="RTFToC72">Section 10. Vacancies.
</a></h4>
<p>Vacancies in the membership of any committee may be filled by appointments made
in the same manner as provided in the case of the original appointments.
Vacancies on any committee having the authority of the Council may be filled
only by the Executive Council.
</p>
<h4>
<a name="RTFToC73">Section 11. Quorum.
</a></h4>
<p>Unless otherwise provided in the resolution of the Executive Council
designating a committee, a majority of the whole committee shall constitute a
quorum, and the act of a majority of the members present at a meeting at which
a quorum is present shall be the act of the committee.
</p>
<h4>
<a name="RTFToC74">Section 12. Rules.
</a></h4>
<p>Each Committee may adopt rules for its own government not inconsistent with
these Bylaws, with rules adopted by the Executive Council, or with the
California Nonprofit Corporation Law.
</p>
<h4>
<a name="RTFToC75">Section 13. Termination or Removal.
</a></h4>
<p>The Executive Council may, by affirmative majority vote, remove any chairperson
or member of any committee or may eliminate any committee not specifically
designated in these Bylaws.
</p>
<h3><a name="RTFToC76">Article
XIII. Contracts, Checks, Deposits, and Funds
</a></h3>
<h4>
<a name="RTFToC77">Section 1. Contracts.
</a></h4>
<p>The Executive Council may authorize any officer or officers, agent or agents of
the corporation, in addition to the officers so authorized by these Bylaws, to
enter into any contract or execute and deliver any instrument in the name of
and on behalf of the corporation, and such authority may be general or confined
to specified instances. Any authority so delegated is subject to the same
provisions of law and limitations of the Articles of Incorporation and these
Bylaws as pertain to the Executive Council.
</p>
<h4>
<a name="RTFToC78">Section 2. Checks, Drafts, or Orders for Payment.
</a></h4>
<p>All checks, drafts, or orders for payment of money, notes or other evidences of
indebtedness issued in the name of the corporation shall be signed by such
officer or officers as shall, from time to time, be determined by resolution of
the Executive Council. In the absence of such determination by the Executive
Council, such instruments shall be signed by the Secretary-Treasurer or by the
President of the corporation, unless otherwise specified by law.
</p>
<h4>
<a name="RTFToC79">Section 3. Deposits.
</a></h4>
<p>All funds of the corporation shall be deposited from time to time to the credit
of the corporation in such banks, trust companies, or other depositories as the
Executive Council may select.
</p>
<h4>
<a name="RTFToC80">Section 4. Gifts.
</a></h4>
<p>The Executive Council may accept on behalf of the corporation any contribution,
gift, bequest, or devise for the general purposes or for any special purpose of
the corporation.
</p>
<h3><a name="RTFToC81">Article
XIV. Miscellaneous
</a></h3>
<p></p>
<h4>
<a name="RTFToC82">Section 1. Books and Records.
</a></h4>
<p>The corporation shall keep correct and complete books and records of account
and shall also keep minutes of the proceedings of its members, Executive
Council, and committees having the authority of the Executive Council, and
shall keep at the registered or principal office or such other place as the
Secretary-Treasurer may designate a record giving the names of the members
entitled to vote. All books and records of the corporation may be inspected by
any member or his or her agent or attorney for any proper purpose at any
reasonable time.
</p>
<h4>
<a name="RTFToC83">Section 2. Fiscal Year.
</a></h4>
<p>The fiscal year of the corporation shall be as determined by law and a majority
vote of the Executive Council.
</p>
<h4>
<a name="RTFToC84">Section 3. Corporate Seal.
</a></h4>
<p>The Executive Council shall provide a corporate seal, which shall be in the
form of "ASSOCIATION FOR THE ADVANCEMENT OF ARTIFICIAL INTELLIGENCE (AAAI)."
</p>
<h3><a name="RTFToC85">Article
XV. Parliamentary Authority
</a></h3>
<h4>Section 1.</h4> <p>The rules contained in the most recent edition of Robert's
Rules of Order shall govern this corporation in all cases to which they are
applicable and in which they are not inconsistent with these Bylaws.
</p>
<h3><a name="RTFToC86">Article
XVI. Indemnification of Councilors, Officers, Employees, and Other Agents
</a></h3><p></p>
<h4>
<a name="RTFToC87">Section 1. Definitions.
</a></h4>
<p>For purposes of this Article, </p>
<p>(a) <i>agent </i>means any person who is or was a councilor, officer, employee,
or other agent of this corporation, or is or was serving at the request of this
corporation as a councilor, officer, employee, or agent of another foreign or
domestic corporation, partnership, joint venture, trust, or other enterprise,
or was a councilor, officer, employee, or agent of a foreign or domestic
corporation that was a predecessor corporation of this corporation of another
enterprise at the request of the predecessor corporation;</p>
<p>(b) <i>proceeding</i> means any threatened, pending, or completed action or
proceeding, whether civil, criminal, administrative, or investigative; and </p>
<p>(c) <i>expenses</i> includes, without limitation, all attorneys' fees, costs,
and other expenses incurred in the defense of any claims or proceedings against
any agent by reason of his or her position or relationship as agent and all
attorneys' fees, costs, and other expenses incurred in establishing a right to
indemnifications under this Article.
</p>
<h4>
<a name="RTFToC88">Section 2. Mandatory Indemnity.
</a></h4>
<p>In any case where the agent "wins" on a claim brought against him or her that
arises out of the agent's actions for or on behalf of the corporation, the
corporation must indemnify that agent against any expenses incurred in
connection with the claim or suit. To "win," the case must be fully decided
based on its merits. Most commonly, a decision on the merits occurs when a
court or other tribunal reaches a verdict or issues an order based on the facts
and the law in the case.
</p>
<h4>
<a name="RTFToC89">Section 3. Prohibited or Restricted Indemnity.
</a></h4>
<p>The association may provide only limited indemnification (or cannot provide any
indemnification) in the following cases:</p>
<p>(a) Suits brought by or on behalf of the corporation against the agent or
agents; </p>
<p>(b) Suits in which the agent is accused of self-dealing; or </p>
<p>(c) Suits involving a breach of a duty regarding assets held in charitable
trust.
</p>
<h4>
<a name="RTFToC90">Section 4. Permissive Indemnity.
</a></h4>
<p>In all other cases that do not fall into the situations mentioned above, the
agent may be indemnified, provided that the Executive Council authorizes the
indemnification or that the court authorizes indemnity. Permissive Indemnity
situation typically involves claims by third parties in which both the
corporation and one or more of its agents individually are named as defendents
in the suit.</p>
<p>Where the Executive Council authorizes indemnity, it must first determine that
the agent acted in good faith and with a reasonable belief that his or her acts
were in the AAAI's best interests. The Executive Council should make a thorough
investigation regarding the extent of the agent's good faith, belief of
corporate best interest, and prudence of conduct in any situation where
indemnity issues arise.
</p>
<h4>
<a name="RTFToC91">Section 5. Determination of Agent's Good Faith Conduct.
</a></h4>
<p>The indemnification granted to an agent in Sections 2 and 4 above is
conditioned on the following:</p>
<p>(a) <i>Required standard of conduct.</i> The agent seeking reimbursement must
be found, in the manner provided below, to have acted in good faith, in a
manner he or she believed to be in the best interest of this corporation, and
with such care, including reasonable inquiry, as an ordinary prudent person in
a like position would use in similar circumstances. The termination of any
proceeding by judgment, order, settlement, conviction, or on a plea of <i>nolo
contendere </i>or its equivalent shall not, of itself, create a presumption
that the person did not act in good faith or interest of this corporation or
that he or she had reasonable cause to believe that his or her conduct was
unlawful. In the case of criminal proceeding, the person must have had no
reasonable cause to believe that his or her conduct was unlawful.</p>
<p>(b) <i>Manner of determination of good faith conduct.</i> The determination
that the agent did act in a manner complying with Paragraph (a) above shall be
made by:</p><dl>
<dd>(i) the Executive Council by a majority vote of a quorum consisting of
councilors who are not parties to proceedings; or</dd> <dd>
(ii) the members by an affirmative vote (or written ballot) of a majority of
the members represented and voting at a duly held meeting of members at which a
quorum is present (which affirmative votes also constitute a majority of the
required quorum); or</dd> <dd>
(iii) the court in which the proceeding is or was pending. Such determination
may be made on application brought by this corporation or the agent or the
attorney or other person rendering a defense to the agent, whether or not the
application by the agent, attorney, or other person is opposed by this
Corporation.
</dd></dl>
<h4>
<a name="RTFToC92">Section 6. Limitations.
</a></h4>
<p>No indemnification or advance shall be made under this Article, except as
provided in Section 2 or 5(b) (iii), in any circumstance which it appears:</p>
<p>(a) That the indemnification or advance would be inconsistent with a provision
of the articles, a resolution of the members, or an agreement in effect at the
time of the accrual of the alleged cause of action asserted in the proceeding
in which the expenses were incurred or other amounts were paid, which prohibits
or otherwise limits indemnification; or </p>
<p>(b) That the indemnification would be inconsistent with any condition expressly
imposed by a court in approving a settlement.
</p>
<h4>
<a name="RTFToC93">Section 7. Advance of Expenses.
</a></h4>
<p>Expenses incurred in defending any proceeding may be advanced by this
corporation before the final disposition of the proceeding on receipt of an
undertaking by or on behalf of the agent to repay the amount of the advance
unless it is determined ultimately that the agent is entitled to be indemnified
as authorized in this Article.
</p>
<h4>
<a name="RTFToC94">Section 8. Contractual Rights of Noncouncilors and Nonofficers.
</a></h4>
<p>Nothing contained in this Article shall affect any right to indemnification to
which persons other than councilors and officers of this corporation, or any
subsidiary hereof, may be entitled to by contract or otherwise.
</p>
<h4>
<a name="RTFToC95">Section 9. Insurance.
</a></h4>
<p>The Executive Council may adopt a resolution authorizing the purchase and
maintenance of insurance on behalf of any agent of the corporation against any
liability other than for violating provisions against self-dealing asserted
against or incurred by the agent in such capacity or arising out of the agent's
status as such, whether or not his or her corporation would have the power to
indemnify the agent against that liability under the provisions of this section.
</p>
<h3><a name="RTFToC96">Article
XVII. Amendments to These Bylaws
</a></h3>
<h4>
<a name="RTFToC97">Section 1. Amendment by Executive Council.
</a></h4>
<p>(a) Subject to the limitations set forth below, the Executive Council may
adopt, amend or repeal bylaws unless the action would materially and adversely
affect the members' rights as to voting. The Executive Council may not extend
the term of a Councilor beyond that for which the Councilor was elected.</p>
(b) The Executive Council may not, without the approval of the members, specify
or change any bylaw provision that would:<p></p><dl><dd>
(i) Fix or change the authorized number of councilors;</dd> <dd>
(ii) Fix or change the minimum or maximum number of councilors;</dd> <dd>
(iii) Change from a fixed number of councilors to a variable number of
councilors or vice versa;</dd><dd>
(iv) Increase or extend the terms of councilors; or</dd><dd>
(v) Increase the quorum for members' meetings.</dd></dl>
<h4>
<a name="RTFToC98">Section 2. Approval by Members.
</a></h4>
<p>New bylaws may be adopted or these Bylaws may be amended or repealed by
approval of the members. No amendment may extend the term of a director beyond
that for which the director was elected.</p>


