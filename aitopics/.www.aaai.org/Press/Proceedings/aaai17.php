<div class="hr1"><hr /></div>

<p>The AAAI Conference on Artificial Intelligence promotes theoretical and applied AI research as well as intellectual interchange among researchers and practitioners. The technical program features substantial, original research and practices influencing AI's development throughout the world.</p>

<p>Over the last few years we have seen a resurgence of interest in AI technologies from industry as well as an increase in interest in AI research from undergraduate and graduate students and faculty from around the world. As specialized conferences in machine learning, deep learning, natural language processing, and computer vision, thrive and grow, it is especially pleasing to note that AAAI, a venue that attracts papers from across the breadth of AI, is also thriving and growing. We believe there is tremendous value to bringing together the diverse communities with shared long-term goals, facilitating collaboration and transfer of ideas, as well as welcoming strong papers that cross subarea boundaries and may thus fall through the cracks.</p>

<p>The AAAI-17 program featured a comprehensive slate of tutorials, workshops, the satellite IAAI conference and the EAAI symposium, demos, videos, exhibits, invited talks and panels, student abstracts, and special presentations by senior members and representatives from affiliated communities. These activities were organized around a core of technical sessions comprising presentations of original research contributions.
</p>

 
