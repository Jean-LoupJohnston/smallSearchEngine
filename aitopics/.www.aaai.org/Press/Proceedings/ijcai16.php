<h2 class="backgrnd12"><span class="text1">
Proceedings of the Twenty-Fifth International Joint Conference on Artificial Intelligence (IJCAI 16)</span></h2>
<div class="content">
<p><img height="150" width="113" src="http://www.aaai.org/Press/Proceedings/Covers/ijcai16.jpg" alt="Proceedings Cover Image" style="float: left; margin-right: 5px;" /><b>IJCAI-16<br />New York, NY, USA, 9&ndash;15 July 2016</b><br />Edited by
Subbarao Kambhampati<br />
5 volumes, references, index, illus.<br />
(For international orders please <a href="http://www.aaai.org/Press/orders.php#Shipping">shipping options</a> before ordering on website.)</p>


<p>The International Joint Conference on Artificial Intelligence (IJCAI) is one of the premier international gathering of leading AI researchers and practitioners from all over the world. Held biennially in odd-numbered years since 1969 and annual since 2015, IJCAI highlights the latest theoretical and applied results in all subfields of artificial intelligence.</p>
<p>The theme of IJCAI-16 was Human-Aware Artificial Intelligence. For this reason, besides the main track, the conference had invited talks and panels organized around this theme, focusing on the many benefits of AI systems working in collaboration with humans.</p>
<p><i>The volumes of the 2016 proceedings are sold separately.</i></p>
<div class="hr1"><hr /></div>


<h4>Volume One</h4>

<p>
This volume contains papers in agent-based and multiagent systems, combinatorial and heuristic search, constraints and satisfiability, and human-aware artificial intelligence.
</p>
