<p>AAAI members in good standing may elect to use the membership 20% discount coupon, which will be applied to eligibile items. The coupon is available on the AAAI member's website. You must be a member in good standing in order to access this site. Orders not place directly through AAAI are not eligibile for the discount. Retroactive discounts are not accepted.</p>
