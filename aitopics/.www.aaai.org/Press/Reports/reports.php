<h2 class="backgrnd12"><span class="text1">AAAI Press Technical Reports In Print</h2>
<div class="content">
<p>The AAAI Press technical reports series is designed to facilitate technical interaction within the international AI community. Reports in this series contain the latest research results from AAAI-sponsored and select other workshops, symposia, and conferences. Like all technical reports, the materials listed herein are informally presented and bound, and do not constitute formal publication. Symposia and workshop authors who are interested in their work becoming a technical report should check with their event organizer, and sign the <a href="http://www.aaai.org/Publications/Author/distribute-permission.pdf">permission to distribute form</a>.</p>
<p> The AAAI Press publishes a variety of high-quality publications in the field of artificial intelligence. AAAI Press also offers high-quality proceedings of conferences, many of which were cosponsored by AAAI. Finally, AAAI Press offers the AAAI Technical Reports Series, collections of unpublished, generally preliminary papers arising from AAAI workshop and symposia meetings. </p>
<p>This document contains links to all currently available AAAI Technical Reports. Reports are grouped according to type of meeting: workshop, symposium, or conference.</p>
