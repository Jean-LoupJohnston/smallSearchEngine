<p><a href="javascript:new_window('books-index.php')">Author/Title Index</a></p>
<p>AAAI Press / The MIT Press books are copublished by AAAI and The MIT Press, and reflect the commitment of both organizations to facilitating technical interaction within the international AI community. AAAI Press/The MIT Press books are marketed and distributed by The MIT Press, while editorial and production activities are handled by the AAAI Press. The acquisition of manuscripts is handled jointly.</p>
<p>Since its inception, the AAAI Press has published more than twenty-five edited or monograph collections. Each year, the Press adds as many as five new titles to the series. Interested authors should consult the author guidelines for information on submitting proposals to the Press for consideration.</p>
