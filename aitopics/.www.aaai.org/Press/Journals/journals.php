<h2 class="backgrnd12"><span class="text1">AAAI Press Journals In Print</span></h2>
<div class="content">

<p> The AAAI Press publishes a variety of high-quality publications in the field of artificial intelligence. Carefully crafted monographs and edited collections are generally AAAI Press/ MIT Press books. AAAI Press also offers high-quality proceedings of conferences, many of which were cosponsored by AAA, and the AAAI Technical Reports Series, collections of unpublished, generally preliminary papers arising from AAAI workshop and symposia meetings. Finally, AAAI Press is proud to offer, in cooperation with the AI Access Foundation, the <i>Journal of Artificial Intelligence Research (JAIR)</i></p>
<p>This document contains links to all currently available <i>JAIR</i> volumes.</p>
<h3><i>Journal of Artificial Intelligence Research (JAIR)</i></h3>
<p>The <i>Journal of Artificial Intelligence Research</i> (JAIR) (ISSN 1076-9757) covers all areas of artificial intelligence (AI), publishing refereed research articles, survey articles, and technical notes. Established in 1993 as one of the first electronic scientific journals, JAIR is indexed by INSPEC, Science Citation Index, and MathSciNet. The hard-copy version of <i>JAIR</i> is published twice a year, exclusively by AAAI Press, in agreement with the AI Access Foundation. Volumes 18 on have been published by AAAI Press. Additional stock of some earlier volumes (originally published by Morgan Kaufmann Publishers) is also available. Earlier volumes not listed are out of print and unavailable.</p>
