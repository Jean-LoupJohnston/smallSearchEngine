<h2 class="backgrnd12"><span class="text1">AAAI Outstanding Paper Award</span></h2>
<div class="content"> 
<p>The AAAI Conference on Artificial Intelligence (formerly the AAAI National Conference on Artificial Intelligence) honors papers that
exemplify the highest standards in technical contribution and exposition.
During the blind review process, members of the Program Committee recommend
papers to consider for the Outstanding Paper Award. A subset of the Senior
Program Committee, carefully chosen to avoid conflicts of interest, reviews all
nominated papers and selects the winning paper(s).</p>

