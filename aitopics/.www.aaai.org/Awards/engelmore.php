<h3><a name="Past">Past Recipients</a></h3>
<ul>

<li><i>2019:</i> Milind Tambe (University of Southern California)<br />
For outstanding research contributions in the area of multi-agent systems and their application to problems of societal significance.<br />&nbsp;</li>



<li><i>2018:</i> Stephen F. Smith (Carnegie Mellon University)<br />

For sustained research excellence in constraint-based planning and scheduling technologies, deployment of those technologies to a range of significant real-world problems, and extensive service to the AI community that includes significant outreach to related technical fields.<br />&nbsp;</li>

<li><i>2017:</i> David W. Aha (Naval Research Laboratory)<br />
For pioneering research contributions and high-impact applications in autonomous systems, machine learning, and case-based reasoning, and for extensive contributions to AAAI, including educating the broader AI community through AAAI doctoral consortia and video competitions.<br />&nbsp;</li>


<li><i>2016:</i> Reid G. Smith (i2k Connect)<br />
For pioneering research contributions and high-impact applications in knowledge management and for extensive contributions to AAAI, including educating and inspiring the broader community about AI through AITopics.<br />&nbsp;</li>

<li><i>2014:</i> Craig A. Knoblock ( University of Southern California)<br />
For seminal contributions to machine learning and information 
integration, high-impact deployed applications and open-source 
projects, and extensive service to AAAI and international AI<br />&nbsp;</li>

<li><i>2013:</i>   Deborah L. McGuinness (Rensselaer Polytechnic Institute)<br />
For leadership in semantic web research and in bridging AI and 
eScience, significant contributions to deployed AI applications, and 
extensive service to the AI community.<br />&nbsp;</li>

<li><i>2012:</i>  Steven Minton (InferLink Corporation)<br />
For seminal contributions in scheduling, planning, and machine learning, advancement of real-world AI systems through public and private organizations, and leadership in new dissemination methods for AI research.<br />&nbsp;</li>

<li><i>2011:</i>  Ramon Lopez de Mantaras (Artificial Intelligence Research Institute [IIIA] and Spanish National Research Council [CSIC])<br />
For pioneering research contributions in a breadth of artificial intelligence areas, especially pattern recognition and case-based reasoning, leading to novel applications in design, diagnosis, and music, and for extensive international leadership and service for the AI community.<br />&nbsp;</li>

<li><i>2010:</i> Jay M. Tenenbaum (CollabRx Inc.)<br />
For pioneering artificial intelligence contributions with extensive applications impact, including seminal work in computer vision and manufacturing along with a visionary role in the birth of electronic commerce.<br />&nbsp;</li>
<li><i>2009:</i> William Swartout (Institute for Creative Technologies, University of Southern California)<br />
For seminal contributions to knowledge-based systems and explanation, groundbreaking research on virtual human technologies and their applications, and outstanding service to the artificial intelligence community.
<br />&nbsp;</li>
<li><i>2008:</i> Kenneth M. Ford (Institute for Human and Machine Cognition)<br />
For leadership in AI science and technology though establishing and guiding AI research centers and programs, pioneering contributions to areas such as human-centered computing, and advancement of AI as a scientific endeavor and enabler for society's goals.
<br />&nbsp;</li>
<li><i>2007:</i> Oren Etzioni (University of Washington)<br />
For longstanding technical and entrepreneurial contributions to artificial intelligence, including seminal research on AI on the Web and the deployment of AI technologies in high-impact applications.
<br />&nbsp;</li>
<li><i>2006:</i> Bruce G. Buchanan (University of Pittsburgh)<br />
For leadership in artificial intelligence and pioneering contributions to knowledge-based systems, machine learning, and automated discovery, along with significant applications in medicine, biology, and chemistry.
<br />&nbsp;</li>
<li><i>2005:</i> James A, Hendler (University of Maryland, College Park)<br />
For two decades of technical and government leadership in artificial intelligence, as well as pioneering research on agent-based systems and the semantic web.
<br />&nbsp;</li>
<li><i>2004:</i> Edward A. Feigenbaum (Stanford University)<br />
For decades of technical, business, and government leadership in artificial intelligence, as well as pioneering research on knowledge-based systems.
<br />&nbsp;</li>
<li><i>2003:</i> Larry Hunter (University of Colorado)</li></ul>

