<h2 class="backgrnd12"><span class="text1">Robert S. Engelmore Memorial Lecture Award</span></h2>
<div class="content">
<p>Sponsored by IAAI and
<i>AI Magazine</i></p>
<p>This annual keynote lecture will be presented at the IAAI conference in honor of Dr. Robert S. Engelmore's extraordinary service to AAAI, <i>AI Magazine,</i> and the AI applications community, and his contributions to applied AI. </p><p>Bob's service was extraordinary both in the selfless and insightful manner in which it was conducted, and for its extensiveness, with weekly service for over ten years and consistent contributions in many capacities for another ten. Bob was editor emeritus of the <i>AI Magazine,</i> and an AAAI fellow. He served on the Executive Council, the IAAI Program Committee (chair, 2000), the AAAI Publications Committee (chair, 1993-1997), the AAAI Office Committee, and the National Conference Program Committee. </p><p>Bob had deep interests in both the technical and entrepreneurial aspects of AI. He was a DARPA program manager, and an executive director of the Heuristic Programming Project in Stanford's computer science department. He was the author of many AI papers and a seminal book on blackboard systems. He was also a cofounder of Teknowledge, and an advisor to many AI startup companies. </p><p>The memorial lecture topics will encompass Bob&rsquo;s wide interests in AI, and each lecture will be linked to a subsequent article to be published upon approval by <i>AI Magazine.</i> The lecturer and, therefore, the author for the magazine article, will be chosen jointly by the IAAI Program Committee and the editor of <i>AI Magazine.</i>  The Robert S. Engelmore Memorial Lecture includes a certificate from IAAI, and a $1,000.00 honorarium.
</p>
<p><a href="http://www.aaai.org/Awards/engelmore-call.php">Call for Nominations, 2019</a></p>

<p>For more information regarding these awards, please
contact <a href="http://www.aaai.org/Organization/contact.php">Carol Hamilton, Executive Director, AAAI,</a> or call
650-328-3123.</p>
