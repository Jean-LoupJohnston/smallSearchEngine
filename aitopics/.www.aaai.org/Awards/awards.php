<h2 class="backgrnd12"><span class="text1">Welcome to the AAAI Awards Page!</span></h2>
<div class="content">

<p>From this location, you can find information about ways in which AAAI honors those who have made significant contributions to the science of AI and the AAAI organization.</p>
<p>Each year, the Association for the Advancement of Artificial Intelligence recognizes its members, esteemed members of the AI community, and promising students, with the following awards and honors.
</p>
<div class="hr1">
<hr />
</div>

<h3>AAAI Fellows</h3>
<p>
The <a href="fellows.php">AAAI Fellows</a> program recognizes a small group of AAAI members who have made significant, sustained contributions to the field of artificial intelligence, and are recognized as having unusual distincition in the profession.
</p>
<div class="hr1">
<hr />
</div>

<h3>AAAI Classic Paper Award</h3>
<p>The <a href="http://www.aaai.org/Awards/classic.php">Classic Paper Award</a> honors the author(s) of paper(s)
deemed most influential, chosen from a specific past
conference year.
</p>

<div class="hr1">
<hr />
</div>

<h3>AAAI Distinguished Service Award</h3>
<p>The <a href="http://www.aaai.org/Awards/distinguished.php">AAAI Distinguished Service Award </a>recognizes one individual each year for extraordinary service to the AI community. 
</p>

<div class="hr1">
<hr />
</div>

<h3>ACM / AAAI Allen Newell Award</h3>
<p>
The <a href="http://awards.acm.org/newell/">ACM / AAAI Allen Newell Award,</a> sponsored by the Association for Computing Machinery, was established in 1993 to honor the memory and achievements of Allen Newell. This endowed award is supported through a grant from AAAI. The award is presented to an individual selected for career contributions that have breadth within computer science, or that bridge computer science and other disciplines.
</p>
<div class="hr1">
<hr />
</div>

<h3>AAAI Certificates of Appreciation</h3>
<p>AAAI issues certificates of appreciation to recognize the
efforts of individuals who have volunteered their time over
a period of years to serve in responsible Association roles.
</p>
<div class="hr1">
<hr />
</div>

<h3>AAAI Outstanding Paper Award</h3>
<p>AAAI's National Conference on Artificial Intelligence honors papers that exemplify high standards in technical contribution and exposition. 
Authors who receive this honor are awarded the <a href="http://www.aaai.org/Awards/paper.php">AAAI Outstanding Paper Award.</a></p>

<div class="hr1">
<hr />
</div>

<h3>The Feigenbaum Prize</h3>
<p>
The <a href="http://www.aaai.org/Awards/feigenbaum.php">AAAI Feigenbaum Prize</a> will be awarded biennially to recognize and encourage outstanding Artificial Intelligence research advances that are made by using experimental methods of computer science.</p>
<div class="hr1">
<hr />
</div>
<h3>Innovative Applications of Artificial Intelligence Awards</h3>
<p>
The Innovative Applications of Artificial Intelligence Conference, through the <a href="../Conferences/IAAI/iaai.php">Innovative Applications of Artificial Intelligence Awards</a>  honors case-study papers that 
describe deployed applications with measurable benefits and include some aspect of AI technology.
</p>

<div class="hr1">
<hr />
</div>
<h3>Outstanding Educator Award</h3>

<p>The <a href="http://www.aaai.org/Awards/eaai-award.php">AAAI/EAAI Outstanding Educator</a> award is presented annually to a person (or group of people) who has made major contributions to AI education that provide long-lasting benefits to the AI community.  </p>

<div class="hr1">
<hr />
</div>
<h3>Robert S. Engelmore Memorial Lecture Award</h3>
<p>The <a href="http://www.aaai.org/Awards/engelmore.php">Robert S. Engelmore Memorial Lecture Award</a> is presented annually to an individual who has shown extraordinary service to AAAI and the AI community. In conjunction with the award, the recipient is invited to present a keynote lecture at the IAAI conference, and to prepare an companion article for <i>AI Magazine.</i> The award was established in 2003 to honor Dr. Robert S. Engelmore&rsquo;s extraordinary service to AAAI, <i>AI Magazine,</i> and the AI applications community, and his contributions to applied AI.</p>
<h3>AAAI INTEL Science and Engineering Awards</h3>
<p>AAAI presents special <a href="http://www.aaai.org/Awards/intel.php">AAAI INTEL Science and Engineering </a>awards at the Intel Science and Engineering Fair for the best projects in the area of computer science with an artificial intelligence component.
</p>

<div class="hr1">
<hr />
</div>

<h3>AAAI Robotic Awards</h3>
<p>AAAI honors the winners of the annual Mobile Robot Competition and Robot Building Lab, both held at the National Conference on Artificial Intelligence.
</p>

<div class="hr1">
<hr />
</div>

<h3>AAAI Senior Member Status</h3>
<p><a href="http://www.aaai.org/Awards/senior.php">Senior Member status</a> is designed to recognize AAAI members who have achieved significant accomplishments within the field of artificial intelligence. To be eligible for nomination for Senior Member, candidates must be consecutive members of AAAI for at least five years and have been active in the professional arena for at least ten years</p>
<div class="hr1">
<hr />
</div>
<h3>AAAI Conference Senior Program and Program Committee Awards</h3>

<p>
Each year AAAI recognizes outstanding service by members of the AAAI Conference senior program and program committees through a number of <a href="http://www.aaai.org/Awards/conference.php">conference awards,</a> which are presented at the conference.
</p>

