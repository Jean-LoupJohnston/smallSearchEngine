<h3>Past Recipients</h3>
<h4>2019 </h4>
<ul><li><i>Classic Paper Award Recipients:</i> <b> Prem Melville, Raymond J. Mooney, and Ramadass Nagarajan</b><br />
<a href="javascript:new_window('../Library/AAAI/2002/aaai02-029.php')">Content-Boosted Collaborative Filtering for Improved Recommendations</a>
