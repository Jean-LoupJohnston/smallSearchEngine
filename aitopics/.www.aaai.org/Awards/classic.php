<h2 class="backgrnd12"><span class="text1">AAAI Classic Paper Award</span></h2>
<div class="content"> 
<p>The AAAI Classic Paper award honors the author(s) of paper(s) deemed most influential, chosen from a specific conference year. Each year, the time period considered will advance by one year. The 2020 award will be given to the most influential paper(s) from the Nineteenth National Conference on Artificial Intelligence, held in 2004 in San Jose, California USA.</p>

<p>Papers will be judged on the basis of impact, for example:</p>
<ul><li>Started a new research (sub)area</li>
<li>Led to important applications</li>
<li>Answered a long-standing question/issue or clarified what had been murky</li>
<li>Made a major advance that figures in the history of the subarea</li>
<li>Has been picked up as important and used by other areas within (or outside
of) AI</li>
<li>Has been very heavily cited</li></ul>
<p>All members of the artificial intelligence community are encouraged to submit
nominations for this award. Candidacy is not limited to AAAI members, nor to
people still active in artificial intelligence. Posthumous awards will be
considered.</p>
<p>Nominations will be reviewed by the AAAI Awards Committee. Nominations are due
September 27, 2019.</p>
<p>The award will be presented at the AAAI-20 conference in New York, NY, USA. A total of $1,000 in travel support is available to enable the author(s) to travel to the conference to accept the award in person.</p>
<p>For more information regarding these awards, please
contact Carol Hamilton, Executive Director, AAAI  (&#97;&#119;&#97;&#114;&#100;&#115;&#50;&#48;&#64;&#97;&#97;&#97;&#105;&#46;&#111;&#114;&#103;).</p>
