<h3>Past Recipients</h3>
<ul>
<li><i>2019:</i> Shlomo Zilberstein (University of Massachusetts Amherst, USA)<br />
For his sustained and conscientious service and leadership both to AAAI as a councilor and conference committee chair, and to the broader AI community, as the president of ICAPS.</li>


<li><i>2018:</i> Peter E. Friedland <br /> 
For his contributions to the field of artificial intelligence through sustained service nationally in establishing AI R&D at NASA, structuring AI programs at AFOSR, and pioneering service in computational molecular biology.</li>


<li><i>2017:</i>  James A. Hendler (Rensselaer Polytechnic Institute, USA)<br />
For his outstanding contributions to the field of artificial intelligence through sustained service to AAAI, other professional societies, and government activities promoting the importance of Artificial Intelligence research.</li>


<li><i>2016:</i> Maria Gini (University of Minnesota)<br />
For her outstanding contributions to the field of artificial intelligence through sustained service leading AI societies, journals, and conferences; mentoring colleagues; and working to increase participation of women in AI and computing.</li>

<li><i>2015:</i> Kenneth M. Ford (Florida Institute for Human and Machine Cognition)<br />
For his outstanding contributions to the field of artificial intelligence through sustained service, including the founding of the Florida Institute for Human and Machine Cognition (IHMC), leadership roles at NASA, and his work on the advisory boards of federal science and technology research organizations.
</li>

<li><i>2014:</i> David Leake, Indiana University<br />
In recognition of his outstanding work as Editor-in-Chief of <i>AI Magazine,</i> the journal of record for the AI community, for more than 15 years, his sustained service as AAAI Publications Chair, and his seminal work and service in the case-based reasoning and learning community.
</li>
<li><i>2013:</i> Ted E. Senator, SAIC, Inc.<br />
In recognition of his sustained service to AAAI as a driving force behind the IAAI conference, as the secretary-treasurer for the society, and through his role in securing funding for AI research.
</li>
<li><i>2012:</i> Anthony G. Cohn, University of Leeds<br />
For his contributions to the discipline of artificial intelligence through sustained service in professional societies, conferences, journals, and funding agencies around the world.
</li>
<li><i>2011:</i> David L. Waltz, Center for Computational Learning Systems, Columbia University<br />
For his extraordinary and long-term technical contributions to artificial intelligence including ground-breaking work in computer vision, memory-based reasoning, classification, and information retrieval, and dedicated organizational leadership within the AI research community.  Beyond the influence of his ideas and guidance, his insights, wisdom, and generous mentorship have been of great value in the nurturing and support of numerous students and colleagues.</li>
<li><i>2010:</i> Alan K. Mackworth, University of British Columbia<br />
For his outstanding service to artificial intelligence, including seminal scientific contributions in constraint-based representations and methods, with pioneering efforts in machine vision, robotics, and situated agents, and his sustained service in numerous key leadership roles, including the presidencies of AAAI, IJCAI, and the Canadian Society for Computational Studies of Intelligence (CSCSI), amidst a lifetime of catalyzing and promoting AI research.</li>
<li><i>2008:</i> Ronald J. Brachman, Yahoo! Research<br />
For his contributions to the field of artificial intelligence through sustained service in numerous leadership roles in industry, government and professional societies, tirelessly instigating, facilitating and promoting successful AI research and development.</li>
<li><i>2007:</i> Tom Mitchell, Carnegie Mellon University<br />
For his outstanding service to artificial intelligence and computer science, including seminal intellectual and service contributions to machine learning and leadership at Carnegie Mellon University and in AAAI, NRC, and AAAS.</li>
<li><i>2006:</i> Edward Feigenbaum, Stanford University<br />
For a lifetime of service to artificial intelligence as a tireless and effective champion of the field, including seminal contributions to the theory and practice of knowledge-based systems, coeditorship of the first major collection of AI papers, mentorship of numerous leading AI researchers, facilitation of the commercialization of AI technology, and service to the AI and computer science communities in many key leadership roles, including president of AAAI and chief scientist of the US Air Force</li>
<li><i>2005:</i> Nils Nilsson, Stanford University<br />
For a lifetime of service to artificial intelligence, including seminal scientific contributions to pattern recognition, heuristic search, planning, robotics and expert systems, pioneering achievements in scientific publishing and skilled exposition in many influential textbooks, and service to the AI and computer science communities in many key leadership roles including president of AAAI.</li>
<li><i>2004:</i> Bruce Buchanan, University of Pittsburgh<br />
For a lifetime of service to the science of artificial intelligence, including seminal scientific contributions to knowledge based systems and machine learning, educational impacts on many graduate students and on researchers in medicine, philosophy, and other fields beyond the borders of AI, and selfless professional service as secretary-treasurer and president of AAAI.</li>
<li><i>2002:</i> Raj Reddy, Carnegie Mellon University<br />
For his lifetime of service to the science of artificial intelligence, including significant advances in speech understanding, reasoning, representation, and robotics, the initial formation of the AAAI and service as president, global leadership in the digital library project, and wise counsel to numerous students, colleagues, administrators, and politicians.</li>
<li><i>2000:</i> Daniel Bobrow, Xerox Palo Alto Research Center<br />
For his significant contributions to the field of artificial intelligence through sustained service to AAAI and stewardship of artificial intelligence.</li>
<li><i>1999:</i> Barbara Grosz, Harvard University<br />
For her contributions to the field of artificial intelligence through sustained
service in a multitude of academic, professional society, and government
leadership roles, at the local, national and international levels, through
which she has had a major effect on the field and on those working in it.</li>
</ul>
