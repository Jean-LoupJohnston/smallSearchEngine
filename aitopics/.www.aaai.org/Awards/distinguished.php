<h2 class="backgrnd12"><span class="text1">AAAI Distinguished Service Award</span></h2>
<div class="content">
<p>The AAAI Distinguished Service award recognizes one individual each year for 
extraordinary service to the
AI community. Areas of service could include, but are not limited to: society
service, service as an editor, conference organization, representation of AI in
other organizations (such as CRA, ACM, or IEEE), or influential service as a
government agency contract monitor or program director, resulting in positive
effects on the field of AI. Nominees must be current members of AAAI.</p>
<p>All regular members of AAAI are encouraged to place an individual's name in
nomination. Nominations will be reviewed by the AAAI Awards Committee, and
their final recommendation will be approved by the AAAI President. Nominations
are due September 27, 2019.</p>
<p>The winner will receive a $1,000 prize.</p>
<p>For  more information regarding these awards, please
contact  Carol Hamilton, Executive Director (&#97;&#119;&#97;&#114;&#100;&#115;&#50;&#48;&#64;&#97;&#97;&#97;&#105;&#46;&#111;&#114;&#103;).</p>
