<h2 class="backgrnd12"><span class="text1">AAAI Intel Science and Engineering Awards</span></h2>
<div class="content">

<p>AAAI established special awards in 1999 at the annual Intel International Science and Engineering Fair (ISEF) to recognize outstanding achievement in the area of intelligent computation and robotics. AAAI presented three awards in 1999 for the best projects in the area of computer science with an artificial intelligence component. Finalists in other areas with significant computer science components were also eligible. The number of awards was expanded in subsequent years, and has ranged between 5 and 15. The current award amounts are $1,500 (first place), $1,000 (second place), and $500 (third place). In addition, up to five honorable mentions are awarded. All winners receive a certificate. Winners and their schools also receive a one-year membership in the Association for the Advancement of Artificial Intelligence, including a subscription to <i>AI Magazine.</i></p>
<p>(Further information is located at <a href="https://student.societyforscience.org/intel-isef">student.societyforscience.org/intel-isef</a>)</p>

