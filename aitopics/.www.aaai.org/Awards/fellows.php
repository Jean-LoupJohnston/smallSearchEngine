<h2 class="backgrnd12"><span class="text1">The AAAI Fellows Program</span></h2>
<div class="content">
<p>Each year the AAAI recognizes a group of individuals who have made significant, sustained contributions to the field of artificial intelligence through the continuation of its Fellows program.</p>
<p>
The AAAI Fellows Program, as originally chartered, honors a small percentage of the AAAI membership. Fellows are recognized as having unusual distinction in the profession. </p>
<p>Usually only members who have made contributions to the field for a decade or more will be selected in any year. A Fellow may be recognized for individual technical contributions or for having performed significant extended service for the AAAI. Evidence of technical contribution will often be in the form of technical results and publications, but other evidence will also be considered, such as patent awards or statements of longstanding contribution to an industrial group effort.</p>
<p>Fellows are selected by a Fellows Selection Committee comprising nine current Fellows, and chaired by the Past President of AAAI. One third of the members are replaced each year. The policies governing the operation of the Fellows Committee are under the review of the Executive Council. At the Executive Council meetings, the Fellows Committee Chair reviews current operations of the committee with the Council.
</p>
 
