<h3>Past Recipients</h3>

<h4>2019 </h4>
<ul><li><i>Stuart Russell</i> University of California, Berkeley<br />
In recognition of his high-impact contributions to the field of artificial intelligence through innovation and achievement in probabilistic knowledge representation, reasoning, and learning, including its application to global seismic monitoring for the Comprehensive Nuclear-Test-Ban Treaty. </li></ul>


<h4>2017</h4>
<ul><li><i>Yoav Shoham</i> (Stanford University/Google)<br />
For high-impact basic research in artificial intelligence &mdash; including knowledge representation, multiagent systems, and computational game theory &mdash; and translating the basic research into impactful and innovative commercial products.</li></ul>

<h4>2015</h4>

<ul><li><i>Eric Horvitz</i> (Microsoft Research)<br />
For sustained and high-impact contributions to the field of artificial intelligence through the development of computational models of perception, reflection and action, and their application in time-critical decision making, and intelligent information, traffic and healthcare systems.</li></ul>



<h4>2013</h4>
<ul><li><i>IBM Watson Team<sup>*</sup></i><br />
For demonstrating that a synthesis of AI techniques, including symbolic knowledge representation, natural language understanding, and statistical machine learning, can achieve human-level performance in real-time factual question-answering.<br />
<i><sup>*</sup>Team Members:</i> Sugato Bagchi
Michael Barborak, Branimir Boguraev, Eric Brown, David Carmel, Jennifer Chu-Carroll, Jaroslaw Cwiklik, Edward Epstein, James Fan, David Ferrucci, Tong-Haing Fin, David Gondek, Bhavani Iyer, Aditya Kalyanpur, Hiroshi Kanayama, Adam Lally, Jonathan Lenchner, Anthony Levas, Burn Lewis, Michael McCord, Erik Mueller, J. William Murdock, Yue Pan, Siddharth Patwardhan, John Prager, Marshall Schor, Dafna Sheinwald, David Shepler, Kohichi Takeda, Gerald Tesauro, Chang Wang, Chris Welty, Wlodek Zadrozny, Lei Zhang
</li></ul>

<h4>2011</h4>
<ul>
<li><i>Sebastian Thrun</i> (Stanford University) and <i>William A. "Red" Whittaker </i>(Carnegie Mellon University)<br />
	For their influential contributions to artificial intelligence via achievements in autonomous vehicle research, including experimental efforts and research leadership of teams addressing challenges with the fielding of robotic systems in the open world.</li>
</ul>

