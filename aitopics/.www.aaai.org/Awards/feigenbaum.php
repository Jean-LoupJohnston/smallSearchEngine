<p>The AAAI Feigenbaum Prize is awarded biennially to recognize and encourage outstanding Artificial Intelligence research advances that are made by using experimental methods of computer science. The "laboratories" for the experimental work are real-world domains, and the power of the research results are demonstrated in those domains. The Feigenbaum Prize may be given for a sustained record of high-impact seminal contributions to experimental AI research; or it may be given to reward singular remarkable innovation and achievement in experimental AI research. The prize is $10,000 and is provided by the Feigenbaum Nii Foundation and administered by AAAI.</p>
<p>Edward Feigenbaum is a Kumagai Professor of Computer Science Emeritus at Stanford University. Feigenbaum earned his Ph.D at Carnegie Mellon University from 1956&ndash;1959. In the 1960s and 1970s, he was a pioneer in AI research as experimental computer science, and in the applications of AI research. In 1986, he was elected to the National Academy of Engineering, and in 1995, he received computer science's highest research honor &mdash; The ACM Turing Award. Feigenbaum was the second president of the American Association for Artificial Intelligence, serving from 1980&ndash;1981, and was elected to AAAI Fellowship in 1990. </p>

<p>The first Feigenbaum Prize was awarded in 2011 in conjunction with the Twenty-Fifth Annual AAAI Conference on Artificial Intelligence (AAAI-11), held August 7&ndash;11, in San Francisco, California. The next Feigenbaum Prize will be awarded in 2021. The nomination period will open in June 2020.</p>

