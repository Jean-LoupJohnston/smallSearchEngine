<h2 class="backgrnd12"><span class="text1">AAAI Senior Member Status</span></h2>
<div class="content">
<p>Senior Member status is designed to recognize AAAI members who have achieved significant accomplishments within the field of artificial intelligence. To be eligible for nomination for Senior Member, candidates must be consecutive members of AAAI for at least five years and have been active in the professional arena for at least ten years. Applications should include information that details the candidate's scholarship, leadership, and/or professional service.
</p><p>AAAI members who meet these qualifications are welcome to apply for Senior Member grade by completing the<a href="https://aaaiforms.wufoo.com/forms/mrqvkqq1gowly1/" rel="nofollow"> online application.</a> At least two references, one of which must be written by a AAAI Fellow or Senior Member, must accompany the application. References should be submitted by colleagues who know the candidate, and are familiar with their work and accomplishments. </p>

<p>Individuals selected for Senior Member status will receive a certificate and pin at the annual AAAI conference (or by mail if not available to attend in person), and will be listed on the AAAI website and in <i>AI Magazine.</i> A letter reporting the honor will be sent to the member's employer on the achievement of senior member grade (per request of the newly elected senior member). Senior members can serve as a reference for other applicants for AAAI Senior grade.</p>

<p>Senior Member applications will be reviewed by a subcommittee of the AAAI Awards Committee, which is chaired by the immediate Past President of the Association. All applications and references must conform to the requirements listed on the form, and must be received by 11:59 PM, September 27, 2019. No late or incomplete applications will be reviewed.</p>

<p>Each year a maximum of 25 members will be elected to the Senior status; individuals whose applications are not successful during the first round will be notified and they may choose to have their applications reviewed the following year as long as their memberships stay current. After two years of consideration, individuals must wait one year before applying again.</p>

<p>For information regarding the Senior Member program, please send an email to  &#115;&#101;&#110;&#105;&#111;&#114;&#109;&#101;&#109;&#98;&#101;&#114;&#50;&#48;&#64;&#97;&#97;&#97;&#105;&#46;&#111;&#114;&#103;.</p>

<p>We look forward to your participation in the AAAI Senior member program.</p>

