<h2><a name="1">For Authors of Conference, Symposia, and Workshop Papers</a></h2>
<p>AAAI provides instructions and forms for authors who have an accepted paper in a Proceedings or Technical Report that will be formally published by AAAI Press. To obtain such information, please download the author kit for your specific conference. The URL for this kit appears in your acceptance letter.</p>
<p>Authors of AAAI Press monographs, edited collections, and <i>AI Magazine</i> authors should consult the instructions on the <a href="../Press/press.php">AAAI Press</a> and <a href="../Magazine/magazine.php"><i>AI Magazine</i></a> Web pages first. These instructions are provided primarily for persons who plan to publish a conference, workshop, or symposium paper</p>
