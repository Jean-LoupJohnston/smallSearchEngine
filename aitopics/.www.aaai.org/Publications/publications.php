<h2 class="backgrnd12"><span class="text1">Welcome to the AAAI Publications Pages!</span></h2>
<div class="content">
<p>From this page, you can access information about all the publications that AAAI produces, along with links to information for authors. 
AAAI publishes nearly one hundred proceedings, technical reports, edited collections, and magazine issues each year, in hardcopy, CD, and electronic form. The AAAI divisions responsible for the bulk of this activity are <i>AI Magazine</i> and AAAI Press. </p>
<div class="hr1">
<hr />
</div>
<h2><i>AI Magazine</i></h2>
<p>Called the "journal of record for the AI community," <i>AI Magazine</i> helps AAAI members stay abreast of significant new research and literature across the entire field of artificial intelligence. This quarterly publication features timely, feature-length articles, consistently crafted to be clear enough to permit specialists to review work outside their particular area of expertise. For more information about AI Magazine, please consult the following:</p>
<ul><li><a href="../Magazine/magazine.php"><i>AI Magazine</i> Home Page</a></li></ul>
<div class="hr1">
<hr />
</div>

<h2>AAAI Press</h2> 
<p>The Association for the Advancement of Artificial Intelligence and The MIT Press established the AAAI Press in 1989 as a publishing imprint founded to serve the information needs of the international AI community. AAAI also offers selected proceedings from other AI-related conferences. These books, generally from AAAI cosponsored conferences, are available directly from AAAI. AAAI also has available an extensive list of technical reports, derived mainly from our workshop and symposia series. For more information about AAAI Press, please consult the following:</p>
<ul><li><a href="../Press/press.php">AAAI Press Home Page</a></li></ul>
<div class="hr1">
<hr />
</div>

<h2>AAAI Digital Library</h2> 
<p>The Association for the Advancement of Artificial Intelligence established the digital library of AAAI publications shortly after creating a website in 1995. In this location, members of the scientific community and the general public can freely access most of papers published by AAAI, including technical reports, proceedings papers, and most issues of AI Magazine. For more information about the AAAI digital library, please consult the following:</p>
<ul><li><a href="../Library/library.php">AAAI Digital Library Home Page</a></li></ul>
