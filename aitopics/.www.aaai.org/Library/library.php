<h2 class="backgrnd12"><span class="text1">Welcome to the AAAI Digital Library!</span></h2>
<div class="content">

<p>We extend a special welcome to our guests. From this page, the AI community has full access to most of the publications of the Association for the Advancement of Artificial Intelligence &mdash; in digital form.
Access is provided by publication contents, author, title, and subject indices, as well as a new search engine. Publications and papers are added monthly, so please visit often!</p>
<h3>Journal Articles</h3>
<p>In this location, AAAI provides full text and abstracts of articles from <i>The Journal of Artificial Intelligence Research.</i></p> <ul><li><a href="http://www.aaai.org/Library/JAIR/jair-library.php">View List of Journal Articles</a></li></ul>
<h3>Magazine Articles</h3>
<p>In this location, AAAI provides full text and abstracts of the complete set of articles from <i>AI Magazine.</i></p> <ul><li><a href="http://www.aaai.org/Library/Magazine/magazine-library.php">View List of <i>AI Magazine</i> Articles</a></li></ul>
<h3>Proceedings and Conference Papers</h3>
<p>The AAAI Digital library includes the full set of electronic AAAI proceedings papers &mdash; abstracts and full text of <i>all</i> the published papers. Also included are papers from selected ISMB, AIPS, ICAPS, ICML, FLAIRS, ICML, MAICS, IAAI, KDD, and other conferences.</p>
<ul><li><a href="http://www.aaai.org/Library/conferences-library.php">View List of Conference Proceedings</a></li>
</ul>
<h3>Technical Reports</h3>
<p>All AAAI technical reports (papers and abstracts from Spring and Fall AAAI Symposia, the AAAI Workshop series, and other related meetings) are available. (Working notes are not citable and are unavailable in any form.)</p>
<ul><li><a href="http://www.aaai.org/Library/Symposia/symposia-library.php">AAAI Symposia Papers</a></li>
<li><a href="http://www.aaai.org/Library/Workshops/workshops-library.php">AAAI Workshop Papers</a></li>
<li>AAAI Conference Reports</li></ul>
<p><i>(Conference technical reports may be found under the proceedings for the conference in which they were issued.)</i></p>

