<h2 class="backgrnd12"><span class="text1">Proceedings of the AAAI Conference on Artificial Intelligence and Interactive Digital Entertainment</h2>
<div class="content">
<p><i>Sponsored by the Association for the Advancement of Artificial Intelligence</i></p>

<p>AIIDE is intended to be the definitive point of interaction between entertainment software developers interested in AI and academic and industrial AI researchers. Sponsored by the Association for the Advancement of Artificial Intelligence (AAAI), the conference is targeted at both the research and commercial communities, promoting AI research and practice in the context of interactive digital entertainment systems with an emphasis on commercial computer and video games.</p>
