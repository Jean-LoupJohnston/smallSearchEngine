<h2 class="backgrnd12"><span class="text1">Journal of Artificial Intelligence Research</span></h2>
<div class="content">

<p>Established in 1993, the <i>Journal of Artificial Intelligence Research</i> includes refereed research articles, survey articles, and technical notes. The scientific journal encompasses all areas of artificial intelligence, including automated reasoning, cognitive modeling, knowledge representation, learning, natural language, neural networks, perception, and robotics.</p>
