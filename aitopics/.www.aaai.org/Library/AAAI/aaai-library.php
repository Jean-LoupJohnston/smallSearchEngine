<h2 class="backgrnd12"><span class="text1">AAAI Conference on Artificial Intelligence</span></h2>

<div class="content">

<p>The AAAI Conference on Artificial Intelligence &mdash; AAAI&rsquo;s primary summer conference &mdash; promotes theoretical and applied AI research as well as intellectual interchange among researchers and practitioners. The technical program features substantial, original research and practices. Conference panel discussions and invited presentations identify significant social, philosophical, and economic issues influencing AI&rsquo;s development throughout the world.</p>
