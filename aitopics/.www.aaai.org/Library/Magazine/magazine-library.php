<h2 class="backgrnd12"><span class="text1">AI Magazine Issues</h2>
<div class="content">

<p>Called the "journal of record for the AI community," <i>AI Magazine</i> has been published since 1980. Tables of contents for all issues are available. Abstracts and full-text is also available for all issues (a few articles may not be available because of copyright restrictions).</p>
<p><i>AI Magazine</i> is published quarterly in March (Spring), June (Summer), September (Fall) and December (Winter). To view the articles from a selected issue, select from the following list.</p>

