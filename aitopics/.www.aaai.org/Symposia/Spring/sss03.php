<h2 class="backgrnd12"><span class="text1">AAAI 2003 Spring Symposium Series</span></h2>
<div class="content">

<p>The 2003 AAAI Spring Symposium series was held March 24&ndash;26, 2003 at Stanford University in Palo Alto, California. The topics of the eight symposia were:</p>

<ul>
<li> Agent-Mediated Knowledge Management</li>
<li>Computational Synthesis: From Basic Building Blocks to High Level Functionality</li>
<li>Foundations and Applications of Spatio-Temporal Reasoning (FASTR)</li>
<li>Human Interaction with Autonomous Systems in Complex Environments</li>
<li>Intelligent Multimedia Knowledge Management</li>
<li>Logical Formalization of Commonsense Reasoning</li>
<li>Natural Language Generation in Spoken and Written Dialogue</li>
<li>New Directions in Question Answering Motivation</li>
</ul>
<p>For more information about the 2003 AAAI Spring Symposium Series, you should consult the following:</p> 
<ul>
<li><a href="http://www.aaai.org/Symposia/Spring/sss03proposals.pdf">Call for Proposals</a> (2003)</li> 
<li><a href="http://www.aaai.org/Symposia/Spring/sss03participation.pdf">Call for Participation</a> (2003)</li> 
<li><a href="http://www.aaai.org/Symposia/Spring/sss03registration.pdf">Registration</a> (2003)</li>
</ul>
<p>Individual papers presented at the symposium are available in the <a href="http://www.aaai.org/Library/Symposia/symposia-library.php">AAAI digital library.</a></p>
