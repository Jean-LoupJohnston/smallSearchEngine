<h3><a href="../../Conferences/AAAI">The AAAI Symposium on Educational Advances in Artificial Intelligence</a></h3>
<p>The  EAAI symposia</a> provide a venue for researchers and educators to discuss pedagogical issues and share resources related to teaching AI and using AI in education across a variety of curricular levels (K-12 through postgraduate training), with a natural emphasis on undergraduate and graduate teaching and learning. The symposium is held in conjunction with the AAAI Conference on Artificial Intelligence. Papers are published in the AAAI Conference proceedings.</p>
