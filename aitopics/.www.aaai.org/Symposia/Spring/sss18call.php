<h2 class="backgrnd12"><span class="text1">AAAI 2018 Spring Symposium Series</span></h2>
<div class="content">
<p><b>March 26&ndash;28, 2018</b></p>
<p><i>Sponsored by the Association for the Advancement of Artificial Intelligence<br />
In cooperation with the Stanford University Computer Science Department</i></p>
<div class="hr1">
<hr />
</div>
<p><b>June 15, 2017: Proposals Due</b></p>

<p>AAAI invites proposals for the 2018 Spring Symposium Series, to be held Monday â¬ Wednesday, March 26&ndash;28 at Stanford University in Palo Alto, California. The Spring Symposium Series is an annual set of meetings run in parallel at a common site. It is designed to bring colleagues together in an intimate forum while at the same time providing a significant gathering point for the AI community. The two and one half day format of the series allows participants to devote considerably more time to feedback and discussion than typical one-day workshops. It is an ideal venue for bringing together new communities in emerging fields.</p>

<p>The symposia are intended to encourage presentation of speculative work and work in progress, as well as completed work. Ample time should be scheduled for discussion. Novel programming, including the use of target problems, open-format panels, working groups, or breakout sessions, is encouraged. AAAI Technical Reports will be prepared, and distributed to the participants. Most participants of the symposia will be selected on the basis of statements of interest or abstracts submitted to the symposia chairs; some open registration will be allowed. All symposia are limited in size, and participants will be expected to attend a single symposium.</p>

<p>Proposals for symposia should be between two and five pages in length, and should contain the following information:</p>

<p><i>A title for the symposium.</i></p>
<p><i>A description of the symposium,</i> identifying specific areas of interest, and general symposium format.</p>
<p><i>The names and addresses (physical and electronic) of the organizing committee:</i> preferably three or more people at different sites, all of whom have agreed to serve on the committee.</p>
<p><i>A list of potential participants</i> that have been contacted and that have expressed interest in participating. A common way of gathering potential participants is to send email messages to email lists related to the topic(s) of the symposium. Note that potential participants need not commit to participating, only state that they are interested.</p>

<p>Ideally, the entire organizing committee should collaborate in producing the proposal. If possible, a draft proposal should be sent out to a few of the potential participants and their comments solicited. Approximately eight symposia on a broad range of topics within and around AI will be selected for the 2017 Spring Symposium Series.</p>

<p>The criteria for acceptance of proposals include the following:</p>

<p>Perceived interest to the AAAI community.</i> Although AAAI encourages symposia that cross disciplinary boundaries, a symposium must be of interest to some subcommunity of the AAAI membership. Symposia that are of interest to a broad range of AAAI members are also preferred. Appropriate number of potential participants. Although the series supports a range of symposium sizes, the target size is around 40-60 participants. </p>
<p><i>Lack of a long-term ongoing series of activities on the topic.</i> The Spring Symposium Series is intended to nurture emerging communities and topics, so topics that already have yearly conferences or workshops are inappropriate.</p> 
<p><i>An appropriate organizing committee.</i> The organizing committee should have (1) good technical knowledge of the topic, (2) good organizational skills, and (3) connections to the various communities from which they intend to draw participants. Committees for cross-disciplinary symposia must adequately represent all the disciplines to be covered by the symposium.</p>

<p>Accepted proposals will be distributed as widely as possible over the subfields of AI, and balanced between theoretical and applied topics. Symposia bridging theory and practice and those combining AI and related fields are particularly solicited.</p>

<p>Symposium proposals should be submitted as soon as possible, but no later than June 15, 2017. Proposals that are submitted significantly before this deadline can be in draft form. Comments on how to improve and complete the proposal will be returned to the submitter in time for revisions to be made before the deadline. Notifications of acceptance or rejection will be sent to submitters around July 1, 2017. The submitters of accepted proposals will become the chair of the symposium, unless alternative arrangements are made. The symposium organizing committees will be responsible for the following tasks:</p>

<ul>
	<li>Writing short calls for participation and registration, which AAAI will distribute to its membership</li>
	<li>Additional publicity of the symposium, especially to potential audiences from outside the AAAI community</li>
	<li>Reviewing requests to participate in the symposium and determining symposium participants</li>
	<li>Submitting accepted author lists and text for the frontmatter of the symposium technical report</li>
	<li>Scheduling the activities of the symposium</li>
	<li>Preparing a short review of the symposium, to be printed in AI Magazine</p></li>
</ul>

<p>AAAI will provide logistical support, will take care of all local arrangements, and will arrange for reproducing and distributing the technical reports. Please submit your symposium proposals by electronic mail (no postal submissions), and inquiries concerning symposia, to the symposium chair and cochair:</p>

<p>Christopher Geib (Drexel University, USA) <br />
Ron Petrick (Heriot-Watt University, UK)<br />
SSS-18 Chair and Cochair<br />
&#115;&#115;&#115;&#49;&#56;&#99;&#104;&#97;&#105;&#114;&#115;&#64;&#97;&#97;&#97;&#105;&#46;&#111;&#114;&#103;</p>

<p>All proposals will be reviewed by the AAAI Symposium Committee, including the chair, cochair, associate chair: Mike Genesereth (Stanford University); and AAAI council members Sonia Chernova (WPI), Vincent Conitzer (Duke University), and Boi Faltings (EPFL).</p>

<p>General inquiries regarding the symposium series should be directed to AAAI at &#115;&#115;&#115;&#49;&#56;&#64;&#97;&#97;&#97;&#105;&#46;&#111;&#114;&#103;.</p>



