<h3><a name="Transportation">Ground Transportation and Parking</a></h3>

<p>This information is the best available at time of publication. Fares and routes change frequently. Please check by telephoning the appropriate numbers below for the most up-to-date information. </p>

<h4><a href="http://www.supershuttle.com" rel="nofollow">SuperShuttle</a></h4>
<p>24 hour van service to and from San Francisco to Palo Alto. The shared ride fare from San Francisco Airport to Palo Alto is $29 per person one-way plus $10 per additional passenger. Cash or major credit cards only. For reservations write to &#114;&#101;&#115;&#101;&#114;&#118;&#97;&#116;&#105;&#111;&#110;&#115;&#64;&#115;&#117;&#112;&#101;&#114;&#115;&#104;&#117;&#116;&#116;&#108;&#101;&#46;&#110;&#101;&#116; or call (800) BLUE-VAN (800-258-3826).  </p>

<h4>Stanford Marguerite Shuttle</h4>
<p>The Stanford University Marguerite Shuttle Bus provides service from several points along El Camino Real, the CalTrain station, and other surrounding locations to the Stanford Oval as well as transportation around the Stanford Campus. For route and schedule information, see the <a href="http://transportation.stanford.edu/marguerite/" rel="nofollow">Marguerite Shuttle Bus web page.</a>
</p>

<h4>CalTrain</h4>
<p>
CalTrain runs between San Francisco and San Jose, with stops in Palo Alto starting at 5:00 AM with the last train leaving San Francisco at 11:59 PM (weekdays). For up-to-date fare information and timetables, please visit the <a href="http://www.caltrain.org/" rel="nofollow">CalTrain website</a> or call 1-800-660-4287. 
</p>

<h4>Parking</h4>
<p>AAAI has arranged for symposium attendee parking in the Galvez Lot on the Stanford campus, March 26&ndash;28, at a discounted cost of $8.00 per day. All symposium attendees will receive a parking coupon code, which may be used to secure the discounted parking rate for their numbered space in the Galvez lot. Attendees will pay the fee directly at one of the pay machines at the lot. The machines accept MasterCard, Visa, or exact cash/coins only. A map of the Galvez lot location will be mailed to all registrants prior to the symposium. Registrants can also refer to the parking map available on the <a href="https://visit.stanford.edu/plan/" rel="nofollow">Stanford University's Visitor Information page.</a> Note that Stanford has very strict parking guidelines for visitors. The AAAI Spring Symposium parking coupon is <b>valid only in the Galvez lot. </b></p>
</p>

<p><i>Note:</i>If you park in the SSS-18 designated parking lot, you will need to take the campus shuttle (Marguerite) to the Spring Symposium registration area and sessions. Please allow an extra thirty minutes travel time in your schedule for the shuttle. </p>

