<h2 class="backgrnd12"><span class="text1">AAAI 2018 Spring Symposium Series</span></h2>
<div class="content">
<p><b>March 26&ndash;28, 2018</b></p>
<p><i>Sponsored by the Association for the Advancement of Artificial Intelligence<br />
In cooperation with the Stanford University Computer Science Department</i></p>
<div class="hr1">
<hr />
</div>
<h3><a href="https://www.regonline.com/sss18">Registration Form</a></h3>

<div class="hr1"><hr /></div>
<p><b><a href="https://www.aaai.org/Publications/Templates/AuthorKit18.zip" rel="nofollow">Author Kit</a></b></p>
<div class="hr1"><hr /></div>
<p><b>Important Deadlines</b></p>


<ul>
	<li>January 29, 2018: Accepted camera-ready copy due to AAAI.</li>
<li>February 17, 2017: Accepted authors, invited speakers, symposium participants, and other invited attendees must register.</li>
</ul>

<div class="hr1"><hr /></div>
<p>
The Association for the Advancement of Artificial Intelligence, in cooperation with Stanford University's Department of Computer Science, is pleased to present the 2018 Spring Symposium Series, to be held Monday through Wednesday, March 26-28, 2018 at Stanford University. The titles of the seven symposia are as follows:
</p>

<ul>
<li><a href="http://www.aaai.org/Symposia/Spring/sss18symposia.php#ss01"><b> AI and Society: Ethics, Safety and Trustworthiness in Intelligent Agents</b></a></li>
<li><a href="http://www.aaai.org/Symposia/Spring/sss18symposia.php#ss02"><b> Artificial Intelligence for the Internet of Everything</b></a></li>
<li><a href="http://www.aaai.org/Symposia/Spring/sss18symposia.php#ss03"><b> Beyond Machine Intelligence: Understanding Cognitive Bias and Humanity for Well-Being AI</b></a></li>
<li><a href="http://www.aaai.org/Symposia/Spring/sss18symposia.php#ss04"><b> Data Efficient Reinforcement Learning</b></a></li>
<li><a href="http://www.aaai.org/Symposia/Spring/sss18symposia.php#ss05"><b> The Design of the User Experience for Artificial Intelligence (the UX of AI)</b></a></li>
<li><a href="http://www.aaai.org/Symposia/Spring/sss18symposia.php#ss06"><b> Integrated Representation, Reasoning, and Learning in Robotics</b></a></li>
<li><a href="http://www.aaai.org/Symposia/Spring/sss18symposia.php#ss07"><b> Learning, Inference, and Control of Multi-Agent Systems</b></a></li>
</ul>

<div class="hr1"><hr /></div>
<h3 class="text2"><a href="https://www.regonline.com/sss18">Registration</a></h3>
<div class="hr1">
<hr />
</div>

<p>ALL ATTENDEES MUST PREREGISTER. All accepted authors, invited speakers, symposium participants, and other invited attendees must register by February 9, 2018. Participation will be open to active participants as well as interested individuals on a first-come, first-served basis. All registrations should be completed by March 2, 2018. Registrations will be accepted until March 26 via the online form, but earlier registration is preferred. The conference registration fee includes admission to one symposium, one copy of the electronic technical report, coffee breaks, and the opening reception. </p>

<h4>Fee Schedule</h4><ul>
	<li>Member: $345.00</li>
	<li>Nonmember: $520.00</li>
	<li>Student Member: $175.00</li>
	<li>Nonmember student: $285.00</li>
</ul>

<h4>AAAI Platinum Registration</h4>
<p>(Includes 1, 3, or 5 years of AAAI membership, plus the conference registration fee) </p>
<ul>
	<li>Regular One-Year $490.00</li>
	<li>Regular Three-Year $780.00</li>
	<li>Regular Five-Year $1070.00</li>
	<li>Student (one-year only) $250.00</li>
</ul>

<h4>Payment Information</h4>
<p>
Payment of registration fees is required at the time of registration. Checks and bank transfers must be in US dollars. Checks (drawn on US bank) or international money orders should be made out to AAAI. American Express, MasterCard, VISA, and Discover are also accepted. Registration applications must be submitted online via the URL below. If you qualify for student rates, you will be asked to submit your proof of student status via the online form.</p>

<p>The registration form is available <a href="https://www.regonline.com/sss18" rel="nofollow">online</a>. </p>

