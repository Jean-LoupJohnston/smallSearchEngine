<h2 class="backgrnd12"><span class="text1">AAAI 2013 Spring Symposia</span></h2>
<div class="content">
<h5><a href="http://www.regonline.com/aaaisss13" rel="nofollow">Registration Is Now Open!</a></h5>
<p>The Association for the Advancement of Artificial Intelligence is pleased to present the AAAI 2013 Spring Symposium Series, to be held Monday through Wednesday, March 25-&ndash;27. The titles of the eight symposia are as follows:</p>

<ul>
<li><a href="http://www.aaai.org/Symposia/Spring/sss13symposia.php#ss01"><b>Analyzing Microtext</b></a></li>
<li><a href="http://www.aaai.org/Symposia/Spring/sss13symposia.php#ss02"><b>Creativity and (Early) Cognitive Development</b></a></li>
<li><a href="http://www.aaai.org/Symposia/Spring/sss13symposia.php#ss03"><b>Data Driven Wellness: From Self-Tracking to Behavior Change</b></a></li>
<li><a href="http://www.aaai.org/Symposia/Spring/sss13symposia.php#ss04"><b>Designing Intelligent Robots: Reintegrating AI II</b></a></li>
<li><a href="http://www.aaai.org/Symposia/Spring/sss13symposia.php#ss05"><b>Lifelong Machine Learning</b></a></li>
<li><a href="http://www.aaai.org/Symposia/Spring/sss13symposia.php#ss06"><b>Shikakeology: Designing Triggers for Behavior Change</b></a></li>
<li><a href="http://www.aaai.org/Symposia/Spring/sss13symposia.php#ss07"><b>Trust and Autonomous Systems</b></a></li>
<li><a href="http://www.aaai.org/Symposia/Spring/sss13symposia.php#ss08"><b>Weakly Supervised Learning from Multimedia</b></a></li>
</ul>



<p>An informal reception will be held on Monday, March 25. A general plenary session, in which the highlights of each symposium will be presented, will be held on Tuesday, March 26.</p>
<p>Symposia are generally limited to 40&ndash;60 participants each. Participation will be open to active participants as well as other interested individuals on a first-come, first-served basis. Each participant will be expected to attend a single symposium. AAAI technical reports will be distributed to participants in electronic format and posted in the AAAI Digital Library. Technical reports will be prepared and distributed in electronic format to the participants in each symposium.</p>
<p>To register, please fill out the <a href="http://www.regonline.com/aaaisss13" rel="nofollow">online registration form.</a> If you prefer to register by mail, please complete the <a href="http://www.aaai.org/Symposia/Spring/sssregform.pdf">PDF version of the registration form</a> and send it along with payment to: </p>

