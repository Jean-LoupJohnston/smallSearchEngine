<h3 class="text1">Ground Transportation and Parking</h3>

<p>
This information is the best available at time of publication. Fares and routes change frequently. Please check by telephoning the appropriate numbers below for the most up-to-date information.
</p>

<h3 class="text2"><i>South and East Bay Shuttle</i></h3>
<p>
Van service from San Francisco Airport (SFO) to Palo Alto is $21 for one-person, shared van, one way, and from San Jose Airport (SJC) to Palo Alto is $39 for one-person, shared van, one way. For reservations call 408-559-9477 or 1-800-548-4664. (<a href="http://www.southandeastbayairportshuttle.com/" rel="nofollow">Website.</a>)
</p>

<h3 class="text2"><i>SuperShuttle</i></h3>
<p>
24 hour van service to and from San Francisco to Palo Alto. The shared ride fare from San Francisco Airport to Palo Alto is $26 per person one-way plus $10 per additional passenger. Cash or major credit cards only. For reservations write to reservations@supershuttle.net or call (800) BLUE-VAN (800-258-3826).  (<a href="http://www.supershuttle.com" rel="nofollow">Website.</a>)
</p>

<h3 class="text2"><i>Stanford Marguerite Shuttle</i></h3>
<p>
The Stanford University Marguerite Shuttle Bus provides service from several points along El Camino Real, the CalTrain station, and other surrounding locations to the Stanford Oval as well as transportation around the Stanford Campus. For route and schedule information, see the Stanford <a href="http://transportation.stanford.edu/marguerite/" rel="nofollow">website.</a>
</p>

<h3 class="text2"><i>CalTrain</i></h3>
<p>
CalTrain runs between San Francisco and San Jose, with stops in Palo Alto starting at 5:00 AM with the last train leaving San Francisco at 11:59 PM (weekdays). For up-to-date fare information and timetables, please visit the CalTrain <a href="http://www.caltrain.org/" rel="nofollow"> website</a>  or call 1-800-500-4636.
</p>

<h3 class="text2"><i>Parking</i></h3>
<p>
AAAI has arranged for symposium attendee parking in the Galvez Lot on the Stanford campus, March 25&ndash;27, at a discounted cost of $8.00 per day. All symposium attendees will receive a parking coupon code, which may be used to secure the discounted parking rate for their numbered space in the Galvez lot. Attendees will pay the fee directly at one of the nine (9) new pay machines at the lot. The machines accept MasterCard, Visa or exact cash only. A map of the Galvez lot location will be mailed to all registrants prior to the symposium. Registrants can also refer to the parking map available at the <a href="http://www.stanford.edu/dept/visitorinfo/plan/maps.html" rel="nofollow">Stanford website.</a> Note that Stanford has very strict parking guidelines for visitors. The AAAI Spring Symposium parking coupon is valid in the Galvez lot only.
</p>

<p>
<i>Note:</i> If you park in the SSS-13 designated parking lot, you will need to take the campus shuttle (Marguerite) to the Spring Symposium registration area and sessions. Please allow an extra thirty minutes travel time in your schedule for the shuttle.
</p>
