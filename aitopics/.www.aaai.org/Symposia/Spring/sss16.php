<h4>Parking</h4>
<p>
AAAI has arranged for symposium attendee parking in the Galvez Lot on the
Stanford campus, March 21-13, at a discounted cost of $8.00 per day. All
symposium attendees will receive a parking coupon code, which may be used
to secure the discounted parking rate for their numbered space in the
Galvez lot. Attendees will pay the fee directly at one of the nine (9) pay
machines at the lot. The machines accept MasterCard, Visa, or exact
cash/coins only. A map of the Galvez lot location will be mailed to all
registrants prior to the symposium. Registrants can also refer to the
<a href="http://www.stanford.edu/dept/visitorinfo/plan/maps.html" rel="nofollow">parking map.</a> Note that Stanford
has very strict parking guidelines for visitors. The AAAI Spring Symposium
parking coupon is valid in the Galvez lot only.
</p>

<p>
<i>Note:</i> If you park in the SSS-16 designated parking lot, you will need to
take the campus shuttle (Marguerite) to the Spring Symposium registration
area and sessions. Please allow an extra thirty minutes travel time in your
schedule for the shuttle.
</p>

