<p>
The Association for the Advancement of Artificial Intelligence, in cooperation with Stanford University's Department of Computer Science, is pleased to present the 2016 Spring Symposium Series, to be held Monday through Wednesday, March 21-23, 2016 at Stanford University. The titles of the seven symposia are:
</p>
<ul>
<li><a href="http://www.aaai.org/Symposia/Spring/sss16symposia.php#ss01"><b>AI and the Mitigation of Human Error: Anomalies, Team Metrics and Thermodynamics</b></a></li>

<li><a href="http://www.aaai.org/Symposia/Spring/sss16symposia.php#ss02"><b>Challenges and Opportunities in Multiagent Learning for the Real World</b></a></li>

<li><a href="http://www.aaai.org/Symposia/Spring/sss16symposia.php#ss03"><b>Enabling Computing Research in Socially Intelligent Human-Robot Interaction: A Community-Driven Modular Research Platform</b></a></li>

<li><a href="http://www.aaai.org/Symposia/Spring/sss16symposia.php#ss04"><b>Ethical and Moral Considerations in Non-Human Agents</b></a></li>

<li><a href="http://www.aaai.org/Symposia/Spring/sss16symposia.php#ss05"><b>Intelligent Systems for Supporting Distributed Human Teamwork</b></a></li>

<li><a href="http://www.aaai.org/Symposia/Spring/sss16symposia.php#ss06"><b>Observational Studies through Social Media and Other Human-Generated Content</b></a></li>

<li><a href="http://www.aaai.org/Symposia/Spring/sss16symposia.php#ss07"><b>Well-Being Computing: AI Meets Health and Happiness Science</b></a></li></ul>
<div class="hr1">
<hr />
</div>

<p>An informal reception will be held on Monday, 
March 21. A general plenary session, in which the 
highlights of each symposium will be presented, 
will be held on Tuesday, March 22.</p>
<div class="hr1">
<hr />
</div>

<p><b><a href="http://www.sentient.ai/come-celebrate-the-life-of-ai-pioneer-marvin-minsky/" rel="nofollow">Marvin Minsky Remembrance at AAAI Spring Symposia</a></b></p>
<p>7 PM  &ndash; 9 PM, Building 420 Room 040 (Jordan Hall)<br />Stanford University<br />
Tuesday March 22 2016 </p>

<p>This symposium is organized to give a place for people who knew and learned from Marvin Minsky's work to share memories of how he touched them. The symposium is open to all</p>

<p>We will focus on some of the specialness he shared:

<ul>
	<li>Personal/ relationship stories that teach us how to think, do research and play with others.</li>

	<li>Professional experience stories of getting inspiration from Marvin</li>

	<li>Scientific stories that tell what Marvin accomplished</li>

	<li>Historical stories that help us understand legacy and paths that occurred</li>
</ul>

