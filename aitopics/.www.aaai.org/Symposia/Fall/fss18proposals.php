<h2 class="backgrnd12"><span class="text1">AAAI 2018 Fall Symposium Series</span></h2>
<div class="content">

<h3>Call for Proposals</h3>

<p><i>Sponsored by the Association for the Advancement of Artificial Intelligence</i><br />Washington, DC &nbsp; &nbsp;  October 18&ndash;-20, 2018 </p>

<ul>
	<li>PROPOSAL DUE: April 20, 2018</li>
</ul>
<div class="hr1">
<hr />
</div>

<p>
AAAI invites proposals for the 2018 Fall Symposium Series, to be held Thursday - Saturday, October 18-20 at the Westin Arlington Gateway in Arlington, Virginia adjacent to Washington, DC. The Fall Symposium Series provides the AI community with a unique venue in which researchers from different areas of AI can accomplish the following:
</p>

<ul>
	<li> Present speculative work and work in progress</li>
	<li> Hold focused discussions over several days</li>
	<li> Build new communities for emerging disciplines</li>
	<li>Build ties between existing disciplines</li>
</ul>


<p>
The series brings together a set of two and one-half day symposia at a common site, providing a unique and intimate forum for colleagues in a given discipline. The series also provides an important gathering point for the AI community as a whole. Efforts are made to keep registration and lodging costs low so that graduate students can afford to attend. The series is designed to minimize the administrative burden on the organizing committees of the individual symposia. AAAI manages registration and local arrangements, and provides organizational support, publicity within the AAAI community, and an optional submission, review, and paper distribution site via EasyChair (executive level) for interested organizers. AAAI will no longer be producing symposium technical reports, so organizers are free to arrange for final publication of their symposium papers elsewhere.
</p>

<p>
The series runs twice yearly (spring and fall). Approximately eight symposium topics will be selected for the 2018 Fall Symposium Series. Accepted proposals will be distributed as widely as possible over the subfields of AI, and balanced between theoretical and applied topics. Symposia bridging theory and practice and those combining AI and related fields are particularly solicited.
</p>
<div class="hr1">
<hr />
</div>

<p>
Proposals for symposia should be between two and five pages in length, and should contai the following:
</p>

<p>
<i>A title</i> for the symposium.
</p>

<p>
<i>A description of the symposium,</i> identifying specific areas of interest, and, optionally, general symposium format. The symposia are intended to encourage presentation of speculative work and work in progress, as well as completed work. Therefore, ample time should be scheduled for discussion. Novel programming, including the use of target problems, open-format panels, working groups, or breakout sessions, is encouraged.
</p>

<p>
<i>The names and (physical and electronic) addresses of the organizing committee,</i> preferably three or more people at different sites, all of whom have agreed to serve on the committee.
</p>

<p>
<i>A list of potential participants who have been contacted and who have expressed interest in participating.</i>  A common way of gathering potential participants is to send email messages to email lists related to the topic(s) of the symposium. Note that potential participants need not commit to participating, only state that they are interested.
</p>

<p>
Ideally, the entire organizing committee should collaborate in producing the proposal. If possible, a draft proposal should be sent out to a few of the potential participants and their comments solicited.
</p>

<p>
Proposals will be reviewed by the Symposium Series Standing Committee (Chair: Christopher Geib, Drexel University; Cochair: Ron Petrick, Heriot-Watt University; Mausam, Indian Institute of Technology Delhi; and Claire Monteleoni, George Washington University).
</p>
<div class="hr1">
<hr />
</div>

<p>
The criteria for acceptance of proposals include the following: 
</p>

<p>
<i>Perceived interest to the AAAI community.</i> Although AAAI encourages symposia that cross disciplinary boundaries, a symposium must be of interest to some community within the AAAI membership. Symposia that are of interest to a broad range of AAAI members are, of course, also encouraged.
</p>

<p>
<i>Expected size.</i> Although the series supports a range of symposium sizes (roughly 25-75), the target size is around 40 participants.
</p>

<p>
<i>Lack of established venues on the topic.</i> The AAAI Symposium Series is intended to nurture emerging communities and topics, so topics that already have yearly conferences or workshops are inappropriate.
</p>

<p>
<i>Qualifications of the organizing committee.</i> The organizing committee should have (1) a good technical knowledge of the topic, (2) good organizational skills, and (3) connections to the various communities from which they intend to draw participants. Committees for cross-disciplinary symposia should adequately represent the disciplines to be covered by the symposium.
</p>
<div class="hr1">
<hr />
</div>

<p>
Symposium proposals should be submitted as soon as possible, but no later than April 20, 2018. Proposals that are submitted significantly before this deadline can be in draft form. Comments on how to improve and complete the proposal will be returned to the submitter in time for revisions to be made before the deadline. Notifications of acceptance or rejection will be sent to submitters around May 11, 2018.
</p>

<p>
The submitters of accepted proposals will become the chairs of the symposium, unless alternative arrangements are made. Symposium organizing committees are responsible for:
</p>


<ul>
	<li> Writing short calls for participation and registration, which AAAI will distribute to its membership</li>
	<li> Additional publicity of the symposium, especially to potential audiences from outside the AAAI community</li>
	<li> Reviewing requests to participate in the symposium and determining symposium participants</li>
	<li> Arranging for the distribution of symposium papers to attendees via EasyChair or another publication venue</li>
	<li> Scheduling the activities of the symposium</li>
	<li> Preparing a short review of the symposium, to be printed in AI Magazine.</li>
</ul>


<p>
Please submit (preferably by electronic mail) your symposium proposals, and inquiries concerning symposia, to:
</p>

<p>
Christopher Geib (SIFT)
Ron Petrick (Heriot-Watt University)
FSS-18 Chair and Cochair
