<h3>Special Plenary Events at the AAAI 2019 Fall Symposium Series</h3>
<p>
The 2019 Plenary Session, to be held November 8 at 6:00 PM, will have a modified format this year. The session will feature the following two special presentations, followed by brief highlights of the 8 symposia:
</p>
<p><b>A 20-Year Roadmap for AI Research</b><br />
<i>Bart Selman</i><br />Cornell University<p>

<p>
Bart Selman will summarize the findings of a recently completed, community-driven study to set research directions for AI Research for the next 20 years. Over 100 prominent AI researchers contributed to the study, which was sponsored by the US-based Computing Community Consortium (CCC), working with the National Science Foundation (NSF) and the Association for the Advancement of AI (AAAI). He cochaired the study together with Yolanda Gil (University of Southern California and AAAI President).
</p>

<p>
Bart Selman is the Joseph C. Ford Professor of Engineering and Computer Science at Cornell University. Selman is the president-elect of the Association for the Advancement of Artificial Intelligence (AAAI), the main international professional society for AI researchers and practitioners. He is also the cochair of a national study to determine the Roadmap for AI research to guide US government research investments in AI. Selman was previously at AT&amp;T Bell Laboratories. His research interests include artificial intelligence, computational sustainability, efficient reasoning procedures, machine learning, deep learning, reinforcement learning, planning, knowledge representation, and connections between computer science and statistical physics. He has (co)authored over 150 publications, including six best paper awards and two classic paper awards. His papers have appeared in venues spanning <i>Nature, Science, Proceedings of the National Academy of Scences,</i> and a variety of conferences and journals in AI and computer science. He has received the Cornell Stephen Miles Excellence in Teaching Award, the Cornell Outstanding Educator Award, an NSF Career Award, and an Alfred P. Sloan Research Fellowship. He is a Fellow of the American Association for Artificial Intelligence (AAAI), a Fellow of the American Association for the Advancement of Science (AAAS), and a Fellow of the American Association for Computing Machinery (ACM). He is the recipient of the Inaugural IJCAI John McCarthy Research Award, awarded in recognition of outstanding AI researchers at their mid-career stage.
</p>

<p><b>Creating National AI Institutes (and other NSF AI Funding Opportunities)</b><br />

<i>Henry Kautz</i><br />
Division Director, Information and Intelligent Systems (IIS)<br />
Computer and Information Science and Engineering (CISE)<br />
National Science Foundation</p>


<p>On October 8, 2019, the National Science Foundation announced the creation of National Artificial Intelligence Research Institutes, a transformational approach to federal funding for AI. The $200 million program will support both fundamental and use-inspired research in AI, as well as innovative approaches to growing the research pipeline. I will describe how CISE bought together partners across the NSF directorates and many federal agencies in order to pioneer a whole-of-government approach to supporting AI research.  The 2020 solicitation calls for proposals for full institutes on themes of national importance, as well as substantial planning grants for future solicitations. In addition to Institutes, I will describe other opportunities for AI researchers, including the Future of Work at the Human-Technology Frontier, National Robotics Initiative 2.0, the core IIS programs, and more.</p>

<p>Henry Kautz began a rotation as Division Director for IIS in June 2018.  He is a professor and founding director of the Goergen Institute for Data Science at the University of Rochester. He received the 2018 ACM-AAAI Allen Newell Award.</p>

