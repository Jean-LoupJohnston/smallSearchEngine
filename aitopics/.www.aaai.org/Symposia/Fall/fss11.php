<h4>Registration Information</h4>
<p>ALL ATTENDEES MUST PREREGISTER. Each symposium has a limited attendance, with priority given to invited attendees. All accepted authors, symposium participants, and other invited attendees must register by September 16, 2011. After that period, registration will be opened up to the general membership of AAAI and other interested parties. All registrations must be postmarked by October 14, 2011.</p>
<p>
The conference registration fee includes admission to one symposium, one CD of symposium technical report, coffee breaks, and the opening reception.
Checks (drawn on US bank) or international money orders should be made out to AAAI. VISA, MasterCard and American Express are also accepted. Please fill out the attached registration form and mail it with your fee to:<br />
AAAI 2011 Fall Symposium Series<br />
445 Burgess Drive<br />
Menlo Park, CA 94025 USA</p>
