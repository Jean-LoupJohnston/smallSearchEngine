<h2 class="backgrnd12"><span class="text1">AAAI 2011 Fall Symposia</span></h2>
<div class="content">

<p><b><a href="http://www.aaai.org/Forms/fssregistration-form.php">Online Registration</a></b>&nbsp;&nbsp;|&nbsp;&nbsp;
<b><a href="http://www.aaai.org/Symposia/Fall/fssregform.pdf">Mail-in or Fax-in Registration Form</a></b></p>
<p>The Association for the Advancement of Artificial Intelligence is pleased to present the 2011 Fall Symposium Series, to be held Friday through Sunday, November 4&ndash;6, at the Westin Arlington Gateway in Arlington, Virginia. The Symposium Series will be preceded on Thursday, November 3 by a one-day AI funding seminar. The titles of the seven symposia are as follows:</p>

<ul>
<li>Advances in Cognitive Systems</li>
<li>Building Representations of Common Ground with Intelligent Agents</li>
<li>Complex Adaptive Systems: Energy, Information and Intelligence</li>
<li>Multiagent Coordination under Uncertainty</li>
<li>Open Government Knowledge: AI Opportunities and Challenges</li>
<li>Question Generation</li>
<li>Robot-Human Teamwork in Dynamic Adverse Environment</li>
</ul>

<p>An informal reception will be held on Friday, November 4. A general plenary session, in which the highlights of each symposium will be presented, will be held on Saturday, November 5.</p>

<p>Notes will be prepared and distributed to participants in each symposium, but will not otherwise be available unless published as an AAAI technical report or edited collection. </p>
<p>Each symposium will have limited attendance. Participants will be expected to attend a single symposium throughout the symposium series. In addition to participants selected by the program committee of the symposia, a limited number of other interested parties will be allowed to register in each symposium on a first-come, first-served basis. To register, please fill out the registration form, and send it along with payment to:</p> 
