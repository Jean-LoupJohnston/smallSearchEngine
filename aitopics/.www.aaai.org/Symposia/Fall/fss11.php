<h4>Airport Transportation</h4>
Transportation from the airport is availably by metro, taxi, rental car, and shuttle.
<h5>Metro Rail</h5>
<p>Metro service is available from Reagan National Airport to The Westin Arlington Gateway. The cost is approximately $2.60 per person one way. Take the Blue Line towards Largo Town Center Metro Station and arrive at Rosslyn Metro Station. Transfer to the Orange Line towards Vienna/Fairfax GMU. Arrive at Ballston Metro Station and walk .30 mile SW to The Westin Arlington Gateway.</p>
<p><i>Rail System Map:</i> For a metro rail system map, visit <a href="http://www.wmata.com/rail/maps/map.cfm" rel="nofollow">wmata.com.</a></p><p>
<i>Metro Station Map:</i> For a map of the station area in relation to the Arlington Gateway, please see the <a href="http://www.stationmasters.com/System_Map/BALLSTON/ballston.html" rel="nofollow">StationMasters website</a>  or the <a href="http://www.wmata.com/rail/station_detail.cfm?station_id=99" rel="nofollow">Washington Metropolitan Area Transit Authority website.</a>
<h5>Shuttle</h5>
<p>
The Super Shuttle van service will take guests directly from the airport to The Westin Arlington Gateway. The shuttle service picks up passengers outside of the terminal. Approximate costs from each of the airports are listed below and may be subject to change. Please visit the website (www.supershuttle.com) or call Super Shuttle to confirm current rates (800-BLUE-VAN [258-3826]):</p>
<p class="tab"><i>National Airport:</i> $14.00 for a shared ride one way<br />
<i>Dulles International:</i> $29.00 for a shared van one way<br />
<i>Baltimore-Washington:</i> $48.00 for a shared ride one way
</p>

<h5>Car</h5>
<p>Take the George Washington Memorial Parkway North, and then merge onto I-395 South toward Richmond. Merge onto Washington Boulevard via Exit 8A toward Ridge Road and then onto US-50 W/Arlington Boulevard toward Falls Church. Take the Glebe Road exit, turn right onto North Glebe Road/VA-120 North. The hotel is on the right.</p>
<p>For directions from Washington Dulles Airport or other points, please see the <a href="http://www.starwoodhotels.com/westin/property/overview/index.html?propertyID=1513" rel="nofollow">Starwood Hotels website</a> and click on &ldquo;Local Area.&rdquo;</p>
<h5>Parking</h5>
<p>Valet parking is available at the Westin Arlington Gateway for a maximum of $24.00 per day/overnight.</p>
<h5>Taxi</h5>
