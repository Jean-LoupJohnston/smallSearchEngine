<h4>Growing Federal Support for AI Research</h4>
<p>Henry Kautz<br />
Division Director, CISE/IIS, National Science Foundation<br />
Friday, October 19, 2018<br />
(At the beginning of the Friday evening plenary session)</p>
<p>KKautz will describe current efforts to expand and coordinate support by government and industry in AI research. At the National Science Foundation, support of AI is now an agency-wide priority. In addition to traditional support from CISE/IIS, support for AI is coming from a number of recent cross-division and interagency programs, such as the Future of Work at the Human-Technology Frontier. NSF is growing funding for research on the social impacts of AI. All major federal agencies that fund AI R&amp;D, including NSF, Department of Defense, Health and Human Services, NASA, and many others, have begun to coordinate their efforts through a new AI working group at NITRD. The Community Computing Consortium is leading a study with support from NSF to create a â¬Sroadmapâ¬ý that is expected to influence government support for AI research over the next decade. </p>

<p>Henry Kautz is Director for the Division Information & Intelligent Systems in the Directorate for Computer and Information Science and Engineering at the National Science Foundation. He is professor at the University of Rochester, where he was founding director of the Goergen Institute for Data Science. He is a past president of AAAI and past chair of the Section on Information and Computing at the American Association for the Advancement of Science. His research is in knowledge representation and reasoning, social media analytics, and healthcare applications.</p>
