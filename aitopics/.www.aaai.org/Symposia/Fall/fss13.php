<h3>Registration and General Information</h3>

<p>
ALL ATTENDEES MUST PREREGISTER. All accepted authors, invited speakers, symposium participants, and other invited attendees must register by September 20, 2013. Participation will be open to active participants as well as interested individuals on a first-come, first-served basis. All registrations should be completed by October 18, 2013.
</p>

<p>
The conference registration fee includes admission to one symposium, one copy of the electronic proceedings, coffee breaks, and the opening reception.
</p>

