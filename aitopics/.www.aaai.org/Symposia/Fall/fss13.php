<h4>Payment Information</h4>
<p>
Payment of registration fees is required at the time of registration. Checks and bank transfers must be in US dollars. Checks (drawn on US bank) or international money orders should be made out to AAAI. American Express, MasterCard, and VISA are also accepted. Registration applications must be submitted online by the registration deadlines to qualify for reduced rates. If you qualify for student rates, please submit your proof of student status to AAAI by email attachment to fss13@aaai.org or by fax to +1-650-321-4457.
</p>

<p>
The registration form is available online.
</p>


