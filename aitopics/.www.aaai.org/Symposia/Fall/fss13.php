<h2 class="backgrnd12"><span class="text1">AAAI 2013 Fall Symposium Series</span></h2>
<div class="content">


<p><i>Sponsored by the Association for the Advancement of Artificial Intelligence</i></p>
<p><b><a href="https://www.regonline.com/fss13" rel="nofollow">Online Registration</a></b></p>
<p>The Association for the Advancement of Artificial Intelligence is pleased to present the 2013 Fall Symposium Series, to be held Friday through Sunday, November 15&ndash;17, at the Westin Arlington Gateway in Arlington, Virginia. The titles of the five symposia are as follows:
</p>

<ul>
<li><a href="http://www.aaai.org/Symposia/Fall/fss13symposia.php#fs01">Discovery Informatics: AI Takes a Science-Centered View on Big Data</a></li>
<li><a href="http://www.aaai.org/Symposia/Fall/fss13symposia.php#fs02">How Should Intelligence be Abstracted in AI Research: MDPs, Symbolic Representations, Artificial Neural Networks, or &mdash; ?</a></li>
<li><a href="http://www.aaai.org/Symposia/Fall/fss13symposia.php#fs03">Integrated Cognition</a></li>
<li><a href="http://www.aaai.org/Symposia/Fall/fss13symposia.php#fs04">Semantics for Big Data</a></li>
<li><a href="http://www.aaai.org/Symposia/Fall/fss13symposia.php#fs05">Social Networks and Social Contagion: Web Analytics and Computational Social Science</a></li>
</ul>

<p>
An informal reception will be held on Friday, November 15. A general plenary session, in which the highlights of each symposium will be presented, will be held on Saturday, November 16.
</p>

<p>Symposia will be limited to 40&ndash;60 participants each. Participation will be open to active participants as well as other interested individuals on a first-come, first-served basis. Each participant will be expected to attend a single symposium. AAAI technical reports will be distributed to participants in electronic format and posted in the AAAI Digital Library. To register, please complete the <a href="https://www.regonline.com/fss13" rel="nofollow">online registration form.</a> For questions or further information, please contact AAAI at &#102;&#115;&#115;&#49;&#51;&#64;&#97;&#97;&#97;&#105;&#46;&#111;&#114;&#103;.</p>

