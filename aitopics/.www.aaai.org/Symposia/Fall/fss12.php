<p>
The Association for the Advancement of Artificial Intelligence was pleased to present the 2012 Fall Symposium Series, held Friday through Sunday, November 2&ndash;4, at the Westin Arlington Gateway in Arlington, Virginia. The titles of the eight symposia were as follows:
</p>

<ul>
	<li>AI for Gerontechnology</li>
	<li>Artificial Intelligence of Humor</li>
	<li>Discovery Informatics: The Role of AI Research in Innovating Scientific Processes</li>
	<li>Human Control of Bio-Inspired Swarms</li>
	<li>Information Retrieval and Knowledge Discovery in Biomedical Text</li>
	<li>>Machine Aggregation of Human Judgment</li>
	<li>Robots Learning Interactively from Human Teachers</li>
	<li>Social Networks and Social Contagion</li>
</ul>

<p>
An informal reception was held on Friday, November 2. A general plenary session, in which the highlights of each symposium will be presented, was held on Saturday, November 3.
</p>

<p>
Symposia were limited to 40&ndash;75 participants each. Participation was be open to active participants as well as a limited number of interested individuals on a first-come, first-served basis. Each participant was expected to attend a single symposium. Technical reports were prepared and distributed in electronic format only to the participants in each symposium. </p>

