<h2 class="backgrnd12"><span class="text1">AAAI 2017 Fall Symposium Series</span></h2>
<div class="content">
<div class="hr1"><hr /></div>
<h4>Important Deadlines</h4>
<ul>
	
	<li>August 29, 2017: <a href="https://www.aaai.org/Publications/Author/fss-submit.php" rel="nofollow">Accepted camera-ready copy due to AAAI</a></li>
	<li>September 15, 2017: Invitees/Authors registration deadline </li>
	<li>October 9, 2017: Hotel Cut-Off deadline (5pm ET)</li>
	<li>October 13, 2017: Final registration deadline </li>
</ul>
<div class="hr1"><hr /></div>

<p>
The Association for the Advancement of Artificial Intelligence is pleased to present the 2017 Fall Symposium Series, to be held Thursday through Saturday, November 9-11, at the Westin Arlington Gateway in Arlington, Virginia.  The titles of the six symposia are as follows:

<ul>
		<li><a href="http://www.aaai.org/Symposia/Fall/fss17symposia.php#fs01">Artificial Intelligence for Human-Robot Interaction</a></li>
<li><a href="http://www.aaai.org/Symposia/Fall/fss17symposia.php#fs02">Cognitive Assistance in Government and Public Sector Applications</a></li>
	<li><a href="http://www.aaai.org/Symposia/Fall/fss17symposia.php#fs03">Deep Models and Artificial Intelligence for Military Applications: Potentials, Theories, Practices, Tools and Risks</a></li>
	<li><a href="http://www.aaai.org/Symposia/Fall/fss17symposia.php#fs04">Human-Agent Groups: Studies, Algorithms and Challenges</a></li>
	<li><a href="http://www.aaai.org/Symposia/Fall/fss17symposia.php#fs05">Natural Communication for Human-Robot Collaboration</a></li>
	<li><a href="http://www.aaai.org/Symposia/Fall/fss17symposia.php#fs06">A Standard Model of the Mind</a></li>
</ul>

<p>
Symposia generally range from 40-75 participants each. Participation will be open to active participants as well as other interested individuals on a first-come, first-served basis. Each participant will be expected to attend a single symposium. AAAI technical reports will be distributed to participants in electronic format and posted in the AAAI Digital Library. Registration information will be available on the AAAI web site in August 2017.
</p>

