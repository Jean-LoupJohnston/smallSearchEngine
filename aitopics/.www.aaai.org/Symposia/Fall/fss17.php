<h3><a name="hotel">Hotel Information</a></h3>
<p>
For your convenience, AAAI has reserved a block of rooms at the Westin Arlington Gateway. The Westin Arlington Gateway is located in the Ballston area of Arlington. It is a short walk from the Ballston Metro Station, which allows guests to easily explore Arlington, downtown Washington, DC, Alexandria, or Georgetown. Reagan National Airport is easily accessible via the Washington Metro rapid transit. 
</p>

<p>
The conference room rate per night is $199.00 (King/Double). 
</p>


<p>
Rates do not include applicable state and local taxes (approximately 13%), or hotel fees in effect at the time of the meeting. Symposium attendees must contact the Westin Arlington Gateway directly. Please request the group rate for the Association for the Advancement of Artificial Intelligence (AAAI) when reserving your room. The cut-off date for reservations is October 9, 2017 at 5:00 PM ET, local time at the hotel. Reservations after this date will be accepted based on availability at the hotel's prevailing rate. All reservations must be secured by one night's deposit per room, via credit card. Reservations may be cancelled with no penalty up to 5:00 pm, 72 hours prior to the date of arrival. After that time, a penalty of one night's room and tax will be incurred. Upon check-in, date of departure must be confirmed. Early departure will result in a fee equal to one night's guest room rate. 
</p>

