<h3><a name="registration">Registration and General Information</a></h3>

<p>
ALL ATTENDEES MUST PREREGISTER. All accepted authors, invited speakers, symposium participants, and other invited attendees must register by September 15, 2017. Participation will be open to active participants as well as interested individuals on a first-come, first-served basis. All registrations should be completed by October 13, 2017. 
</p>

<p>
To register, please complete the online registration form available at <a href="https://www.regonline.com/fss17" rel="nofollow">www.regonline.com/fss17</a>. For questions or further information, please contact AAAI at &#102;&#115;&#115;&#49;&#55;&#64;&#97;&#97;&#97;&#105;&#46;&#111;&#114;&#103;. 
</p>

<p>
The conference registration fee includes admission to one symposium, access to the electronic proceedings, coffee breaks, and the opening reception. 
</p>

