<h2 class="backgrnd12"><span class="text1">AAAI 2010 Symposium on Educational Advances in Artificial Intelligence</span></h2>
<div class="content">
<p><i>Sponsored by the Association for the Advancement of Artificial Intelligence</i></p>


<p>
The Association for the Advancement of Artificial Intelligence is pleased to present, in conjunction with the AAAI-10 Conference in Atlanta Georgia, the First AAAI Symposium on Educational Advances in Artificial Intelligence (EAAI-10), to be held July 13&ndash;14, 2010.</p>

<p>EAAI-10 provides a venue for researchers and educators to discuss pedagogical issues and share resources related to teaching AI and using AI in education across a variety of curricular levels (K-12 through postgraduate training), with a natural emphasis on undergraduate and graduate teaching and learning. The symposium seeks contributions showing how to more effectively teach AI, as well as how themes from AI may be used to enhance education more broadly; for example in introductory computing courses or as a means for teaching computational thinking.  We encourage the sharing of innovative educational approaches that convey or leverage AI and its many subfields, such as robotics, machine learning, natural language, computer vision, and so on.</p>

<p>EAAI-10 provides several paths for participation, including the following:</p>

<ul>
<li>Full-length papers (6 pages) and presentations for the symposium</li>
<li>Extended abstract/poster contributions (2 pages) highlighting
 preliminary or ongoing work</li>
<li>A <a href="http://www.aaai.org/Symposia/EAAI/2010/model.php">"Model AI" session</a> highlighting innovative, ready-to-adopt materials</li>
<li>A post-symposium workshop for mentoring new faculty, instructors,
 and graduate students on teaching</li>
<li>A Student/Educator Track within the AAAI-10 Robotics Exhibition and
 Workshop</li></ul>


<p>EAAI-10 welcomes paper submissions on a variety of topics, including, but not limited to the following:</p>
<ul>
<li>Educational resources, including syllabi, assignments, project
 ideas, and pedagogical strategies, related to teaching AI</li>
<li>Multi-disciplinary curricula highlighting the application of AI in
 other contexts (computational biology, algorithmic game theory,
 computational economics, etc.) and/or the theoretical concepts of
 roots of AI from other fields (philosophy, cognitive science,
 linguistics, psychology)</li>
<li>The use of robotics and other tangible media both in AI courses and
 elsewhere in the curriculum</li>
<li>Software that assists the teaching/learning process - everything
 from software to help visualize search spaces and search algorithms,
 to software substrates that can be used by students to do projects</li>
<li>Resources and strategies for teaching specific AI subareas or
 topics: machine learning, robotics, computer vision, natural
 language processing, game playing, and many others</li>
<li>Strategies for appropriately situating AI within a wider CS
 curriculum</li>
<li>Ways to incorporate or address popular entertainment and media
 portrayal of AI (in movies, news, advertisements, new products,
 etc.)</li>
<li>Real-world examples of successful AI deployments, described in
 sufficient detail to provide case studies and/or serve as useful
 springboards for other teachers</li>
<li>Innovative means for integrating research as part of coursework in AI</li>
</ul>
<p>A list of relevant keywords is included below.</p>
<p><b>Important Dates</b><br />

<i>February 23, 2010:</i> Electronic paper submissions due<br />
<i>March 26, 2010:</i> Author notification<br />
<i>April 13, 2010:</i> Camera-ready copy due at AAAI office</p>

<h4>Author Registration</h4>
<p>Authors must register at the <a href="http://eaai10.confmaster.net/pages/login.php?Conf=EAAI10" rel="nofollow">AAAI-10 EAAI web-based technical paper submission site.</a> The software will assign a password, which will enable the author to log on to submit an abstract and paper. In order to avoid a rush at the last minute, authors are encouraged to register as soon as possible, and no later than February 21.</p>

<h4>Abstract and Paper Submission</h4>
<p>Electronic abstract and paper submission through the AAAI-10 EAAI paper submission site is required on or (preferably) before the deadline dates listed above. We cannot accept submissions by e-mail or fax.</p>

<p>Papers must be in trouble-free, high resolution PDF format, formatted for US Letter (8.5" x 11") paper, using Type 1 or TrueType fonts. Papers may be no longer than 6 pages including references, or 2 pages for extended abstract or poster contributions, and formatted in AAAI two-column, camera-ready style <a href="http://www.aaai.org/Publications/Author/author.php">(see the author instructions page).</a> Please note that these formatting instructions are for final, accepted papers; no additional pages can be purchased at the review stage. In addition, the copyright slug may be omitted in the initial submission phase, and no source files are required at the time of submission for review. Please also refer to the instructions on how to prepare your paper for blind review.</p>

<p>Authors will receive confirmation of receipt of their abstracts or papers, including an ID number, shortly after submission. AAAI will contact authors again only if problems are encountered with papers. Inquiries regarding lost papers must be made no later than March 2, 2010.</p>

<h4>Submissions to Other Conferences or Journals</h4>
<p>

     Papers submitted to this conference must not have been accepted for publication elsewhere or be under review for another AI conference. However, to encourage interdisciplinary contributions, we may consider unpublished work that has been submitted or presented in part to a forum outside of AI. The guidelines of the AAAI policy on multiple submissions are fully detailed below and must be carefully followed.</p>

<h4>Publication</h4>

<p>Accepted authors of full papers will be allocated six (6) pages in the conference proceedings; up to two (2) additional pages may be used at a cost to the authors of US$275 per page. Accepted authors of extended abstract/poster contributions will be allocated two (2) pages in the conference proceedings; one (1) additional page may be used at a cost to the authors of US$275 per page. Final papers exceeding eight (8) pages, or three (3) for extended abstracts/posters, and those violating the instructions to authors will not be included in the proceedings. Authors will be required to transfer copyright of their paper to AAAI.</p>

