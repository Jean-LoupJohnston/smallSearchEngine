<h3 class="text2">AAAI-10 Policy Concerning Submissions to Other Conferences or Journals</h3>

<p>Papers submitted to this conference must not have been accepted for publication elsewhere or be under review for another AI conference. (The AAAI Nectar and Challenge paper submissions have specific guidelines that can be found in their respective call for papers.)</p>

<p>To encourage interdisciplinary contributions, AAAI will consider work that has been submitted or presented in part elsewhere, if it is unlikely to have been seen by more than a few members of the AAAI audience (however, see the exception for Challenge Track papers). As such, papers may not be dually submitted to other AI or AI subarea conferences. Papers under submission to a journal that contain overlap with AAAI papers will be considered as long as the author specifies the dual submission and certifies that the journal submission contains significant material that is not included in the AAAI submission. Papers that have been published in full in another conference or journal will not be accepted for review. Novelty is an important criterion in the selection of papers.</p>

<p>AAAI requires the following:</p>
<ol>
<li>Authors must specify the conferences and journals to which the paper has been dually submitted.</li>
<li>Authors must withdraw papers under review or accepted for other AI conference venues if the paper is submitted to AAAI.</li>
<li>Papers not dually submitted should so indicate on the title page.</li>
</ol>

<p>For questions as to whether a given meeting is considered under the dual submission policy, or for clarifications of this policy, submitters should contact the Program Chairs.</p>

