<h2 class="backgrnd12"><span class="text1">The Third Symposium on Educational Advances in Artificial Intelligence</span></h2>
<div class="content">
<p><i>Sponsored by the Association for the Advancement of Artificial Intelligence</i></p>
<p><i>Call for Participation</i></p>
<ul>
	<li><i>February 8, 2012:</i> Paper submission deadline</li>
	<li><i>March 13, 2012:</i> Notification of acceptance or rejection</li>
	<li><i>April 10, 2012:</i> Camera-ready copy due at AAAI office</li>
	<li><i>July 23&ndash;24, 2012:</i> Symposium</li></li>
</ul>
<div class="hr1">
<hr />
</div>
<h3 class="text2"><a href="http://eaai.confmaster.net" rel="nofollow">EAAI Paper Submission Site</a></h3>
<p><a href="http://www.aaai.org/Publications/Author/author.php">Author Instructions Page and Author Kit</a></p>
<div class="hr1">
<hr />
</div>
<p>A printable <a href="http://www.aaai.org/Symposia/EAAI/2012/eaai12call.pdf">PDF</a> of this call is also available</p>

<p>The Third Symposium on Educational Advances in Artificial Intelligence (EAAI-12) provides a venue for researchers and educators to discuss pedagogical issues and share resources related to teaching AI and using AI in education across a variety of curricular levels (K&ndash;12 through postgraduate training), with an emphasis on undergraduate and graduate teaching and learning. The symposium seeks contributions showing how to more effectively teach AI, as well as how themes from AI may be used to enhance education more broadly (for example, in introductory computing courses or as a means for teaching computational thinking). We encourage the sharing of innovative educational approaches that convey or leverage AI and its many subfields: robotics, machine learning, natural language, computer vision, and so on.</p>

<p>The proceedings of the symposium will be published by AAAI Press.</p>

<h3 class="text2">Participation Opportunities</h3>


<p>EAAI-12 provides several of paths for participation, including the following: </p>

<ul>
	<li>Full-length papers (6 pages) and presentations for the symposium</li>
	<li>Extended abstract/poster contributions (2 pages) highlighting preliminary or ongoing work, or describing an educational innovation proposed to be presented in a demonstration session</li>
	<li>Model AI Assignments highlighting innovative ready-to-adopt materials</li>
	<li>A workshop for mentoring new faculty, instructors, and graduate students on teaching</li>
	<li>An Educational Video Track within the AAAI-12 Video program</li>
	<li>A Student/Educator Track within the AAAI-12 Robotics Exhibition and Workshop</li>
</ul>

<h3 class="text2">Paper Submission Topics</h3>


<p>EAAI-12 welcomes paper submissions on a variety of topics, including, but not limited to the following:</p>


<ul>
	<li>Educational resources, including pedagogical strategies, innovative assignments and curricular development related to AI</li>

	<li>Multidisciplinary curricula efforts highlighting the application of AI in other contexts (computational biology, algorithmic game theory, computational economics, etc.) and/or foundational concepts of AI in other fields (philosophy, cognitive science, linguistics, psychology)</li>

	<li>The use of robotics and other tangible media both in AI courses and elsewhere in the curriculum</li>

	<li>Software that assists the teaching/learning process -everything from software to help visualize search spaces and search algorithms, to software substrates that can be used by students to do projects</li>

	<li>Resources and strategies for teaching specific AI subareas or topics: machine learning, robotics, computer vision, natural language processing, game playing, etc.</li>

	<li>Strategies for situating AI within a wider CS curriculum</li>

	<li>Ways to incorporate popular entertainment and media portrayal of AI (in movies, news, advertisements, new products, etc.)</li>

	<li>Real-world examples of AI deployments, described in sufficient detail to provide case studies and/or serve as useful springboards for other educators</li>

	<li>Innovative means for integrating research as part of coursework in AI</li>
</ul>

<p>Ideally, full-length submissions to EAAI-12 should describe well developed ideas and/or pedagogical examples, and should include an evaluation of the work. Papers submitted to EAAI-12 should not substantially overlap with papers that have been previously published or are under review elsewhere.</p>


<h3 class="text2">EAAI-12 Model AI Assignments Session</h3>


<p>EAAI-12 will feature a special session on Model AI Assignments suitable for use in a variety of class settings. The Call for Assignments for the Model AI Assignments special session is available at the URL provided below.</p>

