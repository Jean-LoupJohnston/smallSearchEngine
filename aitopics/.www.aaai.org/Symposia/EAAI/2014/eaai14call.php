<p>EAAI-14 provides a venue for researchers and educators to discuss pedagogical issues and share resources related to teaching AI and using AI in education across a variety of curricular levels (K-12 through postgraduate training), with an emphasis on undergraduate and graduate teaching and learning. The symposium seeks contributions showing how to more effectively teach AI, as well as how themes from AI may be used to enhance education more broadly (for example, in introductory computing courses or as a means for teaching computational thinking). We encourage the sharing of innovative educational approaches that convey or leverage AI and its many subfields, including robotics, machine learning, natural language processing, computer vision, and others.</p>
<p>
EAAI-14 provides several submission types:
</p>
<ul>
<li>full-length papers (6 pages)</li>
<li>extended abstract/poster contributions (2 pages)</li>
<li>Model AI Assignments highlighting innovative ready-to-adopt materials</li>
</ul>
<p>
In addition, EAAI-14 provides many other paths for participation including a workshop for mentoring new faculty, instructors, and graduate students on teaching and an Educational Video Track within the AAAI-14 Video program.</p><p>
EAAI-14 welcomes paper submissions on a variety of topics, including, but not limited to the following:
</p>
<ul>
<li>Educational resources, including pedagogical strategies, innovative assignments and curricular development related to AI</li>
<li>Multi-disciplinary curriculum efforts highlighting the application of AI in other contexts (computational biology, algorithmic game theory, computational economics, etc.) and/or foundational concepts of AI in other fields (philosophy, cognitive science, linguistics, psychology)</li>
<li>The use of robotics and other tangible media both in AI courses and elsewhere in the curriculum</li>
<li>Software that assists the teaching/learning process - everything from software to help visualize search spaces and search algorithms, to software substrates that can be used by students to do projects</li>
<li>Resources and strategies for teaching specific AI sub-areas or topics: machine learning, robotics, computer vision, natural language processing, game playing, etc.</li>
<li>Strategies for situating AI within a wider CS curriculum</li>
<li>Ways to incorporate popular entertainment and media portrayal of AI (in movies, news, advertisements, new products, etc.)</li>
<li>Real-world examples of AI deployments, described in sufficient detail to provide case studies and/or serve as useful springboards for other educators</li>
<li>Innovative means for integrating research as part of coursework in AI</li>
</ul>
<p>
Full-length submissions to EAAI-14 should describe well developed ideas and/or pedagogical examples, and in most cases should include an evaluation of the work. Extended abstract/poster submissions may highlight preliminary or ongoing work. Papers submitted to EAAI-14 should not substantially overlap with papers that have been previously published or are under review elsewhere.
</p>
