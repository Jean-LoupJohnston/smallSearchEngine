<h2 class="backgrnd12"><span class="text1">Welcome to <i>AI Magazine!</i></span></h2>
<div class="content">
<p><a href="http://www.aaai.org/Library/Magazine/vol29.php"><img height="128" width="100" src="aimag-issue.jpg" style="float: left; margin-right: 5px;" /></a>This serial is an official publication of the Association for the Advancement of Artificial Intelligence. From these pages, you can access editorial, advertising, and subscriber information about the publication.</p>
<p>Called the &ldquo;journal of record for the AI community,&rdquo; <i>AI Magazine</i> helps AAAI members stay abreast of significant new research and literature across the entire field of artificial intelligence. This quarterly publication features timely, feature-length articles, consistently crafted to be clear enough to permit specialists to review work outside their particular area of expertise.</p>
<p><i>AI Magazine</i> (ISSN 0738-4602) is published quarterly in March, June, September, and December by the Association for the Advancement of Artificial Intelligence (AAAI). The Magazine is a direct benefit of membership in the AAAI. Inquiries regarding <a href="../Membership/membership.php">membership in AAAI</a> can be made using AAAI's <a href="http://www.aaai.org/Organization/contact.php">contact form.</a></p>
<p> For further information about <i>AI Magazine,</i> consult the 
information contained in one of the following categories.</p>
