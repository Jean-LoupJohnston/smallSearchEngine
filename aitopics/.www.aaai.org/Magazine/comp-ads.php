<h2 class="backgrnd12"><span class="text1">Production Guidelines for "In Cooperation" Conference Advertisers</span></h2>
<div class="content">

<p>This page is provided as a supplement to the <i>AI Magazine</i> rate card for advertisers who are submitting an "in cooperation with AAAI" advertisement only. In all cases, the rate card shall take precedent over information provided on this page.</p>

<p>If you have received a letter from the AAAI Executive Director notifying you that your meeting may be held "in cooperation with AAAI" the information on this page may prove to be of help to you in creating your advertisement. (This information isn't going to be useful to regular advertisers however!). First, please download and read the <i>AI Magazine</i> <a href="Media-Kit/rate-card.pdf">rate card</a>. The deadlines and production guidelines apply to you as much as any other advertiser!.</p>
<h3>Guidelines</h3>
<ul><li>In cooperation ads must be no larger than a quarter page, and submitted in black and white and grayscale only. We will not accept any other ad sizes and we will not accept color ads except on a paid basis. </li>
<li>Ad size must be exactly 3.5 inches wide and 4-1/2 inches tall (including borders, rules, etc.) Do not include crop marks with ads.</li>
<li>Ads are accepted on a "space available basis" only. That means that if we are unable to put your ad in a given issue, it will be held for the next issue, or, if time has expired, will not be run at all. We may withdraw your opportunity to advertise at any time. No guarantee of ad placement or position is given or can be construed. To guarantee placement, you must pay for your ad.</li>
<li>You are obliged to write and submit a report of your event for publication in <i>AI Magazine.</i> If you are renewing in-cooperation status and did not submit a report for the previous year, your organization is not eligible to submit an in-cooperation ad. (One of the purposes of this program is to bring information about conferences to our readership. Publishing a report in the magazine furthers that purpose, as well as.</li>
<li>Ads are limited to one advertisement per organization per year (the purpose of this program is to support small organizations hosting a single conference, not large organizations hosting multiple conferences each year). Please also note the report requirement above. 
<li>We will not be able to confirm insertion of the ad prior to publication.</li
<li>Ad must follow all production guidelines listed in the <a href="Media-Kit/rate-card.pdf">rate card</a></li>

<li>Conference organizer must fax or mail (no e-mail) an insertion order to AAAI, containing<br />
1. Conference Name<br />
2. Name of Contact Person (one only)<br />
3. Complete Surface Address of Contact Person<br />
4. Telephone and E-mail Address of Contact Person<br />
5. Issue in Which You Request the Ad to Appear<br />
6. A printed version of the ad as it is to appear<br />
(The AAAI fax number is 650-321-4457).</li>
<li>These benefits may be withdrawn at any time without prior notice.</li>

</ul>

<h3>Electronic Submissions</h3>
<p>You must submit your ad electronically. Unfortunately, because of the additional costs involved, we are unable to accept ads that do not conform to these guidelines. Most graphic designers will be familiar with these programs and formats, and will be able to provide us with an ad that conforms to the guidelines.</p>
<p><i>AI Magazine</i> can accept electronic submissions, (in order of preference) provided the following conditions are met</p><ol>
<li><b>Adobe PDF,</b> provided you choose "PDF/X-1a:2001" as the Distiller setting (files created for the web using the screen optimized setting aren't acceptable;  all fonts must be embedded, and no TrueType fonts can be used)</li>
<li><b>Adobe Illustrator Encapsulated PostScript</b> (EPS files must contain type 1 fonts only &mdash; all fonts must be embedded, and no TrueType fonts can be used)</li>
</ol>
<p><b>Additional Requirements</b></p>
<ol>
<li>File must be created with Adobe PostScript type 1 fonts (no TrueType or Computer Modern fonts)</li>
<li>When submitting EPS or PDF, the fonts must be embedded in the file</li>
<li>We cannot accept electronic submissions created with TeX or LaTeX or DVIPS</li>
<li>A hard copy printout must be mailed to AAAI</li>
<li>Graphics can be embedded in the file, but must also be supplied separately; graphics must be EPS, Tiff, or JPEG. (Gif files, whether embedded or not, are unacceptable.) Bitmapped files must be 300 dpi or higher.) </li>
</ol>

<p>Please note that AAAI is only offering space. If you need production assistance (especially for complicated designs), we suggest you obtain the services of a graphic designer. Simple ads can be produced for you by <i>AI Magazine.</i> There will be fees for such services (minimum charge is $200.00).</p>


