<h2 class="backgrnd12"><span class="text1">AAAI Conference and Workshop Cooperation Guidelines</span></h2>
<div class="content">
<p>The AAAI conference and workshop cooperation program is designed for independent conferences or workshops 
that seek a one-time or ongoing "in cooperation" agreement from AAAI. 
"In cooperation" status entitles a conference or workshop to state 
that the conference is being held "in cooperation with AAAI" on any 
promotional or program materials.</p>

<h3>Benefits</h3>

<ul>
	<li>A listing of your event in the calendar of events in  <i>AI Magazine</i></li>
	<li>The placement of one complimentary quarter-page black and white  in <i>AI Magazine, </i>subject to space available (additional cost of $100 if final artwork provided is not in required camera-ready format) OR one complimentary copy of AAAI membership mailing labels (email addresses not available)</li>
	<li>A listing of your conference on the AAAI web site, along with a complimentary link to your conference home page.</li>
</ul>

<h3>Requirements for Acceptance</h3>
<ul>
	<li>Demonstrated interest by a significant number of people, and  designed to fill a clear void</li>
	<li>Submission of Call for Participation, which should include a  subject description, detailed enough to permit evaluation of  relevance to AI and possible overlap with other meetings; list of  program and/or steering committee of at least 3-10 persons; when and  where the event will be held; event deadlines and contact  information; and conditions of participation, including how attendees  are to be selected</li>
	<li>Key organizer(s) of the event must be current AAAI members</li>
	<li>Neutral event: if commercial interests are involved, people from  different companies should be contacted and have equal access and  opportunity</li>
	<li>Discounted registration rate for AAAI members</li>
	<li>Distribution of AAAI membership and conference materials at event</li>
	<li>Submission of 250-500 word conference or workshop report, suitable  for publication in the AI Magazine, one month after event</li>
	<li>Submission of a list of attendees one month after the event,  including names and addresses</li>
	<li>Commitment to provide a representative speaker to report at the  annual Conference on Artificial Intelligence, if requested</li>
</ul>

