<h2 class="backgrnd12"><span class="text1">Workshops at the Thirty-First AAAI Conference on Artificial Intelligence (AAAI-17)</span></h2>
<div class="content">
<h3>February 4&ndash;5, 2017 &nbsp; &nbsp; San Francisco, California USA &nbsp; &nbsp; Call for Proposals</h3>
<p><i>Sponsored by the Association for the Advancement of Artificial Intelligence</i></p>


<div class="hr1">
<hr />
</div>

<h3 class="text2">Important Dates for Workshop Organizers</h3>

<ul>
<li>September 2, 2016: Workshop CFP Due at AAAI</li>
<li>October 21, 2016: Workshop Submissions Due to Organizers</li>
<li>November 18, 2016: Notifications Sent to Authors</li>
<li>November 20, 2016: List of Participants Due at AAAI</li>
<li>December 8, 2016: Final Workshop Papers Due at AAAI</li>
</ul>


<div class="hr1">
<hr />
</div>

<p>The AAAI-17 Program Committee invites proposals for the Workshop Program of the Association for the Advancement of Artificial Intelligence's Thirty-First AAAI Conference on Artificial Intelligence (AAAI-17).

</p>

<h3 class="text2">Formats</h3>

<p>
Workshops are scheduled to be held at the beginning of the conference, February 4&ndash;5, 2017. Workshop participants will have the opportunity to meet and discuss issues with a selected focus &mdash; providing an informal setting for active exchange among researchers, developers and users on topics of current interest. Members of all segments of the AI community are encouraged to submit proposals. To foster interaction and exchange of ideas, the workshops will be kept small, with 25-65 participants. Attendance is sometimes limited to active participants only, but most workshops also allow general registration by other interested individuals. The format of workshops will be determined by their organizers, who are encouraged to leave ample time for general discussion. Workshops will typically be one full day in length, although half-day and two-day proposals will also be considered.
</p>

<h3 class="text2">Innovations for the AAAI-17 Workshop Program</h3>

<p>
The AAAI-17 Workshop cochairs particularly encourage the submission of proposals for interdisciplinary workshops, which may involve co-organizers that are from disciplines other than AI, or may attract participants from disciplines other than AI. Proposals that focus on important or novel application domains for AI are also encouraged.
</p>

<h3 class="text2">Proposal Content</h3>
<p>
Proposals for workshops should be about two (2) pages in length, and should contain the following:
</p>

<ul>
<li>A description of the workshop topic. Identify the specific issues on which the workshop will focus.</li>
<li>A brief discussion of why the topic is of particular interest at this time, including a discussion of prior workshops in this area (if applicable).</li>
<li>A brief description of the proposed workshop format, regarding the mix of events such as paper presentations, invited talks, panels, and general discussion.</li>
<li>An indication as to whether the workshop should be considered for a half-day, one, or two-day meeting.</li>
<li>The names and full contact information (email and postal addresses, fax and telephone numbers) of the organizing committee - three or four people knowledgeable in the field-and short descriptions of their relevant expertise. (Please specify main contact.) Strong proposals include organizers who bring differing perspectives to the workshop topic and who are actively connected to the communities of potential participants.</li>
<li>A list of comparable workshops, either from AAAI or similar conferences, with corresponding links to further details.  Please indicate specifically if this workshop is part of a continuing series.</li>
</ul>

<p>
Workshops are an excellent forum for exploring emerging approaches and task areas, for bridging the gaps between AI and other fields or between subfields of AI, for elucidating the results of exploratory research, or for critiquing existing approaches. Because workshops are intended for focused exploration of special topics, topics that are already the subject of regular meetings are not appropriate.
</p>


<h3 class="text2">Workshop Organization</h3>

<p>
Workshop organizers will be responsible for the following:
</p>


<p>
<i>Producing a call for participation.</i> The call for participation is due no later than September 2  2016. It will be posted on the AAAI web site. Organizers are responsible for additional publicity such as distributing the CFP to relevant newsgroups and electronic mailing lists, and especially to potential audiences from outside the AAAI community. Organizers are encouraged to maintain their own web site with updated information about the workshop.
</p>

<p>
<i>Selecting participants. </i>Workshop attendance is by invitation of the organizers. Selection of attendees will be made by the organizers on the basis of submissions due October 21, 2016. Workshop organizers will need to provide AAAI with a preliminary list of the participants by November 20, 2016.
</p>

<p>
<i>Facilitating the production of the technical reports.</i> AAAI coordinates the collection, production, and distribution of the technical reports or working notes for the workshops. Workshop organizers will submit lists of papers and author contact information to AAAI by November 20, 2016. Workshop papers and abstracts must be received from authors at AAAI no later than December 8, 2016. Workshop organizers who want to publish the papers from their workshop (or significant portions of it) will have the opportunity to do so through the AAAI Press. The Press (which retains the right of first refusal to publish) will furnish details of its program to interested organizers and authors.
</p>

<p>
AAAI will provide logistical support, meeting places for the workshops, and will determine the dates and times of the workshops. AAAI reserves the right to drop any workshop if the organizers miss the above deadlines or if there is insufficient participation. Workshops are not to be used as a vehicle for marketing products. A reduced workshop registration fee will be offered to AAAI-15 technical program registrants.
</p>

<h3 class="text2">Proposal Submission</h3>
<p>
Workshop proposals should be submitted to the workshop cochairs at &#97;&#97;&#97;&#105;&#49;&#55;&#119;&#115;&#64;&#97;&#97;&#97;&#105;&#46;&#111;&#114;&#103; as soon as possible and must be received no later than August 12, 2016. PDF format is preferred. Organizers will be notified of the committee's decision by August 19, 2016. Please address inquiries concerning workshop submissions to the email address above. 
</p>

