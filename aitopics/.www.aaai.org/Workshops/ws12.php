<h2 class="backgrnd12"><span class="text1">Workshops at the Twenty-Sixth AAAI Conference on Artificial Intelligence (AAAI-12)</span></h2>
<div class="content">


<p><i>Sponsored by the Association for the Advancement of Artificial Intelligence</i></p>


<p>AAAI was pleased to present the AAAI-12 Workshop Program. Workshops, held Sunday and Monday, July 22&ndash;23, 2012 at the Sheraton Centre Toronto Hotel in Toronto, Ontario, Canada. The AAAI-12 workshop program included 10 workshops covering a wide range of topics in artificial intelligence. Most workshops were one day. Each workshop was limited to approximately 25 to 65 participants. Participation at these workshops was by invitation from the workshop organizers. In most cases, workshop papers were made available after the conference as part of the AAAI Press technical report series.</p>
<div class="hr1">
<hr />
</div>

<h4>Program Contents</h4>
<p>The AAAI-12 Workshop program includes the following ten workshops:</p>

<ul>
<li>W1: Activity Context Representation: Techniques and Languages</li>
<li>W2: AI for Data Center Management and Cloud Computing</li>
<li>W3: Cognitive Robotics</li>
<li>W4: Grounding Language for Physical Systems</li>
<li>W5: Human Computation</li>
<li>W6: Intelligent Techniques for Web Personalization and Recommendation</li>
<li>W7: Multiagent Pathfinding</li>
<li>W8:  Neural-Symbolic Learning and Reasoning</li>
<li>W9: Problem Solving Using Classical Planners</li>
<li>W10: Semantic Cities</li>
</ul>
<div class="hr1">
<hr />
</div>
<p><b>Schedule</b><br />
<i>Sunday, July 22:</i> W2, W3, W6, W7, W9<br />
<i>Monday, July 23:</i> W1, W3, W4, W5, W8, W10</p>


