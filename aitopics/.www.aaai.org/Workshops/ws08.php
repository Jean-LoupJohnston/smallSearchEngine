<h2 class="backgrnd12"><span class="text1">Workshops at the Twenty-Third AAAI Conference on Artificial Intelligence (AAAI-08)</span></h2>
<div class="content">


<p><i>Sponsored by the Association for the Advancement of Artificial Intelligence</i></p>

<p>AAAI was pleased to present the AAAI-08 Workshop Program, held Sunday and Monday, July 13&ndash;14, in Chicago, Illinois, USA. The program included the following fifteen workshops:</p>

<ul>
<li><b>W1:</b> <b>Advancements in POMDP Solvers</b></li>

<li><b>W2:</b> <b>AI Education Workshop</b></li>

<li><b>W3:</b> <b>Coordination, Organization, Institutions and Norms in Agent Systems</b></li>

<li><b>W4:</b> <b>Enhanced Messaging</b></li>

<li><b>W5:</b> <b>Human Implications of Human-Robot Interaction</b></li>

<li><b>W6:</b> <b>Intelligent Techniques for Web Personalization and Recommender Systems</b></li>

<li><b>W7:</b> <b>Metareasoning: Thinking about Thinking</b></li>

<li><b>W8:</b> <b>Mobile Robot Workshop</b></li>

<li><b>W9:</b> <b>Multidisciplinary Workshop on Advances in Preference Handling</b></li>

<li><b>W10:</b> <b>Search in Artificial Intelligence and Robotics</b></li>

<li><b>W11:</b> <b>Spatial and Temporal Reasoning</b></li>

<li><b>W12:</b> <b>Trading Agent Design and Analysis</b></li>

<li><b>W13:</b> <b>Transfer Learning for Complex Tasks</b></li>

<li><b>W14:</b> <b>What Went Wrong and Why: Lessons from AI Research and Applications</b></li>

<li><b>W15:</b> <b>Wikipedia and Artificial Intelligence: An Evolving Synergy</b></li>
</ul>

<p>Participation in the workshop was limited to 25&ndash;65 participants. There was a separate fee for attendance at a workshop. Workshop registration was discounted for AAAI-08 technical registrants. Workshop reports were included in the workshop registration fee, and were distributed onsite during the workshop. In most cases, reports were also available after the conference as part of the AAAI Press technical report series.</p>

