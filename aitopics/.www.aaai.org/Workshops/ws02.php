<h2 class="backgrnd12"><span class="text1">Workshops at the Eighteenth National Conference on Artificial Intelligence</span></h2>
<div class="content">


<p><i>Sponsored by the Association for the Advancement of Artificial Intelligence</i></p>
<hr />

<p>AAAI is pleased to present the AAAI-02 Workshop Program. Workshops were held Sunday and Monday, July 28-29, 2002 at the Shaw Convention Center in Edmonton, Alberta, Canada. The AAAI-02 workshop program included nineteen workshops covering a wide range of topics in artificial intelligence. Workshops were one day unless noted otherwise in the individual description. Each workshop was limited to approximately 25 to 50 participants. Participation at these workshops was by invitation from the workshop organizers. Workshop registration information was mailed directly to all invited participants. Workshops were included in the AAAI-02 technical registration. The workshops for 2002 were as follows:</p>
<ul><li>Agent-Based B2B Electronic Commerce Technologies (W1)<br />
Brian Blake<br />
Sunday, July 28</li>

<li>Agent-Based Systems for Information Retrieval (W2)<br /><b>This workshop has been cancelled</b><br />
R. Scott Cost, Charles Nicholas, and Ian Soboroff<br />
Monday, July 29</li>

<li>Artificial Intelligence for Intelligent Business (W3)<br />
Daniel E. O'Leary, Benjamin Grosof, and Alun Preece<br />
Monday, July 29</li>

<li>Automation as Caregiver: The Role of  Intelligent Technology  in Elder Care (W4)<br />
Karen Haigh<br />
Monday, July 29</li>

<li>Autonomy, Delegation, and Control: From Inter-Agent to Groups (W5)<br />
Henry Hexmoor and Rino Falcone<br />
Sunday, July 28</li>

<li>Coalition Formation in Dynamic Multiagent Environments (W6)<br />
Leen-Kiat Soh  and Charlie Ortiz<br />
Monday, July 29</li>

<li>Cognitive Robotics (CogRob2002) (W7)<br />
Chitta Baral and Sheila McIlraith<br />
Sunday, July 28</li>

<li>Game Theoretic and Decision Theoretic Agents (W8)<br />
Piotr Gmytrasiewicz and Simon Parsons<br />
Sunday, July 28</li>

<li>Intelligent Integration of Information and Services on the Web (W9)<br />
Dean Allemang, Eleni Stroulia, and John Mylopoulos<br />
Sunday, July 28</li>

<li>Intelligent Situation-Aware Media and Presentations (ISAMP) (W10)<br />
Rainer Malaka and Antonio Krueger<br />
Monday, July 29</li>

<li>Meaning Negotiation (W11)<br />
Paolo Bouquet<br />
Sunday, July 28</li>

<li>Mobile Robot Competition and Exhibition Workshop (W12)<br />
Bill Smart<br />
Thursday, August 1</li>

<li>Multi-Agent Modeling and Simulation of Economic Systems  (W13)<br />
Koichi Kurumatani, Shu-Heng Chen, and Azuma Ohuchi<br />
Monday, July 29</li>

<li>Ontologies for the Semantic Web (W14)<br />
Adam Pease, Richard Fikes, and Jim Hendler<br />
Monday, July 29</li>

<li>Planning with and for Multiagent Systems (W15)<br />
Michael Brenner and Marie desJardins<br />
Monday, July 29 (may extend to Sunday, July 28)</li>

<li>Preferences in AI and CP: Symbolic Approaches (W16)<br />
Ulrich Junker<br />
Sunday and Monday, July 28-29</li>

<li>Probabilistic Approaches in Search (W17)<br />
Toby Walsh and Carla Gomes<br />
Sunday, July 28</li>

<li>Real-Time Decision Support and Diagnosis Systems (Joint Workshop with KDD-02 and UAI hosted by AAAI-02) (W18)<br />
Haipeng Guo, Eric Horvitz, William H. Hsu, and Eugene Santos Jr. <br />
Monday, July 29</li>

<li>Semantic Web Meets Language Resources (W19)<br />
Nancy Ide and Chris Welty<br />
Sunday, July 28</li>

<li>Spatial and Temporal Reasoning (W20)<br />
Hans W. Guesgen, Frank D. Anger, and Gerard Ligozat<br />
Monday, July 29</li>
</ul>

