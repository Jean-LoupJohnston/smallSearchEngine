<h3><a name="ws01">W01</a> &mdash; Artificial Intelligence Applied to Assistive Technologies and Smart Environments</a></h3>

<p>Ambient intelligence can help, transform, and enhance the way people with disabilities perform their activities of daily living, activities that would otherwise be difficult or impossible for them to do. However, despite the increasing trend toward the development of new assistive technologies to help people with disabilities, no real adoption tendency has been observed yet, regarding the targeted user groups. Indeed, users impairments and particularities are so diverse, that implementing complex technological solutions &mdash; mandatory for user adaptation &mdash; represents a major challenge in terms of universal design. In such a context, the main objective of this workshop is to investigate new solutions to scientific problems occurring in the various topics related to artificial intelligence applied in the domain of impaired people assistance.</p>

<h4>Topics</h4>

<p>This workshop will explore various topics including, but not limited to the following:</p>

<ul>
<li>Algorithms for plan, activity, intent, or behavior recognition or prediction </li>
<li>Personalization (user modeling, user profile, etc.) </li>
<li>Algorithms for intelligent proactive assistance </li>
<li>Context awareness </li>
<li>High-level activity and event recognition </li>
<li>Multi-person localization </li>
<li>Autonomic computing </li>
<li>High-level control of autonomous systems </li>
<li>Fault tolerance of assistive technologies </li>
<li>Pervasive and/or mobile cognitive assistance </li>
</ul>
<h4>Format</h4>

<p>This one-day workshop will consist of invited talks from experts, technical and position papers presentations organized into topical sessions (decided based on submissions), and a poster session depending on the participation. To encourage discussion, the workshop will be limited to 50 invited participants.</p>
<h4>Submissions</h4>

<p>The organizing committee is currently seeking either technical papers up to six pages in the conference format, or else, for poster presentations, authors should submit a short paper or extended abstract, up to 2 pages describing research relevant to the workshop.</p>
