<h3><a name="ws03">W03</a> &mdash; Artificial Intelligence for Cyber Security</a></h3>

<p>The workshop will focus on research and applications of artificial intelligence to cyber security, including machine learning, game theory, natural language processing, knowledge representation, and automated and assistive reasoning. The workshop will emphasize cyber systems and research on techniques to enable resilience in cyber security systems augmented by human-machine interactions.</p>

<p>The human in the loop has been recognized as a common point of weakness in the defense of cyber systems by the cyber security community. One promising approach to mitigate this problem is to build models of human behavior and enable formal reasoning about how human beings interact with cyber systems. Game theory, in particular, has been used to model human behavior and leveraging game-theoretic models of defense for physical security, to suggest incentives and punishments that can be used to induce more secure behavior. Another promising approach is to design automated tools that can perform the task of the human in the loop. This approach mitigates security issues arising from sub-optimal security decision taken by humans. Further, automated tools can be formally specified more precisely, and with stronger guarantees, than human behavior; this enables stating and proving strong formal guarantees about security of the overall cyber-system.</p>

<p>Both of the above techniques for understanding how humans and systems interact have been extensively studied in AI, but not in the context of cyber security. Addressing these challenges requires collaboration between several different research and development communities including the artificial intelligence, cyber-security, game theory, machine learning and formal reasoning communities.</p>

<h4>Topics</h4>
<ul>
	<li>Topics of interest include, but are not limited to:</li>
<li>Machine learning approaches to make cyber systems secure
	<br />Natural language processing techniques
	<br />Anomaly/threat detection techniques
	<br />Human behavioral modeling</li>
<li>Formal reasoning, with focus on human element, in cyber systems</li>
<li>Game theoretic reasoning in cyber security</li>
<li>Economics of cyber security</li>
<li>Multiagent interaction in cyber systems</li>
<li>Modeling and simulation of cyber systems and system components</li>
<li>Decision making under uncertainty in cyber systems</li>
<li>Automated security aids for system administrators</li>
<li>Quantitative human behavior models with application to cyber security </li>
</ul>
<h4>Format</h4>

<p>Invited speakers, presentations, panel and group discussions</p>

<h4>Attendance</h4>

