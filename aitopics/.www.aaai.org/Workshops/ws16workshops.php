<h3><a name="ws14">W14</a> &mdash; Scholarly Big Data: AI Perspectives, Challenges, and Ideas</a></h3>

<p>Academics and researchers worldwide continue to produce large numbers of scholarly documents including papers, books, technical reports, etc. and associated data such as tutorials, proposals, and course materials. For example, PubMed has over 20 million documents, 10 million unique names and 70 million name mentions. Google Scholar has many millions more, it is believed. Understanding how at scale research topics emerge, evolve, or disappear, what is a good measure of quality of published works, what are the most promising areas of research, how authors connect and influence each other, who are the experts in a field, and who funds a particular research topic are some of the major foci of the rapidly emerging field of Scholarly Big Data.</p>

<p>Digital libraries, repositories, databases, Wikipedia, funding agencies and the Web have become a medium for answering such questions. For example, citation analysis is used to mine large publication graphs in order to extract patterns in the data (e.g., citations per article) that can help measure the quality of a journal. Scientometrics is used to mine graphs that link together multiple types of entities: authors, publications, conference venues, journals, institutions, etc., in order to assess the quality of science and answer complex questions such as those listed above. The recent developments in Artificial Intelligence technologies make it possible to transform the way we analyze research publications, funded proposals, patents, etc., on a Web-wide scale.</p>
<h4>Topics</h4>

<p>The workshop aims at bringing together researchers with diverse interdisciplinary backgrounds interested in mining, managing and searching scholarly big data using new AI technologies or analyzing their transferability from one domain to another. The topics of interest include, but are not limited to: (a) New AI approaches to measuring the impact of research funding and publications as well as the impact of researchers in a particular field of study: Identifying influential authors, experts, and collaborators within or across disciplines; Modeling the referencing behavior across disciplines; Automatic citation recommendation; (b) Mining large digital libraries of scientific publications and linking to other databases such as funded proposals and patents: Identifying research trends and topics; Extracting relevant information from research articles, including an article's metadata and keyphrase extraction; Scaling up machine learning algorithms to large research and related datasets; Classification and clustering of scientific trends, publications, funded proposal, patents, etc; Large scale linking of various entities, e.g., articles with articles by similarity, articles with their corresponding presentation slides, articles with the corresponding funded proposals; (c) Presenting open-access, novel datasets (e.g., based on Wikipedia, DBpedia, United States Census Bureau data, Patent data, etc.) that can be linked to entities, and can help researcher develop novel technologies for analyzing scientific publications; (d) Effectively indexing and searching large scale academic documents and other resources.</p>
<h4>Format</h4>

<p>The workshop will have a series of presenters (10 to 12 paper presentations) and two one-hour keynote talks.</p>
<h4>Submissions</h4>

<p>Submissions related to the above topics are invited. Papers must not exceed six pages, must be written in English, and must be formatted according to the AAAI style files. We encourage contributions describing either new problems in scholarly big data or work on established problems using novel approaches. Submissions will made through <a href="https://easychair.org/conferences/?conf=aaai16sbd" rel="nofollow">EasyChair.</a></p>
