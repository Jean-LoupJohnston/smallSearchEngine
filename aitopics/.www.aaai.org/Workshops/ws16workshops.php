<h3><a name="ws04">W04</a> &mdash; Artificial Intelligence for Smart Grids and Smart Buildings</a></h3>

<p>The proliferation of intelligent devices and the availability of electric monitoring facilities, broadband communication networks, computational intelligence, and customer-driven electricity storage and generation capabilities, have posed the foundations for the next generation power grids and buildings: smart grids and smart buildings.</p>

<p>AI plays a key role in smart grids and in smart buildings; the infrastructure provides information to support automated decision making on how to autonomously adapt production and consumption of energy, optimize costs, waste, and environmental impact, and ensure safe, secure, and efficient operation. The goal of this workshop is to bring together researchers from diverse areas of AI to explore both established and novel applications of AI techniques to address the design, implementation, and deployment of smart grids and smart buildings. </p>

