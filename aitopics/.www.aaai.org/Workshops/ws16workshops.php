<h3><a name="ws15">W15</a> &mdash; Symbiotic Cognitive Systems</a></h3>

<p>In his 1960 article on Man-Machine Symbiosis, Licklider predicted a time when â¬Sthe main intellectual advances will be made by men and computers working together in intimate associationâ¬ý. While much of the emphasis within the AI community over the ensuing half century was placed upon tools for automation such as speech recognition or surpassing humans at challenging intellectual tasks such as chess or Jeopardy!, the last few years have witnessed a resurgent interest in symbiotic cognitive systems: collectives of humans and intelligent agents that collaborate to accomplish cognitive tasks better than either can alone.</p>

<p>The objective of this workshop is to bring together researchers working on aspects of symbiotic cognitive computing in various application domains to synthesize a new vision and research agenda for symbiotic cognitive systems and establish a community that will have a continued existence at future AI workshops and conferences.</p>
<h4>Topics</h4>
<p>
The workshop will explore technologies, architectures, applications, or other aspects of symbiotic cognitive computing, including but not limited to the following:
</p>

<ul>
<li>Cognitive assistants; </li>
<li>Natural and/or multi-modal interaction technologies, e.g. dialog, gesture </li>
<li>Technologies that support collaboration among humans and intelligent agents; </li>
<li>Technologies for creating, representing or using shared context as a vehicle for communication and understanding among humans and intelligent agents; </li>
<li>Planning for symbiotic tasks and/or interactions; </li>
<li>Agents that learn models of the environment and humans or other agents situated within it </li>
<li>Software or hardware architectures for symbiotic cognitive systems; </li>
<li>Descriptions and/or evaluations of symbiotic computing systems applied to application domains </li>
<li>Metrics and benchmarks for assessing the cognitive capabilities of human-agent collectives; </li>
<li>Architectures for cognitive agents or cognitive systems; </li>
<li>Theoretical topics pertinent to symbiotic cognitive systems; and </li>
<li>Vision statements for the future of symbiotic cognitive systems.</li>
</ul>
<h4>Format</h4>

<p>Full day of short invited talks, presentations based upon 4-page selected submissions, and moderated discussions among all participants.</p>
<h4>Attendance</h4>
25-40 people, invited on basis of involvement in field and/or submitted papers. 
<h4>Submissions</h4>

