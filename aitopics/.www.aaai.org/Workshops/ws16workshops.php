<h3><a name="ws16">W16</a> &mdash; World Wide Web and Population Health Intelligence</a></h3>

<p>Public health authorities and researchers collect data from many sources, and analyze these data together to estimate the incidence and prevalence of different health conditions, as well as related risk factors. Modern surveillance systems employ tools and techniques from artificial intelligence and machine learning to monitor direct and indirect signals and indicators of disease activities for early, automatic detection of emerging outbreaks and other health-relevant patterns. To provide proper alerts and timely response public health officials and researchers systematically gather news, and other reports about suspected disease outbreaks, bioterrorism, and other events of potential international public health concern, from a wide range of formal and informal sources.</p>

<p>Given the ever increasing role of the world wide web as a source of information in many domains including healthcare, accessing, managing, and analyzing its content has brought new opportunities and challenges. This is especially the case for non-traditional online resources such as social networks, blogs, news feed, twitter posts, and online communities with the sheer size and ever-increasing growth and change rate of their data. Web applications along with text processing programs are increasingly being used to harness online data and information to discover meaningful patterns identifying emerging health threats. The advances in web science and technology for data management, integration, mining, classification, filtering and visualization has given rise to variety of applications representing real time data on epidemics.</p>
<h4>Topics</h4>

<p>The workshop will include original contributions on theory, methods, systems, and applications of data mining, machine learning, databases, natural language processing, knowledge representation, artificial intelligence, semantic web, and big data analytics in web-based healthcare applications, with a focus on applications in public and population health. The scope of the workshop includes, but is not limited to, the following areas:</p>
<ul>
<li>Geographical mapping and visual analytics for health data</li>
<li>Social media analytics</li>
<li>Epidemic intelligence</li>
<li>Predictive modelling and decision support </li>
<li>Knowledge representation, semantic web, and web services</li>
<li>Biomedical ontologies, terminologies and standards</li>
<li>Bayesian networks and reasoning under uncertainty</li>
<li>Temporal and spatial representation and reasoning</li>
<li>Case-based reasoning in healthcare</li>
<li>Crowdsourcing, and collective intelligence</li>
<li>Risk assessment, trust, ethics, privacy, and security </li>
<li>Sentiment analysis and opinion mining</li>
<li>Computational behavioral/cognitive modeling</li>
<li>Health intervention design, modeling and evaluation</li>
<li>Online health education and e-learning </li>
<li>Mobile web interfaces and applications</li>
<li>Applications in epidemiology and surveillance (e.g. bioterrorism, participatory surveillance, population screening)</li>
</ul>

<p>Following the success of the 2014 and 2015 workshops, this workshop aims to bring together a wide range of computer scientists, researchers, students, industry professionals, national and international public health agencies, and NGOs interested in the theory and practice of computational models of web-based population health intelligence to highlight the latest achievements in epidemiological surveillance based on monitoring online communications and interactions on the world wide web. The workshop promotes open debate and exchange of opinions among participants.</p>
<h4>Format</h4>

<p>The workshop will be one full day consist of welcome session, keynote and invited talks, full/short paper presentations, demos, posters, and a panel discussion.</p>
<h4>Attendance</h4>

<p>Estimated number of attendance: 25-30</p>
<h4>Submissions</h4>

<p>We invite researchers and industrial practitioners to submit their original contributions following AAAI format through EasyChair. Three categories of contribution are sought: full-research papers up to 8 pages; short paper up to 4 pages; and posters and demos up to 2 pages.</p>
