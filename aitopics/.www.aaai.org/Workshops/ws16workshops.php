<li>Knowledge-based methods in design of smart grids and smart buildings</li>
<li>Coordination of intelligent agents in smart grids and smart buildings</li>
<li>Human-computer interactions within smart grids and smart buildings</li>
<li>Negotiation and trading strategies in energy markets </li>
<li>Simulations of energy markets, smart grids, and smart buildings</li>
</ul>
