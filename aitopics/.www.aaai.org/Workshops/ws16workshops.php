<h3><a name="ws05">W05</a> &mdash; Beyond NP</a></h3>

<p>A new computational paradigm has emerged in computer science over the past few decades, which is exemplified by the use of SAT solvers to tackle problems in the complexity class NP. According to this paradigm, a significant research and engineering investment is made towards developing highly efficient solvers for a prototypical problem (e.g., SAT), that is representative of a broader class of problems (e.g., NP). The cost of this investment is then amortized as these solvers are applied to a broader class of problems via reductions (in contrast to developing dedicated algorithms for each encountered problem). SAT solvers, for example, are now routinely used to solve problems in many domains, including diagnosis, planning, software and hardware verification.</p>

<p>The goal of this workshop is to help unify and promote research areas that advance this emerging computational paradigm, focusing on solvers that reach beyond NP. This includes, but is not limited to:</p>
<ul>
<li>Model counters, also known as #SAT solvers, which are now established as the prototypical solvers for the complexity class #P.</li>
<li>Knowledge compilers, which reach to other problems in the polynomial and counting hierarchies.</li>
<li>QBF solvers, which are now established as the prototypical solvers for the complexity class PSPACE.</li>
<li>Solvers for function problems, including optimization and subset minimal problems, e.g. MaxSAT, MUS and MCS, that reach different levels of the function polynomial hierarchy.</li>
</ul>

<p>These solvers are increasingly used to effectively tackle a broad class of problems (e.g., probabilistic graphical models, programming and databases; online configuration systems; verification, debugging, and testing).</p>

<h4>Topics</h4>
<p>Topics of interest to the workshop include algorithms; descriptions of implementations and/or evaluations of beyond NP solvers; their applications (including encodings); the complexity classes they reach; and their connections to one another. More broadly, submissions are solicited from three types of community members: those who develop solvers, those who use them to solve concrete problems, and those who are interested in the computational complexity of solvers and related problems. Submissions that can help disseminate â¬Sbest practicesâ¬ý among the relevant research areas are also encouraged (e.g., competitions, benchmarks, and the development of open-source solvers).</p>

<h4>Format</h4>
<p>The format of the workshop will include presentations, posters and potentially panels.</p>
<h4>Submissions</h4>

