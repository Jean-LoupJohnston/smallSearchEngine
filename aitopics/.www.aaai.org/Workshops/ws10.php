<h2 class="backgrnd12"><span class="text1">Workshops at the Twenty-Fourth AAAI Conference on Artificial Intelligence (AAAI-10)</span></h2>
<div class="content">


<p><i>Sponsored by the Association for the Advancement of Artificial Intelligence</i></p>

<p>AAAI wass pleased to present the AAAI-10 Workshop program. Workshops wereheld Sunday and Monday, July 11&ndash;12, 2010 at the Westin Peachtree Plaza in Atlanta, Georgia. 
The AAAI-10 workshop program included 13 workshops covering a wide range of topics in artificial intelligence. Workshops were one day unless noted otherwise in the individual description. Each workshop was limited to approximately 25 to 65 participants, and participation was usually by invitation from the workshop organizers. However, most workshops also allowed general registration by other interested individuals. There was a separate fee for attendance at a workshop, and is discounted for AAAI-10 technical registrants. Registration information was be mailed directly to all invited participants. (For information about AAAI-10, including registration, travel and accommodations, see the <a href="../Conferences/AAAI/aaai10.php">AAAI-10 web page)</a>. All workshop participants must preregister, and must indicate which workshop(s) they will be attending. Workshop reports are included in the workshop registration fee, and will be distributed onsite during the workshop. In most cases, reports were also available after the conference as part of the AAAI Press technical report series.</p>


