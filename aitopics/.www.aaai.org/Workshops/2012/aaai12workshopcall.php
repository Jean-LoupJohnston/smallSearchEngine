<h2 class="backgrnd12"><span class="text1">Workshops at the Twenty-Sixth AAAI Conference on Artificial Intelligence</span></h2>

<p><i>Call for Proposals</i></p>
<div class="hr1">
<hr />
</div>
<p>A printable <a href="http://www.aaai.org/Workshops/2012/aaai12workshopcall.pdf">PDF</a> of this call is also available</p>
<ul>
	<li>November 18, 2011: Proposals for workshops due</li>
	<li>January 6, 2012: Workshop CFP due at AAA</li>
	<li> January 27, 2012: Workshop calls to be posted on AAAI&rsquo;s website</li>
	<li> March 30, 2012: Workshop submissions due to organizers</li>
	<li> April 20, 2012: Notifications sent to authors</li>
	<li> April 20, 2012: List of participants due at AAAI</li>
	<li> May 16, 2012: Final workshop papers due at AAAI</li>
</ul>

<p>The AAAI-12 Program Committee invites proposals for the Workshop Program of the Association for the Advancement of Artificial Intelligence&rsquo;s Twenty-Sixth AAAI Conference on Artificial Intelligence (AAAI-12).</p>

<p>Workshops are scheduled to be held at the beginning of the conference, July 22&ndash;23, 2012. Workshop participants will have the opportunity to meet and discuss issues with a selected focus &mdash; providing an informal setting for active exchange among researchers, developers and users on topics of current interest. Members of all segments of the AI community are encouraged to submit proposals. To foster interaction and exchange of ideas, the workshops will be kept small, with 25&ndash;50 participants. Attendance is limited to active participants only. The format of workshops will be determined by their organizers, who are encouraged to leave ample time for general discussion. Workshops will typically be one full day in length, although half-day and two-day proposals will be considered.</p>
<h3 class="text2">Proposal Content</h3>
<p>Proposals for workshops should be about two (2) pages in length, and should contain the following:</p>
<ul>
	<li>A description of the workshop topic. Identify the specific issues on which the workshop will focus.</li>
	<li>A brief discussion of why the topic is of particular interest at this time.</li>
	<li>A brief description of the proposed workshop format, regarding the mix of events such as paper presentations, invited talks, panels, and general discussion.</li>
	<li>An indication as to whether the workshop should be considered for a half-day, one, or two-day meeting.</li>
	<li>The names and full contact information (e-mail and postal addresses, fax and telephone numbers) of the organizing committee &mdash; three or four people knowledgeable in the field-and short descriptions of their relevant expertise. (Please specify main contact.) Strong proposals include organizers who bring differing perspectives to the workshop topic and who are actively connected to the communities of potential participants.</li>
	<li>A list of potential attendees.</li>
</ul>
<p>Workshops are an excellent forum for exploring emerging approaches and task areas, for bridging the gaps between AI and other fields or between subfields of AI, for elucidating the results of exploratory research, or for critiquing existing approaches. Because workshops are intended for focused exploration of special topics, topics that are already the subject of regular meetings are not appropriate.</p>
<h3 class="text2">Workshop Organization</h3>
<p>Workshop organizers will be responsible for the following:<p>

<p>Producing a call for participation. The Call is due January 6, 2012. This Call will be posted on the AAAI web site. Organizers are responsible for additional publicity such as distributing the Call to relevant newsgroups and electronic mailing lists, and especially to potential audiences from outside the AAAI community. Organizers are encouraged to maintain their own web site with updated information about the workshop.</p>

<p>Selecting participants. Workshop attendance is by invitation of the organizers. Selection of attendees will be made by the organizers on the basis of submissions due March 30, 2012. Workshop organizers will need to provide AAAI with a preliminary list of the participants by April 20, 2012.</p>

<p>Coordinating the production of the workshop notes with AAAI. AAAI coordinates the collection, production, and distribution of the technical reports or working notes for the workshops. Workshop papers and abstracts must be received by AAAI no later than May 16, 2012, and volumes are limited to a total of 200 pages.</p>

<p>Workshop organizers who want to publish the papers from their workshop (or significant portions of it) will have the opportunity to do so through the AAAI Press. The Press (which retains the right of first refusal to publish) will furnish details of its program to interested organizers and authors.</p>

<p>AAAI will provide logistical support, and meeting places for the workshops, and will determine the dates and times of the workshops. AAAI reserves the right to drop any workshop if the organizers miss the above deadlines. Workshops are not to be used as a vehicle for marketing products. A reduced workshop registration fee will be offered to AAAI-12 technical program registrants.</p>
<h3 class="text2">Proposal Submission</h3>
<p>Workshop proposals should be submitted to the workshop cochairs at the addresses below as soon as possible and must be received no later than November 18, 2011. PDF format is preferred. Organizers will be notified of the committee&rsquo;s decision by December 16, 2011. Please address inquiries concerning workshop submissions to the workshop cochairs.</p>
