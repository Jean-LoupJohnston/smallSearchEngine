<h2 class="backgrnd12"><span class="text1">Workshops at the Twenty-Fifth AAAI Conference on Artificial Intelligence (AAAI-11)</span></h2>
<div class="content">


<p><i>Sponsored by the Association for the Advancement of Artificial Intelligence</i></p>

<p>AAAI is pleased to present the AAAI-11 Workshop program. Workshops will be held Sunday and Monday, August 7&ndash;8, 2011 at the at the Hyatt Regency San Francisco. The AAAI-11 workshop program includes 15 workshops covering a wide range of topics in artificial intelligence. Workshops are one day unless noted otherwise in the individual description. Each workshop is limited to approximately 25 to 65 participants, and participation is usually by invitation from the workshop organizers. However, most workshops also allow general registration by other interested individuals. There is a separate fee for attendance at a workshop, and it is discounted for AAAI-11 technical registrants. Registration information will be mailed directly to all invited participants. All workshop participants must preregister, and must indicate which workshop(s) they will be attending. Workshop reports are included in the workshop registration fee, and will be distributed in CD format during the workshop. In most cases, reports will also be available after the conference as part of the AAAI Press technical report series.</p>

<h4>Important Dates</h4>

<ul>
	<li>April 22, 2011: Workshop Submissions Due to Organizers </li>
	<li>May 13, 2011: Notifications Sent to Authors </li>
	<li>May 13, 2011: List of Participants Due at AAAI </li>
	<li>May 27, 2011: Final Workshop Papers Due at AAAI</li>
</ul>
