<h4>Submission Requirements</h4>

<p>Submission requirements vary for each workshop, but most key deadlines are uniform, unless otherwise noted. Submissions are due to the organizers on April 22, 2011, except where noted. Workshop organizers will notify submitters of acceptance by May 13, 2011. Accepted camera-ready copy is due on May 27, 2011. Please mail your submissions directly to the chair of the individual workshop according to their directions. Do not mail submissions to AAAI. For further information about a workshop, please contact the chair of that workshop.</p>

<h4>Format</h4>

<p>AAAI two-column format is often required for workshop submissions, and is <b>always</b> required for all final accepted submissions. Links to styles, macros, and guidelines for this format are located in  the <a href="http://www.aaai.org/Publications/Author/author.php">publications area</a> of the AAAI website</p>



