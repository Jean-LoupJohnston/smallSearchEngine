<h2 class="backgrnd12"><span class="text1">Workshops at the Twenty-Seventh AAAI Conference on Artificial Intelligence (AAAI-13)</span></h2>
<div class="content">


<p><i>Sponsored by the Association for the Advancement of Artificial Intelligence</i></p>


<p>AAAI is pleased to present the AAAI-13 Workshop Program. Workshops will be held Sunday and Monday, July 14&ndash;15, 2013 at the Hyatt Regency Bellevue Hotel in Bellevue, Washington, USA. Exact locations and dates for the workshops will be determined in early spring. The AAAI-13 workshop program includes 12 workshops covering a wide range of topics in artificial intelligence. Workshops are one day unless noted otherwise in the individual description. Participation in the workshop is limited to 25&ndash;65 participants, and participation is usually by invitation from the workshop organizers. However, most workshops also allow general registration by other interested individuals.</p><p>Please note that there is a separate registration fee for attendance at a workshop. Workshop registration is available for workshop only registrants or for AAAI-13 technical registrants at a discounted rate. Registration information will be mailed directly to all invited participants. Electronic copies of the workshop technical reports are included in the workshop registration fee, and will be distributed onsite during the workshop. In most cases, workshop papers will also be available after the conference as part of the AAAI Press technical report series.</p>
