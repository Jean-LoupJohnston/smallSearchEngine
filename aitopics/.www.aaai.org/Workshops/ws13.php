<h3 class="text2">Submission Requirements</h3>

<p>Submission requirements vary for each workshop, but key deadlines are uniform. Submissions are due to the organizers on April 3, 2013 (please check individual workshop websites for extensions). Workshop organizers will notify submitters of acceptance by April 19, 2013. Camera-ready copy is due to AAAI by May 9, 2013 (firm deadline &ndash; no extensions). Please submit your papers for review directly to the individual workshop according to their directions &mdash; do not mail submissions to AAAI. For further information about a workshop, please contact the chair of that workshop.</p>

<h3 class="text2">Formats</h3>

<p>AAAI two-column format is often required for workshop submissions, and will be required for all final accepted submissions. Styles, macros, and guidelines for this format are located in the <a href="http://www.aaai.org/Publications/Templates/AuthorKit.zip">AAAI Press Author Kit.</a>


