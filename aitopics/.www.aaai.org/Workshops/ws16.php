<h3 class="text2">Submission Requirements</h3>

<p>
Submission requirements vary for each workshop, but key deadlines are uniform. Submissions are due to the organizers on October 23, 2015 (please check individual workshop websites for extensions). Workshop organizers will notify submitters of acceptance by November 23, 2015. Camera-ready copy is due to AAAI by <b>December 7, 2015</b> (firm deadline). Please submit your papers for review directly to the individual workshop according to their directions. Do not mail submissions to AAAI. For further information about a workshop, please contact the chair of that workshop.</p>

<h3 class="text2">Formats</h3>

<p>
AAAI two-column format is often required for workshop submissions, and will be <b>required</b>for all final accepted submissions. Styles, macros, and guidelines for this format are located in the <a href="http://www.aaai.org/Publications/Templates/AuthorKit16.zip" rel="nofollow">AAAI-16 AuthorKit</a> which is available for download.
</p>





