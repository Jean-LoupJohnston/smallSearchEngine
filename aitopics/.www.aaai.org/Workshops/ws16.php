<p>
AAAI is pleased to present the AAAI-16 Workshop Program. Workshops will be held  Friday and Saturday, February 12&ndash;13, 2016 at the Phoenix Convention Center in Phoenix, Arizona, USA. Exact locations and dates for the workshops will be determined in January. The AAAI-16 workshop program includes 16 workshops covering a wide range of topics in artificial intelligence. Workshops are one day unless noted otherwise in the individual description. Participation in each workshop is generally limited to 25-65 participants, and participation is usually by invitation from the workshop organizers. However, most workshops also allow general registration by other interested individuals. Please note that there is a separate registration fee for attendance at a workshop. Workshop registration is available for workshop only registrants or for AAAI-16 technical registrants at a discounted rate. Registration information will be mailed directly to all invited participants. Electronic copies of the workshop technical reports are included in the workshop registration fee. In most cases, workshop papers will also be available after the conference as part of the AAAI Press technical report series.</p>

