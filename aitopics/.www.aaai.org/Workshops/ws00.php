<h2 class="backgrnd12"><span class="text1">Workshops at the Seventeenth National Conference on Artificial Intelligence</span></h2>
<div class="content">

<p><i>Sponsored by the Association for the Advancement of Artificial Intelligence</i></p>
<hr />

<p>AAAI-2000 Workshops were held Sunday and Monday, July 30-August 1, at the Hyatt Regency Austin and the Austin Convention Center in Austin, Texas. 
The workshops that were held are listed below:</p>
<ul>
<li><a href="javascript:new_window('2000/ws00.pdf')">Complete List in PDF format</a></li>
<li>Agent-Oriented Information Systems (W1)
<br /><i>Organizer:</i> Yves Lesp&eacute;rance, Gerd Wagner, and Eric Yu, (Sunday, July 30)</li>

<li>Artificial Intelligence and Enterprise Resource Planning / Customer Response Management Systems (W2)<br />
<b>This workshop was CANCELLED</b><br />
<i>Organizer:</i> Daniel O'Leary (Monday, July 31)</li>

<li>Artificial Intelligence and Music: Towards Formal Models for Composition, Performance, and Analysis (W3)<br />
<i>Organizers:</i> William Birmingham, Roger Dannenberg, and Gerhard Widmer, (Monday, July 31)</li>

<li>Artificial Intelligence for Web Search (W4)<br />
<i>Organizers:</i> Kurt Bollacker, C. Lee Giles, and Steve Lawrence (Sunday, July 30)</li>

<li>Constraints and AI Planning (W5)<br />
<i>Organizer:</i> Alexander Nareyek, (Sunday, July 30)</li>

<li>Constraint Databases for AI (W6)<br />
<b>This workshop was CANCELLED</b><br />
<i>Organizers:</i> Marc Gyssens and Peter Revesz,  (Monday, July 31)</li>

<li>Integration of AI and OR Techniques for Combinatorial Optimization (W7)<br />
<i>Organizers:</i> James M. Crawford and J. Paul Walser,  (Sunday, July 30)</li>

<li>Intelligent Lessons Learned Systems (W8)<br />
<i>Organizers:</i> David Aha and Rosina Weber, (Monday, July 31)</li>

<li>Knowledge-Based Electronic Markets (W9)<br />
<i>Organizers:</i> Tim Finin and Benjamin Grosof,  (Monday, July 31 (may expand to Sunday, July 30))</li>

<li>Learning from Imbalanced Data Sets (W10)<br />
<i>Organizer:</i> Nathalie Japkowicz, (Monday, July 31)</li>

<li>Learning Grounded Representations (W11)  <br />
<b>This workshop was CANCELLED</b><br />
<i>Organizers:</i> Paul Cohen  and Tim Oates,  (Monday, July 31)</li>

<li>Learning Statistical Models from Relational Data (W12)<br />
<i>Organizers:</i> Lise Getoor and David Jensen  (Monday, July 31)</li>

<li>Leveraging Probability and Uncertainty in Computation (W13)<br />
<i>Organizers:</i> Carla Gomes and Holger Hoos,  (Monday, July 31)</li>

<li>Mobile Robotic Competition and Exhibition Workshop (W14)<br />
<i>Organizer:</i> Alan Schultz,  (Thursday, August 3)</li>

<li>Modeling Problems in Constraint Programming (W15)<br />
<b>This workshop was CANCELLED</b><br /><i>Organizers:</i> Jean-Charles Regin and Wim Nuijten,  (Monday, July 31)</li>

<li>New Research Problems for Machine Learning (W16)<br />
<i>Organizers:</i> Mirek Kubat and Tom Mitchell, (Sunday, July 30)</li>

<li>Parallel and Distributed Search for Reasoning (W17)<br />
<i>Organizer:</i> Joerg Denzinger, (Sunday, July 30)</li>

<li>Representational Issues for Real-World Planning Systems (W18)<br />
<i>Organizers:</i> Yolanda Gil  and Karen Myers,  (Monday, July 31)</li>

<li>Spatial and Temporal Granularity (W19)<br />
<i>Organizers:</i> Claudio Bettini and Angelo Montanari (Sunday, July 30)</li>

<li>Spatial and Temporal Reasoning for Collaborating Mobile Agents (W20)<br />
<b>This workshop was CANCELLED</b><br />
<i>Organizers:</i> Frank Anger  and Hans Guesgen,  (Sunday, July 30)</li>
</ul>

<hr /><p>For further
information about the 2000 Workshop Program, consult the following:</p>
<ul><li><a href="http://www.aaai.org/Workshops/2000/ws00proposals.pdf">Call for Proposals (2000)</a></li>
<li><a href="http://www.aaai.org/Workshops/2000/ws00participation.pdf">Call for Participation (2000)</a></li>
</ul>
