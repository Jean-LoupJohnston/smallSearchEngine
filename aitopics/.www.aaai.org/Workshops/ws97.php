<h2 class="backgrnd12"><span class="text1">Workshops at the Fourteenth National Conference on Artificial Intelligence</span></h2>
<div class="content">



<p><i>Sponsored by the Association for the Advancement of Artificial Intelligence</i></p>
<hr />
<p>The AAAI-97 Workshop program was held July 27&ndash; 28, 1997, at the Rhode Island Convention Center and surrounding hotels in Providence, Rhode Island</p>

<p>AAAI-1997 Workshops were held Sunday and Monday, July 27-28, at the Rhode Island Convention
Center and surrounding hotels in Providence, Rhode Island. 
The workshops that were held are listed below:</p>
<ul>
<li>W2: AI and Knowledge Management<br />
<i>Organizer:</i> Bradley Whitehall, (Sunday, July 27)
</li>

<li>W3: AI Approaches to Fraud Detection and Risk Management<br />
<i>Organizer:</i>: Tom Fawcett, (Sunday, July 27)</li>

<li>W4: Building Resource-Bounded Reasoning Systems<br />
<i>Organizer:</i>: Shlomo Zilberstein, (Sunday, July 27)
</li>

<li>W5: Constraints and Agents<br />
<i>Organizer:</i>: Eugene C. Freuder, (Sunday, July 27)
</li>

<li>W7: Language and Space (two-day workshop)<br />
<i>Organizer:</i>: Patrick Olivier, (Sunday, July 27 and Monday, July 28)
</li>

<li>W13: Verification &AMP; Validation of Knowledge-Based Systems<br />
<i>Organizer:</i>: Robert Plant, (Sunday, July 27)
</li>

<li>W1: Abstraction, Decisions, and Uncertainty<br />
<i>Organizer:</i>: Christopher Geib, (Monday, July 28)
</li>

<li>W6: Deep Blue Versus Kasparov: The Significance for Artificial Intelligence<br />
<i>Organizer:</i>, (Monday, July 28)
</li>

<li>W7: Language and Space (two-day workshop)<br />
<i>Organizer:</i>: Patrick Olivier, (Monday, July 28)
</li>

<li>W8: Multiagent Learning<br />
<i>Organizer:</i>: Sandip Sen, (Monday, July 28)
</li>

<li>W9: On-Line Search<br />
<i>Organizer:</i>: Sven Koenig, (Monday, July 28)</li>

<li>W10: Robots, Softbots, Immobots: Theories of Action, Planning and Control<br />
<i>Organizer:</i>: Chitta Baral, (Monday, July 28)
</li>

<li>W11: Spatial and Temporal Reasoning<br />
<i>Organizer:</i>: Frank D. Anger, (Monday, July 28)
</li>

<li>W12: Using AI in Electronic Commerce, Virtual Organizations and Enterprise Knowledge Management to Reengineer the Corporation<br />
<i>Organizer:</i>: Daniel E. O'Leary, (Monday, July 28)
</li>

</ul>
<hr /><p>For further
information about the 1997 Workshop Program, consult the following:</p>
<ul><li><a href="http://www.aaai.org/Workshops/1997/ws97proposals.pdf">Call for Proposals (1997)</a></li>
<li><a href="http://www.aaai.org/Workshops/1997/ws97participation.pdf">Call for Participation (1997)</a></li>
</ul>
