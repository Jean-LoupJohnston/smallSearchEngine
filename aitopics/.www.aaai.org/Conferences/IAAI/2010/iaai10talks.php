<h3 class="text2"><i><a name="Vinge">IAAI-10 Invited Talk</a></i><br />
Species of Mind</h3>
<p><b>Vernor Vinge (San Diego State University)</b></p>

<p>More than any other animal, we humans invent ways to outsource cognitive function. We've been doing this for a long time. For instance, writing is an outsourcing of memory; money is a scalar that allows the comparison of vastly different objects.</p>

<p>During the last century, the outsourcing process has become more diverse and intense. The range of our recent activities is leading toward a number of "different kinds" of superhuman intelligence.</p>

<p>In this presentation, I'll discuss several different paths to superintelligence, their relative power, the transformations they might create, and how humans might deal with them.</p>

<p><img src="http://www.aaai.org/Conferences/IAAI/2010/Photos/vinge.jpg" alt="Vernor Vinge" width="59" height="75" class="img1" /><b><a href="http://www-rohan.sdsu.edu/faculty/vinge/misc/singularity.html" rel="nofollow">Vernor Vinge</a></b> is a science-fiction writer, winner of five Hugo Awards. He holds a PhD (mathematics) from University of California, San Diego and taught mathematics and computer science at San Diego State University from 1972 to 2000. In 1982, at a panel for AAAI-82, he proposed that technology would accelerate the evolution of intelligence, leading to a kind of "singularity" beyond which merely human extrapolation was essentially impossible. In the 1980s and 1990s, he elaborated on this theme in both his science fiction and nonfiction writing.</p>


