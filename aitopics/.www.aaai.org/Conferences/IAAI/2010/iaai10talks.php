<h3 class="text2"><i><a name="Tenenbaum">Robert S. Engelmore Memorial Award Lecture</a></i><br />
Cancer: A Computational Disease that AI Can Cure</h3>
<p><b>Jay M. Tenenbaum (CollabRx Inc.)</b></p>

<p>Cancer results from finite genomic mutations that biotechnology can easily list, and that we can mostly understand and reason about in terms of the underlying biochemistry. Tragically, the scientific and medical communities are searching for cures using an incredibly inefficient non-adaptive strategy, where the costs of experiments are measured in lives, as well as money, and where we capture only a small portion of the genomics and outcomes data, i.e., in clinical trials. Inspired by my career experiences as an AI researcher, Internet entrepreneur and cancer survivor, I am attempting to redress this situation through Cancer Commons, a "rapid learning" community of patients, physicians and researchers. Our goal is to cure cancer by collecting the genomic and response data from thousands of adaptively-planned individual treatment experiments, integrating the resulting sparse fragments of evidence to infer the true causal mechanisms of tumors and drugs, and generalizing the resulting knowledge so that it can be applied to new cases. Each patient is treated in accord with the best available knowledge, and that knowledge is continually updated to benefit the next patient. Hopefully, this adaptive approach will efficiently climb the hill to find cures for cancer, one patient at a time.</p>

<p><img src="http://www.aaai.org/Conferences/IAAI/2010/Photos/tenenbaum.jpg" alt="Jay M. Tenenbaum" width="61" height="75" class="img1" /><b>Jay M. Tenenbaum</b> was educated at the Massachusetts Institute of Technology and Stanford University in the 1960s. He spent the 1970s doing artificial intelligence research at SRI, the 1980s managing computer science research for Schlumberger, and the 1990s pioneering Internet commerce. He's currently focused on using the AI and the web to transform medicine.</p>


