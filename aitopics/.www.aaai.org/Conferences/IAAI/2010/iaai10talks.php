<h3 class="text2"><i><a name="Alwan">IAAI-10 Invited Talk</a></i><br />
A New Paradigm of Geriatric Care Empowered by Applied AI</h3>
<p><b>Majd Alwan (Center for Aging Services Technologies)</b></p>


<p>Advances in sensor, communication, artificial intelligence technologies and data processing, coupled the increasing processing power, is causing a shift in the way we care for the elderly. This paper presents a new paradigm for geriatric care based on monitoring and assisting older adults in their own living settings. AI techniques could be applied to mine health and activity data collected in the home to detect indicators of early disease onset, deterioration or improvement, inform providers and allow the delivery of care services, including assistance and support. Examples of monitoring and assistive systems that apply AI techniques are discussed. The approach has significant value to older adults as well as caregivers, and allows care providers to extend services into the community.</p>

<p><img src="http://www.aaai.org/Conferences/IAAI/2010/Photos/alwan.jpg" alt="Majd Alwan" width="50" height="75" class="img1" /><b>Majd Alwan</b> is the director of the Center for Aging Services Technologies (CAST). He is responsible for creating and leading a network of technology companies, providers and research institutions focused on technology solutions for an aging society. Prior to joining CAST, Majd served as an assistant professor and the director of the robotics and eldercare technologies program at the University of Virginia's Medical Automation Research Center. His research interests included passive functional and health assessment, biomedical instrumentation, as well as eldercare and assistive technologies.</p>

<p>Alwan received his Ph.D. in intelligent robotics from Imperial College of Science, Technology and Medicine, University of London, a Master's of Science degree in control engineering with distinction from Bradford University and a bachelor's degree in electrical engineering from Damascus University. He is a Senior Member of the IEEE.</p>

