			<h2 class="bg12"><span class="text1">The Twenty-Fifth Annual Conference on Innovative Applications of Artificial Intelligence</span></h2>
<h3 class="text2">Please Join Us!</h3>
<p>The Twenty-Fifth Annual Conference on Innovative Applications of Artificial Intelligence (IAAI-13) will be held July 14&ndash;18, 2013 in Bellevue, Washington, USA. The conference will focus on successful applications of AI technology. The conference will use technical papers, challenge papers, invited talks, and panel discussions to explore issues, methods, and lessons learned in the development and deployment of AI applications; and to promote an interchange of ideas between basic and applied AI.</p>
<p>IAAI-13 will feature papers in three tracks: (1) deployed application case studies, (2) challenge problem papers, and (3) emerging applications or methodologies. Case-study papers describe deployed applications with measurable benefits that include some aspect of AI technology. The application needs to have been in production use by its final end-users (not the people who created it) for sufficiently long that the experience in use can be meaningfully collected and reported. This period typically spans at least three months. 
Challenge problem papers identify challenges to applying artificial intelligence to real world problems. Challenge problems might include roadblocks to applying existing AI technology to new tasks, or fundamentally new technologies needed for new tasks. Emerging application papers bridge the gap  between basic AI research and deployed AI applications, by discussing efforts to apply AI tools, techniques, or methods to real world problems. The papers report on the engineering of AI applications.</p>
<p>We hope to see you in Bellevue, Washington!</p>
<p>Hector Munoz-Avila, <i>Conference Chair</i> (Lehigh University, USA)<br />
David Stracuzzi, <i>Conference Cochair</i> (Sandia National Laboratories, USA)</p>
