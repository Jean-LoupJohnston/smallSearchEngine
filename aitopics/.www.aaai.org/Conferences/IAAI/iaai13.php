<h3 class="text2">Getting to Bellevue</h3>
<p>The AAAI-13 conference will be held in Bellevue Washington. The closest international airport is  Sea-Tac International Airport (SEA), about 30 minutes away by taxi. For more information about ground transportation options between Sea-Tac International Airport and the Hyatt, please visit  the <a href="http://www.portseattle.org/Sea-Tac/Parking-and-Transportation/Ground-Transportation" rel="nofollow">Sea-Tac,</a><a href="http://www.visitbellevuewashington.com/gettinghere/buses-shuttles.shtml"> Visit Bellevue Washington,</a> or service provider websites.</p>
