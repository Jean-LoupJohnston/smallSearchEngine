<h3 class="text2">The Venue</h3>

<p>Bellevue, Washington, is not only surrounded by the magnificent natural beauty and outdoor spaces of the Pacific Northwest, it is at the hub of a vibrant AI community centered at the University of Washington, Microsoft, Facebook, Amazon, Boeing, and many other Seattle area research centers. Bellevue is a thoroughly walkable city with a host of shopping and dining options adjacent to the Hyatt Regency conference hotel. For outdoor enthusiasts, Bellevue boasts miles and miles of nature trails, public parks and swim beaches within easy walking distance of downtown. Located on Seattle's eastside, Bellevue is also the gateway to the entire Puget Sound region. Within minutes you can explore Seattle's culture and history, visit Kirkland's waterfront parks and galleries, or tour Woodinville's Wine Country, home to 70 wineries and some of the world's best wines. You can even quench your taste for adventure with a day-trip to nearby mountains for hiking, biking, or skiing. For more information, please consult the <a href="http://www.visitbellevuewashington.com/">Visit Bellevue website.</a></p>
