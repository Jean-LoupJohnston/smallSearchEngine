<h3>The Conference Venue</h3>
<p>Vancouver, British Columbia is a dynamic, multicultural city set in a spectacular natural environment. The site of the 2010 Olympics, British Columbia&rsquo;s majestic mountains, ocean, rainforests, and beautiful foliage make Vancouver one of the most beautiful cities in the world. A clean, safe city, Vancouver was voted the &ldquo;Best City in the Americas&rdquo; for 2004 and 2005, based on the categories of ambience, friendliness, culture and sites, restaurants, lodging, and shopping.</p>


