<h3>IAAI-07 Slate of Invited Speakers</h3>
<p>
<p>IAAI-07 features an exciting slate of invited speakers, including Matt Brown from Maxis Electronic Arts, Geoffrey Ling from DARPA&mdash;the military&rsquo;s only neurocritical care physician, and Oren Etzioni from the University of Washington</p>



<img src="http://www.aaai.org/Conferences/IAAI/2007/etzioni.jpg" alt="Oren Etzioni" width="56" height="75" class="img1" /><h3 class="text2">AI in a Moore's Law World: the Stories of Farecast and KnowItAll</h3>
<p><i>Robert S. Engelmore Memorial Award Lecture</i></p>
<p><b>Oren Etzioni (University of Washington)</b> 
Wednesday, July 25. 10:20 &ndash; 11:20 AM</p> 

<p>What is the role for AI as CPUs, disks, and networks become exponentially more powerful? Perhaps, as some have suggested, the application of simple processing techniques to unprecedented mountains of data will suffice. Alternatively, perhaps Luis von Ahn's paradigm of "Human Computation" will yield any intelligence that is required. I will argue, to the contrary, that sophisticated AI techniques such as data mining and information extraction are necessary for exciting new applications. But these techniques have to be improved in important ways. I will make these points through the stories of Farecast.com and the KnowItAll research project.</p>

<p>Oren Etzioni is a professor of computer science at the University of Washington's Computer Science Department, and the founder and director of the university's Turing Center. He received his Ph.D. from Carnegie Mellon in 1990, and his B.A. from Harvard in 1986. Etzioni has authored over 100 technical papers on topics ranging from intelligent agents to data mining and web search. In 2005, he was awarded the IJCAI Distinguished Paper Award for "A Probabilistic Model of Redundancy in Information Extraction." He received a National Young Investigator Award in 1993, and was chosen as a AAAI Fellow a decade later.</p>

<p>Etzioni is the founder of three companies. Most recently, he is the founder of Farecast, a company that utilizes data mining to inform consumers about the right time to buy their air tickets. Farecast has won numerous awards in 2006 including <i>TIME's</i> "50 Coolest Web Sittes" and PC World's "20 Most Innovative Products." His work has been featured in the <i>New York Times, Wall Street Journal,</i> NPR,NBC Nightly News, <i>SCIENCE, The Economist, TIME Magazine, Business Week, Newsweek, Discover Magazine, Forbes Magazine, Wired, </i> and elsewhere.</p>



<img src="http://www.aaai.org/Conferences/IAAI/2007/brown.jpg" alt="Matt Brown" width="55" height="75" class="img1" /><h3 class="text2">Big "A," Small "I": Smart Ends from Simple Means</h3>
<p><b>Matt Brown (Maxis / Electronic Arts)</b> 
Thursday, July 26. 10:20 &ndash; 11:20 AM</p> 

<p>Matt Brown's talk will cover various elements of the game design, behavioral AI and structure behind <i>The Sims 2</i> as well as future efforts in products such as <i>The Sims</i> and <i>SimCity.</i> Emphasis will be on simplification of abstraction and underlying world representations, prototyping and demonstration of practical results. Discussion will focus around players' perceptual models and manipulating available information and expectations to simplify design.</p>

<p>Matt Brown was the technical director and designer for Electronic Art's FIFA franchise for many years and later for <i>The Sims 2</i> at Maxis. He is now the creative/design director for future versions of <i>The Sims</i> and <i>SimCity.</i>. He is obsessed with creating the illusion of life through simplification, scale, and slight of hand as well as the practical application of AI.</p>


<img src="http://www.aaai.org/Conferences/IAAI/2007/ling.jpg" alt="Geoffrey S. F. Ling" width="60" height="75" class="img1" />
<h3 class="text2">Revolutionizing Prostheses: A Program of the Defense Advanced Research Projects Agency (DARPA)</h3>
<p><b>Geoffrey S. F. Ling (DARPA)</b> 
Tuesday, July 24. 1:50 &ndash; 2:50 PM</p> 

<p>The Defense Advanced Research Projects Agency (DARPA) sponsors revolutionary research targeted at bridging gaps between the needs of the warfighter and current technological, medical, and scientific capabilities. In the realm of neurology, specifically, the Human Assisted Neural Devices (HAND) and Revolutionizing Prosthesis programs are efforts aimed at advancing the interfaces between assistive devices and users by leveraging off of biological capabilities. Both programs have provided the basis for paradigm shifts in treatment of extremity injury and traumatic brain injury.</p>

<p>Geoffrey Ling is a program manager with the Defense Advance Research Projects Agency (DARPA). There, he is responsible for a portfolio of programs that include Revolutionizing Prosthesis, Human Assisted Neural Devices, Preventing Violent Explosive Neuro Trauma, Predicting Health and Disease and Long Term Blood Storage. He is also developing new programs with the intent of protecting and restoring injured warfighters.</p>

<p>Ling is also professor and vice chairman of neurology at the Uniformed Services University of the Health Sciences in Bethesda, Maryland. Currently on active duty as an army colonel, he is the military's only neurocritical care physician. At Walter Reed Army Medical Center, he is director of neurointensive care. He is also an associate professor (adjunct) at Johns Hopkins Hospital where he attends on the Neuro Critical Care Unit. He has served two combat deployments as a neuro intensive care physician that included both Operation Enduring Freedom (Afghanistan) and Operation Iraqi Freedom (Iraq). Notably, he served with the 86th Combat Support Hospital ("Baghdad ER").</p>

<p>Geoffrey Ling received his B.A. with honors from Washington University in St. Louis, his M.D. from Georgetown University and his Ph.D. (pharmacology) from Cornell University.
</p>
