<h2 class="backgrnd12"><span class="text1">
The Nineteenth Conference on Innovative Applications of Artificial Intelligence</span></h2>
<div id="box1"><a href="iaai07.php"><img src="http://www.aaai.org/Conferences/IAAI/2007/coalharbouriaai.jpg" alt="Vancouver, BC" width="639" height="167" border="0" /></a></div>
<div class="content">

<p><i>Sponsored by the Association for the Advancement of Artificial Intelligence</i></p>

<p>AAAI was pleased to announce that the Nineteenth Innovative Applications of Artificial Intelligence Conference (IAAI-07), chaired by William Cheetham and cochaired by Mehmet Goker, was collocated with the <a href="../AAAI/aaai07.php">Twenty-Second National Conference on Artificial Intelligence.</a> Both conferences  were held in Vancouver, British Columbia, Canada at the Hyatt Regency Vancouver. The dates of the conferences were July 22&ndash;26 2007.</p>

