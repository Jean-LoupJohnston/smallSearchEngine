<h2 class="backgrnd5"><span class="text1">Technical Programs</span></h2>

<h3 class="text2">Emerging Application or Methodologies Papers</h3>
<p class="backgrnd6">The goal of the emerging application track is to &ldquo;bridge the gap&rdquo; between basic AI research and deployed AI applications, by discussing efforts to apply AI tools, techniques, or methods to re- al world problems.Emerging applications are on aspects of AI applications that are not appropriate for deployed application case studies, or are not sufficiently deployed to be submitted as case studies. This track is distin- guished from reports of scientific AI research appropriate for the AAAI-12 Conference in that the objective of the efforts reported here should be the engineering of AI applications..</p>

<h3 class="text2">Deployed Application Case Study Papers</h3>
<p class="backgrnd6">Case-study papers must describe deployed applications with measurable benefits that include some aspect of AI technology. The application needs to have been in production use by their final end-users (typically for at least three months). The case study may evaluate either a stand-alone application or a component of a complex system.</p>

