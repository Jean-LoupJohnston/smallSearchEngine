<h2 class="backgrnd7"><span class="text1">Please Join Us for IAAI-12!</span></h2>

<div class="content">
<p>AAAI is delighted to announce that the Twenty-Fourth IAAI Conference on Artificial Intelligence will be held will be held in Toronto, Ontario, Canada at the Sheraton Centre Toronto, from July 22&ndash;26, 2012.</p>
<p>The purpose of the IAAI-12 conference is to promote research in AI and scientific exchange among AI researchers, practitioners, scientists, and engineers in related disciplines.  On this page, you will find links to the wide variety of programs being held at IAAI-12 as they become available.
</p>
<p>We hope to see you in  Toronto!</p>
<p>Markus Fromherz (Program Chair) and Hector Munoz-Avila (Program Cochair)</p>
