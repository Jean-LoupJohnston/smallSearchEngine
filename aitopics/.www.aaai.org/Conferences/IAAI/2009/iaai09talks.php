<h3>Robert S. Engelmore Memorial Award Lecture<br />
Building Virtual Humans: a Decade of Research
</h3>
<p><i>William R. Swartout (USC Institute for Creative Technologies)</i></p>

<p>For the last ten years, we have been building virtual humans &mdash; computer-generated characters &mdash; at ICT. Ultimately, virtual humans will look and behave just like real people. They will think on their own, model and exhibit emotions, interact using natural language along with the full repertoire of verbal and non-verbal communication techniques that people use. Although the realization of that goal is still in the future, making steps toward it has required us to weave together different threads of AI research such as computer vision, natural language understanding and emotion modeling that are often treated as independent areas of investigation. Interestingly, this is not just an exercise in systems integration, but instead has revealed synergies across areas that have allowed us to address problems that are difficult to solve if addressed from one perspective alone. I will illustrate some of these synergies in the talk.</p>

<p>Clearly, the goals we have for virtual humans are very ambitious and they would be daunting without some way of limiting the scope. We have found that embedding the virtual humans into a socially-motivated, story-based simulation can create a strong context that limits the range of interaction for both real and virtual humans and makes virtual humans more feasible. We have also found that socially-motivated stories can suggest new areas of research for virtual humans. I will discuss the role of story in our work.</p>

<p>Finally, I will suggest future areas of research in virtual humans and suggest what might be possible in the not too distant future.</p>

<p>William Swartout is director of technology for the USC Institute for Creative Technologies. His particular research interests include virtual humans, intelligent agents and the development of new AI architectures. He is a Fellow of the Association for the Advancement of Artificial Intelligence (AAAI) and is past chair of the Special Interest Group on Artificial Intelligence (SIGART) of the Association for Computing Machinery (ACM). He was Program cochair for AAAI-90, and has served as AAAI conference committee Chair and on the AAAI Board of Councilors. He received his Ph.D. and M.S. in computer science from MIT and his bachelor's degree from Stanford University.</p>

<h3>Invited Talk<br />Human Computation</h3>
<p><i>Luis von Ahn (Carnegie Mellon University)</i></p>
<p><a href="http://videolectures.net/iaai09_vonahn_hc/" rel="nofollow">Video</a></p>
<p>This talk is about harnessing human brainpower to solve problems that computers cannot. Although computers have advanced dramatically over the last 50 years, they still do not possess basic conceptual intelligence or perceptual capabilities that most humans take for granted. By leveraging human abilities in a novel way, I want to solve large-scale computational problems and collect data to teach computers basic human talents. To this end, I treat human brains as processors in a distributed system, each performing a small part of a massive computation.</p>

<p>Luis von Ahn works in the Computer Science Department at Carnegie Mellon University. He is the recipient of a MacArthur Fellowship and a Microsoft New Faculty Fellowship. He has been named one of the 50 Best Minds in Science by <i>Discover Magazine,</i> one of the "Brilliant 10" of 2006 by <i>Popular Science Magazine,</i> one of the 50 most influential people in technology by Silicon.com, and one of the Top Innovators in the Arts and Sciences by <i>Smithsonian Magazine.</i> His research interests include encouraging people to do work for free, as well as catching and thwarting cheaters in online environments.</p>

<h3>Invited Talk<br />Computational Knowledge, Science and Wolfram|Alpha
</h3>
<p><i>Stephen Wolfram (Wolfram Research)</i></p>
<p><a href="http://videolectures.net/iaai09_wolfram_ckswa/" rel="nofollow">Video</a></p>

<p>Wolfram will describe the concepts, technology and science that underlie Wolfram|Alpha&mdash;an ambitious project to make as much knowledge as possible computable.</p>

<p>Stephen Wolfram is the founder and CEO of Wolfram Research, the creator of Mathematica, the author of <i>A New Kind of Science</i> and now the creator of Wolfram|Alpha.</p>




