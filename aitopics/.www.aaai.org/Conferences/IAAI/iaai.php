<h2 class="backgrnd12"><span class="text1">Innovative Applications of Artificial Intelligence Conference (IAAI)</span></h2>

<div class="content">
<p><i>Sponsored by the Association for the Advancement of Artificial Intelligence
</i></p><hr />
<p>Innovative Applications of Artificial Intelligence Conferences (IAAI) traditionally consist of case studies of deployed applications with measurable benefits whose value depends on the use of AI technology. In addition, many IAAI conferences augment these case studies with papers and invited talks that address emerging areas of AI technology or applications. IAAI is organized as an independent program within the AAAI National Conference, with schedules coordinated to allow attendees to move freely between IAAI and AAAI Conference sessions. IAAI and the National Conference often jointly sponsor invited talks that fit the theme of both programs</p>
<hr />



