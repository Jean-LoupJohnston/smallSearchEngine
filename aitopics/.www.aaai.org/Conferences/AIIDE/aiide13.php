<h2 class="backgrnd12"><span class="text1">The Ninth AAAI Conference on Artificial Intelligence and Interactive Digital Entertainment</i></span></h2>
<div class="content">

<p>The Ninth AAAI Conference on Artificial Intelligence and Interactive Digital Entertainment (AIIDE-13) will be held in October 14-18, 2013 at Northeastern University, Boston, Massachusetts, USA (www.aiide.org).</p>
<div class="hr1">
<hr />
</div>

<h2 class="text1">Call for Papers</h2>
<h3 class="text2">Submissions Deadlines</h3>
<li><a href="http://aiide.confmaster.net" rel="nofollow">AIIDE-13 Paper Submission Site</a></li>

<ul>
	<li>February 15  &ndash;  May 7, 2013: Authors register on the AIIDE web site</li>
	<li>March 15, 2013: Workshop proposals due</li>
	<li>May 7, 2013:  Research papers/Practitioner Track abstracts due</li>
	<li>May 7, 2013:  Playable Experiences/Demonstration abstracts due</li>
	<li>June 14, 2013: Doctoral Consortium applications due</li>
	<li>June 26, 2013: Notifications to authors</li>
</ul>
<div class="hr1">
<hr />
</div>

<p>
AIIDE-13 &mdash; the Ninth AAAI Conference on Artificial Intelligence and Interactive Digital Entertainment &mdash; is intended to be the definitive point of interaction between entertainment software developers interested in AI and academic AI researchers. AIIDE-13 will include invited speakers, research and practitioner presentations, playable experiences, project demonstrations, interactive poster sessions, product exhibits, and a doctoral consortium. While traditionally emphasizing commercial computer and video games, we invite researchers and developers to share their insights and cutting-edge results on topics at the intersection of all forms of entertainment and artificial intelligence, including games for impact, entertainment robotics, art, and beyond. AIIDE-13 is sponsored by the Association for the Advancement of Artificial Intelligence (AAAI). </p>

<h3 class="text2">Papers</h3>
<p>
Because AIIDE-13 crosses disciplinary boundaries, submissions will be evaluated based on their accessibility to both commercial game developers and researchers in addition to their technical merit.
</p>

<h3 class="text2">Research Track</h3>
<p>
Research Track papers describe AI research results that make advances towards solving known game AI problems, enable a new form of interactive digital entertainment, or use AI to improve the game design and development process. The novel technique should be validated in a game prototype or test-bed, but need not be tested in a commercial game. Research Track papers are evaluated by the highest standards of academic rigor. The highest rated papers will be presented in short lecture format. The next highest rated group of papers will be presented in a poster session. Authors should submit a paper of no more than 7 pages in the AAAI format for double blind review (i.e., authors names and affiliations are omitted). The final page (page 7) must contain only references, and no other text whatsoever. All papers, whether poster or oral, will be allocated 7 pages in the proceedings. </p>

<h3 class="text2">Practitioner Track</h3>
<p>
AIIDE also solicits submissions from professional game developers and artists on the use of artificial intelligence in games and other cultural artifacts. While these practitioners are also invited to submit to the research track, we recognize that many will have neither the time nor the inclination to prepare full-length papers for archival academic publication. These authors may instead submit a 500-word extended abstract to the practitioner track. Practitioner track papers need not describe new technology, but must describe new ideas relevant to the AIIDE audience and must be based on experience creating deployed games or other cultural artifacts. These papers are exempt from the formatting and blind reviewing requirements for the research track.
</p>

<p>
Example Topics (List is Suggestive Only)
</p>
<ul>
<li>AI in Game Design: AI as a source of novel game mechanics and genres</li>
<li>AI-Based Production and Authoring Tools: Behavior-building, design frameworks, telemetry-supported game design, content authoring support, scripting, sketch-based authoring, automated playtesting</li>
<li>AI Techniques for Games: Planning, reinforcement learning, search, neural networks, Bayesian models, evolutionary algorithms, case-based reasoning, constraint programming, utility-based approaches, animation, camera control, tactical/strategic decision making, terrain analysis, opponent modeling, dynamic difficulty adjustment, spatial decompositions, path planning</li>
<li>AI Storytelling: Interactive drama, story generation, character development</li>
<li>Autonomous Characters, NPCs, and Virtual Humans: Personality, emotion, believability, natural language processing, learning, cognitive modeling, crowd simulation, social robotics</li>
<li>Procedural Content Generation: Level generation, progression design, behavior adaptation</li>
<li>Commercial AI Implementations Case studies, implementation analysis, comparative evaluations</li>
<li>AI in Novel Entertainment Applications: Entertainment robotics, virtual/mixed reality, mobile device games, geo-location based games, games for human-computation</li>
<li>Computational Creativity and Generative Art: Painting, poetry, story, humor, music</li>
<li>AI in Games for Impact: Training, education, intelligent tutoring, games for health, gamification</li>
</ul>

<h3 class="text2">Submissions</h3>
<p><i>Author Registration:</i> Authors must register at the AIIDE-13 paper submission site before they submit their papers. The submission site (http://aiide.confmaster.net) will assign a password to be used for abstract and paper submission. Authors are encouraged to register as soon as possible, and well in advance of the submission deadline. </p>

<p>
<i>Paper Submission:</i> Electronic abstract and paper 
submission through the AIIDE-13 paper submission 
site is required on or (preferably) before May 7. 
We cannot accept submissions by e-mail or fax.
</p>

<p>
All Research Track submissions must be in PDF format, no longer than 7 pages, where page 7 must contain only references. Papers must be formatted in <a href="http://www.aaai.org/Publications/Templates/AuthorKit.zip" name="01">AAAI two-column, camera-ready style.</a> All Research Track papers must be formatted for BLIND REVIEW with NO author or affiliation information listed.
</p>

<p>
Practitioner Track extended abstracts must be submitted in PDF format and be approximately 500 words in length. Authors should also include short bios describing their game industry experience (not counted toward the document length). Papers should preferably be formatted in AAAI two-column, camera-ready style as above; however, Practitioner Track authors may submit their content in any reasonable format for review, and AIIDE will assign an editor to help meet publication formatting requirements for accepted work. Authors of accepted abstracts will be invited to submit a full-length paper to be published as part of a AAAI technical report.
</p>

<p>
Research Track papers and Practitioner Track extended abstracts must be submitted by May 7, 2013. All accepted papers will be published in the conference proceedings. At least one author must register for the conference by the deadline for camera-ready copy submission. As AIIDE is an academic conference, all attendees including presenters pay a registration fee. AIIDE-13 will not accept any paper that is under review for or has already been published or accepted for publication in another journal or conference.
</p>

<h3 class="text2">Demonstrations</h3>
<p>
We invite researchers and practitioners to share insights and cutting-edge results from a wide range of topics and encourage the demonstration of (a) research systems in the context of existing commercial games, (b) new games authored by researchers, (c) contributions demonstrating the adoption and/or extension of AI research results in published games, (d) completely new forms of interactive digital entertainment made possible by AI research, and (e) other relevant work. An electronic submission of a 2-page abstract and demonstration materials is required. Demonstration abstract review is not blind. Submissions should contain a link to the demonstration materials, which can take the form of a recorded demonstration session, an executable version of the demonstration with written instructions, or a detailed description of the demonstration heavily illustrated with screenshots. Please note that these materials are for review only and that all demonstrations will be conducted live at AIIDE-13. Demonstration authors should submit abstracts and materials by May 7, 2013. Submissions will be judged on technical merit, accessibility to developers and researchers, originality, presentation, and significance. Demonstration abstracts will be published in the conference proceedings.
</p>

<h3 class="text2">Playable Experiences</h3>
<p>
Practitioners and researchers who are developing innovative AI-based games or other interactive media (â¬Splayable experiencesâ¬ý) are invited to submit their work to the playable experiences track. We welcome playable experiences that involve some articulable innovation in the use of AI that directly affects the userâ¬"s experience. This includes novel game designs that leverage existing AI techniques, as well as innovations in the techniques themselves that lead to new kinds of playable experiences. Playable experience submissions should be sufficiently complete and polished enough for naÃ¯ve users to play them. Authors should submit a 500-word abstract describing the impetus behind the playable experience, how AI has motivated its design (or vice versa), and what they see as its primary innovation(s). The abstract should include a publicly accessible link to a website that contains a link to download the playable experience and instructions for how to play it; this link must remain live at least through the end of the conference. Playable experience review is not blind. The abstract will be published in the conference proceedings, and the authors will have the opportunity to show their playable experience during the evening poster/demo session of the AIIDE conference. An electronic submission of the abstract is required by May 7, 2013. If your work involves any specialized hardware or software that reviewers may not have access to, please contact the track chair, Michael Mateas (michaelm@cs.ucsc.edu), prior to submission.
</p>

<h3 class="text2">Workshops</h3>
<p>
The AIIDE 2013 committee invites proposals for one- to two-day workshops to be held on October 14-15. Workshop participants will have the opportunity to meet and discuss issues with a selected focus â¬ providing an informal setting for active exchange among researchers, developers, and users on topics of current interest. Members of all segments of the AI in Digital Entertainment community as well as industry researchers are encouraged to submit proposals. The format of workshops will be determined by their organizers. Organizers are highly encouraged to propose alternative formats beyond paper/poster presentations, and should encourage the submission and presentation of position papers that discuss new research ideas. Workshop papers will be published as technical reports and will be archived in the AAAI digital library. Proposals for workshops should be about two (2) to three (3) pages in length. Workshop chairs must submit their proposals via email to the workshops chair, Julian Togelius (&#106;&#117;&#108;&#105;&#97;&#110;&#64;&#116;&#111;&#103;&#101;&#108;&#105;&#117;&#115;&#46;&#99;&#111;&#109;) by March 15, 2013.
</p>

<h3 class="text2">Doctoral Consortium</h3>
<p>
The AIIDE-13 will feature a Doctoral Consortium in which Ph.D. students will be invited to discuss and explore their research interests and career objectives with a panel of established researchers in AIIDE related fields. The consortium is intended primarily for early-stage Ph.D. students who have either not yet proposed their thesis topic or have recently done so. Ph.D. students selected for the Doctoral Consortium will have the opportunity to present their research proposals at the DC session and/or at the poster session. Additionally, each student will be paired with a mentor, a senior member of the AIIDE community. Applications to attend the Doctoral Consortium will include a 4-page research summary, curriculum vita, a 1-page document stating what they hope to gain from attending the Doctoral Consortium, and a letter of recommendation from the dissertation advisor. Application packages are due on June 14, 2013.
</p>

<h3 class="text2">Exhibits</h3>
<p>
AIIDE-13 will have exhibit space available. Companies, publishers and other groups are encouraged to consider purchasing either a tabletop display or an exhibit booth. Exhibit space is limited and will be allocated on a first come, first serve basis. Please contact AAAI at &#97;&#105;&#105;&#100;&#101;&#49;&#51;&#64;&#97;&#97;&#97;&#105;&#46;&#111;&#114;&#103; for more information.
</p>


