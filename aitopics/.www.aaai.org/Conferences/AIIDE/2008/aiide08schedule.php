<h2 class="backgrnd5"><span class="text1">About the Conference</span></h2>

<div class="content">
<h3 class="text2">The Research Track</h3>
<p class="backgrnd6">Research Track papers describe AI research results that make advances towards solving known game AI problems or enabling a new form of interactive digital entertainment. The novel technique should be validated in a game prototype or test-bed, but need not be validated in a commercial game. <a href="http://www.aaai.org/Conferences/AIIDE/2008/aiide08call.php#research">More&#8230;</a></p>


<h3 class="text2">The Industry Track</h3>
<p class="backgrnd6">Individuals that have game development experience but lack the time or need for publishing rigorous academic papers can alternatively apply to the Industry Track. This track will include presentations of AI techniques, issues, or case studies from the perspective of implementing a product in the current commercial environment. <a href="http://www.aaai.org/Conferences/AIIDE/2008/aiide08call.php#industry">More&#8230;</a></p>

