<p>Improved Heuristics for Optimal Pathfinding on Game Maps<br />
<i>Yngvi Bj&ouml;rnsson and K&aacute;ri Halld&oacute;rsson, Reykjavik University</i></p>

<p>Modeling Goal-Directed Players in Digital Games<br />
