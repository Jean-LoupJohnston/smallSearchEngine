<h2 class="backgrnd12"><span class="text1">Preliminary Schedule of the Second Annual Artificial Intelligence and Interactive Digital Entertainment Conference</span></h2>
<p class="left"><i>Subject to change without notice</i></p>
<hr />

<h3>Tuesday, June 20</h3>

<p>8:45 am&ndash;9:00 am</p>
<p>Welcome<br />
<i>John Laird, AIIDE-06 Conference Chair, University of Michigan</i></p>

<h4>AIIDE Tutorial Program</h4>

<p>9:00 am&ndash;12:00 pm</p>
<p>TA1: Automated Planning for Interactive Entertainment<br />
<i>R. Michael Young and Arnav H. Jhala, North Carolina State University</i></p>

<p>TA2: ORTS&mdash;A Free Software RTS Game Engine Not Just for AI Research<br />
<i>Michael Buro and Timothy Furtak, University of Alberta, Canada</i></p>

<p>1:00&ndash;4:00 pm</p>
<p>TP1: Machine Learning for Games<br />
<i>Finnegan Southey, University of Alberta / Google Inc.</i></p>

<p>TP2: Unreal AI: Using the Unreal Tournament Game Engine for AI Research<br />
<i>Mark Riedl, Ryan McAlinden, and Andrew Marshall, Institute for Creative Technologies/University of Southern California</i></p>

<hr />

<h3>Wednesday, June 21</h3>

<p>8:45 am&ndash;9:00 am</p>
<p>Welcome and Introduction<br />
<i>Jonathan Schaeffer, AIIDE-06 Program Chair, University of Alberta</i></p>

<p>9:00&ndash;10:00 am</p>
<p><i>Invited Talk:</i> High Performance Multi-Processor Crowd Simulation<br />
<i>Craig Reynolds, Sony Computer Entertainment America</i><br />
Introduction by John Laird</p>

<p>10:00&ndash;10:30 am</p>
<p>Break</p>

<p>10:30 am&ndash;12:00 pm</p>
<p><i>Session:</i> Abstraction and Planning<br />
<i>Chair:</i> Ian Davis</p>

<p>An Abstraction Framework for Cooperation among Agents and People in a Virtual World<br />
<i>David McDonald, Alice Leung, William Ferguson, and Talib Hussain, BBN Technologies</i></p>

<p>Representational Requirements for a Plan Based Approach to Automated Camera Control<br />
<i>Arnav Jhala and R. Michael Young, North Carolina University</i></p>

<p>Building Robust Planning and Execution Systems for Virtual Worlds<br />
<i>Don M. Dini, Michael Van Lent, Paul Carpenter, and Kumar Iyer, Institute for Creative Technologies/University of Southern California</i></p>

<p>12:00&ndash;1:30 pm</p>
<p>Lunch</p>

<p>1:30&ndash;3:30 pm</p>
<p><i>Session:</i> User Interactions<br />
<i>Chair:</i> Michael Young</p>

<p>Intelligent Tutoring Systems for Commercial Games: The Virtual Combat Training Center Tutor and Simulation<br />
<i>William R. Murray, Boeing Phantom Works</i></p>

<p>Predicting User Physiological Response for Interactive Environments: An Inductive Approach<br />
<i>Scott W. McQuiggan, Sunyoung Lee, and James C. Lester, North Carolina University</i></p>

<p>A Guided Performance Interface for Augmenting Social Experiences with an Interactive Animatronic Character<br />
<i>Seema Patel, William Bosley, David Culyba, Sabrina A. Haskell, Andrew Hosmer, TJ Jackson, Shane J. M. Liesegang, Peter Stepniewicz, James Valenti, Salim Zayat, and Brenda Harger, Carnegie Mellon University</i></p>

<p>Dynamic Game Balancing: An Evaluation of User Satisfaction<br />
<i>Gustavo Andrade, Geber Ramalho, and Alex Sandro Gomes, Universidade Federal de Pernambuco; Vincent Corruble, Universit&eacute; Paris 6</i></p>

<p>3:30&ndash;4:00 pm</p>
<p>Break</p>

<p>4:00&ndash;5:00 pm</p>
<p><i>Invited Talk:</i> The Power of Projection and Mass Hallucination (Practical AI in The Sims 2 and Beyond)<br />
<i>Matt Brown, Electronic Arts / Maxis</i><br />
Introduction by Jonathan Schaeffer</p>

<p>6:00&ndash;8:00 pm</p>
<p>AIIDE-06 Opening Reception<br />
Sierra Room</p>

<hr />

<h3>Thursday, June 22</h3>

<p>9:00&ndash;10:00 am</p>
<p><i>Invited Talk:</i> Adaptive Entertainment<br />
<i>Pieter Spronck, Maastricht University</i><br />
Introduction by Jonathan Schaeffer</p>

<p>10:00&ndash;10:30 am</p>
<p>Break</p>

<p>10:30&ndash;11:00 am</p>
<p><i>Session:</i> Learning<br />
<i>Chair:</i> Michael van Lent</p>

<p>Goal-Directed Hierarchical Dynamic Scripting for RTS games<br />
<i>Anders Dahlbom and Lars Niklasson, University of Sk&ouml;vde</i></p>

<p>The Self Organization of Context for Learning in MultiAgent Games<br />
<i>Chris White and Dave Brogan, University of Virginia</i></p>

<p>Incorporating Advice into Neuroevolution of Adaptive Agents<br />
<i>Chern Han Yong, Institute for Infocomm Research, Kenneth Stanley, University of Central Florida, Risto Miikkulainen and Igor V. Karpov, University of Texas at Austin</i></p>

<p>12:00 pm&ndash;1:30 pm</p>
<p>Lunch Buffet, Promenade Room</p>

<p>1:30&ndash;2:30 pm</p>
<p><i>Invited Talk:</i> Emotions in Human-Agent Interactions<br />
<i>Jonathan Gratch, Institute for Creative Technologies, University of Southern California</i><br />
Introduction by Michael van Lent</p>

<p>2:30&ndash;3:30 pm</p>
<p><i>Invited Talk:</i> The Fun Factor, Machine Learning, and Gameplay Metrics<br />
<i>Ben Geisler, Radical Entertainment and ITT Tech</i><br />
Introduction: John Laird</p>

<p>3:30&ndash;4:00 pm</p>
<p>Break</p>

<p>4:00&ndash;6:00 pm</p>
<p>AIIDE-06 Poster and Demonstration Program</p>

<h4>AIIDE-06 Posters</h4>

<p>A Framework for Summarizing Game Experiences as Narratives<br />
<i>Yun Gyung Cheong and R. Michael Young, North Carolina State University</i></p>

<p>Procedural Level Design for Platform Games<br />
<i>Kate Compton and Michael Mateas, Georgia Institute of Technology</i></p>

<p>Biased Cost Pathfinding<br />
<i>Alborz Geramifard, Pirooz Chubak, and Vadim Bulitko, University of Alberta</i></p>

<p>Using Natural Language to Manage NPC Dialog<br />
<i>Gary Kacmarcik, Microsoft Research</i></p>

<p>Incremental Heuristic Search in Games: The Quest for Speed<br />
<i>Maxim Likhachev, Carnegie Mellon University, and Sven Koenig, University of Southern California</i></p>

<p>Designing a Reinforcement Learning-based Adaptive AI for Large-Scale Strategy Games<br />
<i>Charles Madeira and Vincent Corruble, Universit&eacute; Pierre et Marie Curie; Geber Ramalho, Universidade Federal de Pernambuco</i></p>

<p>Modeling Self-Adapting and Multicultural Non-Player Characters in Artificial Worlds<br />
<i>Samuel Marin, University of Namur</i></p>

<p>Authoring an Interactive Narrative with Declarative Optimization-Based Drama Management<br />
<i>Mark J. Nelson, Calvin Ashmore, and Michael Mateas, Georgia Institute of Technology</i></p>

<p>Plausible Environment Reconstruction Using Bayesian Networks<br />
<i>Robert G. Price and Scott D. Goodwin, University of Windsor</i></p>

<p>AI Wall Building in Empire Earth II<br />
<i>Tara Teich and Ian Lane Davis, Mad Doc Software</i></p>

<h4>AIIDE-06 Demonstrations</h4>

<p>A Testbed for Evaluating AI Research Systems in Commercial Games <br />
<i>David W. Aha, Naval Research Laboratory; Matthew Molineaux and Philip Moore, Naval Research Laboratory and ITT Industries</i></p>

<p>Normative Echoes: Use and Manipulation of Player Generated Content by Communities of NPCs<br />
<i>Eric Baumer, Bill Tomlinson, Man Lok Yau, and Thomas A. Alspaugh, University of California, Irvine</i></p>

<p>A Demonstration of ScriptEase Ambient and PC-Interactive Behavior Generation for Computer Role-Playing Games<br />
<i>Maria Cutumisu, Duane Szafron, Jonathan Schaeffer, Kevin Waugh, Curtis Onuczko, Jeff Siegel, and Allan Schumacher, University of Alberta</i></p>

<p>Using NL Representations to Manage NPC Dialog via Dynamically Generated Menu Interactions<br />
<i>Gary Kacmarcik, Microsoft Research</i></p>

<p>Believable and Reactive Crowds in Next Generation Games<br />
<i>Paul A. Kruszewski, Engenuity Technologies, Inc.</i></p>

<p>Using Environmental Annotations and Affordances to Model Culture<br />
<i>Ryan McAlinden, Michael van Lent, William Clevenger, and Wen Tien, USC Institute for Creative Technologies</i></p>

<p>Automatic Story Generation for Computer Role-Playing Games<br />
<i>Curtis Onuczko, Duane Szafron, Jonathan Schaeffer, Maria Cutumisu, Jeff Siegel, Kevin Waugh, and Allan Schumacher, University of Alberta</i></p>

<p>Mixing Story and Simulation in Interactive Narrative<br />
<i>Mark O. Riedl and Don Dini, Institute for Creative Technologies; Andrew Stern, Procedural Arts LLC</i></p>

<p>The NERO Video Game<br />
<i>Kenneth O. Stanley, University of Central Florida, Igor Karpov, Risto Miikulainen, and Aliza Gold, The University of Texas at Austin</i></p>

<p>Building Test Beds for AI with the Q3 Mode Base <br />
<i>G. Michael Youngblood, Billy Nolen, Michael Ross, and Lawrence B. Holder, The University of Texas at Arlington</i></p>

