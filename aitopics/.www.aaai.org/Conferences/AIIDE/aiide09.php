<h2 class="backgrnd12"><span class="text1">The Fifth AAAI Conference on Artificial Intelligence and Interactive Digital Entertainment</i></span></h2>
<div class="content">

<p>The Fifth Artificial Intelligence for Interactive Digital Entertainment Conference (AIIDE-09) was held October 14&ndash;16, 2009 in Palo Alto, California. </p>
<p>AIIDE-09 is intended to be the definitive point of interaction between entertainment software developers interested in AI and academic and industrial AI researchers. AIIDE-09 will include invited speakers, research and industry presentations, project demonstrations, and product exhibits. While traditionally emphasizing commercial computer and video games, we invite researchers and developers to share their insights and cutting-edge results on all topics at the interface of entertainment and artificial intelligence, including serious games, entertainment robotics, and beyond.
</p>



