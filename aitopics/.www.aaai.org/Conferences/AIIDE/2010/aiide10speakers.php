<h3 class="text2"><i><a name="Jurney">AIIDE-10 Invited Talk</a></i></h3>
<p><b>Chris Jurney (Lead Programmer, Double Fine Productions)</b></p>

<p><img src="http://www.aaai.org/Conferences/AIIDE/2010/Photos/jurney.jpg" alt="Chris Jurney" width="52" height="75" class="img1" /><b>Chris Jurney</b> is a rock and roll experimental game programmer at Double Fine Productions, with 11 years experience in games and simulation. He has shipped 4 titles in the games industry: <i>Company of Heroes, Frontline: Fuel of War, Dawn of War 2,</i> and <i>Brutal Legend.</i> Jurney frequently speaks on the topic of game AI, having presented at GDC, GDC China, Columbia University, the University of Pennsylvania, and the NJ and Philly chapters of the IGDA. He has also written two articles published in <i>AI Game Programming Wisdom 4</i> and wears tin foil weaved into his hair to block the voices.</p>



