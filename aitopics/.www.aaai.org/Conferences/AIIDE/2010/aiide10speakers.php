<h3 class="text2"><i><a name="Basu">AIIDE-10 Invited Talk</a></i><br />
Learning to Make Music: Interactive AI for Music Creation</h3>
<p><b>Sumit Basu (Microsoft Research)</b></p>

 
<p>For those who can play an instrument or have a respectable singing voice, music can be a wonderful means of creative expression, social engagement, and fun. For many others, though, it can be frustrating and inaccessible: even if an inspired youth has great musical ideas, she may not have the knowledge or ability to get her latest song out from her head and into her MP3 player. In this talk, I'll show three vignettes of how we've used interactive machine learning to extend the creative reach of aspiring musicians: a system that that adds a learned notion of style to dull MIDI sequences, a system for interactively creating accompanying chord sequences from a melody input, and a method for helping singers produce the notes they meant to sing. Finally, making music more accessible also makes it more fun, for both novices and experts, and I'll end with some thoughts on how these technologies might lead to interesting gaming experiences.</p>
  
<p><img src="http://www.aaai.org/Conferences/AIIDE/2010/Photos/basu.jpg" alt="Sumit Basu" width="96" height="75" class="img1" /><b>Sumit Basu</b> has been a researcher at Microsoft Research since completing his PhD at the Massachusetts Institute of Technology in 2002. His primary focus area is interactive machine learning, within which he investigates how human judgments, knowledge, and intent can best be leveraged to train or guide complex learning algorithms, as well as how algorithmic approaches can be used to teach humans new information. He has worked in a variety of application areas for this technology, including music creation and analysis, data mining and organization, diagnosing computer systems, and speech/conversational analysis. More information including publications, projects, videos, and released software is available on <a href="http://research.microsoft.com/~sumitb" rel="nofollow">Basu's Microsoft page.</a></p>



