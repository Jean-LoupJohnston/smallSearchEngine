<h3 class="text2"><i><a name="Young">AIIDE-10 Invited Talk</a></i><br />
Cracks in the Fourth Wall: Digging into a Humanistic Phenomenon Using Computational Models
</h3>
<p><b>R. Michael Young (North Carolina State University)</b></p>


<p>Research on computational approaches to narrative push the boundaries on a diverse set of techniques, ranging from planning to constraint solving to machine learning and more. At the core of the area, though, lies narrative itself. Narrative holds a position of privilege in our minds, being a fundamental mode of understanding the worlds around us. In this talk, I'll describe a trajectory of projects from my research group and the role that the nature of narrative and its comprehension by people has played in setting our goals and the methods we use to achieve them. </p>

<p><img src="http://www.aaai.org/Conferences/AIIDE/2010/Photos/young.jpg" alt="R. Michael Young" width="74" height="75" class="img1" /><b>R. Michael Young</b> is an associate professor of computer science at North Carolina State University, where he leads the Liquid Narrative Research Group. His work focuses on the computational modeling of interactive narrative. Young received an NSF CAREER Award in 2000 and has received university-level awards for outstanding teaching and outstanding activities in engagement/economic development. He was a cofounder of AIIDE and served as its first conference chair. Michael was editor-in-chief of the <i>Journal of Game Development</i> from 2007 to 2008. He serves as an associate editor of the IEEE journal <i>Transaction on Computational Intelligence</i> and <i>AI in Games.</i></p>

<h3 class="text2"><i><a name="Young">AIIDE-10 Invited Talk</a></i><br />
</h3>
<p><b>Chris Hecker (Spy Party)</b></p>


<p>Chris Hecker focuses on solving hard problems at the intersection of gameplay, aesthetics,  and technology. He is an outspoken advocate for pushing the current boundaries of design and interactivity, in the hope that games will eventually achieve their full potential as a medium. To this end he helps organize the Indie Game Jam and the Experimental Gameplay Workshop, and his recent work has centered on using proceduralism and artificial intelligence to enhance player creativity and agency. Hecker has been on the advisory board for the Game Developers Conference for many years and is a regular speaker at the GDC, SIGGRAPH, and other conferences. A frequent contributor to <i>Game Developer</i> magazine, Hecker was the technical columnist for the magazine for two years and the editor-at-large for three, and is currently on the editorial board of the computer graphics research publication, <i>?e Journal of Graphics Tools.</i> He has worked at both ends of the development spectrum, as a one-man indie game developer with his company definition six, inc. and on a hundred-person team at Maxis/Electronic Arts. His professional goal is to help games become the preeminent art and entertainment form of the 21st century. His current project  is SpyParty, an indie game about subtle human behavior and deception.</p>
