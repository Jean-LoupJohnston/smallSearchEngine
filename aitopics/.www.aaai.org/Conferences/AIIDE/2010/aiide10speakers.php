<h3 class="text2"><i><a name="Sottilare">AIIDE-10 Invited Talk</a></i><br />
A Perspective on the Use of Digital Media and AI in Serious Games and Training</h3>
<p><b>Bob Sottilare (U.S. Army Simulation and Training Technology Center)</b></p>


<p>This talk will provide a perspective on the role of artificial intelligence (AI) and interactive digital media in serious games and training applications for the military. The perspective will focus primarily on nonkinetic training (for example, bilateral negotiation, medical training and exercising decision making in ill-defined environments). The importance of intelligent agents in enhancing military training, leader development and education (TLE) and in reducing associated support costs will be discussed along with the use intelligent agents to supplement individual and collective training experiences where human support is limited, impractical or completely unavailable. Challenges in the development of adaptive, interactive media for training will also be discussed.</p>

<p><img src="http://www.aaai.org/Conferences/AIIDE/2010/Photos/sottilare.jpg" alt="Bob Sottilare" width="53" height="75" class="img1" /><b>Bob Sottilare</b> is the chief technology officer at the U.S. Army Simulation and Training Technology Center (STTC) in Orlando, Florida. His recent publications have appeared in the <i>Journal for Defense Modeling and Simulation,</i> the NATO workshop on Human Dimensions in Embedded Virtual Simulation and the Intelligent Tutoring Systems Conference. He has a doctorate in modeling and simulation from the University of Central Florida and the focus of his current research program is in machine learning, trainee modeling and the application of artificial intelligence tools and methods to adaptable training environments for Warfighters.</p>


