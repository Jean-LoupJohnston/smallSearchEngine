<h3 class="text2"><i><a name="Zubek">AIIDE-11 Invited Talk</a></i><br />
Social Games and the Role of Simulation in a Social World</h3>
<p><b>Robert Zubek, Zynga</b><br/>
<i>Friday, October 14, 9:00 am &ndash; 10:00 am</i></p>



 
<p>Games on social networks are a recent evolution in gaming, but have already had a profound effect on the industry. They are enjoyed differently than previous game genres - they are played with real-life friends and family members, often ones who do not consider themselves gamers and did not used to play games before. Like parlor games of the past, they have a complex role in players' lives, one that lives on the intersection of socializing, cooperation, light competition, and creative self-expression.</p>

<p>This talk will serve two purposes. First, it will present the genre of social games, discussing the novel design thinking as well as technical challenges it presents, illustrated with examples from CityVille.
Second, it will focus specifically on the role of simulation and AI in the social game world. This new genre is fundamentally grounded in interactions with real friends. I will argue this presents both a great challenge, and also a great opportunity, for the field of artificial intelligence.</p>

<p><img src="http://www.aaai.org/Conferences/AIIDE/2011/Photos/zubek.jpg" alt="Robert Zubek" width="50" height="75" class="img1" /><b>Robert Zubek</b> is a principal software engineer at Zynga, and the tech lead on CityVille. Prior to Zynga, he was at Three Rings Design, and Electronic Arts / Maxis; even earlier, he did research in artificial intelligence and robotics. He holds a PhD in computer science from Northwestern University, where he also earned his other CS degrees.</p>
