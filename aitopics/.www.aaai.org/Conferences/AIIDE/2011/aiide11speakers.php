<h3 class="text2"><i><a name="McHugh">AIIDE-11 Invited Talk</a></i><br />
DARKSPORE AI Post Mortem</h3>
<p><b>Lauren McHugh and Daniel Kline, Electronic Arts</b><br/>
<i>Thursday, October 13, 9:00 am  &ndash;  10:00 am</i></p>


 
<p>This talk will look into some of the challenges faced on Maxis' DARKSPORE. Lauren McHugh will show how DARKSPORE managed both scale and code-sharing for more than 100 different enemies and over 1000 different powers - all while making each AI memorable and unique. Then Dan Kline will dig into DARKSPORE's new approach to AI Direction - how the team identified DARKSPORE's specific challenges and solved them.</p>

<p><img src="http://www.aaai.org/Conferences/AIIDE/2011/Photos/mchugh.jpg" alt="Lauren McHugh" width="50" height="75" class="img1" /><b>Lauren McHugh</b>  has been a Gameplay Programmer at Maxis since 2005. She worked on SPORE where she employed a behavior tree system to develop creature AI. She spoke about this implementation of behavior trees at GDC 2007. McHugh has developed many AI and gameplay systems for DARKSPORE, including the ability system that will be discussed at AIIDE 2011. She will describe how the ability system enabled the team to easily make hundreds of cool and unique heroes and enemies. McHugh loves playing games, and especially likes online cooperative experiences.</p>

<p><img src="http://www.aaai.org/Conferences/AIIDE/2011/Photos/kline.jpg" alt="Daniel Kline" width="113" height="75" class="img1" /><b>Daniel Kline</b>  has been an AI and Game Programmer and Designer for consoles since 2001. He has shipped 6 titles and developed 10 titles, working with companies such as Activision, Electronic Arts, Blizzard, LucasArts, and Midway. He was Head AI and Gameplay Engineer on Star Wars: Force Unleashed, AI programmer on Diablo 3, and the engineer and designer of the first two levels of Call of Duty: Finest Hour. He has spent over 5 years doing Interactive Storytelling research, design, programming, and pre-production for 3 different companies and AAA blockbuster titles. He is currently working on Darkspore at Maxis as a Software Engineer.</p>

