<h3 class="text2"><i><a name="Gascoigne">AIIDE-11 Invited Talk</a></i><br />
Creating the Enemies of Dead Space</h3>
<p><b>Louis Gascoigne, Visceral Games, an Electronic Arts Studio</b><br/>
<i>Thursday, October 13, 1:30 pm  &ndash;  2:30 pm</i></p>


 
<p>This talk will discuss the techniques used to create and evaluate the NPC (non-player character) behavior in the popular Dead Space action-horror franchise. Scaring the players is an essential component of the Dead Space universe. In order to create scary situations the AI systems and even individual units must communicate state and events to each other as well as other systems. Gascoigne will present examples of how this information is used by the audio, level scripting, and lighting systems. He will also give an overview of the decision-making, sensing, knowledge representation, path-following, and debugging techniques used in the titles. The evaluation portion of the talk will cover the production methods used by the Dead Space 2 team to create new NPCs with a special emphasis on the use of focus testing. Specific examples like the Stalker enemy from Dead Space 2 will be examined in detail.</p>
<p><img src="http://www.aaai.org/Conferences/AIIDE/2011/Photos/gascoigne.jpg" alt="Louis Gascoigne" width="95" height="75" class="img1" /><b>Louis Gascoigne</b>  is a game programmer at Visceral Games Redwood Shores, an Electronic Arts studio. He has 14 years of experience working on commercial games and is credited on the following titles at Electronic Arts: Dead Space 2, Dead Space, The Simpsons Game, The Godfather, From Russia With Love, Everything or Nothing, and Agent Under Fire. His primary interest is in making games fun to play by any means necessary and shipping them to (hopefully) large audiences.</p>

