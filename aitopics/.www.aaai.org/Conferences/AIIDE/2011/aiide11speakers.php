<h3 class="text2"><i><a name="Fitch">AIIDE-11 Invited Talk</a></i><br />
Evolution of RTS AI</h3>
<p><b>Bob Fitch, Blizzard Entertainment</b><br/>
<i>Wednesday, October 12, 9:00 am &ndash; 10:00 am</i></p>


 
<p>The RTS genre has evolved considerably since 1993 when Warcraft: Orcs & Humans was first in production. "Evolution of RTS AI" will go back to the beginning and look at a few programming challenges that began with the Warcraft AI, and examine how the challenges and solutions evolved over the last 18 years, or in a few cases why things have not evolved at all. Bob Fitch was the primary AI programmer for Warcaft, Warcraft II, Wacraft III, Starcraft, Brood War, and Starcraft II. He will walk each topic through from Warcraft to Starcraft II while discussing each problem, what he wanted to do with each game, what he ended up doing, and why each decision was made. The topics that might be covered could include general strategy, attack wave management, base building, cheating, time slicing, pathing, transporting, difficulty levels, scouting, and maybe more.</p>

<p><b>Bob Fitch</b> started programming games when he was 8 years old, working on an Atari 2600. He began making games professionally in 1992, programming Rock 'N' Roll Racing while working for Silicon & Synapse. That tiny company would later grow by more than two orders of magnitude and rename itself Blizzard Entertainment. Fitch designed or programmed part of every Blizzard release, including a Lead Programming role throughout the Starcraft franchise. In his free time, Fitch is the DM for a Dungeons & Dragons campaign, plays ice hockey, and continues his attempt to master Texas Hold'Em poker by hosting the Blizzard Home Game.</p>

