<h3 class="text2"><i><a name="Madhani">AIIDE-11 Invited Talk</a></i><br />
Bringing Physical Characters to Life - Lessons and Challenges from Disney</h3>
<p><b>Akhil Madhani, Walt Disney Imagineering Research and Development</b><br/>
<i>Wednesday, October 12, 1:30 pm &ndash; 2:30 pm</i></p>


 
<p>At Imagineering, one way in which we present characters to our Theme Park audiences is through the use of entertainment robots, or Audio-Animatronics Figures, as they are traditionally known. In this talk, I hope to give insight into the design and development of entertainment robots at Disney, and challenges we face in advancing the state-of-the-art. I discuss two characters which engage in two-way interactions with audiences via tele-operation: Lucky the Dinosaur and Wall-E. Lucky was designed to roam freely inside the Disney theme park environment while interacting with guests. Wall-E was developed to represent the character from the film, and has made appearances and given interviews at red carpet premieres, press events, and in television studios around the world. Ultimately, we hope that developments in sensing and perception technologies will lend autonomy to these types of systems, to offload or eventually eliminate the operator and create more lifelike engagement between our characters and our guests.</p>

<p><img src="http://www.aaai.org/Conferences/AIIDE/2011/Photos/madhani.jpg" alt="Akhil Madhani" width="61" height="75" class="img1" /><b>Akhil Madhani</b> is a member of the Technical Staff and a Director at Walt Disney Imagineering R&D. Madhani has had the good fortune to spend his career designing - and working on teams that design - robots in both research and commercial environments. At Disney, he creates physical versions of Disney characters, with a focus on improving the realism and quality of interaction between guests and these characters. Madhani earned a Ph.D. in Mechanical Engineering at the MIT AI Lab and, prior to Disney, worked on manipulator design, human-machine interface, and commercial tele-operated surgical robots.</p>

