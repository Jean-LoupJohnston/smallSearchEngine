<h2 class="backgrnd7"><span class="text1">Please Join Us for AAAI-08!</span></h2>

<div class="content">
<p>AAAI is delighted to announce that the Twenty-Third AAAI Conference on Artificial Intelligence will be held at the Hyatt Regency McCormick Place in Chicago, Illinois, July 13&ndash;17, 2008. Please mark your calendars now for AAAI-08, and feel free to contact us at &#97;&#97;&#97;&#105;&#48;&#56;&#64;&#97;&#97;&#97;&#105;&#46;&#111;&#114;&#103; with any inquiries.</p>
<p>The purpose of the AAAI-08 conference is to promote research in AI and scientific exchange among AI researchers, practitioners, scientists, and engineers in related disciplines.  On this page, you will find links to the wide variety of programs being held at AAAI-08, as they become available.
</p>

