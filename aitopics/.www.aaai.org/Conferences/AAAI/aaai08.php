<p>AAAI-08 / IAAI-08 features an exciting slate of invited speakers, including <a href="http://www.aaai.org/Conferences/AAAI/2008/aaai08speakers.php#Horvitz">Eric Horvitz</a> (delivering his AAAI Presidential address); <a href="http://www.aaai.org/Conferences/AAAI/2008/aaai08speakers.php#Ford">Kenneth M Ford</a> (speaking about building cognitive prostheses); <a href="http://www.aaai.org/Conferences/AAAI/2008/aaai08speakers.php#Efros">Alexei Efros</a> (discussing recent results on inferring properties from a single image); <a href="http://www.aaai.org/Conferences/AAAI/2008/aaai08speakers.php#Haussler">David Haussler </a>(100 Million Years of Evolutionary History of the Human Genome); <a href="http://www.aaai.org/Conferences/AAAI/2008/aaai08speakers.php#Lee">Lillian Lee</a> (speaking about sentiment analysis); <a href="http://www.aaai.org/Conferences/AAAI/2008/aaai08speakers.php#Newman">Mark Newman</a> (discussing how to make sense of complex networks); <a href="http://www.aaai.org/Conferences/AAAI/2008/aaai08speakers.php#Russell">Stuart Russell</a> (&ldquo;What Is to be Done?&rdquo;); <a href="http://www.aaai.org/Conferences/AAAI/2008/aaai08speakers.php#Goldstein">Seth Goldstein</a> (discussing the hardware and software challenges involved in realizing Claytronics); and <a href="http://www.aaai.org/Conferences/AAAI/2008/aaai08speakers.php#Urmson">Chris Urmson</a> (discussing CMU&rsquo;s Boss and the DARPA Urban Challenge). <a href="http://www.aaai.org/Conferences/AAAI/2008/aaai08speakers.php">Details ....</a></p>



