<h2 class="backgrnd12"><span class="text1">Please Join Us for The Twenty-Eighth AAAI Conference (AAAI-14)!</span></h2>

<h2>July 27&ndash;31, 2014, Qu&eacute;bec Convention Center,
Qu&eacute;bec City, Qu&eacute;bec, Canada</h2>
<p><i>Collocated with CogSci 2014</i></p>
<p><a href="http://videolectures.net/aaai2014_quebec/" rel="nofollow">Selected lectures and programs from this conference have been posted at VideoLectures.Net.</a></p>

<p>The Twenty-Eighth AAAI Conference on Artificial Intelligence (AAAI-14) will be held July 27&ndash;31, 2014 in Qu&eacute;bec City, Qu&eacute;bec, Canada. The purpose of this conference is to promote research in artificial intelligence (AI) and scientific exchange among AI researchers, practitioners, scientists, and engineers in affiliated disciplines. AAAI-14 will have a diverse technical track, student abstracts, poster sessions, invited speakers, tutorials, workshops, and exhibit/competition programs, all selected according to the highest reviewing standards. AAAI-14 welcomes submissions on mainstream AI topics as well as novel crosscutting work in related areas. </p>
<p><b>An updated schedule (<a href="http://www.aaai.org/Conferences/AAAI/2014/aaai14schedule.pdf">PDF</a> and <a href="http://www.aaai.org/Conferences/AAAI/2014/aaai14schedule.epub">epub</a> versions) and conference <a href="http://www.aaai.org/Conferences/AAAI/2014/aaai14program.pdf">program</a> is now available.</b> (Posted July 23.)</p><p>Tutorials and workshops will be held July 27&ndash;28. Technical sessions will be held July 29&ndash;31.</p>

<p>Qu&eacute;bec City, a UNESCO World Heritage Treasure, is North America's most European city, filled with museums and other historic attractions. Qu&eacute;bec's beautiful Old Town (Vieux-Qu&eacute;bec) is the only North American fortified city north of Mexico whose walls still exist. The convention center and conference hotel are only steps away from the historic section of Qu&eacute;bec. AAAI will be preceded by the CogSci2014 Conference, July 23&ndash;26, and plans are underway for coordination of events that would be of mutual interest to conference participants.</p><p>For local information, please visit the <a href="http://www.quebecregion.com" rel="nofollow">Qu&eacute;bec City Tourism site.</a>
</p>
