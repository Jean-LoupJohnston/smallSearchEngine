			<h2 class="backgrnd12"><span class="text1">AI and the Web Special Track</span></h2>
<p>The Special Track on Artificial Intelligence and the Web (AIW) focuses on the use and extension of AI concepts and techniques for the world wide web.</p>
<p>The web has evolved from a simple hypertext standard into a ubiquitous, global information system including virtually all of human knowledge. Today's web provides ready access to not only text, images and audio files, but also to structured and semistructured information, sensor data, composable services and communities of people. It offers an open and decentralized environment in which anyone can publish information and services coupled with powerful search engines and agents to discover them.  All of this is ubiquitously available from wired, wireless, and mobile devices. The result is an environment enormously useful to people for research, learning, commerce, socializing, communication and entertainment. We have just begun to explore how this vast amount of machine accessible knowledge can be exploited and used by machines to better serve human needs as well as to discover new knowledge.</p>

<p>This AAAI-07 special track includes research papers on AI techniques, systems, and concepts involving or applied to the web. Papers describe web related research or clearly explain how the work addresses problems, opportunities or issues underlying the web or web-based systems. Relevant topics include the following:</p>

<ul>
	<li>AI for web services: semantic descriptions, planning, matching and coordination</li>
	<li>AI for web-based collaboration and cooperation</li>
	<li>Agents and multiagent systems on the web</li>
	<li>Enhancing web search and information retrieval</li>
	<li>Human language technologies for web systems, including information extraction, question answering, text summarization, machine translation, and NLP.</li>
	<li>Information integration on the web</li>
	<li>Intelligent user interfaces for web systems</li>
	<li>Knowledge acquisition from the web</li>
	<li>Languages, tools and methodologies for representing, managing, and visualizing semantic web data</li>
	<li>Link-analysis and graph mining on the web</li>
	<li>Machine learning and the web</li>
	<li>Mining web logs, query logs, blogs</li>
	<li>Ontologies and the web: creation, extraction, evolution, mapping, merging, and alignment; tags and folksonomies</li>
	<li>Recognizing web spam such as link farms and splogs</li>
	<li>Representing, reasoning and using provenance, trust, privacy, and security on the web</li>
	<li>Searching, querying, visualizing and interpreting the semantic web</li>
	<li>Social networking and community identification</li>
	<li>Web personalization and user modeling</li>
	<li>Web-based opinion extraction and trend spotting</li>
	<li>Web-based recommendation systems</li>
</ul>			<h3 class="text2">Special Track Cochairs</h3>
