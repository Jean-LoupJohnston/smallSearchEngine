<h2 class="backgrnd12"><span class="text1">Robot Teams</span></h2>

<p><b>Brown University</b><br />
Robotics, Learning and Autonomy at Brown<br />
<i>Event:</i> Robot Exhibition</p>

<p><b>Harvey Mudd College</b><br />
<i>Event:</i> Robot Exhibition</p>

<p><b>Kansas State University</b><br />
KSU Willie<br />
<i>Event:</i> Robot Competition</p>

<p><b>Princeton University/University of Illinois at Urbana-Champaign</b><br />
OPTIMOL<br />
Team Members: Fei-Fei Li (Princeton), Jia Li (Illinois), Juan Carlos Niebles (Illinois), Brendan Collins (Princeton), Rahul Mehta (Illinois)<br />
<i>Event:</i> Robot Competition and Exhibition</p>

<p>OPTIMOL is a novel, automatic dataset collecting and model learning system for object categorization developed by a joint UIUC-Princeton team. Our algorithm mimics the human learning process of iteratively cumulating model knowledge and image examples. As a fully automated system, OPTIMOL uses the Internet as the (nearly) unlimited resource for images. The learning and image collection processes are done by applying object recognition techniques in an iterative and incremental way. The goal of this work is to use the tremendous web resource to learn robust object category models in order to detect and search for objects in real-world cluttered scenes.</p>

<p><b>Southern Illinois University, Edwardsville</b><br />
Fishtank Assassin<br />
<i>Event:</i> Robot Exhibition</p>

<p><b>University of British Columbia</b><br />
UBC LCI Robotics<br />
<i>Event:</i> Robot Competition</p>

<p><b>University of Manitoba</b><br />
Keystone Mixed Reality<br />
<i>Event:</i> Robot Exhibition</p>

<p><b>University of Washington</b><br />
Team Sunflowers<br />
Team Contact: Masaharu Kobashi<br />
<i>Event:</i> Robot Competition and Exhibition</p>

<p>Our robot interprets the environment by vision and it does not use any range finder. It has two video cameras whose pan, tilt, vergence, focus, and exposure are controlled by the installed computers to perform the active vision. It can accommodate up to five ATX size computer motherboards to handle the CPU intensive vision computation. The robot is designed for both indoor and outdoor use equipped with two powerful motors and sturdy chassis that can carry up to 250 pounds of batteries for extended operation of powerful computers.</p>

<p><b>Virginia Tech</b><br />
RoMeLa: Robotics and Mechanisms Laboratory<br />
Team Members: Dr. Dennis Hong, Karl Muecke, Brad Pullins, and Gabriel Goldman<br />
<i>Event:</i> Robot Exhibition</p>

<p>&ldquo;DARwIn: Dynamic Anthropomorphic Robot with Intelligence&rdquo; is a humanoid bipedal robot research platform to study dynamic gaits and locomotion. Outfitted with a sensor suite and computers, DARwIn can also perform complicated high-level tasks and autonomous behaviors such as playing soccer. DARwIn will be the first and only US entry into the humanoid division of the international autonomous soccer competition, RoboCup.</p>




