			<h2 class="backgrnd12"><span class="text1">Sixteenth Annual AAAI Mobile Robot Competition</span></h2>
<p>The Sixteenth Annual AAAI Mobile Robot Competition and Exhibition was held Monday &ndash;Thursday, July 23&ndash;26 in the Balmoral room.</p>

<p>This year's robot competition and exhibition brought together teams from universities, colleges, and research laboratories to compete and to demonstrate cutting edge, state of the art research in robotics and artificial intelligence.</p>


<h3 class="text2">Mobile Robot Workshop</h3>

<p>The robot events commence with a workshop where participants describe the research behind their entries. The workshop will include a panel of academic, industrial, and governmental roboticists that will address &ldquo;The Personal Robotics Revolution: Where Does It Stand and Where Is It Going?&rdquo;</p>

<h3 class="text2">Semantic Robot Vision Challenge</h3>

<p>In this competition, robots are given a listing of objects that they must locate and recognize. In order to determine what these objects look like, the robots are given an opportunity to search the web for images of the objects in their list before starting their search. This competition attempts to push the state of the art of semantic image understanding by requiring that robots make use of the wealth of unstructured image data that exist on the Internet today.</p>

<h3 class="text2">The Robot Exhibition</h3>

<p>The mission of the Robot Exhibition is twofold. The first goal is to demonstrate state of the art research in a less structured environment than the competition events. The exhibition gives researchers an opportunity to showcase current robotics and embodied-AI research that does not fit into the competition tasks. Second, the exhibition provides a venue for faculty using robotics in education to present their approaches and experiences.</p>

<h3 class="text2">Additional Information</h3>
<p>Updates to the robot program are available on the <a href="http://palantir.cs.colby.edu/aaai07/">supplemental robot pages</a>. A PDF version of the <a href="http://www.aaai.org/Conferences/AAAI/2007/aaai07robotcall.pdf">call for participation</a> is also available.</p>
