			<h2 class="backgrnd12"><span class="text1">AAAI-07 Headquarters Hotel / Transportation</span></h2>
<p>AAAI has reserved a block of rooms at the Hyatt Regency Vancouver at reduced conference rates.</p>

<p><b>Hyatt Regency Vancouver</b></p>
<ul><li>655 Burrard Street<br />
Vancouver, British Columbia V6C 2R7 Canada<br />
<a href="http://vancouver.hyatt.com/groupbooking/yvrrvaaai2007">AAAI-07 Online Reservations</a><br />
Reservations by telephone: 604-683-1234 or 800-233-1234<br />
Fax: 604-689-3707<br />
Refer to group name "AAAI-07"<br />
Conference Rates available  07/18/07 &ndash; 07/28/07<br />
(Rates are listed in Canadian Dollars)<br />
<i>Single Occupancy:</i> $189.00 CAD<br />
<i>Double Occupancy:</i> $199.00 CAD<br />
<i>Triple Occupancy:</i> $224.00 CAD<br />
<i>Quadruple Occupancy:</i> $259.00 CAD<br />
<i>Regency Club:</i> Additional $65.00 CAD per room per night<br />
<i>Check-in time:</i> 4:00 PM<br />
<i>Check-out time:</i> 12:00 noon<br />
<i>Cut-off date for conference rate reservations:</i> Friday, June 29, 2007<br />
Reservation requests received after the cut-off date will be accepted based on availability at the Hotel's prevailing rates.</li></ul>

<p>Reservations should be made directly with the Hyatt Regency Vancouver through one of the methods listed above. All room rates are subject to the appropriate provincial, local and occupancy taxes in effect at the time of the conference. These taxes are currently ten percent (10 percent) Provincial Rooms tax and seven percent (7 percent) Goods and Services Tax per room night occupied. The G.S.T. portion is refundable to non-Canadian residents as defined under the current legislation and upon receipt of the required documentation. Rooms must be guaranteed by a major credit card. Guests must cancel by 4:00 PM PDT 24-hours prior to arrival in order to avoid forfeit of deposit.</p>

<h3 class="text2">Parking</h3>
<p>Valet parking for overnight guests at the Hyatt Regency Vancouver is $28.00 CAD per day. Self-parking is available for $24.00 CAD per day.</p>

<p>All parking includes 24-hour in/out privileges, as well as taxes. The parking is underground, with a clearance of six feet. If you are bringing a vehicle with a higher clearance, please contact the hotel concierge for additional parking options.</p>

<h3 class="text2">Additional Hotel Information</h3>
<p>Experience the superior service and amenities of <a href="http://vancouver.hyatt.com">Hyatt Regency Vancouver.</a> Perfectly located in the heart of downtown, this Vancouver luxury hotel is the premier choice for visitors. The welcoming and knowledgeable staff is always ready to assist in making your stay the most ideal possible. Newly renovated guest rooms with stunning views await you, as does the onsite health club, delicious restaurants and live entertainment. Business travelers will appreciate services designed specially for them, including a business centre, state-of-the-art meeting facilities and Regency Club accommodations.  There are several options for dining in and around the Hyatt Regency Vancouver including the Mosaic Bar and Grille in the Hyatt.</p>

<h3 class="text2">Vancouver</h3>
<p>Vancouver was voted the &ldquo;Best City in the Americas&rsquo; for 2004, 2005, and 2006 by Cond&eacute; Nast Traveler magazine, based on the categories of ambiance, friendliness, culture and sites, restaurants, lodging, and shopping. This is just one of the city's many awards and accolades that clearly demonstrates the many reasons to visit Vancouver! Please visit <a href="http://www.tourismvancouver.com">Tourism Vancouver</a> for extensive visitor information.</p>

<p>Vancouver is a dynamic, multicultural city set in a spectacular natural environment. No matter what time of the year you visit, there are indoor and outdoor activities to please adults, families, couples and friends to no end. As the proud host of the 2010 Olympic and Paralympic Winter Games, Vancouver looks ahead to ongoing growth and great success in tourism, hospitality and business services each year.</p>

<h3 class="text2">Getting to Vancouver</h3>
<p>Vancouver International Airport (YVR) is located just 25 minutes from downtown. YVR is consistently rated one of the best airports in North America. There are more than a dozen international flights in and out of YVR every day, with 22 bound for the United States. For an estimate of how long it might take to fly to Vancouver, see the international flying times at the <a href="http://www.tourismvancouver.com/visitors/transportation/getting_here">Tourism Vancouver web site.</a></p>

<h3 class="text2">Airport Transportation</h3>
<p>There are several options to go between YVR and downtown Vancouver, including scheduled coach transfers, limousine, taxi, car rentals and transit. For more information, visit the Tourism Vancouver Tourist Info Centre located at the International and Domestic Terminals at Vancouver International Airport, or the <a href="http://www.tourismvancouver.com/visitors/transportation/getting_around/getting_to_the_airport">Tourism Vancouver web site</a>.</p>

<h3 class="text2">Airporter Bus</h3>
<p>The Airporter provides bus service to major hotel destinations in downtown Vancouver, including the Hyatt Regency Vancouver.
Information is available on the<a href="http://www.yvr.ca/guide/toandfrom/bus_shuttle.asp?id=downtown"> Vancouver International Airport Authority web site</a> or by telephone: 604.946.8866 (Vancouver only) or 1.800.668.3141 (outside the Vancouver area)</p>

<p><i>Departure times from the airport:</i>
International Terminal &mdash; every 20 minutes from 8:55 to 21:25.
Domestic Terminal &mdash; every 20 minutes from 9:00 to 21:30.</p>

<p>The Airporter bus is located on the International and Domestic Arrivals level curb. No reservations required. Tickets may be purchased at the Airporter counter, their pickup locations or on the motor coach.</p>

<p><b>Fares</b><br />
<i>Adult:</i>	$13.00 one way; $20.00 round trip.<br />
<i>Child 5&ndash;12 years:</i> $6.00 one way; $12.00 round trip.<br />
<i>Senior 65+ years:</i> $10.00 one way; $20.00 round trip.<br />
<i>Family:*</i>	$26.00 one way; $40.00 round trip.<br />
(* Family fare is 2 adults/2 children 16 years and under)</p>

