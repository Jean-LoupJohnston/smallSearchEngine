			<h2 class="backgrnd12"><span class="text1">Twelfth AAAI/SIGART Doctoral Consortium</span></h2>
			
<p>The Twelfth AAAI/SIGART Doctoral Consortium program will be held on Sunday and Monday, July 22&ndash;23, in Grouse on the 34th Floor of the Hyatt. The Doctoral Consortium provides an opportunity for a group of Ph.D. students to discuss and explore their research interests and career objectives in an interdisciplinary workshop together with a panel of established researchers. The eighteen students accepted to participate in this program will also participate in the AAAI-07 Poster/Demo Session. All interested AAAI-07 student registrants are invited to observe the presentations and participate in discussions at the workshop. AAAI and SIGART gratefully acknowledge grants from the National Science Foundation and Microsoft that provide partial funding for this event. </p>

<p>The AAAI and ACM/SIGART  Doctoral Consortium (DC) provides an opportunity for a group of Ph.D. students to discuss and explore their research interests and career objectives with a panel of established researchers in artificial intelligence. The consortium has the following objectives: (1) to provide a setting for mutual feedback on participants&rsquo; current research and guidance on future research directions; (2) develop a supportive community of scholars and a spirit of collaborative research; (3) support a new generation of researchers with information and advice on academic, research, industrial, and nontraditional career paths; and (4) contribute to the conference goals through interaction with other researchers and participation in conference events. </p>

<p>The Doctoral Consortium will be held as a workshop on July 22&ndash;23, 2007, immediately before the start of the main conference. Student participants in the Doctoral Consortium  receive complimentary conference registration and a fixed allowance for travel/housing.</p>

			<p>The organizers invite all students to attend and participate in the Doctoral Consortium, whether or not they apply to present their work. In previous years, many nonpresenting students said they found it useful to observe their peers&rsquo; presentations and to participate in the ensuing discussions. All participants selected to present their work at the Doctoral Consortium are expected to be present throughout the consortium. Our experience has been that participants gain almost as much by interacting with their peers as by having their presentations critiqued by the faculty panel. As such, we expect a commitment from participating students to attend the entire DC. </p>
