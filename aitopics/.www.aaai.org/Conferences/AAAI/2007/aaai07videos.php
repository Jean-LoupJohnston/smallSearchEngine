			<h2 class="backgrnd12"><span class="text1">AAAI-07 Video Competition Track</span></h2>
<p>The AI Video Competition was held Monday, July 23, 6:00 &ndash; 7:00 PM in the North Regency Foyer. Awards were presented at 7:00 PM.</p>

<p>AAAI held an exciting new event this year at AAAI-07, which took place during the opening reception: The inaugural AI Video Competition (www.aivideo.org). Conference attendees watched some exciting videos about AI and witnessed the best videos in the field win a &ldquo;Shakey&rdquo; &mdash; AAAI&rsquo;s new award for the best short videos in AI. </p>

<p>The objective of this competition was to communicate to the world the fun of pursuing research in AI, and illustrate the impact of some of our applications. Submitters were asked to create narrated videos of 1-5 minutes in length, focused on interesting AI research. Videos were then reviewed by an international program committee, led by the founding chairs of this competition, David Aha and Sebastian Thrun. The creators of the best videos were presented with awards named in honor of Shakey, SRI's pioneering robot. <!--Be sure not to miss the winners of this cool online video about AI research and/or applications! --> AAAI gratefully acknowledged the generous contributions of Microsoft Research and Yahoo! Research, which made this program possible.</p><!--
<h3 class="text2">Call for Video Submissions</h3>

<p>AAAI solicits submissions for a new online video competition. The goal of this competition is to communicate to the world how much fun AI (research and application) is and, in particular, to document exciting research and applications using artificial intelligence. The rules are simple: compose a short video about an exciting AI project, and narrate it in a way that makes your video accessible to a broad online audience. Accepted videos will be displayed in the AAAI-07 registration area on Monday, 23 July. During that evening's session, the developers of the best videos will be presented with awards. We strongly encourage student participation. This is your chance to make a cool online video about your AI research and/or application, and get a ton of attention!</p>

<p><i>Video format:</i> Either 1 minute (max) &ldquo;short video&rdquo; or a 5 minute (max) &ldquo;long video,&rdquo; with narration in English (or English subtitles). Consider combining screen shots, interviews, and video of a system in action. Make the video self-contained, so that newcomers to AI can understand and learn from it. We encourage a good sense of humor, though we will only accept submissions with serious AI content. Your video might cover contemporary research, or document seminal AI research in the past. Creativity is encouraged!</p>

<p><i>Submission:</i> Submit your video to a publicly-accessible www site and notify the cochairs:</p>

