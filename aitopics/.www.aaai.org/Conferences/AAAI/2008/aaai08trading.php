			<h2 class="backgrnd12"><span class="text1">AAAI-08 Trading Agent Competition</span></h2>
<p>Trading in electronic markets is increasingly becoming both a commonplace economic activity and a topic of special interest within the AI, Electronic Commerce, and Multiagent Systems (MAS) research communities.</p>

<p>We invite AAAI-08 conference registrants to see the Ninth Annual Trading Agent Competition (TAC-08) finals taking place during the AAAI-08 conference in Chicago (July 14&ndash;16, 2008).</p>

<p>Building on the success of previous Trading Agent Competition events, this year's event is again designed to spur research on common problems, promote definitions of benchmarks and standard problem descriptions, and showcase current technologies.</p>

<p>The competition will pit software agents&mdash;developed by research groups, students, and others from all over the world&mdash;against each other in challenging market games. This year, there will be two games, and two related challenge events:</p>

<p><b>1. TAC Supply Chain Management (SCM).</b> TAC/SCM simulates a dynamic supply chain environment where agents compete to secure customer orders and components required for production of these orders. The game captures many of the complexities of actual supply chains, where both demand and supply fluctuates and each manufacturer has a limited production capacity. Participants also have the option of entering two TAC-SCM Challenge events: a Procurement Challenge and a Prediction Challenge. These challenges are intended to promote additional research and encourage new entries.</p>

<p><b>2. TAC Market Design (reverse TAC, or "CAT").</b> CAT software agents represent brokers whose goals are to attract potential buyers and sellers as customers, and then to match buyers with sellers. The brokers&mdash;known as specialists&mdash;compete with one another in doing this, in the same way as, say, the London Stock Exchange competes with the NYSE for the business of stock traders.</p>

<p>The market games have been specially designed to present agents with difficult decision problems and admit a wide variety of potential bidding and negotiation strategies.</p>

<p>The SCM competition will be hosted on the SICS TAC SCM Servers available for <a href="http://www.sics.se/tac/" rel="nofollow">open source download.</a> Software agents communicate with the TAC SCM Server via a TCP-based agent programming interface. Simple example <a href="http://www.sics.se/tac/" rel="nofollow">SCM agents are available.</a> Preliminary documentation and software for participating in CAT Tournament are accessible via sourceforge. <a href="http://www.marketbasedcontrol.com/blog/index.php?page_id=5" rel="nofollow">Access details and other CAT information</a> is available as well.</p>

<p>The final rounds of the competition will be held during AAAI-08 (July 14-16, 2008) preceded by qualifying and seeding rounds in June and July. The first day of the final rounds is also scheduled to coincide with the Trading Agent Design and Analysis (TADA) workshop to be held at AAAI-08.</p>

