<p><b>Author Registration</b></p>
<p>Authors must register at the <a href="http://aaai08.confmaster.net">AAAI-08 web-based technical paper submission site.</a> (aaai08.confmaster.net.) The software will assign a password, which will enable the author to log on to submit an abstract and paper. In order to avoid a rush at the last minute, authors are encouraged to register as soon as possible.</p>

<p><b>Abstract and Paper Submission</b></p>

<p>Electronic abstract and paper submission through the <a href="http://aaai08.confmaster.net">AAAI-08 paper submission site</a> is required on or (preferably) before the dates shown in the timetable above. We cannot accept submissions by e-mail or fax.</p>

<p>Papers must be in trouble-free, high resolution PDF format, formatted for US Letter (8.5" x 11") paper, using Type 1 or TrueType fonts. Papers may be no longer than 6 pages (regular papers) or 2 pages (short papers) including references, and formatted in AAAI two-column, camera-ready style (see the <a href="http://www.aaai.org/Publications/Author/author.php">author instructions page.</a> Please note that these formatting instructions are for final, accepted papers; no additional pages can be purchased at the review stage. In addition, the copyright slug may be omitted in the initial submission phase. Please also refer to the instructions on how to prepare your paper for <a href="aaai08blind-review.php">blind review.</a></p>

<p>Authors will receive confirmation of receipt of their abstracts or papers, including an ID number, shortly after submission. AAAI will contact authors again only if problems are encountered with papers. Inquiries regarding lost papers must be made no later than February 6, 2008.</p>

<p><b><a name="supplemental">Supplemental Materials</a></b></p>
<p>Authors are permitted to upload supplemental materials for consideration during the review process. Instructions will be sent to authors after they register their paper. Please note that supplemental material will be reviewed at the discretion of the reviewer, who is not required to consider it in his or her recommendation. In order to maintain the integrity of the blind review, please be sure that supplemental materials do not contain author information. All supplemental material must be submitted no later than January 30, 2008 at 11:59pm PST.</p>


<p><b>Submissions to Other Conferences or Journals</b></p>
<p>Papers submitted to this conference must not have been accepted for publication elsewhere or be under review for another AI conference. The guidelines of the <a href="aaai08multiple-submissions.php">AAAI policy on multiple submissions</a> are fully detailed and must be carefully followed.</p>

<p><b>Review Process</b></p>
<p>Program committee members will identify papers they are qualified to review based on the information submitted electronically (the paper's title, keywords, and abstract). Their reviewing will be done blind to the identities of the authors and their institutions.</p>

<p>Authors will have a limited opportunity to respond to initial reviews. This author's feedback may then be taken into account in the final reviews and recommendations. The program committee's reviews will make recommendations to the senior program committee, which in turn will make recommendations to the program cochairs. Although the program cochairs will formally make all final decisions, in practice almost all will be made earlier in the process.</p>

<p><b>Publication</b></p>
<p>Accepted regular papers will be allocated six (6) pages in the conference proceedings; up to two (2) additional pages may be used for regular papers at a cost to the authors of $275 per page. Accepted short papers will be allocated two (2) pages in the conference proceedings; up to one (1) additional page may be used for short papers at a cost to the authors of $275 per page. Final papers exceeding page limits and those violating the instructions to authors will not be included in the proceedings. Authors will be required to transfer copyright of their paper to AAAI.</p>

<p>Papers submitted to the Nectar or Senior Member tracks are allocated four (4) pages in the conference proceedings. Please refer to the individual calls for these programs for further details.</p>
