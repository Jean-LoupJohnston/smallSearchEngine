<p>AAAI-08 is the Twenty-Third AAAI Conference on Artificial Intelligence (AI). Sponsored by the Association for the Advancement of Artificial Intelligence, the purpose of this conference is to promote research in AI and scientific exchange among AI researchers, practitioners, scientists, and engineers in related disciplines. AAAI-08 will have multiple technical tracks, student abstracts, poster sessions, invited speakers, and exhibit programs, all selected according to the highest reviewing standards.</p>

<p>AAAI-08 welcomed submissions on mainstream AI topics as well as novel cross-cutting work in related areas. Topics included but were not limited to the following:</p>
<ul>
<li>Agents</li>
<li>Cognitive modeling and human interaction</li>
<li>Commonsense reasoning</li>
<li>Constraint satisfaction</li>
<li>Evolutionary computation</li>
<li>Game playing and interactive entertainment</li>
<li>Information integration and extraction</li>
<li>Knowledge acquisition and ontologies</li>
<li>Knowledge representation and reasoning</li>
<li>Machine learning and data mining</li>
<li>Model-based systems</li>
<li>Natural language processing</li>
<li>Planning and scheduling</li>
<li>Probabilistic reasoning</li>
<li>Robotics</li>
<li>Search</li>
<li>Semantic web</li>
<li>Vision and perception</li>
</ul>
