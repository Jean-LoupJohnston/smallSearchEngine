<p><b>Special Tracks</b></p>
<p>In addition to its main technical papers track, AAAI-08 also includes five special paper tracks: three for focused topics, one for ideas that have previously been presented elsewhere but not to a general AI audience, and one for senior researchers to present broad perspectives.<p>

<p>The <a href="http://www.aaai.org/Conferences/AAAI/2008/aaai08aiweb.php">Special Track on Artificial Intelligence and the Web (AIW)</a> focuses on the use and extension of AI techniques, systems, and concepts for the world wide web.</p>

<p><a href="aaai08ii.php">The Special Track on Integrated Intelligence (II)</a> emphasizes research on evaluated approaches and techniques that synergistically combine abilities from distinct areas of AI to achieve intelligent behavior.</p>

<p><a href="http://www.aaai.org/Conferences/AAAI/2008/aaai08pgai.php">The new Special Track Physically Grounded Artificial Intelligence (PGAI)</a> features papers in robotics, vision, activity recognition and other areas of artificial intelligence where computers interface to physical environments.</p>

<p>The <a href="http://www.aaai.org/Conferences/AAAI/2008/aaai08nectar.php">AAAI Nectar (new scientific and technical advances in research)</a> Papers track encourages cross-fertilization of ideas between specific areas of AI and the general AI community, and will consist of papers based on significant AI results presented at other conferences in the last two years.</p>

<p>The<a href="http://www.aaai.org/Conferences/AAAI/2008/aaai08senior.php"> Senior Member Presentation Track</a> provides an opportunity for established researchers to give a broad talk on a well-developed body of research, an important new research area, or a thoughtful critique of trends in the field.</p>

<p>The topics relevant to these special tracks 
are described in detail elsewhere. These tracks are an integral part of the conference program and were be subject to the high standards and rigorous review that are traditional in AAAI. However, each special track had its own submission process and review criteria, and has its own program committee of reviewers with appropriate backgrounds.</p>

