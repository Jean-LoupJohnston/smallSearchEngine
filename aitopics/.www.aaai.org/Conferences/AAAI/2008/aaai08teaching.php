			<h2 class="backgrnd12"><span class="text1">AAAI-08 Teaching Forum</span></h2>
<p>The AAAI 2008 Teaching Forum aims to provide a means for researchers and educators to share ideas, strategies, and resources related to education in AI. Through the events in the forum, AAAI seeks to broaden its scope beyond research results presented in the technical program to also address educational issues in AI, which are critical to the future of the field. The forum has four components, which are integrated into the AAAI 2008 conference events: a colloquium focused on AI-themed educational resources, a track in the video program, a panel during the main technical program and invited posters presented in the Teaching Forum display area.</p>

<div class="hr1">
<hr />
</div>
<h3>Colloquium on AI Education</h3>

<p>The Colloquium on AI Education will bring together educators, researchers, and curriculum designers to present and discuss successful means for teaching AI in a variety of contexts. The goal of the colloquium is to provide a forum where teaching materials and strategies can be shared among educators to improve AI education more broadly. Moreover, the colloquium will also provide the opportunity to discuss common challenges that may be encountered in teaching AI. The colloquium welcomed paper submissions on a variety of topics, including, but not limited to the following:</p>

<ul>
	<li>Educational resources including syllabi, assignments, project ideas and pedagogical strategies related to teaching AI in post-secondary education</li>
	<li>Multidisciplinary curriculum that highlights the use of AI in other contexts (such as computational biology, algorithmic game theory, computational economics, and so on) or the theoretical concepts of roots of AI from other fields (philosophy, cognitive science, linguistics, psychology)</li>
	<li>The use of robotics and other tangible media both in AI courses and elsewhere in the curriculum</li>
	<li>Software that assists the teaching/learning process - everything from software to help visualize search spaces and search algorithms, to software substrates that can be used by students to do projects</li>
	<li>Resources and strategies for teaching specific AI subareas or topics: machine learning, robotics, computer vision, natural language processing, game playing, and many others</li>
	<li>Strategies for appropriately situating AI within a wider computer-science curriculum</li>
	<li>Ways to incorporate or address popular entertainment and media portrayal of AI (in movies, news, advertisements, new products, and so on)</li>
	<li>Real-world examples of successful AI deployments, described in sufficient detail to provide case studies and/or serve as useful springboards for other teachers</li>
	<li>Innovative means for integrating research as part of coursework in AI</li>
</ul>

<p>The colloquium will be held during the workshop program at AAAI-08, which is held right before the conference. <!--The deadlines will be the same as the <a href="http://www.aaai.org/Workshops/ws08.php">workshop submission deadlines.</a> --></p>

<p><a href="http://www.cs.hmc.edu/aieducation/">The Colloquium on AI Education</a> is being cochaired by Zachary Dodds (Harvey Mudd), Haym Hirsh (NSF/Rutgers), and Kiri Wagstaff (JPL/NASA).</p>


<div class="hr1">
<hr />
</div>
<h3>Track within the Video Program</h3>

<p>The Teaching Forum Video Program extended the recently introduced AAAI Video Competition Track with a category focused on videos produced with an educational mission. Such videos include both instructional/expository videos which aim to directly teach a particular topic as well as videos which can effectively serve to demonstrate, in an educational context, various methodologies, algorithms, or systems used in AI. One important differentiator of such education videos is that they should generally be appropriate for students learning AI rather than researchers already engaged in the field.</p>

<!--<p>The <a href="aaai08videos.php">submission deadlines</a> will be the same as the AAAI Video Competition deadlines.</p> -->

<p>The Teaching Track within the Video Program is chaired by Michael Bowling (University of Alberta).</p>


<div class="hr1">
<hr />
</div>
<h3>Additional Events</h3>

<p>To offer educators additional incentive to attend the main conference and
to disseminate the findings and highlight the resources discussed at recent events focusing on AI and Education, the main conference will also include two opportunities for conference attendees to participate in the Teaching Forum. A session in the main technical program will be devoted to a panel discussion of carefully selected topics from the colloquium. In addition, a small number of posters (~10) will be invited to appear in the Teaching Forum poster display area during the main conference. Invitees will include prominent members of the community and the best contributors to recent AI and Education events.</p>

