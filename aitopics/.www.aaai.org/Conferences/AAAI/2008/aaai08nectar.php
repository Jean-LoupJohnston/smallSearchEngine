<p>AAAI-08 will again include the Nectar track (new scientific and technical advances in research). This track aims to make the most significant AI results presented at other conferences in the last two years available to a broad AI audience. One important goal of the track is to offer researchers the opportunity to learn about areas with which they may not already be familiar. Another goal is to encourage the sort of cross-disciplinary AI work that has historically been supported by AAAI.</p>

<p>The Nectar track consists of papers that are based on important results that have already been published in the proceedings of at least one major conference in 2006 and 2007, as either a single paper or a series of papers. Examples of such AI conferences include AAMAS, AIIDE, ALIFE, ACL, CEC, CogSci, CP, FUZZ-IEEE, GECCO, ICAPS, ICCBR, ICML, IEEE CEC, IJCNN, ISWC, IUI, KCAP, KR, NIPS, SAT, UAI and WCCI. Examples of conferences in related fields with relevance to AI include CIKM, COLT, KDD, PODS, ICDTSIGIR, SIGGRAPH, SIGMOD, VLDB, and WWW.</p>

<p>Papers that report on the application of AI techniques in other fields, for example bioinformatics, may also serve as the basis for Nectar papers. <!-- Authors of application papers, however, are advised that they may find the conference on Innovative Applications of AI (IAAI) a more appropriate venue for reaching the AI community since those papers can be longer and thus provide a clearer application setting in which to describe the work. -->
Papers that have appeared in general AI conferences (such as AAAI, ECAI, IJCAI) or journals (for example, <i>Artificial Intelligence, JAIR</i>) cannot serve as the basis for Nectar papers since they have already been presented to the entire AI community.</p>

