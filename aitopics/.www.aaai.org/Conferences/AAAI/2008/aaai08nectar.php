<p>Papers must be received by February 19, 2008. Decisions on the acceptance of papers will be made by March 20, 2008.</p>


<p><b>Author Registration</b></p>
<p>Authors must register at the AAAI-08 Nectar web-based technical paper <a href="http://aaai.confmaster.net/pages/login.php?Conf=AAAINP">submission site.</a> The software will assign a password, which will enable the author to log on to submit an abstract and paper. In order to avoid a rush at the last minute, authors are encouraged to register as soon as possible after December 1.</p>

<p><b>Abstract and Paper Submission</b></p>

<p>Electronic abstract and paper submission through the <a href="http://aaai.confmaster.net/pages/login.php?Conf=AAAINP">AAAI-08 Nectar paper submission site</a> is required on or (preferably) before the deadline dates listed above. We cannot accept submissions by e-mail or fax.</p>

<p>Papers must be in trouble-free, high resolution PDF format, formatted for US Letter (8.5" x 11") paper, using Type 1 or TrueType fonts. Papers may be no longer than 4 pages including references, and formatted in AAAI two-column, camera-ready style <a href="http://www.aaai.org/Publications/Author/author.php">(see the author instructions page).</a> Please note that these formatting instructions are for final, accepted papers; no additional pages can be purchased at the review stage. In addition, the copyright slug may be omitted in the initial submission phase.</p>

<p>Authors will receive confirmation of receipt of their abstracts or papers, including an ID number, shortly after submission. AAAI will contact authors again only if problems are encountered with papers. Inquiries regarding lost papers must be made no later than February 26, 2008.</p>

<p><b>Publication</b></p>
<p>Accepted papers will be allocated four (4) pages in the conference proceedings. Up to two (2) additional pages may be used at a cost to the authors of US$ 275 per page. Final papers exceeding six (6) pages and those violating the instructions to authors will not be included in the proceedings. Authors will be required to transfer copyright of their paper to AAAI.</p>

