<p><!-- AAAI-08 invites submissions to the student abstract and poster program. -->
The goal of the AAAI-08 Student Abstract and Poster program is to provide a forum in which students can present and discuss their work during its early stages, meet some of their peers who have related interests, and introduce themselves to more senior members of the field. The program is open to all pre-Ph.D students. Nonstudent advisors or collaborators should be acknowledged appropriately, as coauthors or otherwise. However, students were requested to honor the spirit of the program by submitting only work for which they are primary investigators.</p>
