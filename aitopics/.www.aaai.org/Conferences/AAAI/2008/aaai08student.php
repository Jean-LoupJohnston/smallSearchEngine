<h3 class="text2"><a name="Doctoral">The AAAI/SIGART Doctoral Consortium</a></h3>
<p>The organizers of the <a href="http://www.aaai.org/Conferences/AAAI/2008/aaai08dc.php">Doctoral Consortium</a> invite all students to attend and participate in the Doctoral Consortium, whether or not they applied to present their work. In previous years, many nonpresenting students said they found it useful to observe their peers' presentations and to participate in the ensuing discussions.</p>
<!--
<p><b>Author Registration</b></p>
<p>Authors must register at the AAAI-08 Student Abstract web-based <a href="http://aaai08.confmaster.net/pages/login.php?Conf=AAAIStudent">submission
site.</a> The software will assign a password, which will enable the author to log on to submit an abstract. In order to avoid a rush at the last minute, authors are encouraged to register as soon as possible.</p>

<p><b>Abstract and Paper Submission</b></p>
<p>Electronic abstract submission through the <a href="http://aaai08.confmaster.net/pages/login.php?Conf=AAAIStudent">AAAI-08 Student Abstract submission site</a> is required on or (preferably) before the deadline dates listed above. We cannot accept submissions by e-mail or fax.</p>

<p>Abstracts must be in trouble-free, high resolution PDF format, formatted for US Letter (8.5" x 11") paper, using Type 1 or TrueType fonts. Abstracts may be no longer than 2 pages including references, and formatted in AAAI two-column, camera-ready style <a href="http://www.aaai.org/Publications/Author/author.php">(see the author instructions page).</a> Please note that these formatting instructions are for final, accepted papers. The copyright slug may be omitted in the initial submission phase. Abstracts exceeding the specified length and formatting requirements are subject to rejection without review.</p>
<p>The abstract must include the following:</p>
<ul>
<li>title</li>
<li>the primary author's full name, affiliation, postal address, telephone number, URL (if available), and e-mail address</li>
<li>all coauthors' full names and affiliations</li>
<li>text and any figures, tables, or diagrams</li>
</ul>

<p>It is recommended that the abstract also contain a URL of a location where reviewers can access complementary material about the student's research. The URL can often be very useful to reviewers because of the brevity of the hard-copy submission.
Authors will receive confirmation of receipt of their abstracts or papers, including an ID number, shortly after submission. AAAI will contact authors again only if problems are encountered with papers. Inquiries regarding lost abstracts must be made no later than January 30, 2008.</p>

<p>Notification of acceptance or rejection of submitted abstracts will be mailed to the author by March 20, 2008. Camera-ready copy of accepted abstracts will be due by April 8, 2008.</p>

<p><b>Submissions to AAAI-08 or Other Conferences</b></p>
<p>Students are free to submit abstracts for work reported in a regular paper submitted to the AAAI-08 or another conference, but not for work that has already been published. Abstracts will be accepted or rejected for the student session regardless of the outcomes of related paper submissions.</p>

<p><b>Publication</b></p>
<p>Accepted abstracts will be allocated two (2) pages in the conference proceedings. Students will be required to transfer copyright of the abstract to AAAI.</p>

<p><b>Poster Session</b></p>
<p>Accepted abstracts will be allocated presentation time and space in the student poster display area at the conference. Student authors of accepted abstracts must agree to prepare a poster representing the work described in their abstracts and to be available to discuss their work with visitors during their allocated time in the student poster display area.</p>

