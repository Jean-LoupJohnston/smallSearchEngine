<p>Robotics, computer vision, activity recognition and the many other disciplines where computers interface to physical environments have proven to be a major source of inspiration and crucial new insights into artificial intelligence. Physical grounding provides ready access to a challenging, real problems of perception and action with rich source of data and the related stochastic effects. Further, applications of physically grounded AI have enormous potential to improve research, learning, entertainment, commerce, and society as a whole.</p>

<p>The special track invited research papers on AI techniques, systems and concepts applied to physically grounded systems including activity recognition, robotics, and machine perception.</p>

<p>Papers either describe related research or clearly explain how the work addresses problems in physically embodied agents, opportunities or issues underlying such systems. Relevant topics include:</p>

<ul>
<li>AI for Robotics</li>
<li>Activity Recognition</li>
<li>Computer Vision</li>
<li>Intelligence and Perception for Human-Robot interaction</li>
<li>Machine Learning applied to robotics, vision, and other activities grounded in the real world</li>
<li>Machine Learning for control and decision making</li>
<li>Motion planning</li>
<li>Prediction and planning for transportation</li>
<li>Sensor Networks</li>
<li>Three-Dimensional Machine Perception</li>
</ul>
<!--
<p>Papers will be reviewed by qualified reviewers drawn from a special track committee, with specific expertise in the above areas, as well as the general AAAI program committee. Submissions to this special track deemed not to be relevant may be considered for review for the general technical papers track at the discretion of the track and conference cochairs.</p>

<p>Please note that the Special Track on Physically Grounded AI will accept a small number of Nectar track style submissions. These should be short, original summaries of important work presented recently in related areas (computer vision, robotics, sensor networks, etc.), for which it would be particularly valuable to be more widely appreciated in the broader AAAI community. For complete submission requirements and instructions, please refer to the specifications in the <a href="aaai08nectar.php">Nectar Track Call for Papers. </a> Authors should select "Nectar Track papers to be considered for PGAI Track" under "Track" at the Nectar Track submission site. Nectar track submissions are due February 19, 2008.</p>

<p><b>Author Registration</b></p>
<p>Authors must register at the <a href="http://aaai.confmaster.net/pages/login.php?Conf=AAAI-PGAI">AAAI-08 PGAI web-based technical paper submission site.</a> The software will assign a password, which will enable the author to log on to submit an abstract and paper. In order to avoid a rush at the last minute, authors are encouraged to register as soon as possible after December 1.</p>

<p><b>Abstract and Paper Submission</b></p>

<p>Electronic abstract and paper submission through the <a href="http://aaai.confmaster.net/pages/login.php?Conf=AAAI-PGAI">AAAI-08 PGAI paper submission site</a> is required on or (preferably) before the deadline dates listed above. We cannot accept submissions by e-mail or fax.</p>

<p>Papers must be in trouble-free, high resolution PDF format, formatted for US Letter (8.5" x 11") paper, using Type 1 or TrueType fonts. Papers may be no longer than 6 pages including references, and formatted in AAAI two-column, camera-ready style <a href="http://www.aaai.org/Publications/Author/author.php">(see the author instructions page).</a> Please note that these formatting instructions are for final, accepted papers; no additional pages can be purchased at the review stage. In addition, the copyright slug may be omitted in the initial submission phase. Please also refer to for instructions on how to prepare your paper for <a href="aaai08blind-review.php">blind review.</a></p>

<p>Authors will receive confirmation of receipt of their abstracts or papers, including an ID number, shortly after submission. AAAI will contact authors again only if problems are encountered with papers. Inquiries regarding lost papers must be made no later than February 6, 2008.</p>

<p><b><a name="supplemental">Supplemental Materials</a></b></p>
<p>Authors are permitted to upload supplemental materials for consideration during the review process. Instructions will be sent to authors after they register their paper. Please note that supplemental material will be reviewed at the discretion of the reviewer, who is not required to consider it in his or her recommendation. In order to maintain the integrity of the blind review, please be sure that supplemental materials do not contain author information. All supplemental material must be submitted no later than January 30, 2008 at 11:59pm PST.</p>

<p>Submissions to Other Conferences or Journals</p>
<p>Papers submitted to this conference must not have been accepted for publication elsewhere or be under review for another AI conference. However, to encourage interdisciplinary contributions, we may consider unpublished work that has been submitted or presented in part to a forum outside of AI. The guidelines of the <a href="aaai08multiple-submissions.php">AAAI policy on multiple submissions</a> are fully detailed and must be carefully followed.</p>

<p><b>Review Process</b></p>
<p>Program committee members will identify papers they are qualified to review based on the information submitted electronically (the paper's title, keywords, and abstract). Their reviewing will be done blind to the identities of the authors and their institutions. Authors will have a limited opportunity to respond to initial reviews. This author's feedback may then be taken into account in the final reviews and recommendations. The program committee's reviews will make recommendations to the track cochairs. Although the track cochairs will formally make all final decisions, in practice almost all will be made earlier in the process.</p>

<p><b>Publication</b></p>
<p>Accepted papers will be allocated six (6) pages in the conference proceedings. Up to two (2) additional pages may be used at a cost to the authors of US$275 per page. Final papers exceeding eight (8) pages and those violating the instructions to authors will not be included in the proceedings. Authors will be required to transfer copyright of their paper to AAAI.
</p>


