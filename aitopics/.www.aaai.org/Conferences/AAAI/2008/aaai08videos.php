<h4>Video Formats</h4>
<p>Choose to make either a 1 minute (max) "short video" or a 5 minute (max) "long video," with English narration (or subtitles). Consider combining screen shots, interviews, and video of a system in action. Make the video self-contained, so that newcomers to AI can understand and learn from it. We encourage a good sense of humor, but will only accept submissions with serious AI content. Your video might cover contemporary research, or document seminal AI research in the past. Creativity is encouraged!</p>

<h4>New for 2008</h4>
<p>New for 2008 is first, the Educational Video Track (managed by Michael Bowling, as part of the <a href="aaai08teaching.php">AI Teaching Forum</a>) for classroom-focused videos; and second, the Special Achievement Award.</p>

<h4>Submissions</h4>
<p>Submit your video to a publicly-accessible www site and notify the cochairs: David Aha (&#100;&#97;&#118;&#105;&#100;&#46;&#97;&#104;&#97;&#64;&#110;&#114;&#108;&#46;&#110;&#97;&#118;&#121;&#46;&#109;&#105;&#108;) and Sebastian Thrun (&#116;&#104;&#114;&#117;&#110;&#64;&#115;&#116;&#97;&#110;&#102;&#111;&#114;&#100;&#46;&#101;&#100;&#117;). Videos must not contain any copyrighted video, audio, or characters. Developers of accepted videos will be asked to sign release and copyright forms.</p>

