			<h2 class="backgrnd12"><span class="text1">AI Video Competition</span></h2>

<p>The goal of AAAI's second annual video competition is to show the world how much fun AI is by documenting exciting artificial intelligence advances in research, education, and application. The rules were simple: Compose a short video about an exciting AI project, and narrate it so that it is accessible to a broad online audience. Accepted videos will be screened in the AAAI-08 registration area on Monday, 14 July 2008. During that evening's session, the developers of the best videos will be formally presented with awards. We strongly encouraged student participation.</p>

