<h4>Award Categories</h4>
<p>Best (Long), Best Short, Best Student, Best Educational, and Most Visionary. (Other categories may be added at the discretion of the cochairs.)</p>

<h4>Awards</h4>
<p>Trophies named in honor of SRI's Shakey robot and its inspirational video.</p>

<h4>Sponsors</h4>
<p>Thanks to Google, Microsoft Research, Yahoo! Research, and the AAAI AI Video Archive Project for sponsoring the awards, and to the Jozef Stefan Institute and Knexus Research Corporation for their technical support</p>

