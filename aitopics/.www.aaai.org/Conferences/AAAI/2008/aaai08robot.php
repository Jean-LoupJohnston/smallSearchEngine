			<h2 class="backgrnd12"><span class="text1">AAAI-08 Mobile Robot Competition</span></h2>

<p>AAAI is pleased to announce the Seventeenth AAAI Mobile Robot Exhibition and Workshop, to be held in conjunction with the Twenty-Third AAAI Conference on Artificial Intelligence, July 13&ndash;17, 2008, in Chicago, Illinois. 
The annual AAAI Robotics Exhibition and Workshop has featured innovative and ground-breaking research in robotics. This has provided unique opportunities for the community to formulate roadmaps and transform the field. This year, the focus will be on "Creativity and Robotics" and "Mobility and Manipulation."
