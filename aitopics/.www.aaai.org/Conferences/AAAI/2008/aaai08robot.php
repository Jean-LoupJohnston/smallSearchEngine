<p>A day of workshops will precede the exhibition. The workshops host panelists like NSF and DARPA program managers and leading scholars from both academe and industry. This will give exhibitors strong exposure to showcase cutting-edge work.</p>


<h4>Category 1: Robotics and Creativity</h4>
<p>In this category, exhibits look at the roles creativity plays in robotics. The first role looks at research where robots display creativity. Such research often employs cognitive models and computation that explains or simulates creativity. The second role examples cases where creativity is combined with design and engineering to stimulate robotics research. Collaborations that involve people from diverse backgrounds often generate a creative synergy that stimulates breakthroughs.</p>

<p>Examples include the following:</p>

<ul>
<li>Robots involved in gaming, dancing, painting, and instrument playing. Such work demonstrate cognition for the strategy, interpretation and expression needed in creativity.</li>
<li>Research that involves performance artists, therapists and linguists that discover new ways for human-robot interaction and socialization.</li>
</ul>


<h4>Category 2: Mobility and Manipulation</h4>
<p>In this category, exhibits demonstrate advanced perception and cognition that significantly advances and/or speeds robot mobility and/or manipulation. The underlying motivation is that current implementations often are too slow to be useful. For example, autonomous urban driving at real-world speeds and unprepared settings will demand systems that can quickly recognize traffic patterns, forecast actions and strategize reactions to update driving behavior. Another example is manipulation where robot arms and grippers must quickly recognize part shapes, plan motions in the robot's workspace, and configure appropriate grasps.
</p>

<p>Examples include the following:</p>

<ul>
<li>Applying new cognition and perception models in multi-robot coordination, human-robot interaction, path planning, or gait generation</li>
<li>Live exhibit or three-minute research videos demonstrating the most interesting thing your robot Has manipulated</li>
</ul>

