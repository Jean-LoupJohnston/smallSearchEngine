<h3>Call For Exhibits</h3> 
<p>The committee seeks proposals to exhibit work that demonstrate "Robotics and Creativity" or "Mobility and Manipulation." Invited exhibitors will receive travel, lodging, and equipment transport support. Support levels will depend on the number of applicants and availability of funds. Technical prizes and blue ribbons will be awarded in various categories. The AAAI conference proceedings will include short papers submitted by invited exhibitors. </p>
