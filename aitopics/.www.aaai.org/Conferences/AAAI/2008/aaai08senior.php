			<h2 class="backgrnd12"><span class="text1">Senior Member Presentation Track</span></h2>

<p>The Senior Member Presentation Track provides an opportunity for established researchers to give a broad talk on a well-developed body of research, an important new research area, or a thoughtful critique of trends in the field. These presentations should provide a &ldquo;big picture&rdquo; view&mdash;in contrast to regular papers, which may focus on a specific contribution. Submissions are short (a two-page abstract, plus biography and references) and review will be expedited.</p>

<p>To be eligible to submit to the senior member track, researchers had to be at least ten years out from their Ph.D., and have a track record of publishing on their topic. First authors on other AAAI-08 track submissions were not eligible to submit to the senior member track.</p>

