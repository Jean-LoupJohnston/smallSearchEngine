<p><b>Paper Submission</b></p>

<p>Electronic paper submission through the AAAI-08 Senior Member paper <a href="http://aaai.confmaster.net/pages/login.php?Conf=AAAISMP" target="_top">submission site</a> is required on or (preferably) before February 19. We cannot accept submissions by e-mail or fax.</p>

<p>Submissions must be no more than four pages long, and must adhere to the following guidelines. The first two pages should comprise an extended abstract for the proposed talk (including the title, author name, and affiliation); these pages should be formatted in AAAI two-column, camera-ready style. The submission should also include a two page abbreviated vita of the speaker, including a list of 5-10 relevant publications. Detailed style and format as well as submission instructions are available at the <a href="http://www.aaai.org/Publications/Author/author.php" target="_top">author instructions page.</a> Please note that these formatting instructions are for final, accepted papers; no additional pages can be purchased at the review stage. In addition, the copyright slug may be omitted in the initial submission phase. Papers must be in trouble-free, high resolution PDF format, formatted for US Letter (8.5" x 11") paper, using Type 1 or TrueType fonts.</p>

<p>Authors will receive confirmation of receipt of their abstracts or papers, including an ID number, shortly after submission. AAAI will contact authors again only if problems are encountered with papers. Inquiries regarding lost papers must be made no later than February 26, 2008.</p>

<p><b>Submissions to Other Conferences or Journals</b></p>
<p>Papers submitted to this conference must not have been accepted for publication elsewhere or be under review for another AI conference. However, to encourage interdisciplinary contributions, we may consider unpublished work that has been submitted or presented in part to a forum outside of AI. The <a href="aaai08multiple-submissions.php">guidelines of the AAAI policy on multiple submissions</a> are fully detailed and must be carefully followed.</p>

<p><b>Publication</b></p>

<p>Accepted Senior Member papers will be allocated four (4) pages in the conference proceedings. Up to two (2) additional pages may be used at a cost to the authors of US$ 275 per page. Final papers exceeding six (6) pages and those violating the instructions to authors will not be included in the proceedings. Authors will be required to transfer copyright of their paper to AAAI.</p>


