<h4><a name="ma2">MA2</a>
Discourse Models for Generating Optimized User Interfaces: Theory from AI and Application in HCI</h4>
<p><b>Hermann Kaindl</b></p>

<p>This intermediate-level tutorial shows how human-computer interaction can be based on discourse modeling, even without employing speech or natural language. <i>Communicative acts</i> as abstractions from speech acts can model, for example, a question or an answer. These are glued together as a so-called <i>adjacency pair.</i> Deliberately complex discourse structures can be modeled using relations from Rhetorical Structure Theory (RST). The content of a communicative act can refer to <i>ontologies</i> of the domain of discourse. Taking all this together, we created a new discourse metamodel that specifies what discourse models may look like. Such discourse models can specify an interaction design. This tutorial also sketches how such an interaction design can be used for automated user-interface generation.</p>

<p>Experience has been gained, for example, having applied this approach to the interaction design and user interface generation for a semi-autonomous robot (in the form of a shopping cart for a supermarket). For small devices like current Smart Phones, these user interfaces are ready for use in real-world applications, through optimization based on heuristic search.</p>

<p>In effect, such user interfaces are generated from models underpinned through theories from artificial intelligence (AI), and these theories are applied to human-computer interaction (HCI) and user-interface generation.</p>

<p>There are no prerequisites such as knowledge about AI or HCI in general, nor on heuristic search in particular.
</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/kaindl.jpg" alt="Hermann Kaindl" width="57" height="75" class="img1" /><b>Hermann Kaindl</b> joined the Vienna University of Technology in Vienna, Austria, in early 2003 as a full professor, and he is currently the director of an institute. Prior to moving to academia, he has gained more than 24 years of industrial experience. He is a Distinguished Scientist Member of the ACM, and he is on the executive board of the Austrian Society for Artificial Intelligence.
</p>
<p>&nbsp;</p>

