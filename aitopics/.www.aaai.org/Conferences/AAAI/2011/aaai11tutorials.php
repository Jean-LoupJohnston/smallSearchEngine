</div>

<h4><a name="mp1">MP1</a>
Algorithms for Classical Planning</h4>
<p><b>Jussi Rintanen</b></p>

<p>Planning is a fundamental aspect of the behavior of intelligent beings. Construction of intelligent agents, physical or virtual, often requires the solution of complex forms of the planning problem, identifying sequences of actions that fulfill the performance objectives of the agent. Understanding of planning has made dramatic progress in the last 10 years, tremendously improving its applicability to hard problems requiring intelligent decision making and acting.</p>

<p>The focus of the tutorial is in the most important state-space traversal methods: heuristic state-space search and logic-based symbolic methods such as planning as satisfiability. These directly solve the classical planning problem, but are also the basis of solving its extensions such as temporal and conditional planning. We explain the most important algorithms for classical planning, relations between them, and their applicability to more general planning problems. We also give a practical overview of existing planning systems.</p>

<p>The target audience is AI researchers interested in understanding the landscape of modern planning and the issues arising in their application. As background knowledge we assume a basic understanding of standard search algorithms and logic.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/rintanen.jpg" alt="Jussi Rintanen" width="54" height="75" class="img1" /><b>Jussi Rintanen</b> is a principal researcher at NICTA, the leader of NICTA's planning group, as well as an adjunct professor at the Australian National University. In the last years, his main interests have been in constraint-based search methods and their application to hard combinatorial problems such as diagnosis, planning and control.
</p>
<p>&nbsp;</p>

