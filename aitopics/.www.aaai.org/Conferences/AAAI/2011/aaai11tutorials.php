<h4><a name="sa1">SA1</a>
Machine Learning in Time Series Databases (and Everything Is a Time Series!)</h4>
<p><b>Eamonn Keogh</b></p>

<p>Time series and multimedia data are ubiquitous; large volumes of such data are routinely created in scientific, industrial, entertainment, medical and biological domains. Examples include gene expression data, medical imagery, electrocardiograms, electroencephalograms, gait analysis, stock market quotes, space telemetry, microarrays, zoology, and others. Furthermore, many kinds of data that are not true time series can be fruitfully transformed into pseudo-time series, including text, DNA, shapes, video, and others.</p>

<p>To deal with such data we must carefully chose algorithms and data representations. While most representations used in the past have been real valued (wavelets and Fourier methods) in this tutorial I advocate for using discrete (symbolic) representations of the data. Symbolic representations allow us to use very useful algorithms and data structures which are not available for real data, for example suffix trees, hashing and Markov models.</p>

<p>The tutorial will be illustrated with numerous real world examples created just for this tutorial, including examples from archeology (petroglyphs and projectile points), microscopy (nematodes and blood cells), historical manuscripts, zoology, motion capture and biometrics. The data mining tasks considered include indexing, classification, clustering, novelty discovery, motif discovery and visualization.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/keogh.jpg" alt="Eamonn Keogh" width="54" height="75" class="img1" /><b>Eamonn Keogh's</b> research interests are in data mining, machine learning and information retrieval, especially for time series data. He is one of the top-ten most prolific authors in the ACM SIGKDD, IEEE ICDM and SIAM SDM conferences, and his time series papers have been cited thousands of time are used in research and commercial efforts worldwide.
</p>
