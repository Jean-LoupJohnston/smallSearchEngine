<h4><a name="sp2">SP2</a>
Human Computation: Core Research Questions and State of the Art</h4>
<p><b>Luis von Ahn and Edith Law</b></p>

<p>Human computation is the study of systems where humans perform a major part of the computation or are an integral part of the overall computational system. With the growth of the web, human computation systems, for example, Games With A Purpose (the ESP Game), crowdsourcing marketplaces (Amazon Mechanical Turk, oDesk), and identity verification tasks (reCaptcha), can now leverage the abilities of an unprecedented number of people to solve complex problems that are beyond the scope of existing AI algorithms.</p>

<p>Our tutorial will highlight the core research questions in human computation, and focus on the design of mechanisms, algorithms and interfaces for tackling each of those questions. The tutorial will be divided into two sections: (1) human computation algorithms (for example, programming paradigms and tools, efficiency, output aggregation, task routing, the role of machine intelligence), and (2) design (for example, games with a purpose, incentives, human computation markets, crowd-driven interfaces). We expect participants to leave with a bird's eye view of the research landscape, as well as some tools to begin their own investigations. More details can be found at the <a href="http://humancomputation.com/Tutorial.html" rel="nofollow"> supplemental tutorial site.</a></p>
<p><i>Prerequisite Knowledge:</i> Basic Knowledge of AI and machine learning.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/law.jpg" alt="Edith Law" width="62" height="75" class="img1" /><b>Edith Law</b> is a Ph.D. candidate at Carnegie Mellon University, who is doing research on human computation systems that harness the joint efforts of machines and humans, particularly in the context of games. She is the co-organizer of HCOMP 2009 and 2011, and the recipient of the Microsoft Graduate Research Fellowship 2009&ndash;2011.
</p>
<p>&nbsp;</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/vonahn.jpg" alt="Luis von Ahn" width="57" height="75" class="img1" /><b>Luis von Ahn</b> is a professor at Carnegie Mellon University. He is the recipient of a MacArthur Fellowship, a Packard Fellowship, a Microsoft New Faculty Fellowship, and a Sloan Research Fellowship and has been named one of the 50 best minds in science by <i>Discover Magazine</i> and one of the "brilliant 10" scientists of 2006 by <i>Popular Science Magazine.</i>
</p>
