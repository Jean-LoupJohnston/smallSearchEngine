<h4><a name="sa3">SA3</a>
Discourse Structure: Theory and Practice</h4>
<p><b>Bonnie Webber, Markus Egg, and Valia Kordoni</b></p>

<p>Understanding natural language is a long-standing goal of AI. Since language is more than just individual sentences, achieving this goal means confronting discourse and its structure. But doing so can also improve system performance on tasks such as information extraction, summarization, essay grading, and opinion mining.</p>

<p>This tutorial presents aspects of discourse structure and the improvements they can bring to system performance. The tutorial eschews a monolithic view of discourse structure in favor of an integrated approach to topic modeling and segmentation, genre-specific discourse segmentation, entity-based structure, structure based on discourse relations, and hierarchical structure.</p>

<p>Part 1 of the tutorial presents complementary bases for organizing and structuring discourse and their formal properties, to demonstrate the challenges of discourse modeling. Since an integrated, multifaceted approach to discourse structure can use different algorithms to recognize different structures, Part 2 describes algorithms in current use and resources for their training and/or evaluation. The possibility of joint modeling, addressing multiple aspects of discourse structure simultaneously, will emerge as a possible way forward. Part 3 outlines established and novel uses of discourse structure in the tasks noted above. We close with a list of problems for attempts to use discourse structure more widely or, more significantly, to achieve AI's Holy Grail of understanding natural language.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/webber.jpg" alt="Bonnie Webber" width="54" height="75" class="img1" /><b>Bonnie Webber</b> (&#98;&#111;&#110;&#110;&#105;&#101;&#64;&#105;&#110;&#102;&#46;&#101;&#100;&#46;&#97;&#99;&#46;&#117;&#107;) is a professor in the School of Informatics, Edinburgh University. She is best known for work on question answering (starting with LUNAR in the early 1970s) and discourse phenomena (starting with her PhD thesis on discourse anaphora). She has also carried out research on animation from instructions, medical decision support systems and biomedical text processing.
</p>
<p>&nbsp;</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/egg.jpg" alt="Markus Egg" width="56" height="75" class="img1" /><b>Markus Egg</b> (&#109;&#97;&#114;&#107;&#117;&#115;&#46;&#101;&#103;&#103;&#64;&#97;&#110;&#103;&#108;&#105;&#115;&#116;&#105;&#107;&#46;&#104;&#117;&#45;&#98;&#101;&#114;&#108;&#105;&#110;&#46;&#100;&#101;) is a professor of linguistics at the Department of English and American Studies of the Humboldt University in Berlin. His main areas of interest are syntax, semantics, pragmatics, and discourse; the interfaces between them; and their implementation in NLP systems.
</p>
<p>&nbsp;</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/kordoni.jpg" alt="Valia Kordoni" width="62" height="75" class="img1" /><b>Valia Kordoni</b> (&#107;&#111;&#114;&#100;&#111;&#110;&#105;&#64;&#100;&#102;&#107;&#105;&#46;&#100;&#101;) is a senior researcher at the Language Technology Lab of the German Research Centre for Artificial Intelligence (DFKI GmbH) and an assistant professor at the Department of Computational Linguistics of Saarland University. Her main areas of interest are syntax, semantics, pragmatics and discourse. She works on the theoretical development of these areas as well as on their implementation in NLP systems.
</p>
