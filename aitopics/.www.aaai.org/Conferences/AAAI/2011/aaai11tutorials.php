<h4><a name="sa2">SA2</a>
Security Games</h4>
<p><b>Chris Kiekintveld, Nicola Gatti, and Manish Jain</b></p>

<p>Game theory is an increasingly important paradigm for modeling and decision-making in security domains, including homeland security resource allocation decisions, robot patrolling strategies, and computer network security. Several deployed real-world systems use game theory to randomize critical security decisions to prevent terrorist adversaries from exploiting a predictable security schedule. The ARMOR system deployed at the LAX airport and the IRIS system deployed by the Federal Air Marshals Service were first presented at the AAMAS conference.</p>

<p>This tutorial will introduce a wide variety of game-theoretic modeling techniques and algorithms that have been developed in recent years for security problems. Introductory material on game theory and mathematical programming (optimization) will be included in the tutorial, so there is no prerequisite knowledge for participants. After introducing the basic security game framework, we will describe algorithms for scaling to very large games, methods for modeling uncertainty and attacker observation capabilities in security games, and applications of these techniques for randomized resource allocation and patrolling problems. At the end we will highlight the many opportunities for future work in this area, including exciting new domains and fundamental theoretical and algorithmic challenges.
</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/kiekintveld.jpg" alt="Christopher Kiekintveld " width="50" height="75" class="img1" /><b>Christopher Kiekintveld </b> is currently an assistant professor at the University of Texas at El Paso, USA. He has done extensive work recently in applications of game theory for Homeland Security, including work on deployed software tools for the Federal Air Marshals Service and Transportation Security Administration. He has coauthored several papers on the subject of security games that have been presented at previous AAMAS conferences, in both the main track and industry track (including the winner of the 2009 industry track best paper award). He has given numerous presentations at conferences, workshops, seminars, and guest lectures for courses, including many on the topic of security games. He is currently teaching an undergraduate course on data structures and algorithms, and was previously a teaching assistant in three different courses.
</p>
<p>&nbsp;</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/gatti.jpg" alt="Nicola Gatti" width="50" height="75" class="img1" /><b>Nicola Gatti</b> is currently assistant professor with tenure at the Politecnico di Milano, Italy. His research topics are artificial intelligence, multiagent systems, and specifically algorithmic game theory. Currently, he is lecturer of informatics systems (undergraduate course) and algorithmic game theory (PhD course), and assistant of the graduate courses of artificial intelligence and of autonomous agents and multiagent systems in computer engineering, at Politecnico di Milano. He has coauthored more than ten papers on the subject of security games that have been presented at several conferences (for example, AAAI, AAMAS, IAT, GAMESEC), receiving two best paper awards at IAT and ICUMT, respectively.
</p>
