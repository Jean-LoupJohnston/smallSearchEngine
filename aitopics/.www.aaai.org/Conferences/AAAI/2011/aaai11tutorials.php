<h4><a name="sp4">SP4</a>
Recognizing Behavior in a Spatio-Temporal Context</h4>
<p><b>Hans W. Guesgen, Mehul Bhatt, and Stephen Marsland</b></p>

<p>Recognizing human behavior plays a significant role in many applications, ranging from ensuring security in public and private places to monitoring people (for example, with diminished or challenged mental and physical capabilities) and their interactions with systems and artefacts in a smart home, to name just two examples. Recent years have seen significant progress in methods, algorithms, and technologies for behavior recognition, but the task remains to be a challenging one and far from being solved.</p>

<p>The tutorial will provide an introduction to the recent developments of the area of behavior recognition, highlighting the AI techniques that are most frequently used to achieve the task. It will argue that behavior recognition in isolation is likely to fail, independently of which method is used. Based on this observation, it will demonstrate how considering the spatio-temporal context in which the behavior occurs can boost the performance of the recognition process. The tutorial concludes by highlighting some challenges and opportunities for AI research in this vibrant and emerging area of sociological, scientific, and economic interest. Since the topic integrates many areas of AI, it is appropriate to a general audience.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/guesgen.jpg" alt="Hans W. Guesgen" width="56" height="75" class="img1" /><b>Hans W. Guesgen</b> holds a chair in computer science at Massey University, New Zealand. He has taught courses at all levels of the computer science curriculum for about 20 years and has published extensively in his area of research, which in particular includes spatio-temporal aspects of ambient intelligence.
</p>
<p>&nbsp;</p>
<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/bhatt.jpg" alt="Mehul Bhat" width="60" height="75" class="img1" /><b><a href="http://cindy.informatik.uni-bremen.de/cosy/staff/bhatt/www/" rel="nofollow">Mehul Bhatt</a></b> is a research fellow at the Cognitive Systems Institute at the University of Bremen, Germany. His research encompasses the areas of spatial and temporal reasoning, commonsense reasoning, cognitive robotics, ontology, and parallel and distributed systems. He has been a recipient of the Alexander von Humboldt Fellowship (Germany), a German Academic Exchange Service (DAAD) Award for Young Scientists and Academics, and an Australian Post-graduate Award. Bhatt has contributed in areas such as architectural design, cognitive robotics, ambient intelligence and smart environments, and medical decision support systems.
</p>
<p>&nbsp;</p>
<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/marsland.jpg" alt="Stephen Marsland" width="60" height="75" class="img1" /><b>Stephen Marsland</b> is an associate professor in the School of Engineering and Advanced Technology at Massey University, New Zealand. His research interests are in diffeomorphism groups and shape spaces, machine learning and smart homes, and complex networks and complexity. He is the author of <i>Machine Learning: An Algorithmic Perspective.</i>
</p>
<p>&nbsp;</p>
