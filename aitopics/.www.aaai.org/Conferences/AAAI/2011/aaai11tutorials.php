<h4><a name="ma3">MA3</a>
From Structured Prediction to Inverse Reinforcement Learning</h4>
<p><b>Hal Daume III</b></p>

<p>Machine learning is all about making predictions; in many AI application domains (language, vision, biology) we see lots of complex rich structure. Structured prediction marries these two. However, structured prediction isn't always enough: sometimes the world throws even more complex data at us, and we need reinforcement learning techniques. This tutorial is all about the <i>how</i> and the <i>why</i> of structured prediction and inverse reinforcement learning (also known as inverse optimal control): participants should walk away comfortable that they could implement many structured prediction and IRL algorithms, and have a sense of which ones might work for which problems.</p>

<p>The first half of the tutorial will cover the basics of structured prediction the structured perceptron and Magerman's incremental parsing algorithm. It will then build up to more advanced algorithms that are shockingly reminiscent of these simple approaches: maximum margin techniques and search-based structured prediction.</p>

<p>The second half of the tutorial will ask the question: what happens when our standard assumptions about our data are violated? This is what leads us into the world of reinforcement learning (the basics of which we'll cover) and then to inverse reinforcement learning and inverse optimal control.
</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/daume.jpg" alt="From Structured Prediction to Inverse Reinforcement Learning
Hal Daume III" width="74" height="75" class="img1" /><b>Hal Daume III</b> is an assistant professor in computer science at the University of Maryland, with a joint appointment in Linguistics. He is primarily interested in the interface between natural language processing, computational linguistics and machine learning. His work in statistical modeling spans multiple aspects of language processing, including structured prediction, Bayesian methods, domain adaptation, and linguistic typology.
</p>
<p>&nbsp;</p>


