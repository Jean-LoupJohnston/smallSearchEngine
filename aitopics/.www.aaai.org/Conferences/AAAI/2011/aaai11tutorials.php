<h4><a name="sp3">SP3</a>
Large-Scale Data Processing with MapReduce</h4>
<p><b>Jimmy Lin</b></p>

<p>This tutorial provides an introduction to large-scale data processing with MapReduce, focusing in particular on scalability and the tradeoffs associated with distributed processing of large datasets. Emphasis will be placed on analysis of large unstructured text collections, although material will touch on management of structured data and large-scale graph algorithms as well. Content will include general discussions about algorithm design (for example, representational issues associated with large event spaces), presentation of illustrative algorithms (for example, iterative optimization algorithms), and case studies in a range of applications.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/lin.jpg" alt="Jimmy Lin" width="57" height="75" class="img1" /><b>Jimmy Lin</b> is an associate professor in the iSchool at the University of Maryland, College Park. He joined the faculty in 2004 after completing his Ph.D. in EECS at MIT. Lin's research interests lie at the intersection of NLP and IR, with a particular focus on large-scale distributed algorithms. He is currently on sabbatical at Twitter.
</p>
