<h4><a name="mp4">MP4</a>
Philosophy as AI and AI as Philosophy</h4>
<p><b>Aaron Sloman</b></p>

<p>Although most AI researchers have engineering objectives, some are primarily interested in the scientific study of minds, both natural and artificial. Some of the deep connections between both scientific and applied AI are linked to old problems in philosophy about the nature of mind and knowledge, what exists, how minds are related to matter, about causation and free will, about the nature of consciousness, about how language is possible, about creativity, and about whether non-biological machines can have minds. Questions linking AI and philosophy motivated AI pioneers such as Ada Lovelace, Alan Turing, Marvin Minsky, John McCarthy and Herbert Simon, and are also addressed by Margaret Boden, Andy Clark, David Chalmers, Daniel Dennett, John Searle, and others. Yet many questions remain unanswered and some philosophers and scientists think AI is only engineering.</p>

<p>I'll try to explain how progress can be based on unnoticed relationships between AI and Philosophy, including the connections of both with unexplained features of biological evolution and the development of various kinds of intelligence in individual animals. The tutorial will be highly interactive and provocative.</p>

<p><i>Prerequisites:</i>: None, except interest in how AI and philosophy are mutually relevant, and illuminate the nature of mind and intelligence. See the <a href="http://www.cs.bham.ac.uk/research/projects/cogaff/aaaitutorial/">author's website</a> for details.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/sloman.jpg" alt="Aaron Sloman" width="59" height="75" class="img1" /><b>Aaron Sloman</b> received his first degree in mathematics and physics, DPhil in philosophy of mathematics, then worked in philosophy, cognitive science, AI and theoretical biology. He is the author of <i>The Computer Revolution in Philosophy</i> (1978) and many articles and book chapters, a Fellow of AAAI, and also a contributor to Poplog multilanguage toolkit for AI research and teaching.
</p>
<p>&nbsp;</p>


