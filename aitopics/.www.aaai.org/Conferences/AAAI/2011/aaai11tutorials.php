<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/jain.jpg" alt="Manish Jain" width="52" height="75" class="img1" /><b>Manish Jain</b> is currently a PhD candidate at the University of Southern California, USA. He is a part of the Teamcore Research group. His work is on the applications of game theoretic and large scale optimization techniques for homeland security, including deployed software tools for the Federal Air Marshals Service and Los Angeles World Airports police. He has coauthored papers on the subject of security games that have been presented at AAMAS and AAAI conferences, in both the main track and the industry track. He has given presentations at AAMAS, AAAI and IJCAI conferences, and several other workshops. He has been a teaching assistant for courses on multiagent systems at both the graduate and the under-graduate level. His work published at the operations research venue Interfaces was recently nominated as a finalist for the EURO excellence in Practice award. In addition to many presentations at technical conferences and workshops, he has been a teaching assistant for Milind Tambe.
