<h4><a name="sp1">SP1</a>
Event Processing &mdash; State of the Art and Research Challenges</h4>
<p><b>Opher Etzion and Yagil Engel</b></p>

<p>The term <i>event processing</i> refers to an approach to software systems that is based on reaction to events, often under time constraints, and that includes specific decision making logic based on detection of patterns in events as they occur. In comparison to traditional AI methods, event processing avoids comprehensive decision theoretic analysis in favor of tractability and quick reaction given high volumes of real-time data. Event processing is being used for dynamic operational behavior (for example, real-time algorithmic trading in capital markets), active diagnostics (for example, problem determination in network management), information dissemination (for example, detecting patterns of signals from multiple monitors connected to a patient), and observation (for example, a luggage arrived at a cart destined to the wrong flight).
 
The tutorial is intended for the AI community; it is self-contained and does not require any prior knowledge. The audience will gain insights about event processing, and on how it relates to various AI subdisciplines. The first part of the talk will detail the current state of the art, the leading architectures, the basic building blocks, and the various programming styles; the second part will be dedicated to research challenges in temporal probabilistic reasoning, semantic correctness, pattern matching, inexact processing, and machine learning.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/etzion.jpg" alt="Opher Etzion" width="53" height="75" class="img1" /><b>Opher Etzion</b> is an IBM senior technical staff member, chair of the Event Processing Technical Society, and an ACM distinguished speaker, and in addition is an adjunct senior teaching fellow at the Technion. He is one of the pioneers of the event processing area, coauthor of the first technical book in this area, and published more than 70 papers on related topics.
</p>
<p>&nbsp;</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/engel.jpg" alt="Yagil Engel" width="69" height="75" class="img1" /><b>Yagil Engel</b> is a research scientist at IBM Research Lab &ndash; Haifa, and an adjunct lecturer at the Technion &ndash; Israel Institute of Technology. He joined IBM after two years of post-doc research at the Technion, in the areas of planning and decision theory in artificial intelligence. He received his PhD degree in computer science from the University of Michigan, where his thesis work is in the areas of graphical models for decision making, in the context of business-to-business electronic commerce and procurement problems. Before graduate studies he worked in the software industry, mainly in the area of electronic commerce. His current research activities combine operational decision making, probabilistic reasoning, and decision theoretic planning.
</p>
