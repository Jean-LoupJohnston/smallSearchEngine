</div>

<h4><a name="ma4">MA4</a>
Opinion Mining and Sentiment Analysis</h4>
<p><b>Bing Liu</b></p>

<p>Opinion mining or sentiment analysis is the computational study of people's opinions, appraisals, and emotions toward entities, individuals, topics and their attributes expressed in text. Opinions are important because they are key influencers of our behaviors. Our beliefs and perceptions of reality are to a considerable degree conditioned on how others see the world. For this reason, when we need to make a decision we often seek out the opinions of others. In recent years, opinion mining from social media has emerged as a popular research area in NLP and text mining due to many challenging research problems and a range of applications. It has gone from computer science to reach social sciences and management science. In fact, apart from NLP, it also presents many challenges to other areas of AI, for example, machine learning, data mining, and automated reasoning. In the tutorial, I will first define the problem, describe its main tasks, and then present the current state-of-the-art techniques. Many examples will also be given to help participants better understand the key concepts. One feature of the tutorial is that it will not only address seminal research issues but also will look at the technology from an application angle.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/liu.jpg" alt="Bing Liu" width="64" height="75" class="img1" /><b>Bing Liu</b> is a professor of computer science at the University of Illinois at Chicago. He received his PhD from the University of Edinburgh. His research interests include opinion mining or sentiment analysis, and data mining. He has given more than 20 keynote and invited talks on opinion mining and has served as program chairs of KDD, ICDM, WSDM, SDM, CIKM, and PAKDD.
</p>
<p>&nbsp;</p>

