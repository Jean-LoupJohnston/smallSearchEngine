<h4><a name="ma1">MA1</a>
Collective Intelligence</h4>
<p><b>Haym Hirsh</b></p>

<p>Collective intelligence refers to our ability to bring people and computing together in ways that exhibit behaviors and achieve outcomes that, collectively, are more intelligent than is possible by people or machines alone. Collective intelligence makes contact with AI in three ways. First, AI practitioners are increasingly using collective intelligence as a routine element in their work, such as to create corpora in computer vision or to evaluate results in information retrieval. Second, AI methods are a key enabler of many examples of collective intelligence, such as mining consumer behaviors and product review sentiments to facilitate product recommendation. Third, collective intelligence offers a provocative phenomenon to consider by those seeking computationally tractable understandings of intelligence. This tutorial will survey the state of the art in collective intelligence from an AI perspective. It will discuss examples of collective intelligence in which people knowingly act to achieve collective outcomes, such as editing Wikipedia articles, identifying astronomical objects, or rating news articles, as well as examples in which collectively intelligent outcomes arise through automated analysis of people's routine activities, as exhibited by Google's page ranking algorithm and Amazon's recommendation system. The tutorial will conclude with a discussion of prospects for the future.
</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/hirsh.jpg" alt="Haym Hirsh" width="56" height="75" class="img1" /><b>Haym Hirsh</b> is a professor of computer science at Rutgers University, and a visiting scholar at MIT's Center for Collective Intelligence. His BS is in mathematics-computer science from UCLA and MS and PhD in computer science from Stanford. From 2006&ndash;2010 he was director of information and intelligent systems at the National Science Foundation.
</p>
<p>&nbsp;</p>

