<h4><a name="mp2">MP2</a>
Conformal Predictions for Reliable Machine Learning: Theory and Applications</h4>
<p><b>Vineeth N. Balasubramanian and Shen-Shyang Ho</b></p>

<p>Reliable estimation of confidence remains a significant challenge as learning algorithms proliferate into challenging real-world applications. The Conformal Predictions framework is a recent development in machine learning to associate reliable measures of confidence with results in classification and regression. This framework is founded on the principles of algorithmic randomness, transductive inference and hypothesis testing, and has several desirable properties for potential use in various real-world applications, such as the calibration of the obtained confidence values in an online setting. Further, this framework can be applied across all existing classification and regression methods, thus making it very generalizable. In recent years, there has been a growing interest in applying this framework to real-world problems such as clinical decision support, medical diagnosis, sea surveillance, network traffic classification, and face recognition. This tutorial will: (1) expose the audience to the basic theory of the framework; (2) demonstrate examples of how the framework can be applied in real-world problems, and (3) provide sample adaptations of the framework to related problems such as active learning, transfer learning, anomaly detection, and model selection.</p>

<p>A basic understanding of machine learning approaches (such as classification, clustering, regression, and others) is the only prerequisite for this tutorial.
</p>
<p>Sethuraman Panchanathan and Vladimir Vovk served as co-organizers of this tutorial.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/balasubramanian.jpg" alt="Vineeth Balasubramanian" width="60" height="75" class="img1" /><b>Vineeth Balasubramanian</b> is an assistant research professor at Arizona State University. His research interests include pattern recognition, machine learning, computer vision and multimedia computing. His work on the conformal predictions framework was nominated as an outstanding PhD dissertation at ASU, as well as the annual ACM Doctoral Dissertation Award.
</p>
<p>&nbsp;</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/ho.jpg" alt="Shen-Shyang Ho" width="70" height="75" class="img1" /><b>Shen-Shyang Ho</b> is a research associate at the University of Maryland, Institute for Advanced Computer Studies (UMIACS). His research interests include data mining, machine learning, and pattern recognition in spatiotemporal/data streaming settings. His current research involves the application of data mining and machine learning approaches to support NASA Earth science research.
</p>
<p>&nbsp;</p>

