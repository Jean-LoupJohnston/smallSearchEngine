<h4><a name="mp3">MP3</a>
Information Organization and Retrieval with Collaboratively Generated Content</h4>
<p><b>Eugene Agichtein and Evgeniy Gabrilovich</b></p>

<p>Ubiquitous access to the Internet enables millions of web users to collaborate online. These efforts often result in the construction of large repositories of knowledge, either as their primary aim (for example, Wikipedia) or as a by-product (for example, Yahoo! Answers). The unprecedented amounts of information in collaboratively generated content (CGC) enable new, knowledge-rich approaches to information access. Some examples include the use of human-defined concepts to augment the bag of words, using large-scale taxonomies to construct additional class-based features, or using Wikipedia for better word sense disambiguation. However, the quality of CGC varies significantly, and a substantial amount of post-processing is necessary to take full advantage of the knowledge therein.</p>

<p>The tutorial will cover two complementary directions: (1) Using CGC as an enabling resource for knowledge-enriched, intelligent information retrieval algorithms, and (2) Development of supporting technologies for extracting, filtering, and organizing CGC.</p>

<p>As we will show, not only the knowledge repositories can be used to improve information retrieval methods, but the reverse pollination is also possible. For example, better information extraction methods can be used to automatically collect more knowledge, or to verify the contributed content.</p>

<p>Understanding of basic concepts in machine learning and data mining is expected. Information retrieval and NLP concepts will be introduced as needed.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/agichtein.jpg" alt="Eugene Agichtein" width="47" height="75" class="img1" /><b>Eugene Agichtein</b> is an assistant professor in the Emory University Mathematics and Computer Science Department, where he directs the Intelligent Information Access Lab (IRLab). Agichtein 's expertise is in web search and information retrieval, with an emphasis on organizing and searching collaboratively-generated content. His work has been supported by NSF, HP Labs, and Yahoo.</p>

<p>&nbsp;</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/gabrilovich.jpg" alt="Evgeniy Gabrilovich" width="56" height="75" class="img1" /><b>Evgeniy Gabrilovich</b> is a senior research scientist and manager of the NLP and IR Group at Yahoo! Research. His research interests include information retrieval, machine learning, and computational linguistics. Gabrilovich has published extensively on computational advertising, as well as on using world knowledge to enhance text representation beyond the bag of words, and has served as a senior pc / area chair at AAAI, IJCAI, SIGIR, EMNLP, and ICWSM.
</p>
<p>&nbsp;</p>

