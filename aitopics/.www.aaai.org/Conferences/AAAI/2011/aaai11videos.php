			<h2 class="backgrnd12"><span class="text1">AAAI-11 AI Video Competition</span></h2>

<h4>Call for Video Submissions</h4>
<p><i>AAAI-11 AI Video Competition Submission Deadline Extended to June 17, 2011!</i></p>
<img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/shakey.jpg" alt="AAAI Video Awards" width="258" height="272" class="img1" />
<p>AAAI solicits submissions for the fifth annual video competition! Its goal is to show the world how much fun AI is by documenting exciting artificial intelligence advances in research, education, and application.</p>

<p>The rules are simple: Compose a short video about an exciting AI project, and narrate it so that it is accessible to a broad online audience. The developers of award-winning videos will be presented with Oscarlike trophies ("Shakeys," in honor of SRI's Shakey robot and its pioneering video) in a ceremony to he held at AAAI-11. We strongly encourage student participation. So go ahead and make a cool, online video about your AI project, and get a ton of attention!</p>

<h4>Video Formats</h4>
<p>Videos should be either 1-minute (max) short video or a 5-minute (max) long video, with English narration (or English subtitles). Consider combining screen shots, interviews, and video of a system in action. Make the video self-contained, so that newcomers to AI can understand and learn from it. We encourage a good sense of humor, but will only accept submissions with serious AI content. For example, we welcome submissions of videos that accomplish the following:</p>

<ul>
	<li>Highlight your own (contemporary or previous) research </li>
	<li>Focus on the activities (research or application) of your (or another) group </li>
	<li>Introduce viewers to an exciting new AI-related topic </li>
	<li>Document some long-term activity of a senior researcher </li>
	<li>Include interviews with key researchers/practitioners </li>
	<li>Can be used in the classroom </li>
	<li>Would otherwise interest prospective students to the field of AI </li>
	<li>Includes a narration</li>
</ul>

<p>This is an incomplete list. No matter what your choice, creativity is encouraged! (Please note: The developers of previous, award-winning AI Videos typically highlighted humor, background music/tracks, and/or carefully selected movie clips to make their presentations come alive and interest prospective viewers and PC members.)</p>

<h4>Submission</h4>
<p>Submit your video by placing it at a publicly-accessible www site and notifying the Video Competition cochair, Arnav Jhala (&#106;&#104;&#97;&#108;&#97;&#64;&#115;&#111;&#101;&#46;&#117;&#99;&#115;&#99;&#46;&#101;&#100;&#117;), by e-mail (with the subject line AAAI Video Competition Submission). You are also asked to complete a submission form. All developers of nominated videos will be asked to sign a distribution license form. Videos must not contain any copyrighted video, audio, or characters.</p>

<ul>
		<li><i>Submission:</i> June 17, 2011</li>
	<li><i>Notification::</i> July 8, 2011</li>
	<li><i>Final Video:</i> July 22, 2011</li>
</ul>
<h4>Reviewing</h4>
<p>
Reviewing will be conducted by a committee that will be formed by the chair. Reviewing criteria (from most to least-weighted):</p>

<ol>
	<li>Relevance to AI (research, instructional, and/or application) </li>
	<li>Degree of excitement: Entertainment value, enticing, novelty, and so on. </li>
	<li>Educational content </li>
	<li>Presentation</li>
</ol>

<h4>Potential Award Categories</h4>

<p>Potential award categories include the following (other categories may be added at the discretion of the chair):</p>
<ul>
	<li>Best (Long)</li>
	<li>Best Short</li>
	<li>Best Student</li>
	<li>Best Educational</li>
	<li>Most Innovative</li>
	<li>Most Informative</li>
	<li>Best Narration</li>
	<li>Best Sound</li>
</ul>


<h4>Awards</h4>
<p>Awards will be trophies named in honor of SRI's Shakey robot and its inspirational video.</p>


<h4>More Information</h4>
<p>For more Information contact David Aha (&#100;&#97;&#118;&#105;&#100;&#46;&#97;&#104;&#97;&#64;&#110;&#114;&#108;&#46;&#110;&#97;&#118;&#121;&#46;&#109;&#105;&#108;) or Arnav Jhala (&#106;&#104;&#97;&#108;&#97;&#64;&#115;&#111;&#101;&#46;&#117;&#99;&#115;&#99;&#46;&#101;&#100;&#117;). For complete instructions about the submission and review process, please visit the supplementary AI Video website <a href="http://www.aivideo.org/">(www.aivideo.org).</a>
</p>

<p>The AAAI-11 call for videos is also available as a printable <a href="http://www.aaai.org/Conferences/AAAI/2011/aaai11videocall.pdf">PDF document</a></p>
