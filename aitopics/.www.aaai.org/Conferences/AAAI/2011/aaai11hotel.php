			<h2 class="backgrnd12"><span class="text1">AAAI-11 &ndash; IAAI-11 Hotel and Site Information</span></h2>



<p>AAAI has reserved a block of rooms at the Hyatt Regency San Francisco at reduced conference rates. Space is limited. ((Information about student accommodations is available by writing to &#97;&#97;&#97;&#105;&#49;&#49;&#64;&#97;&#97;&#97;&#105;&#46;&#111;&#114;&#103;. Proof of full-time student status required.)</p>

<h4>Hyatt Regency San Francisco</h4>
<p>5 Embarcadero Center<br />
San Francisco, CA  94111<br />
<a href="http://sanfranciscoregency.hyatt.com" rel="nofollow">Hotel Website</a><br />
Phone: (415) 788-1234<br />
Fax: (415) 398-2567<br />
AAAI-11 Online Reservations: <br />
<a href="https://resweb.passkey.com/go/AAAI2011" rel="nofollow">resweb.passkey.com/go/AAAI2011</a><br />
Reservations by telephone: (415) 788-1234<br />
Refer to group name "AAAI-11" when making phone/fax reservations.<br />
Conference Rates available 08/02/2011 &ndash; 8/14/2011<br />
<i>Single/Double Occupancy:</i> $199.00<br />
<i>Triple Occupancy:</i> $249.00<br />
<i>Quadruple Occupancy:</i> $274.00<br />
<i>Check-in time: </i>3:00 PM<br />
<i>Check-out time:</i> 12:00 Noon<br />
<i>Cut-off date for conference rate reservations:</i> Friday, July 15, 2011</p>
<p>Reservation requests received after the cut-off date will be based on availability at the Hotel's prevailing rates.</p>

<p>Reservations should be made directly with the Hyatt Regency San Francisco through one of the methods listed above. Rate is per room, per night, excluding service charges, taxes (currently 14 percent occupancy tax), California Tourism assessment (currently $.13), and applicable service or hotel specific fees in effect at the Hotel at the time of the conference. Rates include complimentary wireless Internet access in guest sleeping rooms and hotel's lobby public spaces.</p>

<p>Rooms must be guaranteed by a major credit card. Guests must cancel by 4:00 pm PDT 24 hours prior to arrival in order to avoid forfeit of deposit.</p>

<h4>Parking</h4>
<p>The Hyatt Regency San Francisco has valet parking available to its guests for $50.00 per night. In and out privileges are available to those who bill their parking charges to their guest rooms. Non-hotel guests are subject to a 14 percent tax. Alternate parking options are available on Clay Street for $29.50 per day.</p>

<h4>Additional Hotel Information</h4>
<p>Luxury and location converge in perfect balance at Hyatt Regency San Francisco. The only AAA Four Diamond downtown San Francisco hotel situated right on the Embarcadero waterfront, guests will enjoy immediate access to both the Financial District and the City's <a href="http://sanfranciscoregency.hyatt.com/hyatt/hotels/activities/offsite/index.jsp" rel="nofollow">most famous attractions.</a> You'll find the historic Ferry Building on one side of the expansive hotel, the ferry to Alcatraz and the Bay on the other, and stunning views all around. With so many options, you may choose to wander through Chinatown to Fisherman's Wharf, shop in Union Square or board the cable cars for a riding tour of the city.</p>

<h4>Getting to San Francisco</h4>
<p>The Hyatt Regency San Francisco is accessibly by San Francisco International Airport (SFO) or Oakland International Airport (OAK). The Hyatt is about 20 minutes by taxi from SFO and will cost approximately $40.00 &ndash; $45.00 one way. Oakland is located approximately 30&ndash;40 minutes away on the other side of the San Francisco Bay. See below for alternate transportation options.</p>

<h4>Airport Transportation</h4>
<p>There are several options to use between SFO and the Hyatt, and also between Oakland and the Hyatt, including shuttle, bus, train, rental car, limousine, or taxi cab. For more information about schedules, reservations and rates, please visit  the <a href="http://www.onlyinsanfrancisco.com/transportation/" rel="nofollow">Only in San Francisco website,</a> the <a href="http://sanfranciscoregency.hyatt.com/hyatt/hotels/services/transportation/index.jsp" rel="nofollow">hotel's website,</a> or service provider websites for more information. If you are flying into SFO, we highly recommend that you use BART, as the Hyatt is only steps away from the Embarcadero station.</p>

<h4><a href="http://www.bart.gov/" rel="nofollow">BART (Bay Area Rapid Transit)</a></h4>

<p><b>SFO:</b> $8.10 each way &mdash; Exit at Embarcadero from San Francisco International Airport. Any San Francisco bound (northbound) train will stop at "Embarcadero Station." The Hyatt Regency Hotel is located directly outside the station. Trains available every 15 &ndash; 20 minutes.</p>

<p><i>Hours for Embarcadero Station:</i><br />
4:55 am &ndash; 1:01 am &ndash; Monday to Friday<br />
6:32 am &ndash; 1:01 am &ndash; Saturday<br />
8:32 am &ndash; 1:01 am &ndash; Sunday</p>

<p><b>Oakland:</b> Approximately $3.80 plus $3.00 for AirBart Shuttle. Exit at Embarcadero.</p>	

