<p><b>Timetable for Students</b></p>
<ul>
	<li><i>February 15, 2011:</i> Application package submission deadline</li>
	<li><i>March 30, 2011:</i> Acceptance Notification</li>
	<li><i>April 26, 2011:</i> Camera-ready copy due at AAAI office</li>
	<li><i>August 7&ndash;11, 2011:</i> Student Abstract and Poster Program</li>
	</ul>
<div class="hr1">
<hr />
</div>

<h4 class="text2">Submissions and Dates</h4>

<p>Electronic submission in PDF format is required. Students should submit an abstract describing their research no later than February 15, 2011. Abstracts must be no longer than 2 pages including references, and formatted in AAAI two-column, camera-ready style. Instructions about how to submit abstracts will be available at the  <a href="http://aaai-studentabstracts.confmaster.net/" rel="nofollow">AAAI conference submission web site.</a> Papers exceeding the specified length and formatting requirements are subject to rejection without review.</p>

<p>The abstract must include the following: title; the primary author's full name, affiliation, postal address, phone number, URL (if available), and e-mail address; all coauthors' full names and affiliations; text; and any figures, tables, or diagrams. The abstract should also contain a URL of a location where reviewers can access complementary material about the student's research. The URL is critical to reviewers because of the brevity of the submissions.</p>

<p>Notification of acceptance or rejection of submitted abstracts will be mailed to the author by March 30, 2011.</p>

<p>Camera-ready copy of accepted abstracts will be due by April 26, 2011.</p>


<h4 class="text2">Submissions to AAAI-11 or Other Conferences</h4>
<p>Students are free to submit abstracts for work reported in a regular paper submitted to the AAAI-11 or another conference, but not for work that has already been published. Abstracts will be accepted or rejected for the student session regardless of the outcomes of related paper submissions.
</p>

<h4 class="text2">Publication</h4>

<p>Accepted abstracts will be allocated two (2) pages in the conference proceedings. Students will be required to transfer copyright of the abstract to AAAI. A copy of the AAAI copyright form is available on the <a href="http://www.aaai.org/Press/Author/copyrightletter.php">AAAI web site.</a> </p>


<h4 class="text2">Poster Session</h4>

<p>Accepted abstracts will be allocated presentation time and space in the student poster display area at the conference. Student authors of accepted abstracts must agree to prepare a poster representing the work described in their abstracts and to be available to discuss their work with visitors during their allocated time in the student poster display area.</p>
<h4 class="text2">Student Abstract Keywords</h4>
A full list of <a href="http://www.aaai.org/Conferences/AAAI/2011/aaai11student-keywords.php">student-abstract keywords</a></a> is available on this site.
.</p>

