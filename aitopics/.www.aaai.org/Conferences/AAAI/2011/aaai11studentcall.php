			<h2 class="backgrnd12"><span class="text1">AAAI-11 Student Abstract and Poster Program</span></h2>
<p>AAAI-11 invites submissions to the student abstract and poster program. The goal of this program is to provide a forum in which students can present and discuss their work during its early stages, meet some of their peers who have related interests, and introduce themselves to more senior members of the field. The program is open to all pre-Ph.D students. Nonstudent advisors or collaborators should be acknowledged appropriately, as coauthors or otherwise. However, students are requested to honor the spirit of the program by submitting only work for which they are primary investigators.</p>

<div class="hr1">
<hr />
</div>
<h3 class="text2"><a href="http://aaai.org/Conferences/AAAI/2011/aaai11student-accepts.pdf">List of Accepted Papers</a></h3> 
