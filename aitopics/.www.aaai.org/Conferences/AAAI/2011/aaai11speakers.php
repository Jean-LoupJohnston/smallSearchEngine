<h3 class="text2"><i><a name="Gunning">IAAI-11 Invited Talk</a></i><br />
HaloBook and Progress Towards Digital Aristotle</h3>
<p><b>David Gunning (Vulcan Inc.)
</b></p>

<p>Project Halo is a long-range research effort, pursuing the vision of the "Digital Aristotle" &mdash; a system containing large volumes of scientific knowledge and capable of applying sophisticated problem-solving methods to answer novel questions, with applications in education and scientific research. The current focus of the project is the development of HaloBook &mdash; an electronic textbook capable of answering a student's questions. This talk will summarize the history and motivation for Project Halo, describe the current work on HaloBook, and discuss possible Grand Challenges to motivate future research.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/gunning.jpg" alt="David Gunning" width="54" height="75" class="img1" /><b>David Gunning</b> is a senor research program manager at Vulcan Inc., where he leads the HaloBook development. Prior to joining Vulcan, he served as a program manager at the Defense Advanced Research Projects Agency (DARPA) from 2003&ndash;2008 and 1994&ndash;2000, where he managed a number of AI projects, including the Personalized Assistant that Learns (PAL), Command Post of the Future (CPOF), Evidence Extraction and Link Discovery (EELD), and High-Performance Knowledge Bases (HPKB). Between tours at DARPA, Gunning worked as vice president of Cycorp, Inc. and SET Corporation. He holds MS degrees in computer science from Stanford University and experimental psychology from the University of Dayton.</p>
