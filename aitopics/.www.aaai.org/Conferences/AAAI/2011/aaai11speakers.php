<h3 class="text2"><i><a name="Thielscher">AAAI-11 Invited Talk</a></i><br />
From Turn-Taking to Social Ties</h3>
<p><b>Karrie Karahalios (University of Illinois)</b></p>

<p>Online communities have been studied from various perspectives since the 1980s. Much of this work has taken existing sociology techniques and molded them to fit a specific electronic environment such as IRC, Usenet, Facebook, and others. The existence of digital traces of online interaction has made this research possible at a large scale.</p>

<p>In this talk, Karahalios begins by discussing a brief history of the study of online interaction and the cues used by researchers to formulate their research. She continues describing how the study of online social spaces has changed through the lens of the work done in the Social Spaces Group. Karahalios argues that digital traces can be misleading and new techniques and interfaces are necessary to improve and study social online interaction. This discussion includes work highlighting the differences between interaction between rural and urban areas, tie strength from social network software, and implications of this work. Finally, Karahalios concludes by highlighting how online social interaction is diverging from face-to-face interaction and the importance of new methodologies and interfaces for studying this change.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/karahalios.jpg" alt="Karrie Karahalios" width="56" height="75" class="img1" /><b>Karrie Karahalios</b> is an associate professor in computer science at the University of Illinois where she heads the Social Spaces Group. Her work focuses on the interaction between people and the social cues they emit and perceive in networked electronic spaces.  Her work is informed by studies and visualizations of social communities. Of particular interest are interfaces for pubic online and physical gathering spaces such as twitter, chatrooms, cafes, parks, and others. One goal is to create interfaces that enable users to perceive conversational patterns that are present, but not obvious, in traditional communication interfaces. Karahalios completed a S.B. in electrical engineering, an M.Eng. in electrical engineering and computer science, and an S.M. and Ph.D in media arts and science at MIT.</p>
