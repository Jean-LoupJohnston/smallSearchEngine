<h3 class="text2"><i><a name="Mantaras">IAAI-11 Robert S. Engelmore Memorial Award Lecture</a></i><br />
Playing with Cases: Rendering Expressive Music Performance with Case-Based Reasoning</h3>
<p><b>
Ramon Lopez De Mantaras (Artificial Intelligence Research Institute (IIIA) and Spanish National Research Council (CSIC))
</b></p>

<p>Rendering expressive music performances involves complex processes that constitute a challenging research area for computer music research. Besides, it is a rich field for investigating aspects of human intelligence, emotion, and creativity. Case-based reasoning is one of the AI techniques that have produced more promising results in rendering expressive music performances. Furthermore, it has advanced the state of the art in case-based reasoning through the invention of new approaches to case representation, case retrieval and case reuse adapted to musical knowledge. In this talk Lopez de Mantaras will describe in some detail two successful case-based reasoning systems applied to expressive music performance that have been developed at the Artificial Intelligence Research Institute.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/mantaras.jpg" alt="Ramon Lopez de Mantaras" width="66" height="75" class="img1" /><b>Ramon Lopez de Mantaras</b> is a research professor in the Spanish National Research Council (CSIC) and director of the Artificial Intelligence Research Institute (IIIA). He earned his MS in computer science from the University of California, Berkeley, a PhD in physics from the University of Toulouse, and a PhD in computer science from the Technical University of Barcelona. He is an associate editor of the<i> AI Journal</i>, and an editorial board member of several international journals including <i>AI Magazine.</i> He was program committee Chairman of UAI-94 and ECML-00, conference chairof ECAI-04, ECML-07 and IJCAI-07, and is an ECCAI Fellow and recipient of several awards including ECCAI's DEC European AI Research Paper Award, the "City of Barcelona" Research Prize, and the International Computer Music Association "Swets &amp; Zeitlinger" Award. Lopez de Mantaras was president of the Board of Trustees of IJCAI from 2007 to 2009. His research interests include case-based reasoning, machine learning, artificial intelligence and music, and object recognition for robotics.</p>

