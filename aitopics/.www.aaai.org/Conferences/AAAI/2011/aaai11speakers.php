<h3 class="text2"><i><a name="Thielscher">AAAI-11 Invited Talk</a></i><br />
Strategic Intelligence in Social Networks</h3>
<p><b>Michael Kearns (University of Pennsylvania)</b></p>


<p>For the past six years at Penn, we have been conducting controlled human-subject experiments on strategic interaction in social networks. The overarching goal of these experiments is to provide a behavioral counterpart to the flourishing research on mathematical models of social networks, diffusion dynamics, influence in social networks, and related topics.</p>

<p>To date we have conducted experiments on a wide variety of strategic and computational tasks in social networks, including graph coloring (which can be viewed as a problem of social differentiation), consensus, biased voting, trading and bargaining in networks, and network formation. These experiments have yielded a wealth of findings and data on the ability of human subjects to solve challenging collective tasks from only local interactions, and have shed light on basic topics such as influence and altruism in social networks, and the relationship between network structure and collective and individual performance and behavior. The experiments also raise interesting challenges for notions of collective intelligence in humans and machines, and for the application of machine learning to the resulting data.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/kearns.jpg" alt="Michael Kearns" width="54" height="75" class="img1" /><b>Michael Kearns</b> is a professor in the Deparment of Computer and Information Science at the University of Pennsylvania, where he pursues research in machine learning, algorithmic game theory, social networks and computational finance. Prior to joining the Penn faculty he spent a decade in research at AT&amp;T/Bell Labs, where he was head of the Artificial Intelligence Research Department. Kearns received his undergraduate degree from the University of California, Berkeley in mathematics and computer science, and his doctorate from Harvard University in computer science.</p>
