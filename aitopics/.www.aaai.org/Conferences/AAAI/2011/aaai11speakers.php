<h3 class="text2"><i><a name="Ferrucci">AAAI-11 / IAAI-11 Invited Talk</a></i><br />
Building Watson: An Overview of DeepQA for the <i>Jeopardy! Challenge</i></h3>
<p><b>David Ferrucci (IBM T. J. Watson Research Center)</b></p>


<p>Computer systems that can directly and accurately answer peoples' questions over a broad domain of human knowledge have been envisioned by scientists and writers since the advent of computers themselves. Open domain question answering holds tremendous promise for facilitating informed decision making over vast volumes of natural language content. Applications in business intelligence, healthcare, customer support, enterprise knowledge management, social computing, science and government would all benefit from deep language processing. The DeepQA project is aimed at exploring how advancing and integrating natural language processing, information retrieval, machine learning, massively parallel computation, and knowledge representation and reasoning can greatly advance open-domain automatic Question Answering. An exciting proof-point in this challenge is to develop a computer system that can successfully compete against top human players at the <i>Jeopardy!</i> quiz show. Attaining champion-level performance Jeopardy! requires a computer system to rapidly and accurately answer rich open-domain questions, and to predict its own performance on any given category/question. The system must deliver high degrees of precision and confidence over a very broad range of knowledge and natural language content with a 3-second response time. To do this DeepQA evidences and evaluates many competing hypotheses. A key to success is automatically learning and combining accurate confidences across an array of complex algorithms and over different dimensions of evidence. Accurate confidences are needed to know when to "buzz in" against your competitors and how much to bet. High precision and accurate confidence computations are just as critical for providing real value in business settings where helping users focus on the right content sooner and with greater confidence can make all the difference. The need for speed and high precision demands a massively parallel computing platform capable of generating, evaluating and combing 1000s of hypotheses and their associated evidence. In this talk I will introduce the audience to the <i>Jeopardy! Challenge</i> and how we tackled it using DeepQA.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/ferrucci.jpg" alt="David Ferrucci" width="65" height="75" class="img1" /><b>David Ferrucci</b> is the lead researcher and principal investigator for the Watson/Jeopardy! project. He has been a research staff member at IBM's T. J. Watson's Research Center since 1995 where he heads up the Semantic Analysis and Integration department. Ferrucci focuses on technologies for automatically discovering valuable knowledge in natural language content and using it to enable better decision making.</p>

<p>As part of his research he led the team that developed UIMA. UIMA is a software framework and open standard widely used by industry and academia for collaboratively integrating, deploying and scaling advanced text and multimodal (speech, video) analytics. As chief software architect for UIMA, Ferrucci led its design and chaired the UIMA standards committee at OASIS. The UIMA software framework is deployed in IBM products and has been contributed to Apache open-source to facilitate broader adoption and development.
In 2007, Ferrucci took on the<i> Jeopardy! Challenge</i> &mdash; tasked to create a computer system that can rival human champions at the game of <i>Jeopardy!.</i> As the PI for the exploratory research project dubbed DeepQA, he focused on advancing automatic, open-domain question answering using massively parallel evidence based hypothesis generation and evaluation. By building on UIMA, on key university collaborations and by taking bold research, engineering and management steps, he led his team to integrate and advance many search, natural language processing, and semantic technologies to deliver results that have out-performed all expectations and have demonstrated world-class performance at a task previously thought insurmountable with the current state of the art. Watson, the computer system built by Ferrucci's team is now competing with top <i>Jeopardy!</i> champions. Under his leadership they have already begun to demonstrate how DeepQA can make dramatic advances for intelligent decision support in areas including medicine, finance, publishing, government and law.</p>

<p>Ferrucci has been the principal investigator on several government-funded research programs on automatic question answering, intelligent systems and saleable text analytics. His team at IBM consists of 28 researchers and software engineers specializing in the areas of natural language processing, software architecture, information retrieval, machine learning and knowledge representation and reasoning.</p>

<p>Ferrucci graduated from Manhattan College with a BS in biology and from Rensselaer Polytechnic Institute in 1994 with a PhD in computer science specializing in knowledge representation and reasoning. He is published in the areas of AI, knowledge representation and reasoning, natural language processing, and automatic question-answering.</p>

