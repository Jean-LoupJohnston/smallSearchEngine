<h3 class="text2"><i><a name="Thielscher">AAAI-11 Invited Talk</a></i><br />
Towards Artificial Systems: What Can We Learn From Human Perception?</h3>
<p><b>Heinrich H. Buelthoff (Max Planck Institute for Biological Cybernetics)</b></p>


<p>Recent progress in learning algorithms and sensor hardware has led to rapid advances in artificial systems. However, their performance continues to fall short of the efficiency and plasticity of human behavior. In many ways, a deeper understanding of how humans process and act upon physical sensory information can contribute to the development of better artificial systems. In this presentation, Buelthoff will highlight how the latest tools in computer vision, computer graphics, and virtual reality technology can be used to systematically understand the factors that determine how humans behave and solve tasks in realistic scenarios.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/buelthoff.jpg" alt="Heinrich Buelthoff" width="50" height="75" class="img1" /><b>Heinrich Buelthoff</b> is scientific member of the Max Planck Society and director at the Max Planck Institute for Biological Cybernetics in Tuebingen. He is head of the Department of Human  Perception, Cognition, and Action in which a group of about 70 researchers investigate psychophysical and computational aspects of higher level visual processes in object and face recognition, sensory-motor integration, spatial cognition, and perception and action in virtual environments. He holds a Ph.D. degree in the natural sciences from the Eberhard-Karls-Universitaet in Tuebingen. From 1980 to 1988 he worked as a research scientist at the Max Planck Institute for Biological Cybernetics and the Massachusetts Institute of Technology and from 1988-1993 he was an assistant, associate and full professor of cognitive science at Brown University in Providence. He is an honorary professor at the Eberhard-Karls-Universitaet (Tuebingen) and Korea University (Seoul) and editor of several
international journals.</p>

