<h3 class="text2"><i><a name="Thielscher">AAAI-11 Invited Talk</a></i><br />
Registration and Recognition for Robotics</h3>
<p><b>Kurt Konolige (Willow Garage, Inc and Stanford University)</b></p>

<p>Robotic manipulation around the home and office requires perception of the environment and objects within it. In this talk, Konolige highlights the key roles played by visual registration. The first role is in keeping track of where the robot is, and for understanding how multiple views of the environment correspond to each other. The second is in finding and manipulating objects in the world. Registration and recognition methods will be illustrated with examples from Willow Garage's PR2 robot.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/konolige.jpg" alt="Kurt Konolige" width="63" height="75" class="img1" /><b>Kurt Konolige</b> is a senior researcher at Willow Garage, a robotics startup dedicated to open-source software for personal robotics. He is also a consulting professor of computer science at Stanford University, and a Fellow of AAAI. He received his PhD in computer science from Stanford University in 1984. He has authored or coauthored over 300 scientific publications, including 3 books and best papers at the 1995 IJCAI conference and the 1998 IROS conference. He teaches a course in mobile robotics at Stanford University, and codeveloped the Pioneer mobile robots, and the low-cost laser sensor in the Neato vacuuming robot. His recent research has concentrated on realtime perception and
navigation for mobile robots.</p>

