<h2 class="backgrnd12"><span class="text1">Annual Computer Poker Competition of the Twenty-Fifth AAAI Conference on Artificial Intelligence</span></h2>
<p><i>Call for Participation</i></p>

The AAAI Annual Computer Poker Competition at AAAI-11 will be held August 9&ndash;10, 2011 in San Francisco, California USA.</p>
<p>In recent years, poker has emerged as an important, visible challenge problem for the field of AI. Just as the development of world-class chess-playing programs was considered an important milestone in the development of intelligent computing, poker is increasingly being seen in the same way.  Several important features differentiate poker from other games: the presence of imperfect information (due to hidden cards), stochastic events, and the desire to maximize utility instead of simply winning.  Hence, traditional AI game-playing techniques do not apply and novel methods are required.</p>

<p>The AAAI Annual Computer Poker Competition, now in its sixth year, showcases state-of-the-art intelligent programs for playing poker. This is the premiere venue for demonstrating poker-playing software systems, as exemplified by the previous years' competitions. The 2010 competition featured 30 poker-playing programs submitted from 11 countries.</p>

<h4 class="text2">Competition Formats</h4>

<p>The poker variants considered in the 2011 competition will be Texas Hold'em poker. The competition will build on the success of the previous years' two-player (heads-up) competitions, with both limit and no-limit betting structures, and last year's three-player limit competition. With many interesting challenges in all three categories, we expect this year's competition to continue to spur the development of new techniques.</p>

<p>If you have any questions or comments about the AAAI Computer Poker Competition, we encourage you to address them to the competition cochairs or participate in the online discussion forum.</p>

<p>David Parkes will be serving in the role of Arbiter to verify the fairness of the competition.</p>

<p>All of the software needed for the competition is open-source, and the source code, rules of the competition, and other information may be found at the competition website.</p>


