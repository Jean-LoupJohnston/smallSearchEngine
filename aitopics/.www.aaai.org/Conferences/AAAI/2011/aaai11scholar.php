			<h2 class="backgrnd12"><span class="text1">Student Scholar and Volunteer Program</span></h2>
<p>AAAI is pleased to announce the continuation of its Student Scholarship Program for 2011, which is sponsored by the AI Journal and Google. The Student Scholar Program provides partial travel support for students who are full-time undergraduate or graduate students at colleges and universities; are members of AAAI; submit papers to the conference program or letters of recommendation from their faculty advisor; and submit scholarship applications to AAAI by May 20, 2011. In addition, repeat scholarship applicants must have fulfilled the volunteer and reporting requirements for previous awards. In the event that scholarship applications exceed available funds, preference will be given to students who have an accepted technical paper, and then to students who are actively participating in the conference in some way. However, all eligible students are encouraged to apply.</p>

<ul>
	<li><a href="http://www.aaai.org/Conferences/AAAI/2011/aaai11scholar-volunteer-application.pdf">Scholarship and Volunteer Application</a></li>
</ul>


<p>After the conference, an expense report will be required to account for the funds awarded. For further information about the Scholarship Program, or to obtain an application, please contact AAAI at &#115;&#99;&#104;&#111;&#108;&#97;&#114;&#115;&#104;&#105;&#112;&#115;&#49;&#49;&#64;&#97;&#97;&#97;&#105;&#46;&#111;&#114;&#103;.</p>

<p>The Student Volunteer Program is an essential part of the conference and student participation is a valuable contribution. Volunteers will support AAAI organizers in San Francisco. In 2011, a limited number of complimentary technical program registrations will be available for students who volunteer during the conference. Preference will be given to participating students for the volunteer positions. Local students or students not requiring travel assistance can apply for the Volunteer Program if openings are available. AAAI membership is required for eligibility. For further information regarding the Student Volunteer Program, please contact AAAI at &#118;&#111;&#108;&#117;&#110;&#116;&#101;&#101;&#114;&#49;&#49;&#64;&#97;&#97;&#97;&#105;&#46;&#111;&#114;&#103;. The deadline for volunteer applications is May 20, 2011.</p> 
