<h3 class="text2">Paper Submissions</h3>
<p>We solicit short submissions of up to four pages. Each submission should focus on a major result that has already been published in one or more venues as described above. A Nectar paper needs to clarify the relationship of the paper to any other simultaneous related submission to a general AI conference or journal and cannot overlap with them substantially. The Nectar paper should cite the previous publication(s) and will typically devote no more than one or two pages to summarizing the core results. The remainder of the paper should be devoted to putting the results, as well as the problem they solve, into a context that is meaningful to a wide AI audience.</p>

<p>Nectar track papers will be presented as talks or posters at AAAI-11. The papers will also be published in the conference proceedings. Submitted papers will be reviewed according to: (1) significance of the results to the broad goals of AI, (2) potential for the results to influence research beyond their original publication venues, and (3) clarity of the presentation to AI researchers who do not have expertise in the topic described in the paper.</p>

<p>Although papers will describe previously published results, the paper itself must be original. Authors of accepted papers will be required to transfer copyright.</p>

<p>Papers must be received by March 1, 2011. Decisions on the acceptance of papers will be made by April 7, 2011.</p>

<p>Electronic abstract and paper submission through the AAAI-11 Nectar paper submission site is required on or (preferably) before the deadline dates listed above. We cannot accept submissions by e-mail or fax.</p>

<p>Papers must be in trouble-free, high resolution PDF format, formatted for US Letter (8.5" x 11") paper, using Type 1 or True-Type fonts. Papers may be no longer than 4 pages including references, and formatted in AAAI two-column, camera-ready style (see the <a href="http://www.aaai.org/Publications/Author/author.php">author instructions page</a>). Please note that these formatting instructions are for final, accepted papers; no additional pages can be purchased at the review stage. In addition, the copyright slug may be omitted in the initial submission phase. The review process for NECTAR is not double-blind. Author names and affiliations MUST be included on the top of the papers submitted for review for NECTAR.</p>

<p>Authors will receive confirmation of receipt of their abstracts or papers, including an ID number, shortly after submission. AAAI will contact authors again only if problems are encountered with papers. Inquiries regarding lost papers must be made no later than March 8, 2011.</p>
</p>

<p><i>Publication:</i> Accepted papers will be allocated four (4) pages in the conference proceedings. Up to two (2) additional pages may be used at a cost to the authors of US$ 275 per page. Final papers exceeding six 6) pages and those violating the instructions to authors will not be included in the proceedings. Authors will be required to transfer copyright of their paper to AAAI.</p>

