<h2 class="backgrnd12"><span class="text1">New Scientific and Technical Advances in Research (NECTAR)<br />A Program of the Twenty-Fifth Conference on Artificial Intelligence</span></h2>

<p><i>Call for Papers</i></p>
<ul>
	<li><i>March 1, 2011:</i> Paper Submissions Due</li>
	<li><i>April 7, 2011:</i> Author Notifications</li>
	<li><i>May 3, 2011:</i> Camera-ready copy due at AAAI office</li>
</ul>
<div class="hr1">
<hr />
</div>
<h3 class="text2"><a href="http://aaainectar.confmaster.net" rel="nofollow">Paper Submission Site</a></h3>
<p><a href="http://www.aaai.org/Publications/Author/author.php">Author Instructions Page</a></p>
<div class="hr1">
<hr />
</div>


<p>AAAI-11 will again include the Nectar track (new scientific and technical advances in research). This track aims to make the most significant AI results presented at other conferences in the last two years available to a broad AI audience. One important goal of the track is to offer researchers the opportunity to learn about areas with which they may not already be familiar. Another goal is to encourage the sort of cross-disciplinary AI work that has historically been supported by AAAI.</p>

<p>The Nectar track will consist of papers that are based on important results that have already been published in the proceedings of at least one major conference in 2009 and 2010, as either a single paper or a series of papers. Examples of such AI conferences include AAMAS, AIIDE, ALIFE, ACL, CEC, CogSci, CP, FUZZ-IEEE, GECCO, IAT, ICAPS, ICCBR, ICML, IEEE CEC, IJCNN, ISWC, IUI, KCAP, KR, NIPS, SAT, UAI and WCCI. Examples of conferences in related fields with relevance to AI include CHI, CIKM, COLT, CVPR, HRI, ICDT, KDD, PODS, RSS, SIGIR, SIGGRAPH, SIGMOD, VLDB, and WWW.</p>

<p>Papers that report on the application of AI techniques in other fields may also serve as the basis for Nectar papers. Authors of application papers, however, are advised that they may find the conference on Innovative Applications of AI (IAAI) a more appropriate venue for reaching the AI community since those papers can be longer and thus provide a clearer application setting in which to describe the work. Papers that have appeared in general AI conferences (for example, AAAI, ECAI, IJCAI) or journals (such as <i><Artificial Intelligence</i> or <i>JAIR</i>) cannot serve as the basis for Nectar papers since they have already been presented to the entire AI community.</p>

<h3 class="text2">Author Registration</h3>
<p>Authors must register at the Nectar program web-based <a href="http://aaainectar.confmaster.net" rel="nofollow">technical paper submission site.</a> The software will assign a password, which will enable the author to log on to submit an abstract and paper. In order to avoid a rush at the last minute, authors are encouraged to register as soon as possible after the submission site opens, which is expected to be January 14, 2011.</p>

