<h2 class="bg7"><span class="text1">The Twenty-Seventh AAAI Conference (AAAI-13)!</span></h2></div>
<div id="box2">
<div class="content">
<p><a href="http://videolectures.net/aaai2013_washington/" rel="nofollow">Selected lectures and programs from this conference have been posted at VideoLectures.Net.</a></p>

<p>The Twenty-Seventh AAAI Conference on Artificial Intelligence (AAAI-13) was held July 14&ndash;18, 2013 in Bellevue, Washington, USA. The purpose of this conference was to promote research in artificial intelligence (AI) and scientific exchange among AI researchers, practitioners, scientists, and engineers in affiliated disciplines. AAAI-13 will have multiple technical tracks, student abstracts, poster sessions, invited speakers, and exhibit programs, all selected according to the highest reviewing standards. AAAI-13 welcomed submissions on mainstream AI topics as well as novel crosscutting work in related areas.</p>
