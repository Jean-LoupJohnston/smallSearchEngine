<h2 class="backgrnd12"><span class="text1">The Nineteenth National Conference on Artificial Intelligence</span></h2>

<div class="content">
<p><i>Sponsored by the Association for the Advancement of Artificial Intelligence</i></p>
<hr />
<p>The AAAI-04 Conference was held in San Jose, California, at the San Jose Convention Center, from 25-29 July, 2004. There were 453 papers submitted for review to the National Conference on Artificial Intelligence. Another 63 were submitted to the Innovative Applications of Artificial Intelligence Conference.</p>

<p>This year&rsquo;s conference provides a comprehensive survey of the
best research in the various areas within the field of artificial intel -
ligence. Our goal is to provide you with five days of immersion in
the wonderful variety of problems that the best researchers in the
field are tackling. We hope you will find something to interest you
throughout the entire conference, and that you will leave San Jose
with your intellectual batteries recharged and ready for another
year.
</p><p>The conference&rsquo;s highlight is the technical program. The papers
in the technical program were selected by a highly competitive
peer review process. Each paper was reviewed by three members
of the program committee, supervised by a member of the senior
program committee. The reviewing period was followed by discussion
between reviewers, leading to an accept or reject recommendation.
These recommendations were reviewed and approved by
the senior program committee and program chairs in a face-to-face
meeting. In all, out of 453 submitted papers, 120 were accepted
and published. We hope you will agree with us that these papers
represent significant contributions across a wide range of subject
areas.
</p><p>In addition, three papers were selected for special mention. The
AAAI-04 Outstanding Paper award goes to Lin Liao, Dieter Fox,
and Henry Kautz for their paper "Learning and Inferring Transportation
Routines." Honorable mention goes to Lee Joohyung and
Lin Fangzhen for their paper "Loop Formulas for Circumscription,"
and to Trausti Kristjansson, Aron Culotta, Paul Viola, and
Andrew McCallum for their paper "Interactive Information Extraction
with Constrained Conditional Random Fields."
</p><p>The technical program is the heart of the conference, and we
would like to gratefully acknowledge the very substantial contributions
of the program committee members, and particularly the
members of the senior program committee who pulled everything
together.
</p><p>In addition to the technical program, AAAI-04 includes tutorials
(organized by Mathew Stone), workshops (organized by Milos
Hauskrecht and Dieter Fox), the student abstract program (organized
by Avi Pfeffer and Mark Craven), the AAAI/SIGART Doctoral
Consortium (organized by Robert St. Amant), intelligent systems
demonstrations (organized by Chris Welty), and the robot
competition and exhibition (organized by Bill Smart and Sheila Tejada).
We would like to thank the organizers of these programs for
their efforts, and we hope you will enjoy participating in them. We
would also like to thank our invited speakers for sharing their ideas
with us, and we hope you will enjoy their talks.
</p><p>
Finally, we would like to remind the AI community that this
conference would not be the institution that it is without the neverending
work of the AAAI staff, particularly Carol McKenna Hamilton,
Keri Harvey, and Rick Skalsky. It has been a pleasure to work with them and serve the AI
community. We hope you enjoy the show.</p><p class="right">
George Ferguson, <i>University of Rochester</i> and
Deborah McGuinness, <i>Stanford University</i></p>
