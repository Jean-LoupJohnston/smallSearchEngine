<h2 class="bg5"><span class="text1">Activities</span></h2>

<div class="content">
<h3 class="text2">The AI &amp; the Web Track</h3>
<p class="bg6">Learn about the use and extension of AI concepts and techniques for the world wide web. <a href="http://www.aaai.org/Conferences/AAAI/2007/aaai07aiweb.php">More&#8230;</a></p>

<h3 class="text2">The Doctoral Consortium</h3>
<p class="bg6">Ph.D. students explore interests and career objectives. <a href="http://www.aaai.org/Conferences/AAAI/2007/aaai07dc.php">More&#8230;</a></p>

<h3 class="text2">The Exhibition</h3>
<p class="bg6">Showcasing products and services, and tapping an incredible source of talent. <a href="http://www.aaai.org/Conferences/AAAI/2007/aaai07exhibits.php">More&#8230;</a></p>

<h3 class="text2">The Integrated Intelligence Papers Track</h3>
<p class="bg6">Learn about approaches and techniques that combine abilities from areas of AI to achieve intelligent behavior. <a href="http://www.aaai.org/Conferences/AAAI/2007/aaai07iic.php">More&#8230;</a></p>

<h3 class="text2">Innovative AI Applications</h3>
<p class="bg6">Explore issues, methods, and lessons learned in the development and deployment of real-world AI applications. <a href="../IAAI/iaai07.php">More&#8230;</a></p>

<h3 class="text2">The Nectar Papers Track</h3>
<p class="bg6">Learn about significant AI results presented elsewhere during the last two years. <a href="http://www.aaai.org/Conferences/AAAI/2007/aaai07nectar.php">More&#8230;</a></p>

<h3 class="text2">Man Versus Machine</h3>
<p class="bg6">Watch the first scientific man versus machine challenge in poker. <a href="http://www.aaai.org/Conferences/AAAI/2007/aaai07mvmpoker.php">More&#8230;</a></p>

<h3 class="text2">The Poker Competition</h3>
<p class="bg6">Teams develop computer programs for playing heads-up limit
and no-limit Texas Hold&rsquo;em. <a href="http://www.aaai.org/Conferences/AAAI/2007/aaai07poker.php">More&#8230;</a></p>

<h3 class="text2">Senior Member Papers</h3>
<p class="bg6">Seasoned experts give thoughtful critiques on trends in the field. <a href="http://www.aaai.org/Conferences/AAAI/2007/aaai07senior.php">More&#8230;</a></p>

<h3 class="text2">Student Programs</h3>
<p class="bg6">Discuss work with your peers and members of the field. <a href="http://www.aaai.org/Conferences/AAAI/2007/aaai07student.php">More&#8230;</a></p>

<h3 class="text2">The Technical Program</h3>
<p class="bg6">Featuring cutting-edge papers on mainstream and novel cross-cutting work. Includes information on the four special tracks. <a href="http://www.aaai.org/Conferences/AAAI/2007/aaai07technical.php">More&#8230;</a></p>

<h3 class="text2">The Tutorial Forum</h3>
<p class="bg6">Spend two days freely exploring exciting advances areas outside your regular focus. <a href="http://www.aaai.org/Conferences/AAAI/2007/aaai07tutorials.php">More&#8230;</a></p>

<h3 class="text2">The Video Competition</h3>
<p class="bg6">Tell the world how much fun AI can be! <a href="http://www.aaai.org/Conferences/AAAI/2007/aaai07videos.php">More&#8230;</a></p>

<h3 class="text2">Workshops</h3>
<p class="bg6">Discuss issues on a particular topic in an informal setting. <a href="http://www.aaai.org/Conferences/AAAI/2007/aaai07workshops.php">More&#8230;</a></p>

<h3 class="text2">Intelligent Systems Demonstrations</h3>
<p class="bg6">Showcasing AI implementations and research in action. <a href="http://www.aaai.org/Conferences/AAAI/2007/aaai07isdemos.php">More&#8230;</a></p>

<h3 class="text2">Mobile Robot Competition and Exhibition</h3>
<p class="bg6">Teams from universities and labs compete and demonstrate progress in robotics and AI. <a href="http://www.aaai.org/Conferences/AAAI/2007/aaai07robot.php">More&#8230;</a></p>


