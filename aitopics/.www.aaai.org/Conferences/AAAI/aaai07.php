<h3 class="text2">Alan Mackworth to Deliver Presidential Address</h3>
<p><p>AAAI President Alan Mackworth will deliver his keynote presidential address at AAAI-07. A professor of computer science and Canada Research Chair in artificial intelligence at the University of British Columbia, Mackworth works on constraint-based computational intelligence with applications in vision, robotics, and situated agents, and is a pioneer in the areas of constraint satisfaction, robot soccer, and constraint-based agents. <a href="http://www.aaai.org/Conferences/AAAI/2007/aaai07speakers.php">Details ....</a></p>

