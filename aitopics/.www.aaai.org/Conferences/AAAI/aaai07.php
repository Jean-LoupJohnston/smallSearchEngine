<h2 class="bg7"><span class="text1">Please Join Us for AAAI-07!</span></h2>

<div class="content">
<p>AAAI is pleased to announce that the Twenty-Second Conference on Artificial Intelligence (AAAI-07) and the collocated Nineteenth Conference on Innovative Applications of Artificial Intelligence (IAAI-07) will be held in Vancouver, British Columbia at the Hyatt Regency Vancouver, from July 22&ndash;26. The AAAI-07 Program Cochairs are Adele Howe, Colorado State University, and Robert C. Holte at the University of Alberta.</p>
<p>The purpose of the AAAI-07 conference is to promote research in AI and scientific exchange among AI researchers, practitioners, scientists, and engineers in related disciplines. AAAI-07 will have multiple technical tracks, <a href="http://www.aaai.org/Conferences/AAAI/2007/aaai07student.php">student abstracts,</a> poster sessions, invited speakers, and <a href="http://www.aaai.org/Conferences/AAAI/2007/aaai07exhibit-info.pdf">exhibit</a> programs, all selected according to the highest reviewing standards. On this page, you will find links to the wide variety of programs being held at AAAI-07, such as the <a href="http://www.aaai.org/Conferences/AAAI/2007/aaai07technical.php">technical</a> program, the <a href="http://www.aaai.org/Conferences/AAAI/2007/aaai07tutorials.php">Tutorial Forum</a>, the <a href="../../Workshops/ws07.php">Workshop Program,</a> the <a href="http://www.aaai.org/Conferences/AAAI/2007/aaai07student.php">Student Abstract and Poster Program</a>, the <a href="http://www.aaai.org/Conferences/AAAI/2007/aaai07dc.php">Doctoral Consortium</a>, the <a href="http://www.aaai.org/Conferences/AAAI/2007/aaai07isdemos.php">Intelligent Systems Demonstrations</a>, the <a href="http://www.aaai.org/Conferences/AAAI/2007/aaai07robot.php">Mobile Robot Competition and Exhibition</a>, the <a href="../IAAI/iaai07.php">Nineteenth Innovative Applications of Artificial Intelligence Conference</a>, the "<a href="http://www.aaai.org/Conferences/AAAI/2007/aaai07videos.php">AI Video</a> Competition Awards, and the <a href="http://www.aaai.org/Conferences/AAAI/2007/aaai07nectar.php">Nectar</a> program, all of which will take place in Vancouver, British Columbia from 22&mdash;26 July. </p>

