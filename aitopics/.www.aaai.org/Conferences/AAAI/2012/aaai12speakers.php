<h3 class="text2"><i><a name="King">AAAI-12 Invited Talk</a></i><br />
Automating Biology Using Robot Scientists</h3>
<p><b>Ross D. King (University of Manchester, UK)</b>
<br />
Wednesday, July 25<br />
1:25&ndash;2:25 PM</p>


<p>A robot scientist is a physically implemented robotic system that applies techniques from artificial intelligence to execute cycles of automated scientific experimentation. A Robot Scientist can automatically execute cycles of: hypothesis formation, selection of efficient experiments to discriminate between hypotheses, execution of experiments using laboratory automation equipment, and analysis of results. We developed the robot scientist <i>Adam</i> to investigate yeast functional genomics. Adam is the first time a machine has discovered novel scientific knowledge. This knowledge is described in a formal argument involving over 10,000 different research units that relates Adam's 6.6 million observations to its conclusions. Our new robot scientist <i>Eve</i> applies the same approach to drug design. Eve has efficiently found "lead compounds" for malaria and other neglected tropical diseases.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2012/Photos/king.jpg" alt="Ross King" width="75" height="75" class="img1" /><b>Ross King's</b> first degree was in microbiology, and then he switched to computer science, and has been interested in the intersection of these disciplines ever since. His Ph.D. was done on machine learning for bioinformatics in the late 1980s at the Turing Institute in Scotland. This was led by the AI pioneer Donald Michie, and was the best place he has ever worked. In his career he has worked in a statistics department, a startup-company, a structural biology lab, and computer science departments. Unusually, he ran both a "wet" biology laboratory (with his robots), and a computer science lab. In his view science is an excellent application domain for AI research, and AI can have a real impact on making the practice of science more efficient. The physics Nobel Laureate Frank Wilczek is on record as stating that the best physicist in 100 years time may be a machine; King is working towards this vision.</p>
