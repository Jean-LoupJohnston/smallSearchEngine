<h3 class="text2"><i><a name="Tenenbaum">AAAI-12 Invited Talk</a></i><br />
How to Grow a Mind: Statistics, Structure and Abstraction</h3>
<p><b>Joshua B. Tenenbaum (Massachusetts Institute of Technology)</b>
<br />
Thursday, July 26<br />
1:25&ndash;2:25 PM</p>

<p>The fields of cognitive science and artificial intelligence grew up together, with the twin goals of understanding human minds and making machines smarter in more humanlike ways. Yet since the 1980s they have mostly grown apart, as cognitive scientists came to see AI as too focused on applications and technical engineering issues rather than big questions of intelligence, while AI researchers came to see cognitive science as too informal and concerned with peculiarities of human minds and brains rather than general principles. Just in the last few years, however, these fields appear poised to reconverge in exciting and deep ways. Cognitive scientists have begun to adopt the toolkit of modern probabilistic AI as a unifying framework for modeling natural intelligence, while many AI researchers are looking beyond immediate applications to some of the big picture questions that originally motivated the field, and both communities are increasingly aware of and even informed by the other's moves in these directions.</p>

<p>This talk will describe recent work at the center of the convergence: computational accounts of human intelligence that both draw on and advance state-of-the-art AI. I will focus on capacities for which even young children still far surpass machines: learning from very few examples, and common sense reasoning about the physical and social world. These abilities can be explained as approximate forms of probabilistic (Bayesian) inference over richly structured models &mdash; probabilistic models built on top of knowledge representations familiar from earlier, classic AI days, such as graphs, grammars, schemas, predicate logic, and functional programs. In many cases, sampling-based approximate inference with these models can be surprisingly tractable and can predict human judgments with high quantitative accuracy. Extended in a hierarchical nonparametric Bayesian framework, these models can explain how children learn to learn, bootstrapping adult-like intelligence from more primitive foundations. Using probabilistic programming languages, these models can be integrated into a unified cognitive architecture. Throughout the talk I will present concrete examples, along with a few more speculative predictions, of how these cognitive modeling efforts can inform the development of more intelligent machine systems.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2012/Photos/tenenbaum.jpg" alt=" Josh Tenenbaum" width="60" height="75" class="img1" /><b> Josh Tenenbaum</b> studies learning, reasoning and perception in humans and machines, with the twin goals of understanding human intelligence in computational terms and bringing computers closer to human capacities. His current work focuses on building probabilistic models to explain how people come to be able to learn new concepts from very sparse data, how we learn to learn, and the nature and origins of people's intuitive theories about the physical and social worlds. He is Professor of Computational Cognitive Science in the Department of Brain and Cognitive Sciences at MIT, and is a member of the Computer Science and Artificial Intelligence Laboratory (CSAIL). He received his Ph.D. from MIT in 1999, and was a member of the Stanford University faculty in Psychology and (by courtesy) Computer Science from 1999 to 2002. His papers have received awards at the IEEE Computer Vision and Pattern Recognition (CVPR), NIPS, IJCAI and Cognitive Science Society conferences. He is the recipient of early career awards from the Society for Mathematical Psychology (2005), the Society of Experimental Psychologists, and the American Psychological Association (2008), and the Troland Research Award from the National Academy of Sciences (2011).


<h3 class="text2"><i><a name="vonAhn">AAAI-12 Invited Talk</a></i><br />
Duolingo: Translating the Web with Millions of People</h3>
<p><b>Luis von Ahn (Carnegie Mellon University)</b>
<br />
Tuesday, July 24<br />
1:25&ndash;2:25 PM</p>


<p>I want to translate the web into every major language: every web page, every video, and, yes, even Justin Bieber's tweets. With its content split up into hundreds of languages  &mdash;  and with over 50 percent of it in English  &mdash;  most of the web is inaccessible to most people in the world. This problem is pressing, now more than ever, with millions of people from China, Russia, Latin America and other quickly developing regions entering the web. In this talk, I introduce my new project, called <i>Duolingo,</i> which aims at breaking the language barrier, and thus making the web truly world wide.</p>

<p>We have all seen how systems such as Google Translate are improving every day at translating the gist of things written in other languages. Unfortunately, they are not yet accurate enough for my purpose: Even when what they spit out is intelligible, it's so badly written that I can't read more than a few lines before getting a headache. This is why you don't see machine-translated books. With Duolingo, our goal is to encourage people, like you and me, to translate the web into their native languages.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2012/Photos/ahn.jpg" alt="Luis von Ahn" width="113" height="75" class="img1" /><b>Luis von Ahn</b> is the A. Nico Habermann Associate Professor of Computer Science at Carnegie Mellon University. He is working to develop a new area of computer science that he calls Human Computation. He builds systems that combine the intelligence of humans and computers to solve large-scale problems that neither can solve alone. An example of his work is reCAPTCHA, in which over 900 million people  &mdash;  more than 10 percent of humanity  &mdash;  have helped digitize books and newspapers. Among his many honors are a MacArthur Fellowship, a Packard Fellowship, a Sloan Research Fellowship, a Microsoft New Faculty Fellowship, and CMU's Herbert A. Simon Award for Teaching Excellence and Alan J. Perlis Teaching Award.</p>
