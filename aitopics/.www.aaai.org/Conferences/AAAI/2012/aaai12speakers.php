<h3 class="text2"><i><a name="Pearl">ACM A. M. Turing Award Lecture</a></i><br />
The Mechanization of Causal Inference: A "Mini Turing Test" and Beyond</h3>
<p><b>Judea Pearl (University of California, Los Angeles)</b>
<br />
<i>(Open to all registrants and ACM members)</i><br />
Tuesday, July 24<br />
10:20 &ndash; 11:20 AM</p>

<p>Judea Pearl, professor of computer science at the University of 
California, Los Angeles, was recently named the recipient of the 2011 
ACM A. M. Turing Award for fundamental contributions to artificial 
intelligence through the development of a calculus for probabilistic 
and causal reasoning. Pearl has chosen AAAI-12 as the venue 
to deliver his Turing Award Lecture. This lecture is open to all 
conference participants and ACM members.</p>

<p>Judea Pearl will review concepts, principles, and mathematical 
tools that were found useful in applications involving causal and 
counterfactual reasoning. This semantical framework gives rise to a 
coherent and friendly calculus that unifies several approaches to 
causation and resolves long-standing problems in the empirical 
sciences. The mechanization of counterfactual reasoning amounts to 
passing a mini "Turing test" in causal conversations. Its application 
in the empirical sciences unveils several opportunities and 
limitations of the "big-data" enterprise.</p>
</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2012/Photos/pearl.jpg" alt="Judea Pearl" width="79" height="75" class="img1" /><b>Judea Pearl</b> is a professor of computer science and statistics at the 
University of California, Los Angeles. He is a graduate of the 
Technion, Israel, and joined the faculty of UCLA in 1970, where he 
currently directs the Cognitive Systems Laboratory and conducts 
research in artificial intelligence, causal inference and philosophy 
of science. Pearl has authored three books: <i>Heuristics</i> (1984), 
<i>Probabilistic Reasoning</i> (1988), and <i>Causality</i> (2000, 2009), and is 
member of the National Academy of Engineering, and the American 
Academy of Arts and Science. He is the recipient of the 2008 Benjamin 
Franklin Medal for Computer and Cognitive Science and the 2011 David 
Rumelhart Prize from the Cognitive Science Society. In 2012 he 
received the Technion's Harvey Prize and the ACM A. M. Turing Award.
</p>
