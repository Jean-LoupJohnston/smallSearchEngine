<p>This talk provides an update on self-driving car technology developed at Google. Google is on its way to develop technology that can safely control cars that drive in traffic without human attention. In doing so, the team is heavily leveraging AI technology in areas of perception, planning, control, and machine learning. The speaker will discuss these advances, and also reflect on potential applications of this technology.</p>

