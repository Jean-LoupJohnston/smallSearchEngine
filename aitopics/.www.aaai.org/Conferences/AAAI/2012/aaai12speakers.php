<h3 class="text2"><i><a name="Kautz">AAAI Presidential Address</a></i><br />
Revisiting the Dream</h3>
<p><b>Henry Kautz (University of Rochester)</b><br />

Tuesday, July 24<br />
9:00 &ndash; 10:00 AM</p>

<p>For much of its history, the field of AI has been in retreat from the most ambitious goals of its founders. Rather than attempting to understand and replicate general intelligence, research concentrated on smaller, better-defined perceptual and reasoning tasks over limited domains. We are now at a point, however, where the subfields are coming together again, and the idea of achieving the old dream of AI is no longer fanciful. We are entering an age of computerized personal servants, self-driving vehicles, the universal natural language translator, and the solution to the mysteries of the brain. The merging of human and machine intelligence will drive progress on problems across science, industry, and education. I will recap some of the transformative events we have witnessed over the past few years, and describe a vision of the near future that is both realistic and more wildly optimistic than most serious scientists would have dared imagine a decade ago.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2012/Photos/kautz.jpg" alt="Henry Kautz" width="113" height="75" class="img1" /><b>Henry Kautz</b> is chair of the Department of Computer Science at the University of Rochester. He performs research in knowledge representation, satisfiability testing, pervasive computing, and assistive technology. His academic degrees include an A.B. in mathematics from Cornell University, an M.A. in Creative Writing from the Johns Hopkins University, an M.Sc. in Computer Science from the University of Toronto, and a Ph.D. in computer science from the University of Rochester. He was a researcher and department head at Bell Labs and AT&amp;T Laboratories until becoming a professor in the Department of Computer Science and Engineering of the University of Washington in 2000. He left Seattle in 2006. He is president (2010-2012) of the Association for the Advancement of Artificial Intelligence, a Fellow of the Association for the Advancement of Artificial Intelligence, a Fellow of the American Association for the Advancement of Science, and a recipient of the IJCAI Computers and Thought Award.
</p>

