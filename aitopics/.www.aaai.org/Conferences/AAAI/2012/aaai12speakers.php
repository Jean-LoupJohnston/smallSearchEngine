<h3 class="text2"><i><a name="Minton">Robert S. Engelmore Award Lecture</a></i><br />
Building AI: Our Shared Enterpise</h3>
<p><b>Steven Minton (President, InferLink Corporation)</b>
<br />
Wednesday, July 25<br />
10:20&ndash;11:20 AM</p>

<p>The past few decades have seen great progress in AI. Most of this progress has resulted from contributions made by teams of scientists and engineers building on the earlier contributions of other teams, rather than from individual "breakthroughs". Ultimately, this is made possible because we share our methods and results. There are many mechanisms that help us build upon previous work, from traditional scholarly journals and conferences, to open source software, to data repositories. In fact, the development of the internet has facilitated new and exciting ways to build communities and to jointly contribute to the development of AI. This talk will discuss new and evolving models for scientific collaboration, approaches for funding nonprofit enterprises, and innovative ways that we can all contribute to the development of AI.

<p><img src="http://www.aaai.org/Conferences/AAAI/2012/Photos/minton.jpg" alt="Steven Minton" width="76" height="75" class="img1" /><b>Steven Minton</b>  is the president and founder of InferLink Corporation, which is developing technology for integrating massive amounts of entity-oriented data. Previously, he was chairman and cofounder of Fetch Technologies, a project leader and research associate professor at USC's Information Sciences Institute, and a principal investigator at NASA's Ames Research Center. He received his Ph.D from Carnegie Mellon University in 1988. He is a fellow of the AAAI, and a previous recipient of AAAI's Classic Paper and Best Paper awards. He founded AI Access Foundation in 1993, and served as <i>JAIR's</i> first executive editor and managing editor.</p>
