<h3 class="text2"><i><a name="Barzilay">AAAI-12 Invited Talk</a></i><br />
Learning to Behave by Reading</h3>
<p><b>Regina Barzilay (Massachusetts Institute of Technology)</b>
<br />
Thursday, July 26<br />
9:00&ndash;10:00 AM</p>

<p>In this talk, I will address the problem of grounding linguistic 
analysis in control applications, such as game playing and robot 
navigation. We assume access to natural language documents that 
describe the desired behavior of a control algorithm (such as game 
strategy guides). Our goal is to demonstrate that knowledge 
automatically extracted from such documents can dramatically improve 
performance of the target application. First, I will present a 
reinforcement learning algorithm for learning to map natural language 
instructions to executable actions. This technique has enabled 
automation of tasks that until now have required human participation 
&mdash; for example, automatically configuring software by consulting 
how-to guides. Next, I will present a Monte-Carlo search algorithm 
for game playing that incorporates information from game strategy 
guides. In this framework, the task of text interpretation is 
formulated as a probabilistic model that is trained based on feedback 
from Monte-Carlo search. When applied to the Civilization strategy 
game, a language-empowered player outperforms its traditional 
counterpart by a significant margin.</p>
<p><img src="http://www.aaai.org/Conferences/AAAI/2012/Photos/barzilay.jpg" alt="Regina Barzilay" width="77" height="75" class="img1" /><b>Regina Barzilay</b> is an associate professor in the Department of Electrical Engineering and Computer Science and a member of the Computer Science and Artificial Intelligence Laboratory at the Massachusetts Institute of Technolog. Her research interests are in natural language processing. She is a recipient of various awards including of the NSF Career Award, the MIT Technology Review TR-35 Award, Microsoft Faculty Fellowship and several Best Paper Awards in top NLP conferences. She received her Ph.D. in Computer Science from Columbia University in 2003 and spent a year as a postdoc at Cornell University.</p>

