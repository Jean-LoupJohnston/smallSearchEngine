<h3 class="text2"><i><a name="Ng">EAAI-12 Invited Talk</a></i><br />
ml-class.org: Teaching Machine Learning to 100,000 Students</h3>
<p><b>Andrew Ng (Stanford University and Coursera)</b>
<br />
Monday, July 23<br />
9:30&ndash;10:30 AM</p

<p>Last year, Stanford University offered three online courses, which anyone in the world could enroll in and take for free. Students were expected to submit homeworks, meet deadlines, and were awarded a "Statement of Accomplishment" only if they met our high grading bar. Offered this way, my machine learning class had over 100,000 enrolled students. To put this number in context, in order to reach an audience of this size, I would have had to teach my normal Stanford class (enrollment of ~400) for 250 years.</p>

<p>In this talk, I'll report on the outcome of this bold experiment in distributed education. I'll also describe my experience teaching one of these classes, and leading the development of the platform used to teach two of the classes. I'll describe the key technology and pedagogy ideas used to offer these courses, ranging from easy-to-create video chunks, to a scalable online question and answer forum where students can get their questions answered quickly, to sophisticated autograded homeworks. Importantly, using a "flipped classroom" model, we also used these resources to improve the education of the enrolled, on-campus, Stanford students as well.</p>

<p>Whereas technology and automation have made almost all segments of our economy &mdash; such agriculture, energy, manufacturing, transportation &mdash; vastly more efficient, education today isn't much different than it was 300 years ago. Given also the rising costs of higher education, the hyper-competitive nature of college admissions, and the lack of access to a high quality education, I think there is a huge opportunity to use modern internet and AI technology to inexpensively offer a high quality education online. Through such technology, we envision millions of people gaining access to the world-leading education that has so far been available only to a tiny few, and using this education to improve their lives, the lives of their families, and the communities they live in. Following the success of the first set of courses, there are now 14 planned courses for Winter quarter (offered by instructors from the University of Michigan, the University of California, Berkeley, and Stanford), and we hope to grow this effort further over time.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2012/Photos/ng.jpg" alt="Andrew Ng" width="72" height="75" class="img1" /><b>Andrew Ng</b> received his PhD from the University of California, Berkeley, and is now an Associate professor of computer science at Stanford University, where he works on machine learning and AI. He is also director of the Stanford AI Lab, which is home to about 12 professors and 150 PhD students and post docs. His previous work includes autonomous helicopters, the Stanford AI Robot (STAIR) project, and ROS (probably the most widely used open-source robotics software platform today). He current work focuses on neuroscience-informed deep learning and unsupervised feature learning algorithms. His group has won best paper or best student paper awards at ICML, ACL, CEAS, 3DRR. He is a recipient of the Alfred P. Sloan Fellowship, and the 2009 IJCAI Computers and Thought award. He also works on free online education, and recently taught a machine learning class (ml-class.org) to over 100,000 students. He is also a cofounder of Coursera.</p>


