<h2 class="backgrnd12"><span class="text1">AAAI-12 Student Abstract and Poster Program</span></h2>

<p><i>Call for Participation</i></p>
<h3 class="text2"><a href="http://aaai-studentabstracts.confmaster.net" rel="nofollow">Student Abstract and Poster Submission Site</a></h3>
<div class="hr1">
