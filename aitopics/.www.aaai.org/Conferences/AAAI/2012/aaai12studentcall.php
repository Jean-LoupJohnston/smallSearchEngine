<h3 class="text2">Timetable for Proposals</h3>

<ul>
	<li>February 2, 2012 (11:59 PM PST): Application Package Submission Deadline</li>
	<li>March 19, 2012: Acceptance Notification</li>
	<li>April 10, 2012 (11:59 PM PDT): Camera-Ready Copy Due at AAAI Office</li>
</ul>
<p>AAAI-12 invites submissions to the student abstract and poster program. The goal of this program is to provide a forum in which students can present and discuss their work during its early stages, meet some of their peers who have related interests, and introduce themselves to more senior members of the field. The program is open to all pre-Ph.D students. Nonstudent advisors or collaborators should be acknowledged appropriately, as coauthors or otherwise. However, students are requested to honor the spirit of the program by submitting only work for which they are primary investigators.</p>

<h3 class="text2">Submissions and Dates</h3>

<p>Electronic submission in PDF format is required. Students should submit an abstract describing their research no later than February 2, 2012. Abstracts must be no longer than 2 pages including references, and formatted in AAAI two-column, camera-ready style. Instructions about how to submit abstracts will be available at the AAAI conference web site after December 15, 2011. Papers exceeding the specified length and formatting requirements are subject to rejection without review.</p>

<p>The abstract must include the following: title; the primary author's full name, affiliation, postal address, phone number, URL (if available), and e-mail address; all coauthors' full names and affiliations; text; and any figures, tables, or diagrams. The abstract should also contain a URL of a location where reviewers can access complementary material about the student's research. The URL is critical to reviewers because of the brevity of the submissions.</p>

<p>Notification of acceptance or rejection of submitted abstracts will be mailed to the author by March 19, 2012.</p>

<p>Camera-ready copy of accepted abstracts will be due by April 10, 2012.</p>

<h3 class="text2">Submissions to AAAI-12 or Other Conferences</h3>

<p>Students are free to submit abstracts for work reported in a regular paper submitted to the AAAI-12 or another conference, but not for work that has already been published. Abstracts will be accepted or rejected for the student session regardless of the outcomes of related paper submissions.</p>

<h3 class="text2">Publication</h3>

<p>Accepted abstracts will be allocated two (2) pages in the conference proceedings. Papers must be formatted in AAAI two-column, camera-ready style (see the author instructions page). Students will be required to transfer copyright of the abstract to AAAI. </p>

<h3 class="text2">Poster Session</h3>

<p>Accepted abstracts will be allocated presentation time and space in the student poster display area at the conference. Student authors of accepted abstracts must agree to prepare a poster representing the work described in their abstracts and to be available to discuss their work with visitors during their allocated time in the student poster display area.</p>

