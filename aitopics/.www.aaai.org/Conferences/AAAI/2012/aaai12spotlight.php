<h2 class="backgrnd12"><span class="text1">AAAI-12 Subarea Spotlights Track</span></h2>


<p>
AAAI-12 will include a new Subarea Spotlights Track, featuring three talk categories:
</p>

<ol>
	<li>"What's hot in ..." talks, summarizing the state of the art in a particular area. Talks will explain the main research trends of the last 5-10 years.</li>
	<li>Best-paper talks from a broad cross-section of area conferences, highlighting recent strong research results in each area.</li>
	<li>"Challenges in ..." talks, summarizing important challenges in a particular area. Talks will outline a vision on where the area should evolve.</li>
</ol>

<p>
AAAI is pleased to announce the line-up of speakers for the Subarea Spotlight track, representing each of the following areas of research: Constraint programming, games, human-computer interaction, knowledge representation, multiagent systems, planning, robotics, SAT, search, the semantic web, and vision.
</p>

<h4>Constraint Programming</h4>
<p><i>What's Hot:</i><br />
Symmetry Breaking Constraints: Recent Results<br />
Toby Walsh</p>

<p><i>Best Paper:</i><br />
Systematically Identifying and Exploiting Dominance Relations (Best Paper CP-12)<br />
Geoffrey Chu and Peter Stuckey</p>


<p><i>Challenges:</i><br />
Opportunities and Challenges for Constraint Programming<br />
Barry O'Sullivan</p>


<h4>Games</h4>
<p><i>What's Hot:</i><br />
What's Hot in Games from the AIIDE Perspective<br />
Mark Riedl and Vadim Bulitko</p>

<p><i>Best Paper:</i><br />
Goal Recognition with Markov Logic Networks for Player-Adaptive Games (Best Paper AIIDE-11)<br />
Eun Ha, Jonathan Rowe, Bradford Mott, James Lester</p>

<h4>Human-Computer Interaction</h4>


<p><i>What's Hot:</i><br />
Progress and Hot Trends in Human-Computer Interaction Research<br />
Joseph Konstan</p>


<p><i>Best Paper:</i><br />
Communitysourcing: Engaging Local Crowds to Perform Expert Work Via Physical Kiosks (Awarded Paper at CHI-12)<br />
Kurtis Heimerl, Brian Gawalt, Tapan Parikh, Bjoern Hartmann</p>

<p><i>Challenges:</i><br />
Challenges in HCI: The Next 30 Years<br />
Joseph Konstan</p>


<h4>Knowledge Representation</h4>

<p><i>What's Hot:</i><br />
What's Hot in  Knowledge Representation and Reasoning<br />
Sheila McIlraith</p>

<p><i>Best Paper:</i><br />
Optimal Value of Information in Graphical Models (JAIR Best Paper Prize runner-up 2012)<br />
Carlos Guestrin and Andreas Krause</p>

<p><i>Best Paper:</i><br />
Ambiguous Language and Differences in Beliefs (KR-12 Ray Reiter Best Paper Prize)<br />
Joseph Halpern and Willemien Kets</p>

<h4>Machine Learning</h4>

<p><i>What's Hot:</i><br />
Recent Advances in Polyphonic Music Generation and Transcription<br />
Nicolas Boulanger-Lewandowski</p>

<p><i>Best Paper:</i><br />
On the Partition Function and Random Maximum A-Posteriori Perturbations (Best Paper Shortlist ICML-12)<br />
Tamir Hazan and Tommi Jaakkola</p>

<p><i>Best Paper:</i><br />
Bayesian Posterior Sampling via Stochastic Gradient Fisher Scoring (Best Paper Shortlist ICML-12)<br />
Sungjin Ahn, Anoop Korattikara, Max Welling</p>

<p><i>Best Paper:</i><br />
A Production Rule-based Framework for Causal and Epistemic Reasoning (Best Paper RuleML-12)<br />
Theodore Patkos, Abdelghani Chibani, Dimitris Plexousakis, Yacine Amirat</p>

<p><i>Challenges:</i><br />
Challenges for Machine Learning Impact on the Real World<br />
Kiri Wagstaff</p>

<h4>Multiagent Systems</h4>

<p><i>What's Hot:</i><br />
Computing Game-Theoretic Solutions and Applications to Security<br />
Vincent Conitzer</p>

<p><i>Best Paper:</i><br />
PROTECT: An Application of Computational Game Theory for the Security of the Ports of the United States (Best Paper Shortlist AAMAS-12)<br />
Eric Shieh, Bo An, Rong Yang, Milind Tambe, Craig Baldwin, Joseph DiRenzo, Ben Maule, Garrett Meyer</p>

<p><i>Best Paper:</i><br />
Optimal Manipulation of Voting Rules (Best Paper Shortlist AAMAS-12)<br />
Svetlana Obraztsova and Edith Elkind</p>

<p><i>Best Paper:</i><br />
Predicting Your Own Effort (Best Paper Shortlist AAMAS-12)<br />
David F. Bacon, Yiling Chen, Ian Kash, David C. Parkes, Malvika Rao, Manu Sridharan</p>


<p><i>Challenges:</i><br />
Challenges in Delivering the Smart Grid, and How Autonomous Agents and Multi-Agent Systems Can Help<br />
Alex Rogers</p>

<h4>Planning</h4>

<p><i>Best Paper:</i><br />
Semi-Relaxed Plan Heuristics (Best Paper ICAPS-12)<br />
Emil Keyder, J&ouml;rg Hoffmann, Patrik Haslum</p>

<p><i>What's Hot:</i><br />
Standing on the Shoulders of Classical Planners<br />
Ronen Brafman</p>

<p><i>Best Paper:</i><br />
Compiling Uncertainty Away in Conformant Planning Problems with Bounded Width (JAIR Best Paper Prize 2012)<br />
Hector Palacios and Hector Geffner</p>

<p><i>Challenges:</i><br />
Planning as an Iterative Process<br />
David Smith</p>

<h4>Robotics</h4>
<p><i>What's Hot:</i><br />
Advances at the Intersection of Machine Learning and Robotics<br />
Drew Bagnell</p>

<p><i>Best Paper:</i><br />
Identification and Representation of Homotopy Classes of Trajectories for Search-Based Path Planning in 3D  (Best Paper RSS-11)<br />
Subhrajit Bhattacharya, Maxim Likhachev, Vijay Kumar</p>

<p><i>Challenges:</i><br />
Combining Machine Intelligence with Mechanical Intelligence in Manipulation<br />
Lael Odhner</p>

<h4>SAT</h4>

<p><i>What's Hot:</i><br />
SMT-Based Verification of Hybrid Systems<br />
Alessandro Cimatti, Sergio Mover, and Stefano Tonetta</p>

<p><i>Best Paper:</i><br />
On Freezing and Reactivating Learnt Clauses (SAT-11 Best Paper Award)<br />
Gilles Audemard, Jean-Marie Lagniez, Bertrand Mazure, and Lakhdar Sais</p>

<p><i>Challenges:</i><br />
Seven Challenges in Parallel SAT Solving<br />
Youssef Hammadi and Christoph Wintersteiger</p>

<h4>Search</h4>

<p><i>What's Hot:</i><br />
Heuristic Search Comes of Age<br />
Nathan Sturtevant, Ariel Felner, Maxim Likhachev, and Wheeler Ruml</p>

<p><i>Best Paper:</i><br />
New Approaches for Optimally Solving the Multi-Agent Path Finding Problem (Best Paper SOCS-12)<br />
Guni Sharon, Roni Stern, Ariel Felner, Nathan Sturtevant</p>


<p><i>Challenges:</i><br />
Research Challenges in Heuristic Search<br />
Rich Korf</p>

<h4>Semantic Web</h4>
<p><i>What's Hot:</i><br />
What's Hot and What's Not in the Semantic Web<br />
Chris Welty</p>

<p><i>Best Paper:</i><br />
Usage-Centric Benchmarking of RDF Triple Stores (Best Research Paper ISWC-11)<br />
Mohamed Morsey, Jens Lehmann, S&ouml;ren Auer, Axel-Cyrille Ngonga Ngomo</p>

<h4>Vision</h4>
<p><i>What's Hot:</i><br />
The Rise of Applied Vision<br />
John Tsotsos</p>

<p><i>Best Paper:</i><br />
Relative Attributes for Enhanced Human-Machine Communication (David Marr Prize ICCV-11)<br />
Devi Parikh and Kristen Grauman</p>

<p><i>Challenges:</i><br />
The Main Challenges Facing Object Categorization: Perceptual Grouping and Image Abstraction<br />
Sven Dickinson</p>
