			<h2 class="backgrnd12"><span class="text1">AAAI-12 &ndash; IAAI-12 Hotel and Site Information</span></h2>

<p><b>Sheraton Reservation Deadline Extended to July 6!
</b></p>

<p>AAAI has reserved a block of rooms at the Sheraton Centre Toronto Hotel (the conference site) at reduced conference rates. Space is limited. (Information about student accommodations is available by writing to aaai12@aaai.org. Proof of full-time student status required.)</p>

<p>Sheraton Centre Toronto Hotel<br />
123 Queen Street West<br />
Toronto, Ontario M5H 2M9 Canada<br />
<a href="http://www.sheratontoronto.com/" rel="nofollow">Hotel Website</a><br />
Phone: (416) 361-1000; Toll-Free: 1-800-325-3535<br />
Fax: (416) 947-4854<br />
<a href="http://www.starwoodmeeting.com/StarGroupsWeb/res?id=1112095541&key=1F400" rel="nofollow">AAAI-12 Online Reservations</a><br />
Reservations by telephone: 416-947-4955 x 440 or 1-888-627-7175<br />
Refer to group name "AAAI-12" when making phone reservations.<br />
Conference Rates available 7/20/12 - 7/26/2012<br />
Single/Double Occupancy: $199.00 CAD<br />
Triple Occupancy: $229.00 CAD<br />
Quadruple Occupancy: $259.00 CAD<br />
Check-in time: 3:00 PM<br />
Check-out time: 12:00 Noon<br />
Cut-off date for conference rate reservations: 5:00 PM Toronto time (EDT), Friday, July 6, 2012<br />
Reservation requests received after the cut-off date will be based on availability at the Hotel's prevailing rates.</p>

<p>Reservations should be made directly with the Sheraton Centre Toronto through one of the methods listed above. Rate is per room, per night, excluding applicable provincial and local taxes (currently 13% HST tax). Rates include complimentary wireless Internet access in guest sleeping rooms and hotel's lobby public spaces.</p>

<p>Rooms must be guaranteed by a major credit card. Guests must cancel by 72 hours prior to arrival in order to avoid forfeit of deposit. An early departure fee of one night's stay will apply if a guest checks out prior to the confirmed checkout date.</p>

<h4>Parking
</h4><p>The Sheraton Centre Toronto has covered valet parking available for $45.00 CAD per day with full in and out privileges. Within close proximity of the hotel, there are several pay-lots that offer parking options from the half hour - overnight.</p>

<h4>Additional Hotel Information</h4>
<p>Located in the financial and entertainment districts, the CAA/AAA Four Diamond Sheraton Centre Toronto is connected to PATH, a 16-mile underground network of shops and services. The hotel is in the heart of the shopping, theatre, restaurant districts of Toronto. (link to http://www.sheratontoronto.com/).</p>

<h4>Getting to Toronto</h4>
<p>The Sheraton Centre Toronto is accessibly by Toronto Pearson International Airport (YYZ). The Sheraton is about 30 minutes by taxi from YYZ and will cost approximately $50.00-$55.00 one way.</p>

<h4>Airport Transportation</h4>
<p>For more information about ground transportation options between Toronto International Airport and the Sheraton, please visit <a href="http://www.torontopearson.com/en/toandfrom/ground/" rel="nofollow">www.torontopearson.com/en/toandfrom/ground</a>, <a href="http://www.seetorontonow.com,">www.seetorontonow.com,</a> or service provider websites.</p>

<h4>Airport Express</h4>
<p><a href="http://www.torontoairportexpress.com" rel="nofollow">Company Website</a></p>
<p>The exclusive scheduled and on-demand service connecting Lester B. Pearson International Airport to Downtown Toronto! Scheduled departures via luxury motorcoach are offered every 20 minutes during peak periods and every 30 minutes during off-peak periods, 20 hours/day, 365 days/year &mdash; reservations not required. All vehicles are fully accessible, outfitted with seat belts and WiFi enabled. On-demand van service operates over 12 hours/day, 365 days/year - reservations required.</p>

