<h2 class="backgrnd12"><span class="text1">AAAI-12 Special Track on Artificial Intelligence and the Web</span></h2>

<p><i>Call for Papers</i></p>
<h3 class="text2"><a href="http://aiweb.confmaster.net" rel="nofollow">AI and the Web Special Track Paper Submission Site</a></h3>
<div class="hr1">
<hr />
</div>
<p><a href="http://www.aaai.org/Publications/Templates/AuthorKit.zip">Author Kit</a></p>
<div class="hr1">
<hr />
</div>

<p>A printable <a href="http://www.aaai.org/Conferences/AAAI/2012/aaai12webcall.pdf">PDF</a> of this call is also available</p>
<h3 class="text2">Timetable for Authors</h3>
<ul>
	<li>December 2, 2011 &ndash; January 20, 2012: Authors register on the AAAI web site</li>
	<li>January 20, 2012 (11:59 PM PST): Abstract submission deadline</li>
	<li>January 24, 2012 (11:59 PM PST): Electronic papers due</li>
	<li>March 27, 2012: Notification of acceptance or rejection</li>
	<li>April 17, 2012 (11:59 PM PDT): Camera-ready copy due at AAAI office</li>
</ul>
<p>The web has evolved from a simple hypertext standard into a ubiquitous, global information system including much of human knowledge, and sometimes beyond. Today's web provides ready access to not only text, images, and audio files, but also to structured and semistructured information, sensor data, composable services, and communities of people. It offers an open and decentralized environment in which anyone can publish information and services coupled with powerful search engines and agents to discover them. All of this is ubiquitously available from wired, wireless, and mobile devices. The result is an environment enormously useful to people for research, learning, commerce, socializing, communication, and entertainment. We have just begun to explore how this vast amount of machine accessible knowledge can be exploited and used by machines to better serve human needs as well as to discover new knowledge.</p>

<p>This special track invites research papers on AI techniques, systems, and concepts involving or applied to the web. Papers should either describe web related research or clearly explain how the work addresses problems, opportunities or issues underlying the web or web-based systems. Relevant topics include, but are not limited to:</p>

<ul>
	<li>AI for multimedia and multimodal web applications</li>
	<li>AI for web services: semantic descriptions, planning, matching, and coordination</li>
	<li>AI for web-based collaboration and cooperation</li>
	<li>Crowdsourcing techniques and methodologies</li>
	<li>Enhancing web search and information retrieval</li>
	<li>Exploiting Linked Open Data</li>
	<li>Human language technologies for web systems, including text summarization and machine translation</li>
	<li>Intelligent user interfaces for web systems</li>
	<li>Knowledge acquisition from the web</li>
	<li>Languages, tools, and methodologies for representing, managing, and visualizing semantic web data</li>
	<li>Machine learning and the web</li>
	<li>Ontologies and the web: creation, extraction, evolution, mapping, merging, and alignment; tags and folksonomies</li>
	<li>Question answering on the web</li>
	<li>Recognizing web spam such as link farms and splogs</li>
	<li>Representing, reasoning, and using provenance, trust, privacy, and security on the web</li>
	<li>Searching, querying, visualizing, and interpreting the semantic web</li>
	<li>Social networking and community identification</li>
	<li>Web personalization and user modeling</li>
	<li>Web-based opinion extraction and trend spotting</li>
	<li>Web-based recommendation systems </li>
</ul>

<p>Papers will be reviewed by qualified reviewers drawn from a special track committee as well as the general program committee. Submissions to this special track deemed not to be relevant may be considered for review for the general technical papers track at the discretion of the track and conference cochairs.</p>

<h3 class="text2">Author Registration</h3>

<p>Authors must register at the <a href="http://aiweb.confmaster.net" rel="nofollow">Special Track on AI and the Web technical paper submission site</a> before they submit their abstracts and papers. The software will assign a password, which will enable the author to log on to submit an abstract and paper. In order to avoid a rush at the last minute, authors are encouraged to register as soon as possible after December 2, and well in advance of the January 20 abstract deadline.</p>
<ul>
	<li>Registration will close at 11:00 PM PST on January 20, 2012.</li>
	<li>Abstract submission will close at 11:59 PM PST on January 20, 2012.</li>
	<li>Final paper submission will close at 11:59 PM PST on January 24, 2012.</li>
</ul>

<h3 class="text2">Abstract and Paper Submission</h3>

<p>Papers may be no longer than 7 pages, where page 7 must contain only references, and no other text whatsoever. Papers must be in trouble-free, high- resolution PDF format, formatted for US Letter (8.5" x 11") paper, using Type 1 or TrueType fonts. Papers must be formatted in AAAI two-column, camera-ready style (see the author instructions page). Please note that these formatting instructions are for final, accepted papers; no additional pages can be purchased at the review stage. In addition, the copyright slug may be omitted in the initial submission phase.</p>
<div class="hr1">
<hr />
</div>

<h3 class="text2">Blind Review Instructions</h3>

<p>Reviewing for the AAAI Conference is blind to the identities of the authors. The first page, on which the paper body begins, should include the title, abstract, content areas, and ID number (if available), but not the acknowledgments and names or affiliations of the authors. The references should include all published literature relevant to the paper, including previous works of the authors, but should not include unpublished works.</p>

<p>When referring to one&rsquo;s own work, use the third person, rather than the first person. For example, say &ldquo;Previously, Newell (Newell 1996) has shown that...,&rdquo; rather than &ldquo;In our previous work (Newell 1996) we have shown that....&rdquo; Try to avoid including any information in the body of the paper or references that would identify the authors or their institutions. Such information may be added to the final camera-ready version for publication.
</p>

<div class="hr1">
<hr />
</div>
<p>Electronic abstract and paper submission through the <a href="http://aiweb.confmaster.net" rel="nofollow">Special Track on AI and the Web paper submission site</a> is required on or (preferably) before the deadline dates listed above. We cannot accept submissions by e-mail or fax. Authors will receive confirmation of receipt of their abstracts or papers, including an ID number, shortly after submission. AAAI will contact authors again only if problems are encountered with papers. Inquiries regarding lost papers must be made no later than January 27, 2012.</p>

<div class="hr1">
<hr />
</div>

<h3 class="text2">AAAI-12 Policy Concerning Submissions to Other Conferences or Journals</h3>

<p>AAAI-12 will not accept any paper which, at the time of submission, is under review for or has already been published or accepted for publication in a journal or another conference. Authors are also required not to submit their papers elsewhere during AAAI's review period. These restrictions apply only to journals and conferences, not to workshops and similar specialized presentations with a limited audience and without archival proceedings. Authors will be required to confirm that their submissions conform to these requirements at the time of submission.
</p>

<div class="hr1">
<hr />
</div>

<h3 class="text2">Review Process</h3>

<p>Program committee members will identify papers they are qualified to review based on the information submitted electronically (the paper's title, keywords, and abstract). Their reviewing will be done blind to the identities of the authors and their institutions. Authors will have a limited opportunity to respond to initial reviews. This author's feedback may then be taken into account in the final reviews and recommendations. The program committee's reviews will make recommendations to the senior program committee, which in turn will make recommendations to the track cochairs.  In the final decision phase, it will be determined whether the paper will be presented orally and as a poster, or only as a poster. Although the track cochairs will formally make all final decisions, in practice almost all will be made earlier in the process.</p>

<h3 class="text2">Publication</h3>

<p> All accepted papers (independent of oral or poster presentation) will be allocated seven (7) pages in the conference proceedings. Up to two (2) additional pages may be used at a cost to the authors of US$ 275 per page. Final papers exceeding page limits and those violating the instructions to authors will not be included in the proceedings. Authors will be required to transfer copyright of their paper to AAAI.</p>

