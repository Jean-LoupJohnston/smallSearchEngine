<h3 class="text2"><i><a name="Dorsen">Performance of "Hello Hi There"</a></i></h3>
<p><b>Concept and Direction: Annie Dorsen</b>
<br />
Wednesday, July 25<br />
6:30&ndash;7:30 PM, 
Grand Ballroom E, Lower Concourse</p>

<p><b>Question and Answer Session with Annie Dorsen</b>
<br />
Thursday, July 26<br />
11:20 AM&ndash;12:10 PM, 
Kenora, Second Floor</p>

<p><b>Screening of TV Debate between Foucault and Chomksy</b>
<br />Wednesday, July 26<br />
2:25&ndash;4:15 PM, 
Conference Room D/E, Second Floor</p>

<p>
"Hello Hi There" uses a famous television debate between the philosopher Michel Foucault and linguist and activist Noam Chomsky from the 1970s as inspiration and material for a dialogue between two custom-designed chatbots. In every performance, these computer programs, designed to mimic human conversations, generate a new &mdash; as it were, improvised &mdash; live text. The original television debate will itself be screened before the performance at 2:25&ndash;4:15 PM.</p>
</p>

<p>
As Chomsky and Foucault debate language, creativity, the roots of scientific discovery and the nature of political power, the chatbots talk on, endlessly circling the questions of the debate, and frequently veering off into unexpected, at times nonsensical, digressions. Purposely low-tech, the chatbots remind us of the optimism of the late 1960s and early 1970s, when natural language programming was thought to soon crack the code of human language production. This optimism in the face of repeated failure echoes the dynamic in the politics of Chomsky and Foucault &mdash; reflecting a similarly fading optimism in the possibility of people to remake our world.
</p>

<p>
There will be a question and answer session on Thursday, July 26 with Annie Dorsen, the creator of the show. Hello Hi There premiered at the streirischer herbst festival (Graz),
and has been presented at Black Box Teatre (Oslo), BIT Teatergarasjen (Bergen), Hebbel am Ufer (Berlin) and PS122 (New York).
</p>

