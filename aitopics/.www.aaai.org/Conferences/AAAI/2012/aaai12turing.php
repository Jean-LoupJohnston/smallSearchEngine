<h3 class="text2"><i><a name="Papadimtriou">AAAI Turing Lecture</a></i><br />
The Origin of Computable Numbers: A Tale of Two Classics</h3>
<p><b>Christos Papadimtriou (University of California, Berkeley)</b>
<br />
Wednesday, July 25<br />
9:00&ndash;10:00 AM</p>

<p>Turing, like Darwin, transformed scientific and human culture through a singularly disruptive work written in a brilliantly self-conscious style. I shall recount the stories of these two classics, concluding with certain unexpected connections between computational ideas and evolution.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2012/Photos/papadimitriou.jpg" alt="Christos Papadimtriou" width="57" height="75" class="img1" /><b>Christos Papadimtriou</b> is the C. Lester Hogan Professor of Electrical Engineering and Computer Science, Computer Science Division, University of California at Berkeley. Before joining Berkeley in 1996 he taught at Harvard, MIT, Athens Polytechnic, Stanford, and the University of California, San Diego. He has written five textbooks and many articles on algorithms and complexity, and their applications to optimization, databases, AI, economics, evolution, and the Internet. He holds a PhD from Princeton (1976), and honorary doctorates from ETH (Zurich), Athens Polytechnic, and the Universities of Macedonia, Athens, Cyprus, and Patras. He is a member of the Academy of Sciences of the US, the American Academy of Arts and Sciences, and the National Academy of Engineering, and a fellow of the ACM. His novel <i>Turing (a novel about computation)</i> was published by The MIT Press in 2003, and his graphic novel <i>Logicomix</i> (with Apostolos Doxiadis) was translated in more than 25 languages.</p>
