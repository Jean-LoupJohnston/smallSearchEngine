<h2 class="backgrnd12"><span class="text1">AAAI-12 Sponsorship Program</span></h2>

<h3 class="text2"><a href="http://www.aaai.org/Conferences/AAAI/2012/aaai12sponsorship.pdf">Sponsorship Brochure and Contract</a></h3>
<div class="hr1">
<hr />
</div>


<p>On behalf of the Conference Committee of the Twenty-Sixth AAAI Conference on Artificial Intelligence, we invite you to participate in the sponsor program for AAAI-12. The conference will be held July 22-26, 2012 in Toronto, Canada, the capital of Ontario, and is sponsored by the Association for the Advancement of Artificial Intelligence (AAAI). The site of the conference will be the Sheraton Centre Toronto Hotel, located in the financial and entertainment districts, and connected to PATH, a 16-mile underground network of shops and services. AAAI is pleased to announce its partnership with the local AI community at the University of Toronto to showcase local research and bring the flavor of Toronto to AAAI-12.</p>

<p>2012 marks the 100th anniversary of Alan Turing's birth, and AAAI is joining the community-wide centenary celebration with several events, including a special inaugural 2012 AAAI Turing Lecture by Christos Papadimtriou (University of California, Berkeley). This event, as well as other special events, provide ideal vehicles to highlight your company's participation and sponsorship of AAAI-12.</p>

<p>AAAI is the annual North American artificial intelligence conference, attracting over 1,100 AI researchers and practitioners, representing government, university and corporate research facilities from throughout the world. Your participation in the AAAI-12 Sponsor Program will give you instant visibility to this diverse group of AI professionals, representing a host of research areas such as robotics, AI and the web, cognitive systems, AI applications, computational sustainability and AI, human computation, interactive digital entertainment, education, and recommender systems.</p>

<p>As part of most levels of sponsorship, you will be provided with a table-top exhibit throughout the technical portion of the conference. This heightened visibility will provide an opportunity for one-on-one exchange with conference participants. Whether you want to showcase your intelligent technology to the AI community, or tap into this incredible source of talent, AAAI-12 is the premier venue to accomplish these goals.</p>

<h3>Named Events</h3>
<p>The conference includes several major events that can be named in 
honor of major contributors. These naming opportunities bring very 
high visibility but are limited in number. We urge companies 
considering this level of support to contact AAAI as soon as 
possible. Preference will be given to companies who commit early 
and/or commit to support the event for multiple years. See the next 
section for the additional benefits of Platinum, Gold, and Silver 
sponsorships.</p>

<ul>
	<li><i>Conference Banquet:</i> $25,000. Includes a complimentary Platinum sponsorship.</li>
	<li><i>Opening Reception:</i> $25,000. Includes a complimentary Platinum sponsorship.</li>
	<li><i>Poster Session Reception:</i> $15,000. Includes a complimentary Gold sponsorship.</li>
	<li><i>Special 2012 Turing Events:</i> $10,000 (per event). <br />
	In honor of the Alan M. Turing Centenary, AAAI will hold several celebratory events, 
including a special invited talk by Christos Papadimtriou (University 
of California, Berkeley), as well as other special performances, 
panels, and presentations. Includes a complimentary Silver 
sponsorship.</li>
	<li><i>Distinguished Lecture:</i> $10,000 (per lecture). <br />One of the lectures by 
a distinguished invited speaker will be named after your 
organization. Includes a complimentary Silver sponsorship.</li>
	<li><i>WiFi Internet Access:</i> $10,000. Includes a complimentary Silver sponsorship.

</ul>

<h3>Sponsorship Levels and Benefits</h3>

<p>Each AAAI-12 Sponsor will receive their company name and/or logo 
prominently displayed on the following:</p>

<ul>
	<li>Printed materials, as schedule permits</li>
	<li>AAAI-12 website</li>
	<li>AAAI-12 registration area sign</li>
	<li>Signs at appropriate conference events</li>
</ul>

<p>Certain levels of sponsorship also include the following:</p>

<ul>
	<li>Complimentary conference registration(s) and banquet ticket(s)</li>
	<li>Exhibit tabletop(s) in conference center foyer</li>
	<li>Sponsor-supplied inserts in conference registration packets</li>
</ul>


<h4>Platinum ($25,000)</h4>
<p>6 Complimentary registrations; 2 exhibit 
table-tops; a registration packet insert of up to 4 pages</p>

<h4>Gold ($15,000)</h4>
<p>4 Complimentary registrations; 2 exhibit table-tops; 
a registration packet insert of up to 2 pages</p>

<h4>Silver ($10,000)</h4>
<p>2 Complimentary registrations; 1 exhibit table-top; 
a registration packet insert of up to 1 page</p>

<h4>Bronze ($5,000)</h4>
<p>1 Complimentary registration; 1 exhibit table-top</p>

<p>To become a sponsor, please contact AAAI at </p>
<ul>
<li>&#97;&#97;&#97;&#105;12&#64;&#97;&#97;&#97;&#105;&#46;&#111;&#114;&#103;</li>
</ul><p>or  telephone (650) 328-3123 (USA), or see the <a href="http://www.aaai.org/Conferences/AAAI/2012/aaai12sponsorship.pdf">AAAI-12 Sponsorship Brochure and Agreement.</a></p></div>
