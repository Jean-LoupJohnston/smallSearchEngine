<h2 class="backgrnd12"><span class="text1">AAAI-12 AI and Robotics Multimedia Fair</span></h2>

<p><i>Call for Call for Participation</i></p>
<div class="hr1">
<hr />
</div>

<p>A printable <a href="http://www.aaai.org/Conferences/AAAI/2012/aaai12roboticsfair.pdf">PDF</a> of this call is also available</p>
<h3 class="text2">Important Dates</h3>
<ul>
	<li>June 10, 2012 (11:59:59 PM PDT): Submission deadline</li>
	<li>June 23, 2012: Acceptance with suggestions for improvement, if needed</li>
	<li>June 30, 2012: Authors submit final revised version</li>
</ul>
<p>AAAI is pleased to present the inaugural AI and Robotics Multimedia Fair at AAAI-12 in Toronto. The program will consist of contributions to be presented as an illustration of the combination of AI and Robotics. The goal is twofold: for researchers and developers to showcase how they view the use or need of AI techniques in robotics systems; and for AI researchers to understand how their interests can reach out to robotics platforms.
There will be three types of participation: (1) robot demonstrations; (2) videos; and (3i) posters. All contributions should be as much as possible stand-alone and self-explanatory, so that viewers can understand the technical problem, the robotics approach, and the results. Submissions should include links to further information, such as an underlying research paper, or a project's website. Each contribution will be reviewed and authors will be notified of the submission results, including possible comments to improve the contribution.</p>

<p>The posters will be displayed throughout the conference. The videos and robot demonstrations will be shown as widely as possible, and at least during the evening poster session. Contributors of accepted submissions are not required to attend AAAI-12. However AAAI welcomes and encourages participation of the presenters at the Fair and is pleased to offer a discounted registration fee.</p>

<h4>Submission Guidelines for Robot Demonstrations</h4>
<p>
Submit up to a 2-page description of the demonstration, including both the technical content and the requirements for the demonstration, such as space, table, access to power, and any other conditions. Simple demonstrations are encouraged and submissions may not be able to be accepted due to the impossibility of meeting the demonstration conditions.
</p>


<h4>Submission Guidelines for Videos</h4>
<ul>
	<li>Videos should include an English narration and/or title and subtitles.</li>
	<li>Video size should not exceed 100 megabytes, and duration should be limited to a maximum of 5 minutes. Short videos are encouraged.</li>
	<li>Video format is mandatory to be MPEG-4. Only freely available players (such as QuickTime, RealPlayer, MicrosoftWindows Media Player, MPlayer, VLC) should be required for viewing the video. The authors must have copyright of the audio and visual content.</li>
	<li>Video contributions that do not comply with these guidelines and/or contain inappropriate material (for example, commercial or unethical content) will not be accepted.</li>
</ul>

<h4>Submission Guidelines for Posters</h4>
<p>
Posters must be in PDF or PPT format, and no larger than 44 (width) x 34 (height) when printed. Posters with extensive and informative visuals are encouraged.
</p>

<h4>Submission Information</h4>
<p>
Please send an http link to your submitted file to junaed@cs.ubc.ca. In case of
multiple files, please submit a pointer to a ZIP archive combining the files.
</p>
<h4>Discounted Registration Fee for AI and Robotics Multimedia Fair Participants</h4>
<p>A discounted registration fee is offered to presenters of accepted contributions to participate at AAAI-12.
</p>
