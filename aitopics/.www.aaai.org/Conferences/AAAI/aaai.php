<h2 class="backgrnd12"><span class="text1">The AAAI Conference on Artificial Intelligence</span></h2>

<div class="content">
<p><i>Sponsored by the Association for the Advancement of Artificial Intelligence
</i></p>
<hr />
<p>The AAAI Conference on Artificial Intelligence promotes theoretical and applied AI research as well as intellectual interchange among researchers and practitioners. The technical program features substantial, original research and practices. Conference panel discussions and invited presentations identify significant social, philosophical, and economic issues influencing AI&rsquo;s development throughout the world.</p>
<hr />
<h3>Future Conferences</h3>
<ul>
<li><a href="https://aaai.org/Conferences/AAAI-20/">The Thirty-Fourth Conference on Artificial Intelligence</a> will be held in New York City, New York, USA, from February 7&ndash;12, 2020.</li></ul>
