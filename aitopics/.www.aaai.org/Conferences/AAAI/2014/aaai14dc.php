<h2 class="backgrnd6"><span class="text1">Nineteenth AAAI/SIGAI Doctoral Consortium</span></h2>
<p><i>Collocated with the Twenty-Eighth Conference on Artificial Intelligence</i><br />
July 27&ndash;28, 2014, Qu&eacute;bec City, Qu&eacute;bec, Canada</p>
<div class="hr1">
<hr />
</div>
<h3 class="text2"><a href="http://eecs.wsu.edu/~taylorm/aaai2014-dc/">AAAI-14 Doctoral Consortium Supplementary Website</a></h3>
<div class="hr1">
<hr />
</div>

<p>
The AAAI/SIGAI Doctoral Consortium (DC) provides an opportunity for a group of Ph.D. students to discuss and explore their research interests and career objectives with a panel of established researchers in artificial intelligence. The consortium has the following objectives: (1) to provide a setting for mutual feedback on participants' current research and guidance on future research directions; (2) to develop a supportive community of scholars and a spirit of collaborative research; (3) to support a new generation of researchers by offering advice about academic, research, industrial, and non-traditional career paths; and (4) to contribute to the overall conference goals through interaction with other researchers and participation in conference events.
</p>

<p>
The Doctoral Consortium will be held as a workshop on July 27&ndash;28, 2014, immediately before the start of the main AAAI conference. Student participants in the Doctoral Consortium will receive complimentary conference registration and a fixed allowance for travel/housing. All participants selected to present their work at the Doctoral Consortium are expected to be present throughout the consortium. Our experience has been that participants gain almost as much by interacting with their peers as by having their presentations critiqued by the faculty panel. As such, we expect a commitment from participating students to attend the entire DC.
In addition, all students attending AAAI are invited to observe the Doctoral Consortium, whether or not they apply to present their work. It can be a tremendous learning experience to watch peers make presentations, as well as listen to panel discussions.
</p>

