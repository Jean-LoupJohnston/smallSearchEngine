<h3 class="text2">AAAI-14 Online Reservations</h3>
<p><a href="http://www.hilton.com/en/hi/groups/personalized/Y/YQBHIHH-AAAA14-20140725/index.jhtml?WT.mc_id=POG" rel="nofollow">Website</a><br />
