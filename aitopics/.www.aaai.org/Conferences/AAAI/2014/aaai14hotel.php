Reservation requests received after the cut-off date will be based on availability at the Hotel's prevailing rates.</p>

<p>Reservations should be made directly with the Hilton Qu&eacute;bec through one of the methods listed above. Rate is per room, per night, excluding a 3 percent per room per night tax, a 5 percent per room per night secondary tax, and a 9.5 percent per room per night tertiary tax.</p>

<p>Payment for room and tax and all incidental charges is due in full upon check out. Payment can be made by cash, by certified check, or credit card. Hotel accepts American Express, Diners Club, Discover Card, Duet Card, Eurocard, JCB International, Maestro/Switch, Master Card or Visa. If you wish to cancel, please do so by 4pm, hotel local time, on the day of arrival to avoid cancellation penalties. A limited number of rooms for guests with special needs, in accordance with ADA, are available. Please contact the hotel reservations department to confirm your arrangements. At check-in, the front desk will verify your check-out date. Rates quoted are based on check-in date and length of stay. Should you choose to depart early a departure fee will be charged. The Hilton reserves the right to cancel or modify reservations where it appears that a customer has engaged in fraudulent or inappropriate activity or under other circumstances where it appears that the reservations contain or resulted from a mistake or error.</p>

<h3 class="text2">Parking</h3>
<p>Self parking is available at $20 per day, including tax. Valet Parking is available for $30 per day. including tax.</p>

<h3 class="text2">Additional Hotel Information</h3>
<p>Located on Parliament Hill, the Hilton Qu&eacute;bec hotel is only a short drive from the airport and five minutes from the train and bus stations. The Hilton Qu&eacute;bec hotel is steps from all major tourist attractions and festivals. Enjoy close proximity to the city's government and business attractions. Visit the Old Town to take in Qu&eacute;bec's culture and historical significance.</p>

<h3 class="text2">Getting to Qu&eacute;bec</h3>
<p>The Hilton Qu&eacute;bec Hotel is accessibly by the Qu&eacute;bec Airport, <a href="http://www.aeroportdeQuebec.com/en/" rel="nofollow">Jean Lesage International Airport (YQB).</a> The Hilton is approximately 25 minutes by taxi and will cost about $33 CAD one way.</p>

<h3 class="text2">Transportation, Maps, and General Information</h3>
<p>For information about alternate ground transportation options between the Qu&eacute;bec Airport and the Hilton, please see the information on <a href="http://www.aeroportdeQuebec.com/en/to-and-from-the-airport/taxis/" rel="nofollow">taxi companies,</a>   <a href="http://www.aeroportdeQuebec.com/en/to-and-from-the-airport/public-transport/" rel="nofollow">public transport,</a> or service provider websites (a list is available on the <a href="http://www.aeroportdeQuebec.com/en/" rel="nofollow">airport website</a> &mdash; see the To and From the Airport pull-down list on the site.</p><p>For further information about ground 
transportation options in Quebec City, please see <a href="http://www.quebecregion.com/en/transportation/getting-around/" rel="nofollow">Getting Around Qu&eacute;bec City.</a> 
A comprehensive set of maps of Qu&eacute;bec is available on the <a href="http://www.quebecregion.com/en/useful-information/maps-of-quebec-city-and-area/" rel="nofollow">Maps of Qu&eacute;bec</a> web page. 
Finally, a handy list of useful travel information can 
be found on the <a href="http://www.quebecregion.com/en/useful-information/" rel="nofollow">Qu&eacute;bec Useful Information Page.</a> 


