<h2 class="backgrnd12"><span class="text1">AAAI-14 Call for Papers</span></h2>

<div class="hr1">
<hr />
</div>
<h3 class="text2"><a href="https://www.easychair.org/conferences/?conf=aaai14" rel="nofollow">AAAI-14 Main Technical Track Paper Submission Site</a></h3>
<div class="hr1">
<hr />
</div>
<h3 class="text2"><a href="http://www.aaai.org/Publications/Templates/AuthorKit.zip">2014 AAAI Author Kit</a></h3>
<div class="hr1">
<hr />
</div>
<h4 class="text2">Timetable for Authors</h4>
<ul>
<li>December 6, 2013 &ndash; January 31, 2014: Authors register on the AAAI web site</li>
<li>January 31, 2014: Electronic abstracts due</li>
<li>February 4, 2014: Electronic papers due</li>
<li>March 18 &ndash; March 22, 2014: Author feedback about initial reviews</li>
<li>April 7, 2014: Notification of acceptance or rejection</li>
<li>April 22, 2014: Camera-ready copy due at AAAI office</li>
</ul>

<p>
AAAI-14 is the Twenty-Eighth AAAI Conference on Artificial Intelligence (AI). The purpose of this conference is to promote research in AI and scientific exchange among AI researchers, practitioners, scientists, and engineers in affiliated disciplines. AAAI-14 will have multiple technical tracks, student abstracts, poster sessions, invited speakers, and exhibit programs, all selected according to the highest reviewing standards. AAAI-14 welcomes submissions on mainstream AI topics as well as novel crosscutting work in related areas. Topics include but are not limited to the following:
</p>

<ul>
	<li>AI and the Web</li>
	<li>Applications</li>
	<li>Cognitive Modeling</li>
	<li>Cognitive Systems</li>
	<li>Computational Sustainability and AI</li>
	<li>Game Theory and Economic Paradigms</li>
	<li>Game Playing and Interactive Entertainment</li>
	<li>Heuristic Search and Optimization</li>
	<li>Human-Computation and Crowd Sourcing</li>
	<li>Humans and AI</li>
	<li>Knowledge Representation and Reasoning</li>
	<li>Machine Learning Applications</li>
	<li>Multiagent Systems</li>
	<li>NLP and Knowledge Representation</li>
	<li>NLP and Machine Learning</li>
	<li>NLP and Text Mining</li>
	<li>Novel Machine Learning Algorithms</li>
	<li>Planning and Scheduling</li>
	<li>Reasoning under Uncertainty</li>
	<li>Robotics</li>
	<li>Search and Constraint Satisfaction</li>
	<li>Vision</li>
</ul>
<p>The full set of AAAI-14 keywords is available on the <a href="http://www.aaai.org/Conferences/AAAI/2014/aaai14keywords.php">AAAI-14 keywords page</a>.
<p>
Papers that extend the state of the art, and explore parts of the design space of AI that are not well-explored, are particularly encouraged. 
</p>

<h4 class="text2">Special Tracks</h4>

<p>
For the last few years, AAAI has included several special paper tracks on focused topics to increase participation in areas of new challenges to the AI community or special areas of interest.  In 2013, these tracks were Artificial Intelligence and the Web, Cognitive Systems, Computational Sustainability and Artificial Intelligence, and Robotics. The purpose of these special tracks was to ensure that papers related to these areas were reviewed according to appropriate criteria by reviewers with appropriate backgrounds.  AAAI-14 includes these tracks, but due to their increasing visibility has folded them into the main conference track as top-level keywords, with special attention to ensuring proper representation of these areas on the program committee. Submissions that mark these keywords will again be reviewed according to appropriate criteria and by appropriate reviewers.
</p>

<h4 class="text2">Author Registration</h4>
<p>
Authors must register at the AAAI-14 web-based <a href="https://www.easychair.org/conferences/?conf=aaai14" rel="nofollow">technical track paper submission site</a> before they submit their abstracts and papers. The software will assign a password, which will enable the author to log on to submit an abstract and paper. To avoid network problems in the last minute rush, authors are strongly encouraged to register as soon as possible after December 6, well in advance of the January 31 abstract deadline. 
</p>

<ul>
<li>Registration will close at 11:59 PM PST on January 31, 2014.</li>
<li>Abstract submission will close at 11:59 PM PST on January 31, 2014.</li>
<li>Final paper submission will close at 11:59 PM PST on February 4, 2014.</li>
</ul>

<h4 class="text2">Abstract and Paper Submission</h4>
<p>
Papers must be formatted in AAAI two-column, camera-ready style; see the author instructions page for details. Please note that these formatting instructions are for final, accepted papers; no additional pages can be purchased at the review stage. Papers (submitted and final) may be no longer than 7 pages, page 7 must contain only references and acknowledgements (final papers only &mdash; please do not include acknowledgements in submissions due to blind review), and no other text whatsoever. Papers must be in trouble-free, high-resolution PDF format, formatted for US Letter (8.5" x 11") paper, using Type 1 or TrueType fonts. The copyright block may be omitted in the initial submission phase. Please also refer to the instructions on how to prepare your paper for blind review. 
</p>

<h4>Supplemental Materials</h4>
<p>
<i>The length of the main submission is strictly limited as indicated above.</i> However, authors may also choose to submit supplemental material as an attachment. Examples of supplemental material are proofs of theorems that are stated in the main paper, or video demonstrations. Authors may reference the supplemental material in their papers. However, please note that there is no guarantee that the reviewers will review the supplemental material. Authors are advised to make sure the paper is self-contained. If the proofs are an important part of the contribution, then they should at least be summarized in the main paper. Similarly, the video demonstration may be described in the body of the paper.  Reviewers will view the material at their own discretion. IMPORTANT: please be very careful not to violate the blind review requirements in the supplemental material.
</p>

<p>
Electronic abstract and paper submission through the AAAI-14 paper submission site is required on or (preferably) before the deadline dates listed above. We cannot accept submissions by e-mail or fax. Authors will receive confirmation of receipt of their abstracts or papers, including an ID number, shortly after submission. AAAI will contact authors again only if problems are encountered with papers. Inquiries regarding paper receipt must be made no later than February 11, 2014. 
</p>

<h4 class="text2">Blind Review Instructions</h4>
<p>
Reviewing for the AAAI Conference is blind to the identities of the authors. The first page, on which the paper body begins, should include the title, abstract, content areas, and ID number (if available), but not the acknowledgments and names or affiliations of the authors. The references should include all published literature relevant to the paper, including previous works of the authors, but should not include unpublished works. 
</p>

<p>
When referring to one's own work, use the third person, rather than the first person. For example, say "Previously, Newell (Newell 1996) has shown that...," rather than "In our previous work (Newell 1996) we have shown that...." Try to avoid including any information in the body of the paper or references that would identify the authors or their institutions. Such information may be added to the final camera-ready version for publication. 
</p>

<h4 class="text2">Policy Concerning Submissions to Other Conferences or Journals</h4>
<p>
AAAI-14 will not accept any paper that, at the time of submission, is under review for or has already been published or accepted for publication in a journal or another conference. Authors are also required not to submit their papers elsewhere during AAAI's review period. These restrictions apply only to journals and conferences, not to workshops and similar specialized presentations with a limited audience and without archival proceedings. The restrictions also do not apply to preprints posted only on arXiv. Authors will be required to confirm that their submissions conform to these requirements at the time of submission. 
</p>

<h4 class="text2">Review Process</h4>
<p>
Program committee members will identify papers they are qualified to review based on the information submitted electronically (the paper's title, keywords, and abstract). Their reviewing will be done blind to the identities of the authors and their institutions. Authors will have a limited opportunity to respond to initial reviews. This author feedback will then be taken into account in the final recommendations and reviews may be changed accordingly. The program committee's reviews will make recommendations to the senior program committee, which in turn will make recommendations to the program cochairs. The program cochairs will make all final decisions following full consultation with members of the senior program committee during this process. 
</p>

<h4 class="text2">Publication</h4>
<p>
Accepted papers will be allocated seven (7) pages in the conference proceedings (with page 7 restricted to references); up to two (2) additional pages may be used at a cost to the authors of $275 per page. Final papers found to exceed page limits and or otherwise violating the instructions to authors will not be included in the proceedings. Authors will be required to transfer copyright of their paper to AAAI. 
</p>

<h4 class="text2">Journal Opportunities</h4>
<p>
Papers that receive excellent reviews may be selected for fast track reviewing at participating journals, including the <i>AI Journal</i> (AIJ) and the <i>Journal of Artificial Intelligence Research</i> (JAIR). Eligible authors will be invited to submit extended versions of their AAAI-14 papers to the journal of their choice.</p>

<p>
The <i>AI Journal</i> offers fast-track publication to the authors of selected excellent papers as determined by the Program Committee of AAAI-14. They aim at a review process of about 6 weeks. The papers, which should conform to all currently posted requirements relating to the submission of AIJ papers posted on the <a href="http://www.elsevier.com/locate/artint" rel="nofollow">Elsevier</a> and <a href="http://www.aijd.org" rel="nofollow"><i>AI Journal</i></a> sites would be published as regular papers but identified as revisions of papers first given at AAAI-2014.</p>

<p>
The <i>Journal of Artificial Intelligence Research</i> has a special track for award winning papers from prestigious conferences. The purpose is to highlight the research reflected in them and to give the authors a chance to expand their submission, including more details. The papers will get an expedited review process, and accepted papers receive special recognition on the <a href="http://www.jair.org/awardedpapers.html" rel="nofollow">JAIR page</a>.</p>

