<h2 class="backgrnd12"><span class="text1">AAAI-14 Senior Member Presentation Track</span></h2>

<p><i>Call for Papers</i></p>
<h3 class="text2"><a href="https://www.easychair.org/conferences/?conf=sm14" rel="nofollow">Senior Member Presentation Track Submission Site</a></h3>
<p>The Senior Member Track is open to all senior members of the AI community, AAAI Senior Members, AAAI Fellows, and people of similar stature.</p>

<div class="hr1">
<hr />
</div>

<h3 class="text2">Timetable for Presentations</h3>

<ul>
	<li>December 20, 2013 &ndash; February 21, 2014: Authors register on the AAAI web site</li>
	<li>February 21, 2014: Electronic abstracts due</li>
	<li>April 7, 2014: Notification of acceptance or rejection</li>
</ul>

<p>
The Senior Member Presentation Track provides an opportunity for established researchers to give a broad talk on a well-developed body of research or an important new research area. These presentations should provide a "big picture" view-in contrast to regular papers, which may focus on a specific contribution. They generally will also include results obtained by researchers other than the speaker. Submissions from senior members of the AI community, AAAI Senior Members, AAAI Fellows, and people of similar stature are encouraged.
</p>

<p>
To show the interest and novelty of the topic, proposals are required to cite at least 3 papers published in the last 2 years, which are not all by the same authors and of which at least one is in a highly competitive venue similar to AAAI.
</p>

<p>
Presentations will have the length of a regular paper presentation and will be selected on the basis of short proposals. Proposals will be reviewed by qualified reviewers drawn from the entire AAAI program committee.
</p>

<h3 class="text2">Author Registration</h3>
<p>
Authors must register at the <a href="https://www.easychair.org/conferences/?conf=sm14" rel="nofollow"> AAAI-14 Senior Member Presentation Track submission site</a> before they submit their abstracts and papers. The software will assign a password, which will enable the author to log on to submit an abstract and paper. To avoid a rush at the last minute, authors are encouraged to register well in advance of the February 21 abstract deadline.
</p>

<p>
Registration and proposal submission will close at 11:59 PM PST on February 21, 2014.
</p>

<h3 class="text2">Proposal Submission</h3>
<p>
Proposals should be formatted as PDF files but do not have to follow any particular formatting guidelines. They must not be longer than 2 pages and contain at least the following items:
</p>

<ul>
	<li>Title</li>
	<li>Name and affiliation of the speaker</li>
	<li>Keywords according to the AAAI topic classification, to be used for associating reviewers</li>
	<li>Description of the topic (1&ndash;2 paragraphs)</li>
	<li>Evidence that the proposer is qualified to talk about the topic, such as original contributions</li>
	<li>Citations of at least 3 papers</li>
</ul>

<p>
Electronic abstract and paper submission through the AAAI-14 Senior Member Presentation Track proposal submission site is required on or (preferably) before the deadline dates listed above. We cannot accept submissions by email or fax. Authors will receive confirmation of receipt of their proposals, including an ID number, shortly after submission. AAAI will contact authors again only if problems are encountered with papers. Inquiries regarding paper receipt must be made no later than February 28, 2014.
</p>

<h3 class="text2">Review Process</h3>
<p>
All senior member talk proposals will be reviewed by qualified members of the AAAI program committee. To allow assessment of the standing and qualifications of the speaker, reviewing will not be blind. There will be no proposer feedback. Final decisions on acceptance will be made by the track cochairs.
</p>

<h3 class="text2">Publication</h3>
<p>
Papers will not be included in the proceedings. However, each accepted senior member talk will get a chance to provide a web page with the abstract and links to further references or other material.
</p>

