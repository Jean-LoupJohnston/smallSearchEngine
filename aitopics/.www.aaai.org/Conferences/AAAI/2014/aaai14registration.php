<h3 class="text2">Technical Program Registration</h3>

<p>The AAAI-14/IAAI-14 technical program registration includes admission to all technical paper and poster sessions, the Tutorial Forum, EAAI-14, invited talks, exhibits, demos, competitions, the opening reception, the AAAI-14 Conference Fete, and an electronic copy of the AAAI-14/IAAI-14 Conference Proceedings</p>

<p><b>To register, please use the <a href="http://www.regonline.com/aaai14" rel="nofollow">AAAI-14 online registration form.</a></b></p>
<p><b>ALL FEES ARE QUOTED IN US DOLLARS</b></p>

<p><i>Deadlines</i><br />
<i>Early:</i> On or before May 30<br />
<i>Late:</i> On or before June 27<br />
<i>Onsite:</i> After June 27</p>
