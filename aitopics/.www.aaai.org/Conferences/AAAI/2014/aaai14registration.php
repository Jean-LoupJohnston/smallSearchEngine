<h4>Opening Reception and Poster/Demo Session</h4>

<p>The AAAI-14 Opening Reception will be held on Monday evening, July 28, and the Poster/Demo Session will be held on Tuesday evening, July 29, both at the Qu&eacute;bec City Convention Centre. Admittance to the opening reception and the conference-wide poster/demo session are included in the AAAI-14 technical registration fee. Guests and children are welcome for the following fees::</p>

<p><b>Opening Reception Guest</b><br />
  Adult &nbsp; $55.00<br />
  Child &nbsp; $30.00</p>

<p><b>Poster/Demo Session Guest</b><br />
  Adult &nbsp; $35.00<br />
  Child &nbsp; $15.00</p>
<p><a href="http://www.regonline.com/aaai14" rel="nofollow"><i>Online Registration Form</i></a></p>

