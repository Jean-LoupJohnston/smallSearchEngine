<h4>AAAI-14 Conference F&ecirc;te</h4>
<p>The AAAI-14 Conference Fete will be held Wednesday evening, July 30, at Le Th&eacute;&acirc;tre and Le Cabaret du Capitole de Qu&eacute;bec, 972 Rue Saint-Jean, Qu&eacute;bec, QC G1R 1R5, Canada. Established in 1903 and located in the heart of Old Qu&eacute;bec, this unique urban architectural building is considered the loveliest banquet hall in Qu&eacute;bec City. The connecting Cabaret is the old Cin&eacute;ma de Paris and has a charming Art Deco style. A jazz trio will perform, and heavy hors d'oevres and desserts will be served along with a complimentary beverage. Your admittance to the AAAI-14 Fete is included in your conference registration. Guests and children are welcome to attend for an additional fee.</p>

<p><b>AAAI-14 F&ecirc;te Guest</b><br />
<i>Early:</i> &nbsp; $100 per person</p>
