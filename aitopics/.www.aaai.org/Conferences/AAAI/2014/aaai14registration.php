<h4>AAAI-14 Hard Copy Proceedings</h4>

<p>Preregistrants may order a hard copy of the AAAI-14 proceedings, which will be mailed after the conference. The pre-conference discounted price is $95.00 per copy, plus shipping and handling. Individuals who select this option will be contacted regarding the shipping and handling fees after the conference. (Proceedings will be $220 after the conference.)</p>

<div class="hr1">
<hr />
</div>
<h4>Payment Information</h4>

<p>Payment of registration fees is required at the time of registration. Checks and bank transfers must be in US dollars. American Express, MasterCard, and VISA are also accepted. Registration applications must be submitted online by the registration deadlines to qualify for reduced rates. If you qualify for student rates, please submit your proof of student status to AAAI by email attachment to aaai14@aaai.org or by fax to +1-650-321-4457.</p>

