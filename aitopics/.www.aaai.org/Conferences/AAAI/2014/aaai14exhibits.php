			<h2 class="backgrnd12"><span class="text1">AAAI-14 Exhibit Program</span></h2>

<p>On behalf of AAAI, we invite you to exhibit at the Twenty-Eighth AAAI 
Conference on Artificial Intelligence and the Twenty-Sixth Conference 
on Innovative Applications of Artificial Intelligence, to be held 
July 27&ndash;31, 2014 in Qu&eacute;bec City, Qu&eacute;bec, Canada. Each year the AAAI 
conference brings together about 1,000 AI researchers and 
practitioners from around the world. Your participation at AAAI-14 / 
IAAI-14 is an excellent way to reach the leading scientists who are 
conducting groundbreaking research in artificial intelligence. 
Whether you want to showcase your intelligent technology services to 
the AI community, or tap into this incredible source of talent, 
AAAI-14 is the premier venue to accomplish these goals. For more 
information about exhibiting at AAAI, please refer to the following 
documents.
</p>
