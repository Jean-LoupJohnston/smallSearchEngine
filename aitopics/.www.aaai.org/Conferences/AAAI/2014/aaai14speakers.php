<h3 class="text2"><i><a name="2014PA">AAAI 2014 Presidential Address</a></i><br />
AI and AAAI: Fascinating Research and Engaged Community</h3>

<p><b>Manuela Veloso  (Carnegie Mellon University)</b></p>
<p>Wednesday, July 30, 1:00 PM &ndash; 2:00 PM, Hall 200A Second Level, Qu&eacute;bec Convention Centre</p>
<p>
AI research and development are currently in great demand. As we experience an enormous increase in a wide variety of cyber-physical-social systems and the goals to make them increasingly intelligent, AI is needed in all its multiple and varied technical facets. The talk will make the case for the need of an increased integration of our worldwide research, development, and organizational efforts and initiatives. Highlights of the past, of the recent present, and proposed directions for the future of AAAI will be presented. The talk will include and acknowledge input from the AAAI office, members, and current and past leadership.
</p>

<p>
<img src="http://www.aaai.org/Conferences/AAAI/2014/Photos/veloso.jpg" alt="Manuela Veloso" width="66" height="75" class="img1" />Manuela Veloso is the Herbert Simon University Professor at Carnegie Mellon University. She researches in artificial intelligence and robotics, in particular on agents that collaborate, observe, reason, act, and learn (CORAL group). She is a Fellow of AAAI, IEEE, and AAAS. Veloso cofounded RoboCup, a worldwide initiative investigating teams of autonomous robots in highly uncertain environments. With her students, realizing that autonomous robots inevitably have limitations in perception, cognition, and action, Veloso introduced symbiotic autonomous robots that can proactively ask for help from humans, other AI agents, and the web. Her symbiotic CoBot robots have serviced and traversed more 500 kilometers at CMU.
</p>

<h3 class="text2"><i><a name="Cheyer">Joint AAAI/IAAI Invited Talk</a></i><br />

Siri: Back to the Future</h3>

<p><b>Adam Cheyer</b></p>
<p>Monday, July 28, 7:30 PM &ndash; 8:30 PM, Hall 200A, Qu&eacute;bec Convention Centre</p>

<p>Siri is the virtual personal assistant resident inside hundreds of millions of Apple devices. Ask Siri to buy you a movie ticket, make a restaurant reservation, send a message or a tweet, or get the score of the big game and Siri will help you get the job done quickly and easily, through a conversational interaction.
People often ask me, "What technology is really behind Siri" and "What's next for Siri?"  As a former Apple employee, I'm not at liberty to talk about either of these questions. However, without saying anything related to Apple's system or roadmaps, I can describe the past, explaining what got left "on the cutting room floor" as Siri moved forward from research to commercialization up to an eventual acquisition by Apple. In this talk, I will present the technology and features behind a lineage of systems leading towards Apple's Siri: OAA, Vanguard, CALO, Active, the startup Siri. We will do it in reverse:  the farther we go back in time, the more futuristic each version gets, with fantastic capabilities not available in any later version. As Steve Jobs famously said, "You can't connect the dots looking forward, you can only connect them looking backwards..."
</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2014/Photos/cheyer.jpg" alt="Adam Cheyer" width="60" height="75" class="img1" />Adam Cheyer was a founder and vice president of engineering at Siri Inc., and, after Apple acquired the company, a director of engineering in Apple's iOS group. As a startup, Siri won the Innovative Web Technologies award at SXSW, and was chosen a Top Ten Emerging Technology by MIT's Technology Review; Apple's version of Siri was presented "Best Technical Achievement" at the 2011 Crunchies Awards, and is now available on hundreds of millions of devices.</p>
<p>Adam was also a member of the founding team at Change.org, an international community of 40 million members who fight for what's right locally, nationally, and globally. Adam also co-founded Genetic Finance, which distributes machine learning algorithms across a large number of CPUs to discover novel solutions to complex problems. As a researcher in artificial intelligence at SRI International, Adam authored more than 60 publications and 10 patents. He was Chief Architect of CALO, one of DARPA's largest AI and machine learning projects. Adam graduated with highest honors from Brandeis University and received the "Outstanding Masters Student" from UCLA's School of Engineering.
</p>


<h3 class="text2"><i><a name="Knoblock">AAAI 2014 Robert S. Engelmore Award Lecture</a></i><br />

From Virtual Museums to Peacebuilding: Creating and Using Linked Knowledge</h3>

<p><b>Craig A. Knoblock (University of Southern California)</b></p>
<p>Tuesday, July 29, 9:00 AM &ndash; 10:00 AM, Hall 200A Second Level, Qu&eacute;bec Convention Centre</p>

<p>Companies, such as Google and Microsoft, are building web-scale linked knowledge bases for the purpose of indexing and searching the web, but these efforts do not address the problem of building accurate, fine-grained, deep knowledge bases for specific application domains.  We are developing an integration framework, called Karma, which supports the rapid, end-to-end construction of such linked knowledge bases.  In this talk I will describe machine-learning techniques for mapping new data sources to a domain model and linking the data across sources.  I will also present several applications of this technology, including building virtual museums and integrating data sources for peacebuilding. </p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2014/Photos/knoblock.jpg" alt="Craig Knoblock" width="66" height="75" class="img1" />Craig Knoblock is a research professor in computer science at the University of Southern California (USC) and the director of information integration at the USC Information Sciences Institute. He received his Ph.D. from Carnegie Mellon in 1991. His research focuses on techniques related to information integration, semantic web and linked data. He has published more than 250 journal articles, book chapters, and conference papers.  Knoblock is a AAAI Fellow, a Distinguished Scientist of the ACM, and a past President and Trustee of IJCAI.  He and his coauthors have been recognized for the Best Research Paper at ISWC 2012 on Discovering Concept Coverings in Ontologies of Linked Data Sources and the Best In-Use Paper at ESWC 2013 on Connecting the Smithsonian American Art Museum to the Linked Data Cloud.

</p>

<h3 class="text2"><i><a name="Breazeal">AAAI-14 Invited Talk</a></i><br />
The Global Literacy Project: Technology to Power Child-Driven Learning</h3>
<p><b>Cynthia Breazeal (Massachusetts Institute of Technology)</b></p>

<p>Tuesday, July 29, 4:00 PM &ndash; 5:00 PM, Hall 200A Second Level, Qu&eacute;bec Convention Centre</p>

<p>Children are the most precious natural resource of any nation. Education opens the mind of a child to a potential lifetime of knowledge in all its varieties, personal growth, and critical and creative thinking. Yet, it is estimated that around 67 million children live in poor, remote areas where there is no access to schools and where everyone around them is illiterate. There are at least another 100 million children who live where schooling is so inadequate, that they also fail to achieve education any meaningful manner. There are, and always will be, places in every country in the world where good schools will not exist and good teachers will not want to go. Even in developed countries such as the United States, literacy rates, especially in areas of poverty, are unacceptably low. And over 40 percent of preschool aged children in the US are not enrolled in preschool. Too many children enter Kindergarten not ready to learn, and too few ever catch up.</p>

<p>We need a fundamentally different approach to this set of issues. Advances in new, affordable mobile computer technologies, growing ubiquity of connectivity to the Internet with cloud computing, big data analytics, even social robots allows us to explore fundamentally new ways of educating and promoting readiness skills of young children &mdash; even in these extreme contexts. It allows us to develop a new platform for global literacy: to support science, technology and content development, and to evaluate their impact on learning outcomes for even these most extreme contexts.</p>

<p>I will present both the vision and early initiatives and results to date of our multi-university team's work in the pursuit of this provocative mission.  This is a story of technological innovation, community, and the power of child-driven learning on a global scale. What we can learn from this endeavor has to potential to help us think differently about education and technology in both formal and informal learning environments, even in the most extreme learning environments. As a novel platform, we invite participation of the global community to make a difference in the lives of children everywhere.</p>

<p>Cynthia Breazeal is an associate professor at the Massachusetts Institute of Technology where she founded and directs the Personal Robots Group at the Media Lab. She is recognized as a key pioneer of social robotics and human robot interaction. Her research spans both the creation of intelligent and socially responsive robots, as well as studying their impact on contributing to people's quality of life across early childhood learning, creativity, health, telecommunications, and play. She is the author of the book <i>Designing Sociable Robots</i> and has published over 100 peer-reviewed articles. She has presented at TED, is a recipient of <i>Technology Review's</i> TR100/TR35 Award, <i>TIME</i> magazine's Best Inventions, and was honored as finalist in the National Design Awards in Communication. She received her doctoral degree from MIT in 2000.</p>


<h3 class="text2"><i><a name="Kearns">AAAI-14 Invited Talk</a></i><br />

Behavioral Network Science</h3>

<p><b>Michael Kearns (University of Pennsylvania)</b></p>
<p>Tuesday, July 29, 1:00 PM &ndash; 2:00 PM, Hall 200A Second Level, Qu&eacute;bec Convention Centre</p>

<p>For a number of years, we have been conducting human subject experiments on collective and individual behavior and performance in social networks. These experiments have investigated diverse competitive, cooperative and computational tasks that include graph coloring, voting, trading and viral marketing under a wide variety of network structures. In this talk I will survey these experiments and their findings, emphasizing the questions they raise for multi-agent systems, machine learning, and other disciplines.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2014/Photos/kearns.jpg" alt="Michael Kearns" width="54" height="75" class="img1" />
Michael Kearns is a professor in the Computer and Information Science department at the
University of Pennsylvania. His primary research interests are in machine learning, algorithmic game theory, social networks and computational finance. He is the faculty founder and codirector of Penn's Warren Center for Network and Data Sciences, and faculty founder of Penn's Networked and Social Systems (NETS) undergraduate program. He has extensive industry experience in quantitative finance and technology consulting. Prior to joining the Penn faculty, he was head of the AI research department at AT&amp;T Labs.
</p>

<h3 class="text2"><i><a name="Smyth">AAAI-14 Invited Talk</a></i><br />30 Years of Probability in AI and Machine Learning</h3>
<p><b>Padhraic Smyth (University of California, Irvine)</b></p>
<p>Thursday, July 31, 1:30 PM &ndash; 2:30 PM, Hall 200A Second Level, Qu&eacute;bec Convention Centre</p>

<p>Padhraic Smyth received a first class honors degree in electronic engineering from theNational University of Ireland (Galway) in 1984, and the MSEE and PhD degrees (in 1985 and 1988 respectively) in electrical engineering from the California Institute of Technology. From 1988 to 1996 he was a technical group leader at the Jet Propulsion Laboratory, Pasadena, and has been on the faculty at the University of California, Irvine since 1996, where he is a professor in the Department of Computer Science and the Department of Statistics, and is director of the Center for Machine Learning and Intelligent Systems. His research interests include machine learning, pattern recognition, and applied statistics. He is an ACM Fellow (2013), an AAAI Fellow (2010), and a recipient of the ACM SIGKDD Innovation Award (2009). He is coauthor of <i>Principles of Data Mining</i> (with David Hand and Heikki Mannila in 2001), and served as program chair of the UAI 2014 and ACM SIGKDD 2011 conferences. In addition to his academic research he is also active in industry consulting and has worked with companies such as Samsung, eBay, Yahoo!, Microsoft, Oracle, Nokia, and AT&amp;T, as well as serving as an academic advisor to Netflix for the Netflix prize competition from 2006 to 2009.</p>

<p>Smyth has served in editorial and advisory positions for journals such as the <i> Journal of Machine Learning Research, Journal of the American Statistical Association,</i> and the <i>IEEE Transactions on Knowledge and Data Engineering.</i> While at the University of California, Irvine he has received research funding from agencies such as NSF, NIH, IARPA, NASA, and DOE, and from companies such as Google, IBM, Yahoo!, Experian, and Microsoft. In addition to his academic research he is also active in industry consulting, working with companies such as eBay, Yahoo!, Microsoft, Oracle, Nokia, and AT&T, as well as serving as scientific advisor to local startups in Orange County. He also served as an academic advisor to Netflix for the Netflix prize competition from 2006 to 2009. Smyth received a first class honors degree in electronic engineering from National University of Ireland (Galway) in 1984, and the MSEE and PhD degrees (in 1985 and 1988 respectively) in electrical engineering from the California Institute of Technology. From 1988 to 1996 he was a technical group Leader at the Jet Propulsion Laboratory, Pasadena, and has been on the faculty at the University of California, Irvine since 1996.</p>

<h3 class="text2"><i><a name="Tambe">AAAI-14 Invited Talk</a></i><br />

Game Theory for Security: Key Algorithmic Principles, Deployed 
Applications, Research Challenges</h3>

<p><b>Milind Tambe (University of Southern California)</b></p>
<p>Thursday, July 31, 10:15 AM &ndash; 11:15 AM, Hall 200A Second Level, Qu&eacute;bec Convention Centre</p>

<p>Security is a global concern, requiring efficient, randomized 
allocation and scheduling of limited security resources. To that 
end, we have used computational game theory to build decision aids 
for security agencies around the world. These decision aids are in 
use by agencies such as the US Coast Guard for protection of ports 
and ferry traffic, and the Federal Air Marshals Service and LAX 
police for protecting air traffic; our game-theoretic algorithms are 
also under evaluation for suppression of urban crime and for 
protection of wildlife and fisheries. I will overview my group's 
research in this growing area of security games.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2014/Photos/tambe.jpg" alt="Milind Tambe" width="61" height="75" class="img1" />
Milind Tambe is the Helen N. and Emmett H. Jones Professor in 
Engineering at the University of Southern California. He is a fellow 
of AAAI and ACM, and recipient of the ACM/SIGART Autonomous Agents 
Research Award, INFORMS Wagner prize for excellence in Operations 
Research practice, Rist Prize of the Military Operations Research 
Society, Christopher Columbus Fellowship Foundation Homeland 
security award, International Foundation for Agents and Multiagent 
Systems influential paper award, Meritorious Team Commendation 
from the Commandant of the US Coast Guard and LAX Police, and 
Certificate of Appreciation from US Federal Air Marshals Service. He 
received his Ph.D. from Carnegie Mellon University.
</p>


<h3 class="text2"><i><a name="Kaplan">IAAI-14 Invited Talk</a></i><br />

The Conversational User Interface</h3>

<p><b>Ron Kaplan (Nuance Communications)</b></p>
<p>Wednesday, July 30, 9:00 AM &ndash; 10:00 AM, Hall 200A Second Level, Qu&eacute;bec Convention Centre</p>

<p>Work on both the graphical user interface (GUI) and the 
conversational user interface (CUI) started at about the same time, 
about 40 years ago. The GUI was a lot easier to implement, and it 
made computing and information resources available to ordinary 
people.  But over the years it has lost much of its simplicity and 
charm. The CUI has taken many more years to develop, requiring major 
scientific and engineering advances in speech, natural language 
processing, user-modeling, and reasoning, not to mention increases in 
cost-effective computation. But the infrastructure is now in place 
for the creation and widespread distribution of conversational 
interfaces. This talk describes some natural modes of conversational 
interaction and some of the supporting technologies that are now 
under development.

</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2014/Photos/kaplan.jpg" alt="Kaplan" width="60" height="75" class="img1" />
Ron Kaplan is the director of the Natural Language and Artificial 
Intelligence Laboratory at Nuance Communications. He came to Nuance 
from Microsoft, where he managed the Natural Language Platform Team 
for the Bing search engine. Before that, he was the chief technology 
officer and the chief scientific officer at Powerset, a deep 
semantic-search company that Microsoft acquired and merged into Bing. 
Powerset was a spin-out of the (Xerox) Palo Alto Research Center and 
was based on NL technology developed by the natural language research 
group that Kaplan directed at PARC. Inxight and Microlytics were earlier 
spin-out companies based on Kaplan's technologies.

Kaplan is also a consulting professor of linguistics at Stanford 
University. He is a past president and Fellow of the Association for 
Computational Linguistics. He is a corecipient of the 1992 Software 
System Award of the Association for Computing Machinery, and a Fellow 
