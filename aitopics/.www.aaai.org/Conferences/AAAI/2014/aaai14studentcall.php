<h2 class="backgrnd6"><span class="text1">AAAI-14 Student Abstract and Poster Program</span></h2>
<p><i>Call for Participation</i><br />
July 27-31, 2014, Quebec City, Quebec, Canada</p>

<div class="hr1">
<hr />
</div>
<h3 class="text2"><a href="https://www.easychair.org/conferences/?conf=aaaisa14" rel="nofollow">AAAI-14 Student Poster Program Submission Site</a></h3>
<div class="hr1">
<hr />
</div>
<h3 class="text2"><a href="http://www.aaai.org/Publications/Templates/AuthorKit.zip">2014 AAAI Author Kit</a></h3>
<div class="hr1">
<hr />
</div>
<h4 class="text2">Important Dates</h4>
<ul>
	<li>February 13, 2014 (11:59 PM PST): Electronic Abstracts Due</li>
	<li>March 26, 2014: Acceptance Notification</li>
	<li>April 22, 2014 (5:00 PM PDT): Camera-Ready Copy Due at AAAI Office</li>
</ul>
<div class="hr1">
<hr /></div>
<p>
AAAI-14 invites submissions to the student abstract and poster program. The goal of this program is to provide a forum in which students can present and discuss their work during its early stages, meet some of their peers who have related interests, and introduce themselves to more senior members of the field. The program is open to all students at the Undergraduate, Masters, and Doctoral levels. Nonstudent advisors or collaborators should be acknowledged appropriately, as coauthors or otherwise. However, students are requested to honor the spirit of the program by submitting only work for which they are primary investigators.
</p>

<h4 class="text2">Submissions and Dates</h4>

<p>
Electronic submission in PDF format is required. Students should submit an abstract describing their research no later than February 13, 2014. Abstracts must be no longer than 2 pages <i>including references,</i> and formatted in AAAI two-column, camera-ready style. Instructions about how to submit abstracts will be available at the AAAI conference web site after December 15, 2013. Papers exceeding the specified length and formatting requirements are subject to rejection without review.
</p>

<p>
The abstract must include the following: title; the primary author's full name, affiliation, postal address, phone number, URL (if available), and e-mail address; all coauthors' full names and affiliations; text; and any figures, tables, or diagrams. The abstract should briefly cover related work, the novelty of the research contribution, and any relevant empirical results. The abstract should also contain a URL of a location where reviewers can access complementary material about the student's research. The URL is critical to reviewers because of the brevity of the submissions.
</p>

<p>
Notification of acceptance or rejection of submitted abstracts will be mailed to the author by March 26, 2014.
</p>

<p>
Camera-ready copy of accepted abstracts will be due by April 22, 2014.
</p>

<h4 class="text2">Submissions to AAAI-14 or Other Conferences</h4>
<p>Students are free to submit abstracts for work reported in a regular paper submitted to the AAAI-14 or another conference, but not for work that has already been published. Abstracts will be accepted or rejected for the student session regardless of the outcomes of related paper submissions.</p>

<h4 class="text2">Publication</h4>

<p>Accepted abstracts will be allocated two (2) pages in the conference proceedings. Students will be required to transfer copyright of the abstract to AAAI. A copy of the AAAI copyright form is available in the <a href="http://www.aaai.org/Publications/Templates/AuthorKit.zip">AAAI author kit.</a></p>

<h4 class="text2">Poster Session</h4>
<p>
Accepted abstracts will be allocated presentation time and space in the student poster display area at the conference. Student authors of accepted abstracts must agree to prepare a poster representing the work described in their abstracts and to be available to discuss their work with visitors during their allocated time in the student poster display area.
</p>

