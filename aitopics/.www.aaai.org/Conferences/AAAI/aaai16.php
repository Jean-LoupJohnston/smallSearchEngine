<h2 class="backgrnd12"><span class="text1">Please Join Us for the Thirtieth AAAI Conference (AAAI-16)!</span></h2>

<h2>February 12&ndash;17, 2016, Phoenix, Arizona USA</h2>
<p>&nbsp;</p>
<p><b><i>NEW!</i><a href="http://www.aaai.org/Conferences/AAAI/2016/aaai16registration.php" name="Benefits"> Registration Information!</a></b></p>
<p><a href="http://videolectures.net/aaai2016_phoenix/" rel="nofollow">Selected lectures and programs from this conference have been posted at VideoLectures.Net.</a></p>
<p>The Thirtieth AAAI Conference on Artificial Intelligence (AAAI-16) 
will be held February 12&ndash;17 at the Phoenix Convention Center, 
Phoenix, Arizona, USA. Please note the alternate day pattern for 
AAAI-16. The workshop, tutorial, and doctoral consortium programs 
will be held Friday and Saturday, February 12 and 13, followed by the 
technical program, Sunday through Wednesday (at noon), February 14&ndash;17.</p>
<p>The chairs of AAAI-16 are Dale Schuurmans (University of Alberta) and Michael Wellman (University of Michigan)</p>

<p>The purpose of the AAAI conference is to promote research in 
artificial intelligence (AI) and scientific exchange among AI 
researchers, practitioners, scientists, and engineers in affiliated 
disciplines. AAAI-16 will have a diverse technical track, student 
abstracts, poster sessions, invited speakers, tutorials, workshops, 
and exhibit and competition programs, all selected according to the 
highest reviewing standards. AAAI-16 welcomes submissions on 
mainstream AI topics as well as novel crosscutting work in related 
areas.</p>

<p>Phoenix is America's sixth largest city, yet still has real cowboys, 
rugged mountains, and the kind of cactus most people see only in 
cartoons. Phoenix is the gateway to the Grand Canyon, and its history 
is a testament to the spirit of puebloans, ranchers, miners, and 
visionaries. Projected against this rich backdrop is a panorama of 
urban sophistication, with a host of museums (be sure to visit the 
Pueblo Grande Museum and Archaeological Park and the Heard Museum), 
sports stadiums, restaurants, and shopping. Nearby Tempe is the site 
of Arizona State University, home of a leading AI research community. 
Please join us in in 2016 in America's sunniest metropolis!</p>
