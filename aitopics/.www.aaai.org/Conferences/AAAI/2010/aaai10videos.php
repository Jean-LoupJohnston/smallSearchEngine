			<h2 class="backgrnd12"><span class="text1">AI Video Competition</span></h2>

<h4>Call for Video Submissions</h4>

<p>AAAI solicits submissions for the fourth annual video competition! Its goal is to show the world how much fun AI is by documenting exciting artificial intelligence advances in research, education, and application. The rules are simple: Compose a short video about an exciting AI project, and narrate it so that it is accessible to a broad online audience. The developers of award-winning videos will be presented with Oscar-like trophies ("Shakeys," in honor of SRI's Shakey robot and its pioneering video) in a ceremony, currently scheduled for Monday evening, July 12, immediately following the AAAI-10 Opening Reception. We strongly encourage student participation. So: go ahead and make a cool online video about your AI project, and get a ton of attention!</p>

<p><i>Video formats:</i> Either 1 minute (max) short video or a 5 minute (max) long video, with English narration (or English subtitles). Consider combining screen shots, interviews, and video of a system in action. Make the video self-contained, so that newcomers to AI can understand and learn from it. We encourage a good sense of humor, but will only accept submissions with serious AI content. For example, we welcome submissions of videos that:</p>

<ul>
<li>Highlight your own (contemporary or previous) research</li>
<li>Focus on the activities (research and/or application) of your (or another) group</li>
<li>Introduce viewers to an exciting new AI-related topic</li>
<li>Document some long-term activity of a senior researcher</li>
<li>Include interviews with key researchers/practitioners</li>
<li>Can be used in the classroom</li>
<li>Would otherwise interest prospective students to the field of AI</li>
<li>Includes a narration</li>
</ul>

<p>This is an incomplete list. No matter what your choice, creativity is encouraged! (Please note: The developers of previous, award-winning AI Videos typically highlighted humor, background music/tracks, and/or carefully selected movie clips to make their presentations come alive and interest our prospective viewers and PC members.)</p>

<p>Continuing for 2010: Educational Video Track (part of the AAAI Educational Advances in AI Symposium - EAAI-10) for classroom-focused videos</p>

<h4>Submission</h4>
<p>Submit your video by placing it at a publicly-accessible www site and notifying the Video Competition Chair, Arnav Jhala (&#106;&#104;&#97;&#108;&#97;&#64;&#115;&#111;&#101;&#46;&#117;&#99;&#115;&#99;&#46;&#101;&#100;&#117;), by e-mail. You are also asked to complete a submission form. For complete instructions about the submission and review process, please visit the AI Video <a href="http://www.aivideo.org" rel="nofollow">supplementary website.</a> All submissions are due no later than May 17, 2010. Notifications will be mailed on June 1, and final videos will be due to the organizers by June 22. All developers of nominated videos will be asked to sign a distribution license form. Videos must not contain any copyrighted video, audio, or </p>characters.

<h4>Key Dates</h4>

<ul>
<li>Submission Deadline: 17 May 2010</li>
<li>Reviewing Decision Notifications and Award Nominations: 1 June 2010</li>
<li>Final Versions Due: 22 June 2010</li>
<li>Screening and Award Presentations: 12 July 2010 (tentative)</li>
</ul>

<p>Reviewing will be conducted by a committee that will be formed by the chair. Reviewing criteria (from most to least-weighted):</p>
<ol>
	<li>Relevance to AI (research, instructional, and/or application)</li>
	<li>Degree of excitement: Entertainment value, enticing, novelty, etc.</li>
	<li>ducational content</li>
	<li>Presentation</li>
</ol>

<p>The award categories are Best (Long), Best Short, Best Student, Best Educational, Most Innovative, Most Informative, Best Narration, and Best Sound. (Other categories may be added at the discretion of the chair.)</p>

<p>Awards will be trophies named in honor of SRI's Shakey robot and its inspirational video</p>

<p>Sponsors: Thanks to Google and Yahoo! Research for sponsoring the awards, and to the Jozef Stefan Institute and Knexus Research Corporation for their technical support.</p>

