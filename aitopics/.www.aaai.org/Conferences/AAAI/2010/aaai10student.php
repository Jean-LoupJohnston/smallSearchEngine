<h3 class="text2"><a name="Volunteer">Student Scholar and Volunteer Program</a></h3>
<p>AAAI is pleased to announce the continuation of its Student Volunteer Programs for 2010.  The Student Volunteer Program is an essential part of the conference and student participation is a valuable contribution. Volunteers will support AAAI organizers in Atlanta. In 2010, a limited number of complimentary technical program registrations will be made available for students who volunteer during the conference. Preference will be given to students who have an accepted technical paper, and then to students who are actively participating in the conference in some way. However, all eligible students are encouraged to apply. Local students are also encouraged to apply for the Volunteer Program. <a href="../../../Membership/membership.php">AAAI membership is required for eligibility.</a> For further information regarding the Student Volunteer Program, please contact AAAI at &#118;&#111;&#108;&#117;&#110;&#116;&#101;&#101;&#114;&#49;&#48;&#64;&#97;&#97;&#97;&#105;&#46;&#111;&#114;&#103;. The deadline for volunteer applications is April 23, 2010.</p>
