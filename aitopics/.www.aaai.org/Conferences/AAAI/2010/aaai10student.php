<h3 class="text2"><a name="Poster">Student Abstract and Poster Program</a></h3>
<p>The goal of  the AAAI-10 student abstract and poster program program is to provide a forum in which students can present and discuss their work during its early stages, meet some of their peers who have related interests, and introduce themselves to more senior members of the field. The program was open to all pre-Ph.D students. Student presenters honor the spirit of the program by submitting only work for which they are primary investigators.</p>
<h4 class="text2">Poster Session</h4>

<p>The accepted student abstracts will be allocated presentation time and space in the student poster display area at the conference. Student authors of accepted abstracts have prepared a poster representing the work described in their abstracts and will be available at AAAI-10 to discuss their work with visitors during their allocated time in the student poster display area.</p>
