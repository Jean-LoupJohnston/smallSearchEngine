<h2 class="backgrnd12"><span class="text1">AAAI-10 Robotics Programs</span></h2>

<p><b>Deadline:</b> <a href="http://www.aaai.org/Conferences/AAAI/2010/aaai10robotregistration.pdf">June 15, 2010: AAAI Robot Registration Forms Due</a></p>

<p>The Nineteenth Edition of Robotics Program at AAAI features the long-standing Robotics Exhibitions as well as demonstration and challenges in emerging areas of robotics research. The Robotics Program has a long tradition of demonstrating innovating research in robotics at the intersection with artificial intelligence. This year, the AAAI-10 Robotics Program will feature an open robotics exhibition, demonstrations for intelligent robotics challenge problems, and a workshop to present current results and discuss future directions.</p>

<p>Moving forward, the theme for AAAI-10 Robotics is "Enabling Intelligence through Middleware." This theme aims to cultivate challenge experiments to advance specific problems in robotics research and education. Each challenge is intended to be an experiment designed to motivate and evaluate an individual function of artificial intelligence for robotics, similar to the  Semantic Robot Vision Challenge at AAAI-07. These challenges are meant to identify problems of interest as well as promote common metrics and experiments that are reproducible by individual researchers.</p>

<h4>Learning by Demonstration Challenge</h4>

<p>The 2010 AAAI Conference on Artificial Intelligence will be hosting the second annual exhibit and challenge on robot Learning by Demonstration (LbD).  The purpose of this event is to bring together research and commercial groups to demonstrate complete platforms performing LbD tasks.  Our long-term aim is to define increasingly challenging experiments for future LbD events and greater scientific understanding of the area.  We welcome contributions that demonstrate physically embodied robots learning a task or skill from a human teacher.</p>
<p>For more information, see the <a href="http://web.media.mit.edu/~chernova/aaai2010lbd/Home.html" rel="nofollow">supplementary website.</a></p>

<h4>Small Scale Manipulation Challenge: Robotic Chess</h4>

<p>The ICRA-2010 and AAAI-2010 Small-Scale Manipulation Challenge is designed to highlight advances in embodied intelligence using smaller than human size robots. Robotic chess requires the integration of sensing, planning and actuation and provides an opportunity for performance on a common, well-defined task.</p>
<p>For more information, see the <a href="http://aaai-robotics.ning.com/forum/topics/icra2010-and-aaai2010" rel="nofollow">supplementary website.</a></p>

<h4>Robotics Education Track</h4>


<p>The Robotics Education Track offers an accessible and flexible opportunity for undergraduate, early graduate, or pre-college student teams to design, implement, and demonstrate an autonomous robotic system. The tasks involved can span physically-embodied AI: exploration, interaction, and learning within an unknown environment. In the long run, we hope to motivate hands-on AI robotics investigation both for its own sake and in service to other academic disciplines and educational goals.</p><p>For more information, see the <a href="http://www.cs.hmc.edu/aaairoboted/" rel="nofollow">supplementary website.</a>

<h4>Workshop: Enabling Intelligence through Middleware</h4>
<p>The Enabling Intelligence through Middleware workshop focuses on how to leverage robotics knowledge and research in other communities through the use of standardized middleware.  Very few existing robotics development architectures are used outside of the group developing them.  In addition, algorithms and approaches developed in one architecture are rarely ported to another, creating a barrier to reusing good solutions and hampers the ability to validate results in more than one environment.</p>

<p>This workshop features a panel discussion led by prominent architecture research groups. The goal is to create a roadmap to common environments and tools.  This workshop will also feature presentations by exhibitors in challenge areas that highlight current research.</p>

<h4>Semantic Robot Vision Challenge</h4>
<p>The Semantic Robot Vision Challenge (SRVC) is a scavenger hunt for autonomous systems.  The robots are given a list of objects to find and they must learn what those objects look like by searching the internet (autonomously) for images of those objects.  The actual contest is broken into two sections.  First, the robots are hooked up to the internet for the image search phase. Afterwards, the robot league entries are allowed to explore the environment for the objects that they are supposed to find.</p>

<p>Software-only teams are given a  dataset of images to search through that is collected by the competition organizers. Cash prizes of $2,000 and $1,000 will be offered to the winning teams of the robot league and the software-only teams.</p>

<p>For a detailed list of the current rules, see the <a href="http://www.semantic-robot-vision-challenge.org" rel="nofollow">supplementary website</a>. </p>

<h4>AAAI-10 Robotics Exhibition</h4>
<p>The Robotics Exhibition is open to demonstrations in all areas of robotics research, education, and commercial development. Demonstrations are preferred to be live demonstrations of robots in action, but can also be combined video and poster presentations of interesting robotics projects.</p>


<h4>AAAI-10 Robotics Challenges</h4>
<p>The AAAI 10 Robotics Challenges will feature a Semantic Robot Vision Challenge, a  Learning by Demonstration Challenge, a  Small Scale Manipulation Challenge: Robotic Chess, and a  Robotics Education Track. Participation in the Semantic Robot Vision, Chess and Learning by Demonstration challenges will be handled by the organizers of each event. Each challenges has a specific time-table that includes submission of intent, qualification and registration. Please refer to the websites listed previously for the individual challenge questions or consult the corresponding organizers. </p>

