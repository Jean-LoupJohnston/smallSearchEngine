<p>AAAI and ACM/SIGART proudly present the Fifteenth AAAI/SIGART Doctoral Consortium. The Doctoral Consortium (DC) provides an opportunity for a group of Ph.D. students to discuss and explore their research interests and career objectives with a panel of established researchers in artificial intelligence. The consortium has the following objectives: (1) to provide a setting for mutual feedback on participants' current research and guidance on future research directions; (2) develop a supportive community of scholars and a spirit of collaborative research; (3) support a new generation of researchers with information and advice on academic, research, industrial, and non-traditional career paths; and (4) contribute to the conference goals through interaction with other researchers and participation in conference events.</p>

<p>The Doctoral Consortium will be held as a workshop on July 11&ndash;12, 2010, immediately before the start of the main AAAI conference. Student participants in the Doctoral Consortium will receive complimentary conference registration and a fixed allowance for travel/housing.</p>



<h3 class="text2">Dates</h3>
