<h2 class="backgrnd12"><span class="text1">AAAI Fifth Annual General Game Playing Competition</span></h2>

<p>Registration Ongoing<br />
<i><a href="http://games.stanford.edu/" rel="nofollow">More Information</a></i></p>

<p>This year's AAAI competition is designed to test the abilities of general game players by comparing their performance on a variety of previously unseen games. The 2010 championship will be held at the AAAI conference in Atlanta. Preliminary rounds will take place on Monday July 12; semifinals and finals will take place on Tuesday July 13. All teams must register by June 30 in order to compete.</p>

<p>General game players are systems able to accept declarative descriptions of arbitrary games at runtime and able to use such descriptions to play those games effectively. Unlike specialized game players, such as Deep Blue, general game players cannot rely on algorithms designed in advance for specific games. General game playing expertise must depend on intelligence on the part of the game player and not just intelligence of the programmer of the game player. In order to perform well, general game players must incorporate various Artificial Intelligence technologies, such as knowledge representation, reasoning, learning, and rational decsion making; and these capabilities have to work together in integrated fashion.</p>

<p>While general game playing is a topic with inherent interest, work in this area has practical value as well. The underlying technology can be used in a variety of other application areas, such as business process management, electronic commerce, and military operations.</p>

<h3 class="text2">Competition</h3>

<p>This year's GGP competition will be held in conjunction with this year's AAAI conference in Atlanta. The competition will consist of two phases. On Monday, July 12, players will participate in preliminary rounds. On Tuesday, July 13, the top four finishers from the preliminary rounds will participate in semifinal and final rounds to determine an overall winner. (Note that, unlike competitions in previous years, there will not be a competition phase prior to the conference.)</p>

<p>The competition is open to the public with the exception of affiliates of Stanford University. In order to participate, teams must register by June 30 by sending email to the organizers listed below. Participants should check this page periodically for updates.</p>

<p>To encourage participation, an entry fee will not be charged. Onsite competition participants will be admitted to the competition and exhibit areas but not to the technical or social events of the main conference. Finalists are encouraged to register for the AAAI-10 conference to attend research presentations and events relevant to the topics of the competition.</p>

