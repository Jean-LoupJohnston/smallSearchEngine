<p>The AAAI-10 Challenge Track provides an opportunity for researchers to give a focused talk on a problem or problem class that is challenging for existing techniques and that they believe deserves dramatically more attention. In some cases a challenge paper may resemble papers submitted to the Senior Member Track, which welcomes papers about an "important new research area, or a thoughtful critique of trends in the field," but a challenge paper must lay out a specific problem or problem class for other researchers to investigate.  Accepted challenge papers will be published in the AAAI proceedings much like traditional technical papers. Unlike traditional technical papers, however, a challenge is expected to motivate other researchers towards a focused program of research ideally culminating in papers, submitted to subsequent AAAI conferences, that address one or more aspects of the technical challenge. </p>
