<p><b>Author Registration</b></p>
<p>Authors must register at the <a href="http://aaai.confmaster.de/pages/login.php?Conf=AAAI2010" rel="nofollow">AAAI-10 main technical paper submission site.</a> The software will assign a password, which will enable the author to log on to submit an abstract and paper. In order to avoid a rush at the last minute, authors are encouraged to register as soon as possible after December 1.</p>

<p><b>Abstract and Paper Submission</b></p>

<p>Electronic abstract and paper submission through the <a href="http://aaai.confmaster.de/pages/login.php?Conf=AAAI2010" rel="nofollow">AAAI-10 main technical paper submission site,</a> is required no later than January 18 and January 21, 2010, respectively. Challenge paper authors should select "AAAI-10 Challenge Paper" under paper type when registering their paper. We cannot accept submissions by e-mail or fax.</p>

<p>Papers must be in trouble-free, high resolution PDF format, formatted for US Letter (8.5" x 11") paper, using Type 1 or TrueType fonts. Papers may be no longer than 6 pages including references, and formatted in <a href="http://www.aaai.org/Publications/Author/formatting-instructions.pdf">AAAI two-column, camera-ready style</a> (see the <a href="http://www.aaai.org/Publications/Author/author.php">author instructions page).</a> Please note that these formatting instructions are for final, accepted papers; no additional pages can be purchased at the review stage. In addition, the copyright slug may be omitted in the initial submission phase. Please also refer to the <a href="aaai10blind.php">instructions on how to prepare your paper for blind review.</a></p>

<p>Authors will receive confirmation of receipt of their abstracts or papers, including an ID number, shortly after submission. AAAI will contact authors again only if problems are encountered with papers. Inquiries regarding lost papers must be made no later than January 29, 2010.</p>

<p><b>Submissions to Other Conferences or Journals</b></p>
<p>Papers submitted to this conference must not have been accepted for publication elsewhere or be under review for another journal or conference. In contrast to some other AAAI-10 tracks, we will not consider work that has been submitted or published in a non-AI venue. The guidelines of the AAAI policy on multiple submissions are fully detailed below and must be carefully followed.</p>

<p><b>Review Criteria</b></p>
-->
<p>The challenge papers include a clear statement of the technical challenge, an argument as to why the challenge is important, discussion of previous relevant work, along with a means of evaluating progress. Ideally, a challenge issued in one AAAI will spark several years of intense research, the fruits of which are highlighted in the succeeding conferences. Challenges were welcome from specialists in traditional subfields as well as related disciplines that share with AI problems, techniques, or perspectives.<!-- When evaluating a challenge paper, reviewers will consider the following factors: </p>

<ul>
<li>Degree to which the challenge is novel and the strength of its motivation.  Is it likely to galvanize researchers to its cause?</li>
<li>Are sufficient background and references provided to aid researchers in getting up to speed</li>
<li>Are detailed suggestions and concrete sub-problems included in order to help researchers focus their efforts and start making progress?</li>
<li>Does the challenge paper lay out a well-considered methodology for evaluating potential progress in the proposed area?</li>
<li>Is the paper well organized and written?</li>
</ul>


<p>In contrast to a traditional technical paper, a challenge paper need not have theorems, experiments or other types of typical "results;" however, in some cases the presentation of negative results may be appropriate to convince the audience that the challenge is real and important, to help illustrate sub-problems, or to clarify proposed methodology. Challenge papers are allotted up to six (6) pages, but they need not be this long. For example, some challenges may be well served by a four page submission. Longer papers will be held to a higher standard.</p>


<p><b>Review Process</b></p>

<p>Program committee members will identify papers they are qualified to review based on the information submitted electronically (the paper's title, keywords, and abstract). Their reviewing will be done blind to the identities of the authors and their institutions.</p>

<p>Authors will have a limited opportunity to respond to initial reviews. This author's feedback may then be taken into account in the final reviews and recommendations. The program committee's reviews will make recommendations to the track cochairs. Although the track cochairs will formally make all final decisions, in practice almost all will be made earlier in the process.</p>

<p><b>Publication</b></p>

<p>Accepted Challenge papers will be allocated six (6) pages in the conference proceedings. Up to two (2) additional pages may be used at a cost to the authors of US$275 per page. Final papers exceeding eight (8) pages and those violating the instructions to authors will not be included in the proceedings. Authors will be required to transfer copyright of their paper to AAAI.</p>






