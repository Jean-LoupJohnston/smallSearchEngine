<h2 class="bg7"><span class="text1">Join Us for The Twenty-Sixth AAAI Conference (AAAI-12)</span></h2>

<div class="content">
<p><a href="http://videolectures.net/aaai2012_toronto/" rel="nofollow">Selected lectures and programs from this conference have been posted at VideoLectures.Net.</a></p>

<p>AAAI is pleased to announce that the Twenty-Sixth Conference on Artificial Intelligence (AAAI-12) will be held in Toronto, Ontario, Canada at the Sheraton Centre Toronto, from July 22&ndash;26, 2012. The purpose of the AAAI-12 conference is to promote research in AI and scientific exchange among AI researchers, practitioners, scientists, and engineers in related disciplines. We look forward to your visit!</p>
