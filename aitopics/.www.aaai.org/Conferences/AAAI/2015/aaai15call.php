<h3 class="text2"><a href="http://www.aaai.org/Publications/Templates/AuthorKit.zip">2015 AAAI Author Kit</a></h3><p>
Abstracts due: September 10, 2014; Papers due: September 15, 2014
</p>

<div class="hr1">
<hr />
</div>
<h4 class="text2">Timetable for Authors</h4>

<ul>
	<li>May 1, 2014 - September 10, 2014: Authors register on the AAAI web site</li>
	<li>September 10, 2014: Electronic abstracts due</li>
	<li>September 15, 2014: Electronic papers due</li>
	<li>October 22 - 24, 2014: Author feedback about initial reviews</li>
	<li>November 7, 2014: Notification of acceptance or rejection</li>
	<li>November 20, 2014: Camera-ready copy due at AAAI office</li>
</ul>




<p>
The purpose of the AAAI conference series is to promote research in Artificial Intelligence (AI) and scientific exchange among AI researchers, practitioners, scientists, students and engineers in AI and all affiliated disciplines. AAAI-15 is the Twenty-Ninth AAAI Conference on Artificial Intelligence. It will continue the traditions of previous AAAI conferences with multiple technical tracks, invited speakers, workshops, tutorials, student abstracts, senior member papers, poster sessions, a video competition, and exhibit programs, all selected according to the highest standards. AAAI-15, as the inaugural Winter AAAI conference, will also include special programs that celebrate the past and look into the future. A number of exciting innovations are planned, including additional programs for students and young researchers.
</p>


<h4 class="text2">Innovations for the Main Conference Track</h4>

<ul>
<li><i>Resubmissions of Substantially Improved AAAI-14 Submissions:</i> AAAI-15 expects to have a mechanism for authors to submit substantially improved versions of their declined AAAI-14 submissions together with a cover letter that explains how their revised submissions address the comments of the reviewers. The cover letters will be taken into consideration in the review process. <a href="http://www.aaai.org/Conferences/AAAI/2015/aaai15improved.php">(Details)</a></li>

<li><i>Demonstrations:</i> During paper submission, authors will be able to express interest in demonstrating their systems (if applicable) during a demonstration/poster session and describe the features of their intended demonstration.</li>

<li><i>Easily Accessible Papers:</i> AAAI-15 is interested in identifying a small number of papers that make good reading material for people with a general background knowledge of artificial intelligence but no detailed knowledge of the subarea covered in the paper. Authors of such papers will meet at AAAI-15 with students interested in their papers. <a href="http://www.aaai.org/Conferences/AAAI/2015/aaai15accessible.php">(Details)</a></li>

<li><i>Special Tracks:</i> AAAI-15 will include several special paper tracks (listed below) on focused topics to increase participation in special areas of interest.</li>
</ul>

