<h2 class="backgrnd12"><span class="text1">AAAI-15 &mdash; The First Winter AAAI Conference</span></h2>
<h3 class="text2">
Call for Papers<br />January 25&ndash;30, 2015 &nbsp;
Austin, Texas, USA</h3>

<div class="hr1">
<hr />
</div>

<h3 class="text2"><a href="https://www.easychair.org/conferences/?conf=aaai15" rel="nofollow">AAAI-15 Main Technical Track Paper Submission Site</a></h3>
<p><a href="https://easychair.org/conferences/?conf=aaai15intsystrack" rel="nofollow"> AAAI-15 Integrated Systems Track Paper Submission Site</a></p><div class="hr1">
