<h2 class="backgrnd6"><span class="text1">AAAI-15 Demonstrations Program</span></h2>
<p><i>The Twenty-Ninth Conference on Artificial Intelligence</i><br />
January 25&ndash;30, 2015, Austin, Texas USA</p>
<h3 class="text2"><a href="http://www.aaai.org/Publications/Templates/AuthorKit.zip">2015 AAAI Author Kit</a></h3><p>
Expressions of interest due: September 15, 2014; Short papers due: October 20, 2014
</p>

<div class="hr1">
<hr />
</div>
<h4 class="text2">Timetable for Authors</h4>

<ul>
	<li>July 21, 2014 &ndash; September 10, 2014: Authors register on the AAAI-15 web site</li>
	<li>   September 15, 2014: Submission of expressions of interest</li>
	<li>   September 29, 2014: Notification of acceptance or rejection</li>
	<li>   October 20, 2014: Short papers due</li>
	<li>   November 10, 2014: Author feedback</li>
	<li>   November 20, 2014: Camera-ready copy due at AAAI office</li>
</ul>
<h4 class="text1">Call for Demonstrations</h4>

<p>AAAI-15 is the first Winter AAAI Conference. One innovation is the 
Demonstrations Program, which is intended to foster discussion and 
exchange of ideas among researchers and practitioners from academe 
and industry by presenting software and hardware systems and research 
prototypes of such systems, including their capabilities and 
workings. Accepted demonstrations will be allocated one time slot of 
1-1/2 or 2 hours during one of the main conference days, likely 
during the poster program. Robot hardware and software can 
alternatively be showcased in the AAAI-15 Robotics Exhibition 
Program, which provides exhibit space during all conference days with 
the expectation that the exhibit will be showcased during all main 
conference days. (The Demonstration Program is not meant for 
commercial purposes, since AAAI-15 has separate exhibits for these 
purposes.)
</p>
<h3>Topics of Interest</h3>
<p>
The Demonstration Program welcomes presentations of systems on all topics of mainstream 
AI (see the <a href="aaai15call.php">topic descriptions</a> of the main AAAI and IAAI conference tracks). Promising topics include, but are not limited to, the following:</p>
<ul>
	<li>cognitive systems</li>
	<li>decision making systems</li>
	<li>environment-aware agent behaviours</li>
	<li>game playing programs</li>
	<li>human-robot interaction</li>
	<li>intelligent collaborative systems</li>
	<li>intelligent control systems</li>
	<li>intelligent graphical user interfaces</li>
	<li>natural language processing and speech recognition</li>
	<li>peripheral hardware running on dedicated devices</li>
	<li>robots</li>
	<li>visual processing</li>
	<li>web services</li>
</ul>

<p>
Submissions from everyone, including authors of paper submissions to AAAI, IAAI, and AAAI-15 workshops, are encouraged. The systems do not need to run solely on the conference premises nor must they be mature or deployed. Captivating demonstrations will be given an 
advantage in the selection process.</p>

<h4>Relationship with Other AAAI-15 Events</h4>
<p>
Participants in the <a href="aaai15robot-exhibit.php">AAAI-15 Robotics Exhibition</a> and the <a href="aaai15videos.php">AAAI-15 Video Competition</a> are encouraged to contribute to the Demonstration Program with their systems as well, if appropriate. Demonstrations related to video submissions will be publicized in the video proceedings and during the award ceremony, thereby increasing the visibility of the systems.
</p>

<h4>Author Registration</h4>
<p>
Authors must register at the <a href="https://www.easychair.org/conferences/?conf=aaai15" rel="nofollow">AAAI-15 web-based conference management system site</a> before submitting their expressions of interest. Authors should select "Demo Track." The software will assign a password, which will enable the author to log on and submit an expression of interest. Authors are strongly encouraged to register as soon as possible, well in advance of the September 10 abstract deadline.</p>


<ul>
	<li>Registration will close at 11:59 PM PST on September 10, 2014</li>
	<li>Abstract (expression of interest) submission will close at 11:59 PM PST on September 15, 2014</li>
	<li>Final 2-page paper submission will close at 11:59 PM PST on October 20, 2014</li>
</ul>
<h4>Submission and Review Process</h4>
<p>
The submission procedure is a two-step process. First, researchers and practitioners interested in submitting a demo to the AAAI-15 conference are required to submit an "expression of interest" through the web-based conference management system in the abstract field. An expression of interest consists only of a short description  introducing the demonstration and clearly specifying the authors, purpose, and the material needed to perform the demonstration. The expression of interest may be accompanied by supporting material, such as a video or a previously published paper, as long as it is specifically related to the demonstration (additional materials will not be published in the proceedings). Please submit such additional items as a zip file.
</p>

<p>Second, the expression of interest will be evaluated by the Demonstration Program Chair  to evaluate its fit with the Demonstration Program. Upon acceptance, authors will be required to submit a 2-page short paper describing their system, formatted in AAAI two-column, camera-ready style through the AAAI-15 web-based conference management system. Papers must be original, unpublished work, and authors will be required to transfer copyright of their paper to AAAI. For more information on the AAAI style, please download the <a href="http://www.aaai.org/Publications/Templates/AuthorKit.zip">AAAI Press author kit.</a> The Demonstration chair retains the right to reject demonstrations if the submitted 2-page paper has deficiencies that the reviewers believe would be difficult to fix by the authors within the given time frame.

<p>Demonstration participants are welcome to exhibit and distribute additional material, including, for example, videos running on a dedicated console, scientific papers, pamphlets, noncommercial brochures, and so on. Such ancillary materials do not necessarily need to be submitted as part of the evaluation.</p>
<p>At least one member of every demonstration  must register at AAAI-15. The conference registrant  will be responsible for building the exhibit.</p>

<p>A major AI publication has expressed interest in publishing a joint paper by the exhibitors of the AAAI-15 Demonstration Program focusing on the demos exhibited, including claims, strengths, and weaknesses. The purpose of the paper is to offer a rich discussion about the current state of the art in AI.</p>

<h4>Award</h4>
<p>To foster the presentation of exciting demonstrations, AAAI-15 will present an award for the Best Demonstration. The award will be granted on the the basis of audience evaluation through a ballot given to all attendees. </p>

<h4>Services</h4>
<p>
Researchers and practitioners showing a demonstration are required to bring all necessary equipment and material for the successful operation of their exhibit. AAAI will provide shipping instructions, if needed, for the delivery of material to the conference site. Special electrical connections must be arranged through the Demonstration Program Chair no later than 45 days prior to the start of the conference. Electrical and Internet service will be made available to confirmed exhibitors.
</p>

<p>
Tabletops will be allowed for every demo. Each exhibitor will be provided with one, 6 foot draped table on which they may arrange their display. If available and if space permits, additional tables can be provided. Additionally, every exhibitor will be given a panel to show a poster.
</p>


