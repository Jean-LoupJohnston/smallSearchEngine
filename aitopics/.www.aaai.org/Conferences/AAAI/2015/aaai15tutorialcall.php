<h2 class="backgrnd6"><span class="text1">AAAI-15 Tutorial Forum</span></h2>
<p><i>Twenty-Ninth Conference on Artificial Intelligence</i><br />
January 25&ndash;30, 2015, Austin, Texas, USA</p>
<div class="hr1">
<hr />
</div>
<p>The AAAI-15 Program Committee invites proposals for the Tutorial Forum of the Twenty-Ninth AAAI Conference on Artificial Intelligence (AAAI-15). The Tutorial Forum will be held January 25&ndash;26, 2015 in Austin, Texas, USA. Anyone interested in presenting a tutorial at AAAI-15 should submit a proposal to the 2015 Tutorial Forum Cochairs at &#97;&#97;&#97;&#105;&#49;&#53;&#116;&#117;&#116;&#111;&#114;&#105;&#97;&#108;&#115;&#64;&#97;&#97;&#97;&#105;&#46;&#111;&#114;&#103;.
</p><h4 class="text2">Key Dates</h4>
<ul>
	<li>August 15, 2014: Tutorial Proposals Due</li>
	<li>September 12, 2014: Tutorial Acceptances mailed</li>
	<li>October 1, 2014: Tutorial descriptions, autobiographical statements, and speaker photos finalized</li>
	<li>January 2, 2015: Completed course materials must be posted on speaker's website</li>
	<li>January 25&ndash;26, 2015: AAAI-15 Tutorial Forum</li>
</ul>
<h4 class="text2">What Is the Tutorial Forum?</h4>
<p>
The Tutorial Forum provides an opportunity for researchers and practitioners to spend two days each year freely exploring exciting advances in disciplines outside their normal focus. We believe this type of forum is essential for the cross fertilization, cohesiveness, and vitality of the AI field. We all have a lot to learn from each other; the Tutorial Forum promotes the continuing education of each member of AAAI. To encourage full participation by technical conference registrants, no separate fee will be charged for admittance to the Tutorial Forum in 2015.
</p>
<div class="hr1">
<hr /></div>



<h4 class="text2">Topics</h4>
<p>
Our goal is to present a diverse program that includes core areas of AI, new techniques from allied disciplines that can inform research within AI, and emerging applications of AI techniques to new areas. In addition to tutorials that teach a core area within AI, we encourage proposals from areas that are closely related to AI such as core robotics, operations research techniques relevant for AI, linguistics research applicable to language processing, etc. We also very much welcome proposals that have a hands on component. We additionally solicit tutorials by organizers of various competitions within the sub-fields of AI (Robocup, planning competition, SAT competition, etc) describing the current state of the art and tools available for new students to enter a competition. Last but not the least, we also solicit proposals from applied researchers who may have used AI-techniques in real applications within industry. Novel proposals are highly encouraged.
</p>
<h4 class="text2">Duration</h4>
<p>The majority of tutorials in the past have been 4 hours, but we also very much welcome quarter-day or full-day formats as the topic dictates.</p>
<h4 class="text2">Submission Requirements</h4>
<p>
Each proposal should include at least the following:
</p>

<p><i> Goal of the tutorial:</i> Who is the target audience? What will the audience walk away with? What makes the topic innovative?</p>

<p><i>History:</i> List of previous venues and approximate audience sizes, if the same or a similar tutorial has been given elsewhere; otherwise an estimate of the audience size.</p>

<p><i>Content:</i> Detailed outline and list of additional materials, augmented with samples, such as past tutorial slides and survey articles, whenever possible. Be as complete as possible.</p>

<p><i>Tutorial description:</i> A short paragraph summarizing the tutorial outline, and the intended duration of the symposium (default is four hours)</p>

<p><i>Prerequisite knowledge:</i> What knowledge is assumed of the target audience.</p>

<p>Please also submit the following information about the team of presenters: name, mailing address, phone number, email address; background in the tutorial area, including a list of publications and/or presentations; any available examples of work in the area (ideally, a published tutorial-level article or presentation materials on the subject); evidence of teaching experience (courses taught or references); and evidence of scholarship in AI or computer science.
</p>

<h4 class="text2">Submission Deadline</h4>
<p>
Proposals should be submitted by August 15, 2014. Decisions about the tutorial program will be made by September 12, 2014. Speakers should be prepared to finalize their tutorial descriptions and bios by October 1, 2014, and to post completed course materials on their websites by January 2, 2015.
</p>


<p>
Submissions must be in pdf format and sent to the Tutorial Program Cochairs (&#97;&#97;&#97;&#105;&#49;&#53;&#116;&#117;&#116;&#111;&#114;&#105;&#97;&#108;&#115;&#64;&#97;&#97;&#97;&#105;&#46;&#111;&#114;&#103;).
</p>


