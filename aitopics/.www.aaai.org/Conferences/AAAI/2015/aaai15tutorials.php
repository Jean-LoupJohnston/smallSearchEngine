<h3 class="text2"><i><a name="MP4">MP4:</a></i>
Semantic Parsing with Combinatory Categorial Grammars</h3>
<p><b>
Yoav Artzi, Nicholas FitzGerald, and Luke Zettlemoyer</b></p>

<p>
Semantic parsers map natural language sentences to formal representations of their underlying meaning. Building accurate semantic parsers without prohibitive engineering costs is a long-standing, open research problem. This tutorial will describe general principles for building semantic parsers.
</p>

<p>
The tutorial will be divided into two parts: learning and modeling. In the learning part, we will describe a unified approach for learning combinatory categorial grammar (CCG) semantic parsers that induces both a lexicon and parsing parameters. The approach learns from data with labeled meaning representations, as well as from more easily gathered weak supervision. It also enables grounded learning where the semantic parser is used in an interactive environment, for example to read and execute instructions. The modeling section will include best practices for grammar design and choice of semantic representation. We will motivate our use of lambda calculus for building and representing meaning with examples from several domains.
</p>

<p>
The ideas we will discuss are widely applicable. The semantic modeling approach, while implemented in lambda calculus, is applicable to many other formal languages. Similarly, the algorithms for inducing CCG focus on tasks that are formalism independent: learning the meaning of words and estimating parsing parameters. No prior knowledge of CCG is required.
</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2015/Photos/artzi.jpg" alt="Yoav Artzi" width="46" height="65" class="img1" />
Yoav Artzi is a PhD candidate in computer science and engineering at the University of Washington. He studies learning and understanding of natural language within interactive systems, with focus on semantic representation and weakly supervised learning. He is supported by a Microsoft Research PhD Fellowship, and received a Yahoo KSC award.
</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2015/Photos/fitzgerald.jpg" alt="Nicholas FitzGerald" width="50" height="65" class="img1" />
Nicholas FitzGerald is a Ph.D. student at the University of Washington. His research interests are grounded natural language understanding and generation. He is a recipient of an Intel Science and Technology Center Fellowship and an NSERC Postgraduate Scholarship.
