<h3 class="text2"><i><a name="MP5">MP5:</a></i>
Voting Rules for AI</h3>
<p><b>
Toby Walsh, K. Brent Venable, and Francesca Rossi</b></p>

<p>
Voting rules are increasingly being used in many AI settings, from multiagent systems to ensemble methods, from decision making to sentiment analysis, from crowdsourcing to human computation.
</p>

<p>
To use such rules effectively, it is important to take into account their properties, as well as a number of deep normative results about their behaviour. Fortunately, researchers in social choice have been studying such issues for many years. In this tutorial we will introduce several voting rules and compare them according to their properties. We will also show several examples of their use in various AI scenarios. No previous knowledge of social choice is needed. The tutorial can be appreciated by any AI researcher.
</p>

