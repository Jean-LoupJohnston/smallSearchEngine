<h3 class="text2"><i><a name="SA2">SA2:</a></i>
Natural Language Processing in Watson</h3>
<p><b>
James Fan and Ken Barker</b></p>

<p>
Open domain question answering (QA) is a longstanding research problem. Recently, IBM took on this challenge in the context of <i>Jeopardy!,</i> a well-known TV quiz show that has been airing on television in the United States for more than 25 years. The game  pits three human contestants against one another in a competition that requires answering rich natural language questions over a very broad domain of topics. The development of a system able to compete to grand champions in the <i>Jeopardy!</i> challenge led to the design of the DeepQA architecture and the implementation of Watson. The DeepQA project shapes a grand challenge in computer science that aims to illustrate how the wide and growing accessibility of natural language content and the integration and advancement of natural language processing, information retrieval, machine learning, knowledge representation and reasoning, and massively parallel computation can drive open-domain automatic question answering technology to a point where it clearly and consistently rivals the best human performance. Natural language processing (NLP) plays a crucial role in the overall deep QA architecture. It allows to "make sense" of both question and unstructured knowledge contained in the large corpora where most of the answers are located. That's why we decided to focus this tutorial on the NLP technology adopted by Watson and on how it fits in the general Deep QA architecture.
</p>

<p>
<img src="http://www.aaai.org/Conferences/AAAI/2015/Photos/fan.jpg" alt="James Fan" width="75" height="65" class="img1" />James Fan is a research staff member at IBM Research Watson Group in Yorktown Heights, NY. His research interests include question answering, natural language processing, machine learning and knowledge representation and reasoning. Fan is an original team member and a technical lead of the Watson QA project which is advancing the state-of-the-art in automatic, open domain question answering technology. He has been working in the area of question answering since 2003, and has published over 40 peer reviewed papers (including a best paper award at International Semantic Web Conference in 2011) in leading AI journals and conferences. Prior to joining IBM, Fan received his PhD at the University of Texas at Austin.
</p>

<p>
<img src="http://www.aaai.org/Conferences/AAAI/2015/Photos/barker.jpg" alt="Ken Barker" width="53" height="65" class="img1" />Ken Barker is a research staff member in the DeepQA Lab at IBM Watson. His current research uses structured knowledge and reasoning to guide Watson's natural language understanding and bridge conceptual gaps in its interpretations. Prior to joining IBM in 2011, he was a research scientist at the University of Texas at Austin and an assistant professor at the University of Ottawa.
</p>


