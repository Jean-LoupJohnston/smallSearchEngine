<h3 class="text2"><i><a name="SP1">SP1:</a></i>
Computing Game-Theoretic Solutions</h3>
<p><b>
Vincent Conitzer</b></p>

<p>
Game theory concerns how to form beliefs and act in settings with multiple self-interested agents. The best-known solution concept in game theory is that of Nash equilibrium. But there are also others, such as correlated equilibrium and Stackelberg mixed strategies. The latter concept offers a number of conceptual and computational advantages and has recently been deployed in a number of real-world security domains. I will review these concepts as well as algorithms for computing their solutions. I will also discuss learning in games. No previous background in game theory will be required.
</p>

<p>
<img src="http://www.aaai.org/Conferences/AAAI/2015/Photos/conitzer.jpg" alt="Vincent Conitzer" width="44" height="65" class="img1" />Vincent Conitzer is the Sally Dalton Robinson Professor of Computer Science and a professor of economics at Duke University. His research focuses on computational aspects of microeconomics, in particular game theory, mechanism design, voting / social choice, and auctions. This work uses techniques from, and includes applications to, artificial intelligence and multiagent systems.
</p>


