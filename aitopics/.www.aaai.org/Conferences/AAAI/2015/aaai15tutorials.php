<h3 class="text2"><i><a name="SA4">SA4:</a></i>
Probabilistic Programming with Figaro</h3>
<p><b>
Avi Pfeffer and Brian Ruttenberg</b></p>

<p>
This full day tutorial is an in-depth and interactive introduction to probabilistic programming, which allows probabilistic processes, models and algorithms to be specified using familiar programming language constructs. These languages are democratizing the AI field, enabling even users with little ML and AI experience to easily construct and perform inference on large and complex probabilistic models. The first half of the tutorial will present an overview and history of probabilistic programming, an explanation of how to use probabilistic programming to represent a variety of models, and an explanation of inference and learning algorithms used in probabilistic programming, including the advantages and disadvantages of each and their suitability to different tasks. The second half of the tutorial will be a hands-on, interactive introduction to using probabilistic programming for practical applications. Participants will solve a sequence of real-world problems using the open source Figaro PP system. Due to the mathematical nature of probabilistic programming, participants will be expected to have existing knowledge in calculus and comfort with basic concepts related to working with probabilities and statistics.
</p>

<p>
The primary presenter of this tutorial is Avi Pfeffer, who will be assisted by Brian Ruttenberg.
</p>

