<img src="http://www.aaai.org/Conferences/AAAI/2015/Photos/ruttenberg.jpg" alt="Brian Ruttenberg" width="50" height="65" class="img1" />Brian Ruttenberg is a scientist at Charles River Analytics. His primary area of expertise is machine learning, data mining, and probabilistic modeling. He received a B.S.E. in computer engineering from the University of Michigan, Ann Arbor and a Ph.D. in computer science from the University of California, Santa Barbara.
