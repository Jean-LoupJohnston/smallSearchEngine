<h3 class="text2"><i><a name="MP3">MP3:</a></i>
Robot Learning from Demonstration</h3>
<p><b>
Scott Niekum, Sonia Chernova, and Andrea Thomaz</b></p>

<p>
Robot learning from demonstration (LfD) is a growing area of research that aims to provide a natural mechanism for programming robots and providing human insight into tasks. This tutorial will focus on five main areas of LfD that will provide an overview of the full learning from demonstration lifecycle: providing input to the robot, low-level trajectory learning, high-level skill and task learning, refinement of performance, and evaluation / user studies. A background in basic machine learning techniques (supervised learning, graphical models, optimization) may help in understanding aspects of some of the algorithms, but is not required, as a conceptual overview will be provided.
