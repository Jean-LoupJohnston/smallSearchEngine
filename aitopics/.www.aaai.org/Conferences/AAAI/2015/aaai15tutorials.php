<h3 class="text2"><i><a name="SP4">SP4:</a></i>
Human-in-the-Loop Planning and Decision Support</h3>
<p><b>
Subbarao Kambhampati and Kartik Talamadupula</b></p>

<p>
Endowing an automated agent with the ability to "plan" &mdash; that is, converting its high-level goals into an executable course of action &mdash; has been a long-standing quest in AI. For much of the history of automated planning, the dominant research theme has been efficient synthesis of plans under increasingly expressive system dynamics (classical, temporal, stochastic, and so on).
</p>

<p>
An implicit assumption underlying this research has been that the planner's responsibilities start with taking a complete specification, and end with giving out a complete course of action. This assumption is no longer valid when humans are part of the decision making loop, as is the case in an increasing number of decision support and human-machine teaming scenarios.
</p>

<p>
In this tutorial, we will identify the research challenges in human-in-the-loop planning, including the need to interpret the goals/intentions of the humans in the loop, the need to support continual planning and replanning, the need to unobtrusively support team-decision making, and above all the need to handle pervasive incompleteness in the domain models as well as problem specification. We will then survey current solutions to these problems grounded in the context of human-robot teaming, decision support systems and crowd-sourced planning.
</p>

<p>
<img src="http://www.aaai.org/Conferences/AAAI/2015/Photos/kambhampati.jpg" alt="Subbarao Kambhampati" width="65" height="65" class="img1" />Subbarao Kambhampati is a professor of computer science at Arizona State University. He is an AAAI Fellow and president-elect, and IJCAI trustee, AAAI 2005 cochair, and IJCAI 2016 program chair. He has presented several well-attended and successful tutorials in past AAAIs.
</p>

<p>
<img src="http://www.aaai.org/Conferences/AAAI/2015/Photos/talamadupula.jpg" alt="Kartik Talamadupula" width="65" height="65" class="img1" />Kartik Talamadupula is completing his doctoral degree at Arizona State University before joining IBM's T.J. Watson Research Center as a research staff member. His research focuses on automated planners in integrated, human-in-the-loop AI systems. He is a recipient of the SFAz Fellowship and a University Graduate Fellowship.
</p>


