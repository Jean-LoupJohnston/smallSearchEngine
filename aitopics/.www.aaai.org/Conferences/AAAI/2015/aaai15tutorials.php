<h3 class="text2"><i><a name="MA2">MA2:</a></i>
Constraint-Based Temporal Reasoning</h3>
<p><b>
Roman Bart&aacute;k, Robert A. Morris, and K. Brent Venable</b></p>

<p>
The representation of temporal information, and reasoning about time, are important in artificial intelligence. Reasoning about time plays a critical role in building automated planning and scheduling systems, where causal and temporal relations are the most critical concepts. The tutorial introduces to core concepts of constraint-based temporal reasoning. In particular, it will describe the classical temporal models, both qualitative and quantitative, and relevant reasoning (consistency) techniques, and it will cover extensions of these models towards representing preferences and uncertainty. Example applications will also be listed.
</p>

<p>
The tutorial is targeted especially to PhD students and researchers that need to employ temporal reasoning in their systems. No specific prior knowledge is required beyond the basic understanding of logic and algorithms.
</p>

<p>
The tutorial is based on <i>An Introduction to Constraint-Based Temporal Reasoning</i> published by Morgan &amp; Claypool.
</p>

<p>
<img src="http://www.aaai.org/Conferences/AAAI/2015/Photos/bartak.jpg" alt="Roman Bartak" width="49" height="65" class="img1" /><a href="http://ktiml.mff.cuni.cz/~bartak/temporal/" rel="nofollo">Roman Bart&aacute;k</a> is a professor at Charles University, Prague (Czech Republic). His work focuses on techniques of constraint satisfaction and their application to planning and scheduling. His research results are used in products of ILOG/IBM, Visopt, and ManOPT/Entellexi. He is author of the On-Line Guide to Constraint Programming.
</p>

<p>
<img src="http://www.aaai.org/Conferences/AAAI/2015/Photos/morris.jpg" alt="Robert Morris" width="50" height="65" class="img1" />Robert Morris is a senior researcher in computer science in the Exploration Technology Directorate, Intelligent Systems Division at NASA Ames Research Center. His primary professional goal is the application of advanced AI technology in planning, scheduling and plan execution to the next generation of NASA's exploration systems. His primary research interests include temporal constraint-based reasoning for automated planning and scheduling.
</p>

<p>
<img src="http://www.aaai.org/Conferences/AAAI/2015/Photos/venable.jpg" alt="K. Brent Venable" width="50" height="65" class="img1" />K. Brent Venable currently holds a joint appointment as associate professor of computer science at Tulane University and research scientist at IHMC. Her main research interests are within artificial intelligence and regard, in particular, constraints, preferences, temporal reasoning and computational social choice. In particular, her research is dedicated to providing a solid framework for the design and deployment of intelligent systems able to reason about preferences. Before joining Tulane and IHMC, she was a tenured assistant professor of computer science at the University of Padova, Italy. She received her doctorate in computer science and also the Laurea Magna cum Laude in Mathematics from University of Padova. Venable is the coauthor of <i>A Short Introduction to Preferences: Between Artificial Intelligence and Social Choice,</i> (Morgan &amp; Claypool 2011), and<i> An Introduction to Constraint-Based Temporal Reasoning,</i> (Morgan &amp; Claypool 2014) and has published over several journal and conference papers. Venable maintains a lively collaboration with several research centers, among which are NICTA and NASA Ames.
