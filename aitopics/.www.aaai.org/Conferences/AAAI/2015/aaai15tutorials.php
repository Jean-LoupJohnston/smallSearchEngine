<h3 class="text2"><i><a name="MA3">MA3:</a></i>
RoboCup</h3>
<p><b>
Dan Lee, Claude Sammut, and Luca Iocchi</b></p>

<p>
Scientific competitions in different areas of artificial intelligence and robotics are more and more common, and nowadays almost any research area has some form of competition. Among all these competition efforts, RoboCup is the widest one in terms of number of participants every year and one of the oldest one.
</p>

<p>
The tutorial will provide an overview of methods used in the RoboCup competitions in recent years. RoboCup challenges span a wide range of robotic scenarios, including autonomous soccer, search and rescue, and working in home and office environments. The speakers of this tutorial will demonstrate methods and algorithms that have been successfully deployed in these scenarios as well as discuss ongoing and future areas of interest.
</p>

