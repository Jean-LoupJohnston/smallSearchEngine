<h3 class="text2"><i><a name="MP2">MP2:</a></i>
Generalizing Optimization to Equilibration: A New Foundation for AI in the 21st Century</h3>
<p><b>
Sridhar Mahadevan</b></p>

<p>
Much work in AI -from machine learning and natural language processing, to planning, perception, and robotics - is based on optimization. While this foundation has proved useful, the rapidly changing nature of computation and data, may limit the success of classical optimization, as data is increasingly stored heterogeneously in the "cloud", and "crowd" and "cloud" based computation requires balancing multiple competing objectives, such as privacy, reliability and security, across heterogeneous computational actors. The aim of this tutorial is to present a new theoretical foundation for AI, based on a framework called equilibration that elegantly generalizes optimization. The equilibration framework is based on the mathematical formalisms of variational inequalities (VIs) and projected dynamical systems (PDS). Finite-dimensional VIs generalize (convex) optimization, and unifies many disparate areas of significant practical importance, ranging from equilibrium problems in economics, game theory, evolutionary dynamics, linear and nonlinear complementarity problems, network and traffic flow, saddle-point problems, and solutions of nonlinear equations. PDS yield a powerful microscope to examine the dynamic processes that lead to equilibria. The goals of the proposed tutorial are to present the mathematical formalisms of VIs and PDS, and show their utility in diverse practical applications of AI of growing importance in the 21st century.
</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2015/Photos/mahadevan.jpg" alt="Sridhar Mahadevan" width="46" height="65" class="img1" />
Sridhar Mahadevan is a professor and Director of the Autonomous Learning Laboratory at the School of Computer Science, University of Massachusetts, Amherst. He was elected AAAI Fellow in 2014 for "significant contributions to machine learning." He was an invited speaker at AAAI 2005, and taught a tutorial at AAAI 2007.
</p>


