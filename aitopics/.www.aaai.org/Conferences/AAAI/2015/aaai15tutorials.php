<h3 class="text2"><i><a name="MA4">MA4:</a></i>
Submodularity in Machine Learning Applications</h3>
<p><b>
Jeff Bilmes</b></p>

<p>
Submodular functions are receiving increasing attention in the machine learning community owing to their natural and wide ranging applicability and also to their attractive computational properties. In this tutorial, our goal will be to get a good intuitive sense for what submodular functions are and where they can be usefully applied. We will start by defining submodularity and polymatroidality &mdash; we will survey a surprisingly diverse set of functions that are submodular and operations that (sometimes remarkably) preserve submodularity. We'll also define a class of combinatorial optimization problems that use submodularity, and briefly discuss their complexities, and how the submodularity property is what makes the problems approximable in polynomial time. Next, we'll discuss how submodularity is applied in machine learning. We will see how
submodularity can be a useful model of diversity, coverage, and information, and how this can help applications such as document and image summarization, feature selection, data subset selection and sensor placement, and social network influence and viral marketing. We'll also learn how submodularity is a good model for roughness,
cooperatively interacting costs, and complexity, and how this can help applications such as image segmentation in computer vision, inference in high tree-width graphical models, structured convex norms and regularizers, and active / semisupervised learning.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2015/Photos/bilmes.jpg" alt="Jeff Bilmes" width="52" height="65" class="img1" />Jeff A. Bilmes is a professor at the Department of Electrical Engineering at the University of Washington, Seattle and an adjunct professor in the Department of Computer Science and Engineering and also the Department of Linguistics. He received his Ph.D. in computer science from the University of California in Berkeley. He is a 2001 NSF Career award winner, a 2002 CRA Digital Government Fellow, a 2008 NAE Gilbreth Lectureship award recipient, and a 2012/2013 ISCA Distinguished Lecturer. His primary interests lie in signal processing for pattern classification, speech recognition, language processing, bioinformatics, machine learning, graphical models, submodularity in combinatorial optimization and machine learning, active and semi-supervised learning, computer vision, and audio/music processing. Bilmes is one of the first to apply submodularity in machine learning, starting back in 2003.
</p>



