<h3 class="text2"><i><a name="SP2">SP2:</a></i>
Data Analytics with Electronic Health Records</h3>
<p><b>
Fei Wang</b></p>

<p>
Health informatics refers to the process of leveraging information technologies to improve the quality of healthcare delivery. Electronic health records (EHR) is one of the most important data carrier for health informatics research. In recent years, the application of data analytics technologies into EHR has aroused considerable interests in both data analysis and medical communities. In this tutorial, I will introduce EHR and typical health informatics problems including predictive modeling, patient similarity evaluation, risk stratification and disease progression modeling. I will also introduce popular analytic methodologies for solving those problems, and point out challenges and future research directions.
</p>

<p>
<img src="http://www.aaai.org/Conferences/AAAI/2015/Photos/wang.jpg" alt="Fei Wang" width="51" height="65" class="img1" />Fei Wang is a research staff member in healthcare analytics research group, IBM T. J. Watson Research Center. He earned his M.S. and Ph. D. degrees from the Department of Automation, Tsinghua University in 2008. Before his current position, he was a postdoc in Department of Statistical Science, Cornell University. His research interests include data mining, machine learning and their applications in healthcare data analytics. He has published over 100 papers on the leading conferences like SIGKDD, SIGIR, ICML, IJCAI, AAAI, SDM, ICDM. He also serves as a referee for many distinguished journals including IEEE TPAMI, IEEE TKDE, DMKD, ACM TKDD and program committee member for many international conferences including KDD, ICDM and SDM. For more details, one can refer to his personal <a href="https://sites.google.com/site/feiwang03/" rel="nofollo">homepage.</a>
