<h3 class="text2"><i><a name="MA1">MA1:</a></i>
AI for Smarter Cities. Hype or Reality? A Study in Dublin, Bologna, Miami, and Rio</h3>
<p><b>
Pascal Hitzler, Freddy Lecue, Raghava Mutharaju, Jeff Z. Pan, and Jiewen Wu</b></p>

<p>
Cities take more and more advantages of information and communications technology (ICT) to better manage their resources and improve the quality of life of its citizens. ICT spans many departments of the cities, from transportation, water, energy to building management and social-care services. AI techniques are getting more and more attraction from cities to represent and organize information, maintain sustainable networks, predict incidents, optimize distribution, diagnose faults, plan routes and organize their infrastructure. Managing traffic efficiently, among many other domains in cities, is one of the key issues in large cities. In the first part of the tutorial, we describe the domains of applications which could benefit from AI techniques, along with introducing the necessary background knowledge. Then we focus on traffic applications, which make use of recent AI research in knowledge representation, logic programming, machine learning, planning, reasoning and optimization. Specifically we go through the next version of scalable AI driven traffic related application where (1) data from a variety of sources is collected, (2) knowledge about traffic, vehicles, citizens, events is represented and (ii) deductive and inductive reasoning is combined for diagnosing and predicting road traffic congestion. Based on these principles, a real-time, publicly available AI system named STAR-CITY was developed. We discuss the results of deploying STAR-CITY, and its related AI technologies in cities such as Dublin, Bologna, Miami, Rio and the lessons learned. The final part of the tutorial aims at discussing future AI opportunities including scalability issues for large cities.
</p>

