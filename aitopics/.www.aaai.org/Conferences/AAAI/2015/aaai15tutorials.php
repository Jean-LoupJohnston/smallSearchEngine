<h3 class="text2"><i><a name="MP1">MP1:</a></i>
Artificial Intelligence and Technological Unemployment</h3>
<p><b>
Moshe Vardi</b></p>

<p>
Over the past decades years artificial intelligence (AI) has made a remarkable progress. While AI has been proven to be much more difficult than believed by its early pioneers, its inexorable progress over the past 50 years suggests that H. Simon was probably right when he wrote in 1956 "machines will be capable ... of doing any work a man can do." We are now forced to consider the possibility that further progress in AI may be the cause of technological unemployment, which is unemployment caused by technological change. I do not expect this to happen in the very near future, but I do believe that by 2045 machines will be able to do if not any work that humans can do, then, at least, a very significant fraction of the work that humans can do. The following question, therefore, seems to be of paramount importance. If machines are capable of doing almost any work humans can do, what will humans do?
</p>

<p>
In this tutorial, I will review the debate about technological unemployment, from the Luddites in the 19th century to the Neo-Luddites of the 21st century. I will then discuss the societal impact of technological unemployment and their ethical dimensions.
</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2015/Photos/vardi.jpg" alt="Moshe Y. Vardi" width="52" height="65" class="img1" />
Moshe Y. Vardi is the George Distinguished Service Professor in Computational Engineering and Director of the Ken Kennedy Institute for Information Technology Institute at Rice University. He is the corecipient of three IBM Outstanding Innovation Awards, the ACM SIGACT Goedel Prize, the ACM Kanellakis Award, the ACM SIGMOD Codd Award, the Blaise Pascal Medal, the IEEE Computer Society Goode Award, the EATCS Distinguished Achievements Award, and the Southeastern Universities Research Association's Distinguished Scientist Award. He is the author and coauthor of over 400 papers, as well as two books: <i>Reasoning about Knowledge</i> and <i>Finite Model Theory and Its Applications.</i> He is a Fellow of the Association for Computing Machinery, the American Association for Artificial Intelligence, the American Association for the Advancement of Science, and the Institute for Electrical and Electronic Engineers. He is a member of the US National Academy of Engineering, the American Academy of Arts and Science, the European Academy of Science, and Academia Europea. He holds honorary doctorates from the Saarland University in Germany and Orleans University in France. He is the Editor-in-Chief of the <i>Communications of the ACM.</i>
</p>



