<h3 class="text2"><i><a name="SA3">SA3:</a></i>
Neuroevolution Reinforcement Learning</h3>
<p><b>
Risto Miikkulainen</b></p>

<p>
In this four-hour tutorial, neuroevolution RL will first be motivated and compared with value-function RL, and neuroevolution is shown to be strong in domains with large state and action spaces and where the state of the world is not fully known. Basic neuroevolution techniques will be reviewed and illustrated with state of the art applications in robotics, game playing, intelligent agents, and multiagent systems. Future opportunities will then be reviewed, focusing in particular on the generative and developmental approaches to neuroevolution, and applications in games and robotics, art and design, and cognitive science.
</p>

