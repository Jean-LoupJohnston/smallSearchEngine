<h3 class="text2"><i><a name="Debate">AAAI-15 Debate</a></i><br />Debate on Autonomous Weapons</h3>

<p><b>
<i>Participants:</i> Ron Arkin (Georgia Institute of Technology) and Stephen Goose (Human Rights Watch)<br />
<i>Moderator:</i> Thomas G. Dietterich (Oregon State University)</b></p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2015/Photos/arkin.jpg" alt="Ronald C. Arkin" width="61" height="75" class="img1" />
Ronald C. Arkin is a regents' professor, director of the Mobile Robot Laboratory, and associate dean for Research in the College of Computing at Georgia Tech. He served as STINT visiting professor at KTH in Stockholm, sabbatical chair at the Sony IDL in Tokyo, and the Robotics and AI Group at LAAS/CNRS in  Toulouse.  His research interests include behavior-based reactive control and action-oriented perception for mobile robots and unmanned aerial vehicles, hybrid deliberative/reactive software architectures, robot survivability, multiagent robotic systems, biorobotics, human-robot interaction, robot ethics, and learning in autonomous systems. His latest book is <i>Governing Lethal Behavior in Autonomous Robots.</i> Arkin serves on numerous journal editorial boards and is the series editor for The MIT Press book series <i>Intelligent Robotics and Autonomous Agents.</i> He has provided expert testimony to the United Nations, the International Committee of the Red Cross, the Pentagon and others on Autonomous Systems Technology. Arkin served on the Board of Governors of the IEEE Society on Social Implications of Technology, the IEEE RAS AdCom, and is founding cochair of IEEE RAS TC on Robot Ethics. He is a distinguished lecturer for the IEEE Society on social implications of technology and a Fellow of the IEEE.
</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2015/Photos/goose.jpg" alt="Stephen Goose" width="61" height="75" class="img1" />
Stephen Goose, is executive director of Human Rights Watch's Arms Division, was instrumental in bringing about the 2008 convention banning cluster munitions, the 1997 treaty banning antipersonnel mines, the 1995 protocol banning blinding lasers, and the 2003 protocol requiring clean-up of explosive remnants of war. He and Human Rights Watch cofounded the International Campaign to Ban Landmines (ICBL), which received the 1997 Nobel Peace Prize. Goose created the ICBL's Landmine Monitor initiative, the first time that non-governmental organizations around the world have worked together in a sustained and coordinated way to monitor compliance with an international disarmament or humanitarian law treaty. In 2013, he and Human Rights Watch cofounded the Campaign to Stop Killer Robots. Before joining Human Rights Watch in 1993, Goose was a US congressional staffer and a researcher at the Center for Defense Information. He has a master's degree in International Relations from the Johns Hopkins School of Advanced International Studies and a BA in History from Vanderbilt University.
</p>

<p>Moderator Thomas G. Dietterich is the president of the Association for the Advancement of Artificial Intelligence.</p>


<h3 class="text2"><i><a name="Getoor">AAAI-15 Invited Talk</a></i><br />
Using Statistics and Semantics to Solve Big (Graph) Data Problems</h3>

<p><b>Lise Getoor (University of California, Santa Cruz)</b></p>

<p>
Big data problems benefit from modeling both structure and uncertainty, so there is a growing need for tools to develop large, complex probabilistic models. These tools should combine high-level knowledge representation with general purpose, scalable algorithms for learning and inference. In this talk, I will survey some of the recent work from the statistical relational learning community on learning and inference in richly-structured, multi-relational network data. I will highlight both important developments and opportunities in which ideas from AI can have great impact on upcoming challenges within the machine learning, data science and data mining communities.
</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2015/Photos/getoor.jpg" alt="Lise Getoor" width="50" height="75" class="img1" />
Lise Getoor is a professor in the Computer Science Department at the University of California, Santa Cruz. Her research areas include machine learning and reasoning under uncertainty, with an emphasis on graph and network data. She is a AAAI Fellow, serves on the Computing Research Association and International Machine Learning Society boards, was cochair of ICML 2011, is a recipient of an NSF Career Award and eight best paper and best student paper awards. She received her PhD from Stanford University, her MS from the University of California, Berkeley, and her BS from the University of California, Santa Barbara, and was a professor at the University of Maryland, College Park from 2001&ndash;2013.
</p>


<h3 class="text2"><i><a name="Bowling">AAAI-15 Invited Talk</a></i><br />
von Neumann's Dream</h3>

<p><b>Michael Bowling (University of Alberta)</b></p>

<p>
Chess has long served as the measure of progress for artificial intelligence. However, at the very beginning of computing and artificial intelligence, John von Neumann dreamt of a different game: "Real life is not like [chess]. Real life consists of bluffing, of little tactics of deception, of asking yourself what is the other man going to think I mean to do. And that is what games are about in my theory." The game von Neumann hinted at is poker, and it played a foundational role in his formalization of game theory. Shortly after launching the field of game theory, he practically abandoned his new discipline to focus on the budding field of computing. He saw computers as the way to make his mathematics workable. Now, over 70 years later with both significant advances in computing and game theoretic algorithms, von Neumann's dream is now a reality. Heads-up limit Texas hold'em poker, the smallest variant of poker played by humans, is essentially solved. In this talk, I will discuss how we accomplished this landmark result, along with the substantial scientific advances in our failed attempts along the way.
</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2015/Photos/bowling.jpg" alt="Michael Bowling" width="59" height="75" class="img1" />
Michael Bowling is a professor of computing science at the University of Alberta. His research focuses on artificial intelligence, machine learning, and game theory; and he is particularly fascinated by the problem of how computers can learn to play games through experience. Bowling is the leader of the Computer Poker Research Group, which has built some of the strongest poker playing programs in the world. In 2008, one of these programs, Polaris, defeated a team of top professional poker players in two-player, limit Texas Hold'em, becoming the first program to defeat poker pros in a meaningful competition.  His research has been featured on the television programs <i>Scientific American Frontiers, National Geographic Today,</i> and <i>Discovery Channel Canada;</i> in the <i>New York Times</i> and <i>Wired;</i> and twice in exhibits at the Smithsonian Museums in Washington, D.C.
</p>


