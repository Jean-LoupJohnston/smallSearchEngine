<h3 class="text2"><i><a name="Hinton">AAAI-15 Invited Talk</a></i><br />
Deep Learning
</h3>

<p><b>Geoffrey Hinton (University of Toronto and Google Inc.)</b></p>

<p>
I will give a brief history of deep learning explaining what it is, what kinds of task it should be good for and why it was largely abandoned in the 1990's. I will then describe how ideas from statistical physics were used to make deep learning work much better on small datasets. Finally I will describe how deep learning is now used by Google for speech recognition and object recognition and how it may soon  be used for machine translation.
</p>

<p>
<p><img src="http://www.aaai.org/Conferences/AAAI/2015/Photos/hinton.jpg" alt="Geoffrey Hinton" width="50" height="75" class="img1" />Geoffrey Hinton received his PhD in artificial intelligence from Edinburgh in 1978.  He spent five years at Carnegie-Mellon. In 1987 he moved to the Department of Computer Science at the University of Toronto. Since 2013, he has been splitting his time between the University of Toronto and Google. He was one of the researchers who introduced the back-propagation algorithm. His other contributions to neural network research include Boltzmann machines, distributed representations, time-delay neural nets, mixtures of experts, variational learning, products of experts and deep belief nets. His students used deep learning to change the way in which speech recognition and object recognition are done.
</p>


<h3 class="text2"><i><a name="Ghani">IAAI-15 Invited Talk</a></i><br />Data Science for Social Good: Using Your Powers To Make a Social Impact!
</h3>

<p><b>Rayid Ghani (University of Chicago)</b></p>

<p>The past few years have seen an increasing demand for machine learning/data mining/data science powers. That's wonderful for us data scientists but wouldn't the world be so much better if we also used our computational and analytical powers for social good? In this talk, I'll give examples from work going on around the world including from the summer fellowship program we started at University of Chicago on Data Science for Social Good to show that there are a lot of important social problems in the world that could use our help &mdash; from helping students graduate high school to helping disaster victims to improving health.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2015/Photos/ghani.jpg" alt="Rayid Ghani" width="55" height="75" class="img1" />Rayid Ghani is the director of the Center for Data Science and Public Policy at the University of Chicago. He is also the cofounder of Edgeflip, an analytics and social media startup that is focused on helping nonprofits and social causes. Previously, Ghani was the chief scientist for the Obama 2012 election campaign focusing on analytics, data, and technology.</p>

<p>Ghani is focused on education, research, and developing approaches to use data and analytics for social causes, both with Edgeflip and the University of Chicago.He created and runs the Eric and Wendy Schmidt Data Science for Social Good Summer Fellowship, which brings together aspiring data scientists to work on data science projects in partnership with governments and nonprofits.
</p>

<h3 class="text2"><i><a name="Sellmann">AAAI-15 Invited Talk</a></i><br />Intelligent Decisions
</h3>

<p><b>Meinolf Sellmann (IBM Thomas J. Watson Research Center)</b></p>

<p>At its best intelligence creates aesthetic and beauty, yet from a utilitarian perspective intelligence primarily serves the purpose of making better decisions. Today's prescriptive decision support systems are most effective when applied to specific recurring operational problems. Recent advances in AI technology have revived the vision of commercially viable cooperative strategic decision support systems. These cognitive systems integrate information retrieval, knowledge representation, interactive modelling, as well as social and self-learning capabilities with logic reasoning and probabilistic decision making under uncertainty. I provide a snapshot of the current technology status by showcasing several projects that ultimately aim at intelligent human-in-the-loop decision making.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2015/Photos/sellmann.jpg" alt="Meinolf Sellmann" width="56" height="75" class="img1" />Meinolf Sellmann is manager of the AI for optimization group in the cognitive computing department at IBM Watson Research. He received his doctorate degree from Paderborn University from where he went on to Cornell University as postdoctoral associate and Brown University as assistant professor. Sellmann has published over 60 articles in international conferences and journals with central contributions in symmetry breaking, global constraints, the integration of mathematical and constraint programming, search, autonomous algorithm configuration, and algorithm portfolios. He served as conference chair of CP 2007, PC chair of CPAIOR 2013, and associate editor of the Informs Journal on Computing. He received an NSF Early Career Award in 2007 and IBM Outstanding Technical Innovation Awards in 2013 and 2014. Based on their meta-algorithmic research, for four years in a row Sellmann and his team won at international SAT and MaxSAT solver competitions, among others for the overall most efficient parallel SAT Solver in 2013, and three first places at the 2014 MaxSAT Evaluation.</p>


