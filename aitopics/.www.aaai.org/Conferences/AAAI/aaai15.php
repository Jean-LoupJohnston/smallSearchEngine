<h2>The First Winter AAAI &mdash; January 25&ndash;30, 2015, 
Austin Texas, USA</h2>
<p><a href="http://videolectures.net/aaai2015_austin/" rel="nofollow">Selected lectures and programs from this conference have been posted at VideoLectures.Net.</a></p>

<p>The Twenty-Ninth AAAI Conference on Artificial Intelligence (AAAI-15) 
will be held January 25&ndash;30, 2015 at the Hyatt Regency in Austin, 
Texas, USA. The purpose of this conference is to promote research in 
artificial intelligence (AI) and scientific exchange among AI 
researchers, practitioners, scientists, and engineers in affiliated 
disciplines. AAAI-15 will have a diverse technical track, student 
abstracts, poster sessions, invited speakers, tutorials, workshops, 
and exhibit/competition programs, all selected according to the 
highest reviewing standards. AAAI-15 welcomes submissions on 
mainstream AI topics as well as novel crosscutting work in related 
areas.</p>
<p><b>Download the <a href="http://www.aaai.org/Conferences/AAAI/2015/aaai15brochure.pdf">AAAI-15 Flyer!</a></b></p>
<p>Austin, the Live Music Capital of the World, and the state capital of 
Texas, is also home to one of the largest and oldest communities of 
artificial intelligence researchers in the world, with more than a 
dozen AI-related labs at the University of Texas at Austin. Austin is 
filled with music venues, with nearly 200 venues that feature 
everything from rock and blues to country and jazz every night of the 
week. Austin cuisine has expanded from barbecue and Tex-Mex to 
award-winning and inventive international cuisine, and blossomed 
beyond brick-and-mortar restaurants to a vibrant, citywide food truck 
movement. Food-court trailer parks are an Austin phenomenon in which 
multiple trailers cluster to create a dining destination. As the 
state capital, Austin is also home to a wonderful ballet, world-class 
museums, one-of-a-kind shopping and beautiful outdoor spaces.</p>


