<h4 class="text2">Important Dates</h4>
<ul>
	<li>September 15, 2015: Paper submission deadline</li>
	<li>November 6, 2015: Notification of acceptance or rejection</li>
	<li>December 1, 2015: Camera-ready copy due at AAAI office</li>
</ul>
<p><a href="http://www.aaai.org/Conferences/AAAI/2016/eaai16assignments.php">
Model AI Assignments Call for Assignments
</a></p>

<div class="hr1">
<hr />
</div>

<p>
EAAI-16 provides a venue for researchers and educators to discuss 
pedagogical issues and share resources related to teaching AI and 
using AI in education across a variety of curricular levels (K-12 
through postgraduate training), with an emphasis on undergraduate and 
graduate teaching and learning. The symposium seeks contributions 
showing how to more effectively teach AI, as well as how themes from 
AI may be used to enhance education more broadly (for example, in 
introductory computing courses or as a means for teaching 
computational thinking). We encourage the sharing of innovative 
educational approaches that convey or leverage AI and its many 
subfields, including robotics, machine learning, natural language 
processing, computer vision, and others.
</p>

<p>
EAAI-16 provides several submission types:
</p>

<ul>
	<li>full-length papers (6 pages)</li>
	<li>extended abstract/poster contributions (2 pages)</li>
	<li>Model AI Assignments highlighting innovative ready-to-adopt materials</li>
</ul>

<p>
In addition, EAAI-16 provides many other paths for participation 
including a workshop for mentoring new faculty, instructors, and 
graduate students on teaching and an Educational Video Track within 
the AAAI-16 Video program.
</p>

<p>
EAAI-16 welcomes paper submissions on a variety of topics, including, 
but not limited to the following:
</p>

<ul>
	<li>Educational resources, including pedagogical strategies, innovative assignments and curricular development related to AI</li>

	<li>Multi-disciplinary curriculum efforts highlighting the application of AI in other contexts (computational biology, algorithmic game theory, computational economics, etc.) and/or foundational concepts of AI in other fields (philosophy, cognitive science, linguistics, psychology, etc.)</li>

	<li>The use of robotics and other tangible media both in AI courses and elsewhere in the curriculum</li>

	<li>Software that assists the teaching/learning process &mdash; everything from software to help visualize search spaces and search algorithms, to software substrates that can be used by students to do projects</li>

	<li>Resources and strategies for teaching specific AI sub-areas or topics: machine learning, robotics, computer vision, natural language processing, game playing, etc.</li>

	<li>Strategies for situating AI within a wider CS curriculum</li>

	<li>Ways to incorporate popular entertainment and media portrayal of AI (in movies, news, advertisements, new products, etc.)</li>

	<li>Real-world examples of AI deployments, described in sufficient detail to provide case studies and/or serve as useful springboards for other educators</li>

	<li>Innovative means for integrating research as part of coursework in AI</li>

	<li>Material for teaching ethical considerations with regard to AI technology</li>
</ul>

<p>
Full-length submissions to EAAI-16 should describe well-developed 
ideas and/or pedagogical examples, and in most cases should include 
an evaluation of the work. Extended abstract/poster submissions may 
highlight preliminary or ongoing work. Papers submitted to EAAI-16 
should not substantially overlap with papers that have been 
previously published or are under review elsewhere.
</p>

<h4><a href="http://www.aaai.org/Conferences/AAAI/2016/eaai16assignments.php">EAAI-16 Model AI Assignments Session</a></h4>

<p>
EAAI-16 will feature a special session on Model AI Assignments 
suitable for use in a variety of class settings. For more information, see the the <a href="http://www.aaai.org/Conferences/AAAI/2016/eaai16assignments.php" rel="nofollow">Call for 
Assignments for the Model AI Assignments special session.</a>
</p>

<h4>EAAI-16 Hands-On Robotics Education Session</h4>

<p>
In this session, participants will try out some of the most 
accessible and capable hardware and software resources used in AI 
Robotics education today.  A team of AI robotics educators will 
scaffold, demonstrate, and then guide participants to accomplish 
short tasks; several different platforms will be available (wheeled, 
aerial, pan/tilt sensing), and we will also place the robots and 
software in the context of several field-tested, 
undergraduate-focused curricula.
</p>

<h4>EAAI-16 Relevant Topics for a Modern AI Course</h4>

<p>
There is a disparity between what is taught in many AI courses and 
what education and research colleagues would like to have taught. The 
aim of this panel is to propose a set of topics most appropriate for 
a modern introductory AI course.
</p>

