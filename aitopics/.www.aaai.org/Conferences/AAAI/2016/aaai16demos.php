<h2 class="backgrnd6"><span class="text1">AAAI-16 Demonstrations Program</span></h2>
<p>The Thirtieth Conference on Artificial Intelligence
February 1&ndash;17, 2016, Phoenix, Arizona, USA</p>
<div class="hr1">
<hr />
</div>

<h3 class="text2"><a href="https://www.easychair.org/conferences/?conf=aaai16" rel="nofollow">AAAI-16 Demos Submission Site</a></h3>
<div class="hr1">
<hr />
</div>
<h3 class="text2"><a href="http://www.aaai.org/Publications/Templates/AuthorKit16.zip">2016 AAAI Author Kit</a></h3>

<div class="hr1">
<hr />
</div>
<h4 class="text2">Timetable for Authors</h4>

<ul>
<li>July 15, 2015 - September 15, 2015: Authors register on AAAI-16 </li>
<li>submission site</li>
<li>September 15, 2015: Submissions of expression of interest</li>
<li>September 29, 2015: Notification of acceptance or rejection</li>
<li>October 20, 2015: Short papers due</li>
<li>November 10, 2015: Feedback/reviews to authors</li>
<li>December 1, 2015: Camera-ready copy due at AAAI office</li>
</ul>
    
    <h4 class="text1">Call for Demonstrations</h4>

<p>The AAAI-16 Demonstrations Program is intended to foster discussion 
and exchange of ideas among researchers and practitioners from 
academe and industry by presenting software and hardware systems and 
research prototypes of such systems, including their capabilities and 
workings. Accepted demonstrations will be allocated one time slot of 
1.5 or 2 hours during one of the main conference evening poster 
programs, and will have a short paper included in the proceedings. 
Robot hardware and software can also be showcased in the AAAI-16 
Robotics Exhibition Program; please check the website for details on 
registration. (The Demonstration Program is not meant for commercial 
purposes, since AAAI-16 has separate exhibits for these purposes.) 
The AAAI-16 Demonstration Program is complementary to the AAAI-16 
Robotics Exhibition Program and potential exhibitors may wish to 
pursue both; note that the AAAI-16 Robotics Exhibition Program 
provides exhibit space during the main conference days with the 
expectation that the exhibit will be showcased for that full period.</p>



<h3>Topics of Interest</h3>
<p>
The Demonstration Program welcomes presentations of systems on all topics of mainstream 
AI (see the <a href="aaai16call.php">topic descriptions</a> of the main AAAI and IAAI conference tracks). Promising topics include, but are not limited to, the following:</p>
<ul>
	<li> cognitive systems</li>
	<li>    decision making systems</li>
	<li>    educational software and hardware tools for AI</li>
	<li>    environment-aware agent behaviours</li>
	<li>    game playing programs</li>
	<li>    human-robot interaction</li>
	<li>    intelligent collaborative systems</li>
	<li>    intelligent control systems</li>
	<li>    intelligent graphical user interfaces</li>
	<li>    intelligent tutoring systems</li>
	<li>    natural language processing and speech recognition</li>
	<li>    peripheral hardware running on dedicated devices</li>
	<li>    robots</li>
	<li>    visual processing</li>
	<li>    web services</li>
</ul>


<p>
Submissions from everyone, including authors of paper submissions to 
AAAI, IAAI, and AAAI-16 workshops, are encouraged. The systems do not 
need to run solely at the conference site nor must they be mature or 
deployed. Captivating demonstrations will be given an advantage in 
the selection process.</p>

<h4>Relationship with Other AAAI-16 Events</h4>
<p>
<i>Participants in the AAAI-16 Robotics Exhibition and the AAAI-16 
Video Competition are encouraged to contribute to the Demonstration 
Program with their systems as well, if appropriate</i>. Demonstrations 
related to video submissions will be publicized in the video 
proceedings and during the award ceremony, thereby increasing the 
visibility of the systems.
</p>

<h4>Author Registration</h4>
<p>
Authors must register at the AAAI-16 submission site before 
submitting their expressions of interest. Authors should select "Demo 
Track." The software will assign a password, which will enable the 
author to log on and submit an expression of interest. Authors are 
strongly encouraged to register as soon as possible, well in advance 
of the September 15 deadline.
</p>


<ul>
	<li>Registration will close at 11:59 PM PST on September 10, 2015</li>
	<li>Abstract (expression of interest) submission will close at 11:59 PM PST on September 15, 2015</li>
	<li>Final 2-page paper submission will close at 11:59 PM PST on October 20, 2015</li>
</ul>


<h4>Submission and Review Process</h4>
<p>
The submission procedure is a two-step process. First, researchers 
and practitioners interested in submitting a demo to the AAAI-16 
conference are required to submit an "expression of interest" through 
the web-based conference management system in the abstract field. An 
expression of interest consists only of a short description 
introducing the demonstration and clearly specifying the authors, 
purpose, and the material needed to perform the demonstration. The 
expression of interest may be accompanied by supporting material, 
such as a video or a previously published paper, as long as it is 
specifically related to the demonstration (additional materials will 
not be published in the proceedings). Please submit such additional 
items as a zip file.
</p>

<p>
Second, the expression of interest will be evaluated by the 
Demonstration Program Chair to evaluate its fit with the 
Demonstration Program. Upon acceptance, authors will be required to 
submit a 2-page short paper describing their system, formatted in 
AAAI two-column, camera-ready style through the AAAI-16 submission 
system. Papers must be original, unpublished work, and authors will 
be required to transfer copyright of their paper to AAAI. For more 
information on the AAAI style, please download the AAAI Press author 
kit. The Demonstration chair retains the right to reject 
demonstrations if the submitted 2-page paper has deficiencies that 
the reviewers believe would be difficult to fix by the authors within 
the given time frame.
</p>

<p>
Demonstration participants are welcome to exhibit and distribute 
additional material, including, for example, videos running on a 
dedicated console, scientific papers, pamphlets, noncommercial 
brochures, and so on. Such ancillary materials do not necessarily 
need to be submitted as part of the evaluation.
</p>

<p>
At least one member of every demonstration must register for and 
attend AAAI-16. The conference registrant will be responsible for 
building the exhibit.
</p>

<p>
<i>AI Magazine</i> has expressed interest in publishing a joint paper by the 
exhibitors of the AAAI-16 Demonstration Program focusing on the demos 
exhibited, including claims, strengths, and weaknesses. The purpose 
of the paper is to offer a rich discussion about the current state of 
the art in AI.
</p>


<h4>Award</h4>
<p>To foster the presentation of exciting demonstrations, AAAI-16 will 
present an award for the Best Demonstration. The award will be 
granted on the the basis of audience evaluation through a ballot 
given to all attendees. </p>

<h4>Services</h4>
<p>
Researchers and practitioners showing a demonstration are required to 
bring all necessary equipment and material for the successful 
operation of their exhibit. AAAI will provide shipping instructions, 
if needed, for the delivery of material to the conference site. Each 
demo program participant will be provided with one 6-foot draped 
table, one monitor, and one poster board, as well as electrical and 
internet access. Special electrical connections must be arranged 
through the Demonstration Program Chair no later than 45 days prior 
to the start of the conference.
</p>


