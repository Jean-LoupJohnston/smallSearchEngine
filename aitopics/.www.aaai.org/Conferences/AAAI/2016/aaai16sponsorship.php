<h2 class="backgrnd12"><span class="text1">AAAI-16 Sponsorship Program</span></h2>

<h3 class="text2"><a href="http://www.aaai.org/Conferences/AAAI/2016/aaai16sponsorship.pdf">Sponsorship Brochure and Contract</a></h3>
<div class="hr1">
<hr />
</div>


<p>Phoenix, Arizona, USA<br />
February 12&ndash;17, 2016
</p>

<p>On behalf of the Conference Committee of the Thirtieth AAAI Conference on Artificial Intelligence, we invite you to participate in the sponsor program for AAAI-16. The conference will be held February 12-17, 2016 in Phoenix, Arizona, USA, and is sponsored by the Association for the Advancement of Artificial Intelligence (AAAI). The site of the conference will be the Phoenix Convention Center. Phoenix, the gateway to the Grand Canyon with a rich history puebloans, ranchers, miners, and visionaries, is also adjacent to Tempe, Arizona, the site of Arizona State University, home to a leading AI research community.</p>

<p>AAAI is the annual North American artificial intelligence conference, attracting about 1,300 AI researchers and practitioners, representing government, university and corporate research facilities from throughout the world. Your participation in the AAAI-16 Sponsor Program will give you instant visibility to this diverse group of AI professionals, representing a host of research areas such as robotics, AI and the web, cognitive systems, AI applications, computational sustainability and AI, human computation, integrated systems, interactive digital entertainment, education, and recommender systems.</p>

<p>As part of most levels of sponsorship, you will be provided with a table-top exhibit throughout the technical portion of the conference. This heightened visibility will provide an opportunity for one-on-one exchange with conference participants. Whether you want to showcase your intelligent technology to the AI community, or tap into this incredible source of talent, AAAI-16 is the premier venue to accomplish these goals.</p>

<h3 class="text2">Named Events</h3>
<p>
The conference includes several major events that can be named in honor of major contributors. These naming opportunities bring very high visibility but are limited in number. We urge companies considering this level of support to contact AAAI as soon as possible. Preference will be given to companies who commit early and/or commit to support the event for multiple years. See the next section for the additional benefits of Platinum, Gold, and Silver sponsorships.
</p>

<ul>
<li><i>Opening Reception:</i> $25,000. Includes a complimentary Platinum sponsorship.</li>
<li><i>Poster Session Reception:</i> $15,000. Includes a complimentary Gold sponsorship.</li>
<li><i>Distinguished Lecture: </i>$10,000 (per lecture). One of the lectures by a distinguished invited speaker will be named after your organization. Includes a complimentary Silver sponsorship.</li>
<li><i>WiFi Internet Access:</i> $10,000. Includes a complimentary Silver sponsorship.</li>
</ul>

<h3 class="text2">Sponsorship Levels and Benefits</h3>

<p>
Each AAAI-16 Sponsor will receive their company name and/or logo prominently displayed on the following:
</p>

<ul>
<li>Printed materials, as schedule permits</li>
<li>AAAI-16 website</li>
<li>AAAI-16 registration area sign</li>
<li>Signs at appropriate conference events</li>
</ul>

<p>
Certain levels of sponsorship also include the following:
</p>
<ul>
<li>Complimentary conference registration(s)</li>
<li>Exhibit tabletop(s) in conference center foyer</li>
<li>Sponsor-supplied inserts in conference registration packets</li>
</ul>

<h3 class="text2">Platinum ($25,000)</h3>
<p>
4 Complimentary registrations; 2 exhibit table-tops; a registration packet insert of up to 4 pages
</p>

<h3 class="text2">Gold ($15,000)</h3>
<p>
3 Complimentary registrations; 2 exhibit table-tops; a registration packet insert of up to 2 pages
</p>

<h3 class="text2">Silver ($10,000)</h3>
<p>
2 Complimentary registrations; 1 exhibit table-top; a registration packet insert of up to 1 page
</p>

<h3 class="text2">Bronze ($5,000)</h3>
<p>
1 Complimentary registration; 1 exhibit table-top
</p>

