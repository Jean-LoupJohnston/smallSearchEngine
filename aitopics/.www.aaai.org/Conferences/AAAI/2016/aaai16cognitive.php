<h2 class="backgrnd6"><span class="text1">AAAI-16 Special Track on Special Track on Cognitive Systems</span></h2>
<p><i>The Thirtieth Conference on Artificial Intelligence</i><br />
February 12&ndash;17, 2016, Phoenix, Arizona, USA
</p>
<h4 class="text1">Call for Papers</h4>
<div class="hr1">
<hr />
</div>

<h3 class="text2"><a href="https://www.easychair.org/conferences/?conf=aaai16" rel="nofollow">AAAI-16 Special Track on Cognitive Systems Paper Submission Site</a></h3>
