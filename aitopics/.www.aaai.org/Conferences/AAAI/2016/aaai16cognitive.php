<h4 class="text2">Timetable for Authors</h4>

<ul>
	<li>July 1, 2015 &ndash; September 10, 2015: Authors register on the AAAI web site</li>
	<li>    September 10, 2015: Electronic abstracts due</li>
	<li>    September 15, 2015: Electronic papers due</li>
	<li>    October 28-30, 2015: Author feedback about initial reviews</li>
	<li>    November 12, 2015: Notification of acceptance or rejection</li>
	<li>    December 1, 2015: Camera-ready copy due</li>
</ul>
<p>For additional information, please consult the <a href="aaai16call.php">main call for papers.</a></p>
<div class="hr1">
<hr />
</div>

<p>
In an attempt to return to the original goals of artificial intelligence and cognitive science, this special track invites papers in human-level intelligence, integrated cognition, cognitive architectures, and related areas that aim to explain intelligence in computational terms and reproduce a range of human cognitive abilities in computational artifacts. Critically, the track will focus on cognitive capabilities "in the context of complete artificial cognitive systems," hence papers on individual component algorithms will only be accepted if they discuss these algorithms and their functions in conjunction with the rest of the integrated artificial cognitive system. Possible topics include the following, as well as other related integrated cognitive functions and issues:</p>
<ul>
	<li>Conceptual inference and reasoning</li>
	<li>Efficiency and real-time performance</li>
	<li>Introspection and meta-cognition</li>
	<li>Learning and knowledge capture</li>
	<li>Memory storage and retrieval</li>
	<li>Methods of integration</li>
	<li>Motivation and emotion</li>
	<li>Natural language understanding and dialogue</li>
	<li>Perception, imagery and motor control</li>
	<li>Problem solving and decision making</li>
	<li>Social cognition and interaction</li>
</ul>

<p>
Papers will be reviewed by qualified reviewers drawn from a special track committee, with general expertise in artificial cognitive systems and specific expertise in at least one of the above areas. We encourage authors to clearly explain how their paper fits into the Special Track on Cognitive Systems and how it answers the call for papers. In cases where a submitted paper is not deemed relevant, it may be considered for review for another special track or the general technical papers track at the discretion of the tracks and conference cochairs.
</p>

