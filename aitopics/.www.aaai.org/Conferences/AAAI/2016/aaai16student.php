<h2 class="backgrnd6"><span class="text1">AAAI-16 Student Programs</span></h2>
<p>February 12&ndash;17, 2016, Phoenix, Arizona, USA</p>

<div class="hr1">
<hr />
</div>

<p><b><a href="http://movingai.com/AAAI16/" rel="nofollow">Student Activities Page</a></b></p> 

<p>
AAAI-16 invites submissions to the student abstract and poster program. The goal of this program is to provide a forum in which students can present and discuss their work during its early stages, meet some of their peers who have related interests, and introduce themselves to more senior members of the field. The program is open to all students at the Undergraduate, Masters, and Doctoral levels. Nonstudent advisors or collaborators should be acknowledged appropriately, as coauthors or otherwise. However, students are requested to honor the spirit of the program by submitting only work for which they are primary investigators.
</p>

<p>
The organizers of AAAI-16 are continuing the strong student outreach 
program developed in 2015 &mdash; in particular, making AAAI-16 student 
friendly by arranging a number of activities for students attending 
the conference. Many of these activities are open to broader 
audiences, and so they are also a good way for anyone to expand their 
knowledge of AI. There are many opportunities for both students and 
researchers to be involved. We have highlight several below.
</p>

<p>
Plan now to involve your students in AAAI-16!
</p>
<div class="hr1">
<hr />
</div>



<h4 class="text2"><a name="openhouse">AI Open House</a></h4>

<p>
On Saturday, February 13, we will be hosting an AI open house for 
high-school students in the Phoenix area, the general public, 
graduate and undergraduate students, and established AI researchers. 
We plan to showcase the latest work in many areas of AI, so be sure 
to arrive in time to participate.
</p>
<div class="hr1">
<hr />
</div>


<h4 class="text2"><a name="jobfair">AAAI/ACM SIGAI Job Fair at AAAI-16</a></h4>

<p>
<i>Call for Companies, Research Organizations and Universities to
Participate</i> &mdash; The inaugural AAAI/ACM SIGAI job fair at AAAI-15 drew many participants, and we are happy to continue it at AAAI-16. The AAAI/ACM SIGAI job fair will be held during AAAI-16, on Sunday, February 14 at 6:10 PM at the Phoenix Convention Center, and is a place for students and professionals looking for internships or jobs to meet with representatives from companies and academia in an informal "meet-and-greet" atmosphere. In the beginning of the event, one representative from each organization has the opportunity to introduce the organization and its job opening(s) for about 1 minute. Afterward, all attendees have a chance to mingle.
</p>

<p>
If you are representing a company, research organization or university and would like to participate in the job fair, please send an email with your contact information to &#115;&#107;&#111;&#101;&#110;&#105;&#103;&#64;&#117;&#115;&#99;&#46;&#101;&#100;&#117; by January 20. You are welcome to participate in the job fair even without prior reservation but we will be able to help you advertise your job openings better if we hear from you by the deadline. Participation is free. The organizers of the AAAI/ACM SIGAI Job Fair are Sanmay Das (&#115;&#97;&#110;&#109;&#97;&#121;&#64;&#115;&#101;&#97;&#115;&#46;&#119;&#117;&#115;&#116;&#108;&#46;&#101;&#100;&#117;) and 
Sven Koenig (&#115;&#107;&#111;&#101;&#110;&#105;&#103;&#64;&#117;&#115;&#99;&#46;&#101;&#100;&#117;).
</p>
<div class="hr1">
<hr />
</div>


<h4 class="text2"><a name="bboard">AAAI / ACM SIGAI Job Market Electronic Bulletin Board</a></h4>

<p>
AAAI and ACM SIGAI are pleased to continue the electronic job 
bulletin board at AAAI-16. Companies with job opportunities, as well 
as job seekers, will provide ads to populate an ongoing kiosk 
display. Opportunities to meet with job seekers will be arranged 
dynamically or through employer-sponsored events.
</p>
<div class="hr1">
<hr />
</div>


<h4 class="text2"><a name="tutorials">Tutorials</a></h4>

<p>
AAAI will feature a number of tutorials that are very well suited for 
students who are new to a PhD program or new to AI research in 
general. In particular, there will be a special mentoring tutorial 
specifically designed for PhD students.</p>
<p>The presenters of the following tutorials are putting particular work into making their tutorials accessible to students with limited backgrounds in AI. Each of these will take place during separate time slots over the two days of tutorials.</p>
<ul>
	<li><a href="http://www.aaai.org/Conferences/AAAI/2016/aaai16tutorials.php#SA4">SA4:</a>How to Automatically Machine Read the Web (Estevam Rafael Hruschka Junior)</li>
	<li><a href="http://www.aaai.org/Conferences/AAAI/2016/aaai16tutorials.php#FA1">FA1:</a> CP-Nets (Thomas E. Allen, Judy Goldsmith, Francesca Rossi)</li>
	<li><a href="http://www.aaai.org/Conferences/AAAI/2016/aaai16tutorials.php#FP4">FP4:</a>Deep Learning: from Foundations to Implementation (Reza Borhani, Jeremy Watt, Aggelos K. Katsaggelos)</li>
	<li><a href="http://www.aaai.org/Conferences/AAAI/2016/aaai16tutorials.php#SP1">SP1:</a> Algorithm Configuration: A Hands on Tutorial (Frank Hutter, Marius Lindauer)</li>
</ul>
</p>
<div class="hr1">
<hr />
</div>
<h4 class="text2"><a name="mentoring">Breakfast with Champions: A Women's Mentoring Event</a></h4>
<p>
AAAI is holding the second annual women's mentoring event for women 
students to meet with senior women in computer science and/or 
artificial intelligence. Breakfast with Champions is tentatively 
scheduled for Monday morning, February 15, 2016. This event follows 
on the successful footsteps of the Doctoral Consortium mentoring 
program. If you are a female student who is interested in joining us, 
please apply by checking the box on the registration form (available 
in November). Attendance is limited to the first 30 applicants. There 
is a $10 registration fee for attendees to help cover a portion of 
the event costs. If this fee would prevent you from attending, please 
contact the organizers directly at &#97;&#97;&#97;&#105;&#49;&#54;&#119;&#111;&#109;&#101;&#110;&#64;&#97;&#97;&#97;&#105;&#46;&#111;&#114;&#103;.
</p>
