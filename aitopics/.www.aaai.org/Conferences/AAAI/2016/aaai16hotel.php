<h2 class="backgrnd12"><span class="text1">AAAI-16 Accommodation  and Transportation Information</span></h2>

<ul>
	<li><a href="http://www.aaai.org/Conferences/AAAI/2016/aaai16hotel.php#hotel">Hotel</a></li>
	<li><a href="http://www.aaai.org/Conferences/AAAI/2016/aaai16hotel.php#transportation">Transportation</a></li>
</ul>

<p>
AAAI has reserved a block of rooms at the Hyatt Regency Phoenix at reduced conference rates. Space is limited and attendees are encouraged to make their reservations early. (Information about student accommodations is available by writing to &#97;&#97;&#97;&#105;&#49;&#54;&#64;&#97;&#97;&#97;&#105;&#46;&#111;&#114;&#103;. Please attach proof of full-time status.)
</p>

