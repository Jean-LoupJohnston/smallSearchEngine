<ul>
	<li>Hyatt Regency Phoenix</li>
	<li>Distance to Phoenix Convention Center (across the street)</li>
	<li>122 North Second Street</li>
	<li>Phoenix, ArizonaÂ  85004</li>
	<li>(602) 252-1234 Telephone</li>
	<li>(602) 256-0801 Fax</li>
	<li><a href="http://phoenix.hyatt.com" rel="nofollow">phoenix.hyatt.com</a></li>
</ul>

<h4>AAAI-16 Online Reservations</h4>
<ul>
	<li><a href="https://resweb.passkey.com/go/30thAAAIConference" rel="nofollow">URL</a></li>
	<li>Conference Rates available: February 10 &ndash; February 16, 2016</li>
	<li>Single/Double Occupancy Rooms: $199.00 per night</li>
	<li>Triple Occupancy Rooms: $229.00 per night</li>
	<li>Quadruple Occupancy Rooms: $239.00 per night</li>
	<li>Cut-off date for conference rate reservations: Friday, January 22, 2016</li>
</ul>
<p>
Reservation requests received after the cut-off date will be based on availability at the Hotel's prevailing rates.
</p>

<p>
The Guest Room Rates are quoted exclusive of applicable state and local taxes (which are currently 12.27%), applicable service fees, and/or Hotel-specific fees in effect at the time of the conference.
</p>

<p>
The Hyatt Regency Phoenix accepts all major credit cards. Cancellations must be made by 3:00 PM Mountain Standard Time 48 hours prior to arrival.
</p>

<h4>Parking</h4>
<p>
Hyatt Regency Phoenix is pleased to offer guests the convenience of covered self and valet parking, with in and out privileges.
<ul>
	<li>Overnight Valet Parking &mdash; $28 per 24 hours</li>
	<li>Short Term Valet Parking  &mdash;  $12 for the first hour, +$1 each additional hour. After 10 hours, $28</li>
	<li>Overnight Self-Parking via the Front Desk  &mdash;  $19 with in-out privileges, charged to your room.</li>
</ul>

<p>
Parking is available at the Regency Parking Garage located adjacent to the hotel. The Regency Garage entrance is located just south of Hyatt Regency Phoenix on North 2nd Street.
</p>

<h4>Additional Hotel Information</h4>
<p>
Experience the tranquil beauty of the Southwest combined with the exhilarating energy of a vibrant cosmopolitan city at Hyatt Regency Phoenix. Situated in the center of downtown Phoenix, the hotel is adjacent to the Phoenix Convention Center and steps away from sports venues, dining and shopping.</p>

<p>
Hyatt Regency Phoenix offers deluxe amenities, world-class service and a downtown setting close to the convention center and city attractions. With several convenient transportation options at your disposal, you will have no problem getting to and from the hotel and wherever else you need to go.
</p>


<h3 class="text2"><a name="transportation">Transportation</a></h3>
<h4>Getting to Phoenix</h4>
<p>
The Hyatt Regency Phoenix is 6.6 miles from the <a href="https://skyharbor.com/" rel="nofollow">Phoenix Sky Harbor International Airport</a> (PHX)

<h4>Taxi</h4>
<ul>
	<li>Taxi Rates to the airport are $15/$18 from the airport to the hotel (4 person maximum).</li>
	<li>Sedans, SUV's, and vans are also available upon request, as well as minicoaches and limos. Wait time is approximately 15&ndash;20 minutes and reservations are recommended.</li>
</ul>

<h4>Super Shuttle</h4>
<ul>
	<li><a href="http://www.supershuttle.com/Locations/PHXAirportShuttlePhoenix.aspx" rel="nofollow">Super Shuttle Website</a></li>
	<li><a href="http://www.aaai.org/Conferences/AAAI/2016/aaai16SuperShuttleDiscount.pdf" rel="nofollow">AAAI-16 Special SuperShuttle Discount Coupon</a></li>
	<li>Rates to/from the airport $13 each way per passenger</li>
	<li>Guests may proceed directly to the hotel pickup area outside the baggage claim</li>
	<li>The Super Shuttle is available 24 hours a day, runs constantly, on demand.</li>
</ul>

<h4>Local Transit</h4>
<p><a href="http://www.valleymetro.org/about_lightrail" rel="nofollow">Valley Metro Light Rail</a></p>
	<ul>
	<li>Proceed to Ground Transportation </li>
	<li>Board the PHX Airport Shuttle or the Phoenix Sky Train to the Valley Metro Light Rail </li>
	<li>Board the Metro Light Rail toward Montebello </li>
	<li>De-board at the Central/Washington Station </li>
	<li>The Hyatt Regency Phoenix is one block to the east </li>
	<li>Metro Light Rail runs from 4:00 AM &ndash; 11:00 PM, daily </li>
	<li>The cost is $2 per ride, or an all-day pass of $4</li>
</ul>


