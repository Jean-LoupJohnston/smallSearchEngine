<h3 class="text2"><i><a name="Dietterich">AAAI 2016 Presidential Address</a></i><br >Steps Toward Robust Artificial Intelligence</h3>

<p><b>Thomas G. Dietterich (Oregon State University)</b></p>
<p>Thomas G. Dietterich is Distinguished Professor of EECS at Oregon State University. He received the A.B. from Oberlin College (1977), an M.S. from the University of Illinois, Urbana (1979), and the Ph.D. from Stanford University (1985). Dietterich studies fundamental problems in AI and machine learning motivated by important challenges in emerging applications &mdash; specifically, computational ecology and ecosystem management, computer security, and robust AI systems.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2016/Photos/dietterich.jpg" alt="Thomas G. Dietterich" width="59" height="75" class="img1" /> Thomas G. Dietterich is a Fellow of the AAAI, ACM, and AAAS. He served as Technical Program cochair of AAAI 1990 and AAAI Councilor. Other roles include program chair of NIPS 2000, general chair of NIPS 2001, NIPS Foundation trustee, and founding president of the International Machine Learning Society (2001-2008). Dietterich served as Executive Editor of the journal <i>Machine Learning</i> (1992-98) and he cofounded the <i>Journal of Machine Learning Research.</i> From 1998&ndash;2015, he edited the MIT Press series on Adaptive Computation and Machine Learning, and from 1998 to the present he has moderated the machine learning area of arXiv. He has advised government funding agencies including DARPA (Information Science and Technology advisory board, 2004-7) and NSF (Advisory Committee for Cyber Infrastructure, 2009-12).</p>

<h3 class="text2"><i><a name="Bostrom">AAAI-16 Invited Talk</a></i><br />
What We Should Think about Regarding the Future of Machine Intelligence</h3>
<p><b>Nick Bostrom (Oxford University)</b></p>

<p>The prospect of machine superintelligence (even if very uncertain and distant) deserves some systematic analysis and discussion, since the consequences would be far-reaching.  But what, specifically, are the questions we should ask?  What kind of research on this topic is possible now?  And what can we do mitigate the predilection of popular media for alarmist stories illustrated with screenshots from Hollywood science fiction movies?</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2016/Photos/bostrom.jpg" alt="Nick Bostrom" width="63" height="75" class="img1" /> Nick Bostrom is a professor in the Faculty of Philosophy at Oxford University. He is the founding director of the Future of Humanity Institute, a multidisciplinary research center that enables a few exceptional mathematicians, philosophers, and scientists to think about global priorities and big questions for humanity. Bostrom has a background in physics, computational neuroscience, and mathematical logic as well as philosophy. He is the author of some 200 publications, including <i>Anthropic Bias</i> (Routledge, 2002), <i>Global Catastrophic Risks</i> (ed., Oxford University Press, 2008), <i>Human Enhancement</i> (ed., Oxford University Press, 2009), and the academic book <i>Superintelligence: Paths, Dangers, Strategies</i> (Oxford University Press, 2014), which became a New York Times bestseller. He is best known for his work in five areas: (1) existential risk; (2) the simulation argument; (3) anthropics (developing the first mathematically explicit theory of observation selection effects); (4) impacts of future technology; and (5) implications of consequentialism for global strategy.</p>

<p>He is recipient of a Eugene R. Gannon Award (one person selected annually worldwide from the fields of philosophy, mathematics, the arts and other humanities, and the natural sciences). He has been listed on Foreign Policy's Top 100 Global Thinkers list; and he was included on <i>Prospect</i> magazine's World Thinkers list, the youngest person in the top 15 from all fields and the highest-ranked analytic philosopher. His writings have been translated into 24 languages. There have been more than 100 translations and reprints of his works.</p>


<h3 class="text2"><i><a name="Hassabis">IAAI-16 / AAAI-16 Joint Invited Talk</a></i><br />
</h3>
<p><b>Demis Hassabis (Google DeepMind)</b></p>

<p>In my talk I will give an overview of the ambitious research program at DeepMind, including some of our latest advances. I will also discuss some of the key challenges we are currently tackling, in the quest to build Artificial General Intelligence, and the approaches we are taking to solve them.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2016/Photos/hassabis.jpg" alt="Demis Hassabis" width="55" height="75" class="img1" /> Demis Hassabis is the cofounder and CEO of DeepMind, a neuroscience-inspired AI company, bought by Google in Jan 2014 in their largest European acquisition to date. He is now vice president of Engineering at Google DeepMind and leads Googleâ¬"s general AI efforts. Hassabis is a former child chess prodigy, who finished his A-levels two years early before coding the multi-million selling simulation game Theme Park aged 17. Following graduation from Cambridge University with a double first in computer science he founded the pioneering videogames company Elixir Studios producing award-winning games for global publishers such as Vivendi Universal. After a decade of experience leading successful technology startups, Hassabis returned to academe to complete a PhD in cognitive neuroscience at UCL, followed by postdocs at MIT and Harvard, before founding DeepMind. His research connecting memory with imagination was listed in the top ten scientific breakthroughs of 2007 by the journal Science. Hassabis is a 5-times World Games Champion, a Fellow of the Royal Society of Arts, and the recipient of the Royal Societyâ¬"s prestigious Mullard Award 2014.</p>


<h3 class="text2"><i><a name="Krause">AAAI-16 Invited Talk</a></i><br />
From Proteins to Robots: Learning to Optimize with Confidence</h3>
<p><b>Andreas Krause (ETH Zurich)</b></p>


<p>With the success of machine learning, we increasingly see learning algorithms make decisions in the real world. Often, however, this is in stark contrast to the classical train-test paradigm, since the learning algorithm affects the very data it must operate on.  I will explain how statistical confidence bounds can guide data acquisition in a principled way to make effective decisions in a variety of complex settings.  I will discuss several applications, ranging from autonomously designing wetlab experiments in protein structure optimization, to safe automatic parameter tuning on a robotic platform.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2016/Photos/krause.jpg" alt="Andreas Krause" width="56" height="75" class="img1" /> Andreas Krause is an associate professor of computer science at ETH Zurich, where he leads the Learning and Adaptive Systems Group. Before that he was an assistant professor of computer science at Caltech. He received his Ph.D. in computer science from Carnegie Mellon University and his Diplom in computer science and mathematics from TU Munich. He is a Microsoft Research Faculty Fellow and received an ERC Starting Investigator grant, an NSF CAREER award as well as several best paper awards at premier conferences and journals.</p>


<h3 class="text2"><i><a name="Murphy">AAAI-16 Invited Talk</a></i><br />
Learning Treatment Policies in Mobile Health</h3>
<p><b>Susan Murphy (University of Michigan)</b></p>


<p>I describe a sequence of steps that facilitate effective learning of treatment policies in mobile health. These include a clinical trial with associated sample size calculator and data analytic methods. An off-policy  Actor-Critic algorithm is developed for learning a treatment policy from this clinical trial data. Open problems abound in this area, including the development of a variety of online predictors of risk of health problems, missing data and disengagement.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2016/Photos/murphy.jpg" alt="Susan Murphy" width="55" height="75" class="img1" /> Susan Murphy is the H. E. Robbins Distinguished University Professor of Statistics, a professor of psychiatry and a research professor at the Institute for Social Research. Her research focuses on improving sequential, individualized, decision making in health, in particular on clinical trial design and data analysis to inform the development of mobile health treatment policies.  Murphy is a Fellow of the College on Problems in Drug Dependence, a former editor of the <i>Annals of Statistics,</i> a member of the US National Academy of Medicine and a 2013 MacArthur Fellow.</p>


<h3 class="text2"><i><a name="Rao">IAAI-16 Invited Talk</a></i><br />Rethinking Computation: Substrates for Machine Intelligence</h3>
<p><b>Naveen Rao (Nervana, Inc.)</b></p>
<p>Deep learning has had a major impact in the last 3 years. Imperfect interactions with machines, such as speech, natural language, or image processing have been made robust by deep learning and deep learning holds promise in finding usage structure in large datasets. However, the training process is lengthy and has proven to be difficult to scale due to constraints of existing compute architectures. Beyond the algorithms, deep learning is a fundamentally new way to express computation. In this talk, I will outline some of these challenges and how fundamental changes to the organization of computation and communication can lead to large advances in capabilities.
</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2016/Photos/rao.jpg" alt="Naveen Rao" width="59" height="75" class="img1" /> Naveen Rao is the cofounder and CEO of Nervana, a deep learning platform startup.  He has a diverse background in both synthetic and biological computation, spanning microprocessor architecture, signal processing and neuroscience. After undergraduate training in electrical engineering and computer science at Duke, Rao spent the next 10 years designing processors and specialized chips at Sun Microsystems and a variety of startups, doing wireless DSP, networking, and video compression. In 2007, to better understand biological computation, Rao pursued a PhD in neural computation and neural prosthetics at Brown University. Nervana is a culmination of this experience, bringing together computer engineering and biological inspiration to build smarter machines.</p>


<h3 class="text2"><i><a name="Smith">IAAI-16 Robert S. Engelmore Memorial Award Lecture</a></i><br />A Quarter Century of AI Applications: What We Knew Then versus What We Know Now</h3>
<p><b>Reid G. Smith (i2k Connect)</b></p>
</p>
 
<p> AI applications have been built, deployed and used for industrial and government purposes for many years. The experiences have been documented in IAAI conference proceedings since 1989. Over that period, the breadth of applications has expanded many times over.  The diversity of technical approaches has also evolved &mdash; from rule-based expert systems to deep learning &mdash; with many modern systems employing a variety of techniques and subsystems.  This presentation will focus on contrasting what (we thought) we knew about building, deploying and using AI applications in the early years with what (we think) we know now.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2016/Photos/smith.jpg" alt="Reid G. Smith" width="60" height="75" class="img1" /> Reid G. Smith is cofounder and CEO of i2k Connect, an AI technology company that transforms unstructured documents into structured data enriched with subject matter expert knowledge. Formerly, he was VP of research and knowledge management at Schlumberger, enterprise content management director at Marathon Oil, and SVP at Medstory, a vertical search company purchased by Microsoft.  He holds a Ph.D. in electrical engineering from Stanford University and is a Fellow of AAAI.  He has served as AAAI Councilor, AAAI-88 Program Cochair, IAAI-91 Program Chair and program committee member for IAAI from its inception in 1989.  He is coeditor of AITopics.</p>


<h3 class="text2"><i><a name="Walsh">AAAI-16 Invited Panel</a></i><br />AI's Impact on Labor Markets</h3>
<p><i>Moderator:</i> Toby Walsh (NICTA and University of New South Wales, Australia)</p>

<p>Will machines take over many jobs in the future? A 2013 Oxford University study predicted 47 percent of jobs in the US are at risk of automation in the next 20 years. More recently, the Chief Economist of the Bank of England predicted 50 percent of jobs in the UK are at risk of automation. How real are such threats? And what should we be doing to prepare for these changes? This panel brings together some leading thinkers in this area: Nick Bostrom, Oxford (author of <i>Superintelligence:  Paths, Dangers, Strategies</i>), Erik Brynjolfsoon, MIT (author of <i>2nd Machine Age: Work, Progress, and Prosperity in a Time of Brilliant Technologies</i>), Oren Etzion, CEO of the Allen Institute for AI (serial entrepreneur) and Moshe Vardi, Rice (editor of <i>CACM)</i>. Chaired by Toby Walsh (UNSW and Data61). </p>



<h3 class="text2"><i><a name="Flight">AAAI-16 Invited Panel</a></i><br />Autonomous Flight</h3>
<p><i>Panelists:  </i> Mykel Kochenderfer (cochair, Stanford University), Ella Atkins (cochair, University of Michigan), Amy Pritchett (Georgia Tech), Claire Tomlin (University of California, Berkeley), Jonathan How (MIT)</p>

<p>Increasingly autonomous manned and unmanned aircraft are becoming safer and more capable. The commercial transport aircraft can fly itself from origin to destination but requires a crew to handle off-nominal situations and interface with air traffic control.  Commercially available small unmanned aircraft systems (SUAS) can autonomously execute a waypoint mission but have little resilience to system failures and environmental hazards. This panel will discuss key autonomy technologies and research needs that will support safer, more efficient flight.  Autonomous systems must be validated and verified to meet certification requirements and also must be accepted and trusted by the flying public and communities over which SUAS will operate at low altitudes. Panelists will discuss critical autonomy applications including emergency flight management, detect-and-avoid, geofencing for airspace segregation, cooperative planning and control, and challenges in operator situational awareness and training.
</p>

<h3 class="text2"><i><a name="Tomlin">IAAI-16 Invited Talk</a></i><br />Reachability and Learning for Hybrid Systems</h3>
<p><b>Claire Tomlin (University of California, Berkeley)
</b></p>
<p>Hybrid systems allow for the composition of continuous and discrete state dynamics, and have been used in aircraft flight management, air and ground transportation systems, robotic vehicles and human-automation systems.  These systems use discrete logic to manage complexity and more naturally accommodate linguistic and qualitative information.   In this talk, we will present reachable set methods for controller design to satisfy safety specifications, and we will present a toolbox of methods combining reachability with machine learning techniques, to enable performance improvement while maintaining safety. We will illustrate these "safe learning" methods on UAV applications.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2016/Photos/tomlin.jpg" alt="Claire Tomlin" width="56" height="75" class="img1" />Claire Tomlin is the Charles A. Desoer Professor of Engineering in EECS at Berkeley.  She was an assistant, associate, and full professor in aeronautics and astronautics at Stanford University from 1998 to 2007, and in 2005 joined the University of California, Berkeley.  Tomlin works in the area of control theory and hybrid systems, with applications to air traffic management, UAV systems, energy, robotics, and systems biology.  She is a MacArthur Foundation Fellow (2006) and an IEEE Fellow (2010).

</p>


