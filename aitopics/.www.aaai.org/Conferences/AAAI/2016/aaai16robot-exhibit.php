<h2 class="backgrnd6"><span class="text1">AAAI-16 Robotics Exhibition</span></h2>
<p><i>The Thirtieth AAAI Conference on Artificial Intelligence</i><br />
February 12&ndash;17, 2016, Phoenix, Arizona USA</p>
<h3 class="text2">Call for Participation</h3>
<div class="hr1">
<hr />
</div>
<p><b>
Registration Deadline: January 6, 2016</b>
</p>

<p>
We invite research groups and robotics companies to participate in the AAAI Robotics Exhibition in Phoenix, Arizona from February 12â¬17, 2016. The exhibition is an opportunity to demonstrate your robot systems to a large audience of artificial intelligence researchers (and beyond).
</p>

<p>
Exhibitors will be given space from Saturday, February 13, during the AAAI-16 Open House and the second day of the tutorials and workshops, until Tuesday evening, February 16. The exhibition will form part of several robotics-related activities at AAAI-16, including an AI Robotics Fellowship program and an AI Robotics Early Career Spotlight Talk. The Open House will be open to high-school students and other selected members of the general public.
</p>

<p>
We especially encourage demonstrations of the incredible progress that has been made in robotics in the past five years. Demonstrations, research platforms, educational tools, spin-offs and consumer robots are all welcome, from companies, commercial vendors, and university research laboratories.
</p>

<p>
Robotics Exhibition participants should contact AAAI (aaai16@aaai.org) for complete information about how to register.
</p>

<p>
The Robotics Exhibit tables will be grouped together in a high-traffic area of the conference facility. As part of our effort to highlight Robotics in AI, robotic exhibitors are entitled to special benefits, and university teams are eligible for a special discounted exhibit registration. (Additional subsidies might be available for university research laboratories with person-intensive or cost-intensive exhibits.)
</p>

<h3 class="text2">Robotics Exhibitor Benefits
</h3>

<p>
AAAI-16 Robotics Exhibitors are entitled to several benefits in addition to their exhibit space:
</p>


<ul>
	<li>A 100-word listing and description in the AAAI-16 / IAAI-16 Program</li>
	<li>Eligibile to have an insert in the AAAI-16 / IAAI-16 Conference bag</li>
	<li>One complimentary full technical registration to the AAAI-16 / IAAI-16 Conference, and two complimentary student technical registrations</li>
	<li>Up to 3 additional exhibit-only registrations for students or others staffing the exhibition table</li>
</ul>
</p>

<h3 class="text2">Robotics Exhibit Fees*
</h3>

<ul>
	<li>Commercial Vendors: $1,225 per tabletop</li>
	<li>University Research Laboratories: $300 per tabletop</li>
</ul>

<p>
*Exhibit fees are based on one (1) 6-foot by 30-inch skirted table. All prices are quoted in US dollars.
</p>

<p>
Please express your interest by sending a message to &#97;&#97;&#97;&#105;&#49;&#54;&#64;&#97;&#97;&#97;&#105;&#46;&#111;&#114;&#103; no later than December 1. AAAI will contact you regarding registration and payment, as well as your logistical needs.
</p>

<p>
The AAAI-16 Demonstration Program provides an alternative to the AAAI-16 Robotics Exhibition Program but accepted demonstrations will be allocated only one time slot during one of the main conference days, while the AAAI-16 Robotics Exhibition Program provides exhibit space for four full days with the expectation that the exhibit will be showcased during all main conference days.
</p>

<p>
Please feel free to contact the robotics cochairs if you have any questions.
</p>

