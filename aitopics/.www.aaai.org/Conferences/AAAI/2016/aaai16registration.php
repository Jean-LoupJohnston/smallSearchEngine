<h4>Opening Reception and Poster/Demo Session</h4>

<p>The AAAI-16 Opening Reception will be held on Saturday evening, February 13, at the Hyatt Regency Phoenix, and the Poster/Demo Sessions will be held on Sunday, Monday, and Tuesday evenings, February 14&ndash;16, at the Phoenix Convention Center. Admittance to the opening reception and the conference-wide poster/demo sessions is included in the AAAI-16 technical registration fee. Guests and children are welcome for the following fees:

</p>

<p><b>Opening Reception Guest</b><br />
  Adult &nbsp; $55.00<br />
  Child &nbsp; $20.00 (children 12 and under are welcome free of charge)</p>

<p><b>Poster/Demo Session Guest per night</b><br />
  Adult &nbsp; $40.00<br />
  Child &nbsp; $15.00 (children 12 and under are welcome free of charge)</p>


<p><a href="http://www.regonline.com/aaai16" rel="nofollow"><b>Online Registration Form</b></a></p>

<div class="hr1">
<hr />
</div>

<h4>Student Newcomer Reception</h4>

<p>
AAAI will hold a Student Newcomer Reception on Friday evening, February 12, at 7:00 pm. This event is designed to welcome first-time students to the conference and give them an opportunity to meet other participants before the launch of the main conference. If you are able to join us, please apply by checking the box on the registration form. All students are welcome!
</p>

<div class="hr1">
<hr />
</div>

<h4>AAAI-16 Open House</h4>

<p>
The AAAI-16 Open House will be held Saturday, February 13, for high-school students in the Phoenix area, the general public, graduate and undergraduate students, and established AI researchers. The latest work in many areas of AI will be showcased. If you plan to attend, please check the appropriate box on the registration form. Attendance is complimentary. 
</p>

<div class="hr1">
<hr />
</div>

<h4>Breakfast with Champions: A Women's Mentoring Event</h4>

<p>
AAAI is holding its second annual women's mentoring event between women students and senior women in CS/AI. Breakfast with Champions will be Monday morning, February 15, 2016. This event follows on the successful footsteps of the Doctoral Consortium Mentoring program. If you are interested in joining us, please apply by checking the box on the registration form. Attendance is limited to the first 40 applicants.
</p>

<p>
Women's Breakfast: $10.00
</p>

<div class="hr1">
<hr />
</div>

<h4>AAAI-16 Hard Copy Proceedings</h4>

<p>Preregistrants may reserve a hard copy of the AAAI-16 proceedings, which will be mailed after the conference. The preconference discounted price will be determined in mid-January. Individuals who select this option will be contacted regarding the final cost, as well as the shipping and handling fees after the conference. </p>

