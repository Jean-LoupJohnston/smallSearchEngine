<h3 class="text2">Technical Program Registration</h3>

<p>The AAAI-16/IAAI-16 technical program registration includes admission to all technical paper and poster sessions, the Tutorial Forum, invited talks, exhibits, demos, competitions, the opening reception, and an electronic copy of the AAAI-16/IAAI-16 Conference Proceedings.</p>

<p><b>To register, please use the <a href="http://www.regonline.com/aaai16" rel="nofollow">AAAI-16 online registration form.</a></b></p>
<p><b>ALL FEES ARE QUOTED IN US DOLLARS</b></p>

<p><b>Deadlines</b><br />
<i>Early:</i> On or before December 21<br />
<i>Late:</i> On or before January 15<br />
<i>Onsite:</i> After January 15</p>
<div class="hr1">
<hr />
</div>

<p><b>*Platinum Registrations</b><br />
The Platinum Regular rate includes a one-year full membership in AAAI, full print and online access to <i>AI Magazine,</i> plus the discounted member rate for the AAAI-16 conference. It's an easy renewal option for existing AAAI members who wish to maintain all benefits of membership!</p>

<p><b>**Silver Registrations</b><br />
Silver is a promotional AAAI-16 rate that includes one-year online-only access to <i>AI Magazine</i> plus the discounted member rate for the AAAI-16 conference.</p>

