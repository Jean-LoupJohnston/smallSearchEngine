<h2 class="backgrnd6"><span class="text1">Twenty-First AAAI/SIGAI Doctoral Consortium</span></h2>
<p><i>Collocated with the Thirtieth Conference on Artificial Intelligence</i><br />
February 12-13, 2016, Phoenix, Arizona USA</p>
<h4 class="text1">Call for Applications</h4>
<div class="hr1">
<hr />
</div>
<h3 class="text2"><a href="http://ciigar.csc.ncsu.edu/aaai2016-dc/" rel="nofollow">AAAI-16 Doctoral Consortium Website</a></h3>
<div class="hr1">
<hr />
</div>
<h3 class="text2"><a href="http://www.aaai.org/Publications/Templates/AuthorKit16.zip">2015 AAAI Author Kit</a></h3>
<div class="hr1">
<hr />
</div>




<h4 class="text2">Important Dates</h4>
    <ul>
	<li>September 22, 2015: Application Package Submission Deadline</li>
	<li>October 24, 2015: Acceptance Notification</li>
	<li>December 1, 2015: Camera-Ready Copy due at AAAI Office</li>
	<li>February 12-13, 2016: Doctoral Consortium</li>
</ul>

<div class="hr1">
<hr />
</div>

<p>
AAAI and ACM/SIGAI invite students to apply for the Twenty-First AAAI/SIGAI Doctoral Consortium. The Doctoral Consortium (DC) provides an opportunity for a group of Ph.D. students to discuss and explore their research interests and career objectives with a panel of established researchers in artificial intelligence. The consortium has the following objectives: (1) to provide a setting for feedback on participants' current research and guidance on future research directions; (2) to develop a supportive community of scholars and a spirit of collaborative research; (3) to support a new generation of researchers by offering advice about academic, research, industrial, and nontraditional career paths; and (4) to contribute to the overall conference goals through interaction with other researchers and participation in conference events.
</p>


<p>
The Doctoral Consortium will be held as a workshop on February 12&ndash;13, 2016, immediately before the start of the main AAAI conference. Student participants in the Doctoral Consortium will receive complimentary conference registration and a fixed allowance for travel/housing. Students will be expected to attend both days of the workshop in Phoenix.
</p>


<h3 class="text2">Application Instructions</h3>
<p>
Applicants to the Doctoral Consortium must submit the following materials via the <a href="https://easychair.org/conferences/?conf=aaai16" rel="nofollow">AAAI online submission site.</a> Please combine the first four documents into one PDF document, formatted for US letter paper (8.5 x 11"). Please submit your application materials via the online Doctoral Consortium submission site. Be mindful of the page limits, 6 pages in total (1-page cover sheet, 2-page thesis summary, 2-page CV, 1-page personal statement).
</p>

<h4 class="text2">1. Cover Sheet</h4>
<p>
 A one-page information sheet that includes the following:
</p>

<ul>
<li>Your full name</li>
<li>Contact details (mailing address, email address, daytime phone number)</li>
<li>Affiliation: department, university, country</li>
<li>Home page (URL)</li>
<li>Your gender</li>
<li>Country of Citizenship</li>
<li>(Optional) Are you a member of an underrepresented minority ethnic group? If so, please specify.</li>
<li>Expected graduation date</li>
<li>Your thesis advisor's full name</li>
<li>Your thesis advisor's contact details (mailing address and email address)</li>
<li>List of up to 5 people in the AAAI community you would suggest as a mentor for yourself during the AAAI Doctoral Consortium (a list of current mentors will be available on the AAAI website at a later date. Students are welcome potential mentors from this list, if appropriate.</li>
<li>List of up to 5 keywords to help us select reviewers for your application</li>
</ul>

<h4 class="text2">2. Thesis Summary</h4>
<p>
A two-page extended abstract that summarizes the research questions being investigated, highlights important related work, describes the proposed research plan and anticipated thesis contribution, and includes a brief timeline. Be mindful of the following:
</p>

<ul>
<li>Be sure to describe your progress as of the date of submission (September 2015), and your anticipated progress by the workshop date (February 2016), being careful to distinguish between work that has already been accomplished and work that remains to be done.</li>

<li>Be sure to clearly identify which work is done by you, and which work is done by collaborators, such as your thesis supervisor and/or other students in your research group (if there are any). It is okay to use the first person voice in the thesis summary.</li>

<li>Be sure to include a title for your work (please do not title it "Thesis Summary").</li>

<li>Remember that the audience for your thesis summary includes people who are knowledgeable about AI, but are not necessarily experts in the narrow topic of your thesis. Be sure to introduce the content at a high enough level so that the general AAAI audience can understand your story, but also include enough low-level detail so that the experts will appreciate your unique contribution.</li>

<li>The thesis summary MUST be 2 pages, in AAAI 2-column format, including references. Yes, this is tricky to do -- but learning how to produce a concise statement of your thesis research is very important!</li>
</ul>

<h4 class="text2">3. Curriculum Vita</h4>
<p>
Include a CV (at most 2 pages) that describes your background and relevant experience (research, education, employment). Include any publications, if you have any. Also include any pending submissions at AI conferences (such as AAAI 2016) and journals.
</p>

<h4 class="text2">4. Personal Statement</h4>
<p>
A short (1 page or less) statement of what you expect to gain from presenting and participating in the DC, as well as what you think you can contribute to the DC. Be sure to indicate if you have previously participated in doctoral consortia at other conferences (if so, which ones and when), and if you are applying to other DC in 2016.
</p>

<h4 class="text2">5. Advisor's Information</h4>
<p>
Please include the name of your thesis advisor and their contact information, including title, department, affiliation and email address. A request will be emailed to your thesis advisor asking them to provide supplementary information about you by completing a separate form. The form will contain multiple-choice questions about the current status of your thesis research and progress through your graduate program, why your advisor would like you to participate in the DC, what your advisor believes you can contribute to the DC, whether you have participated in other DC's in the past, and if you are applying to other DC's in 2016. Note that a letter of recommendation is NOT required (and none will be accepted).
</p>

<h3 class="text2">Review Process</h3>
<p>
The Doctoral Consortium organizing committee solicits applications covering any topic area and methodology within Artificial Intelligence. Students will be selected who have settled on their thesis direction, but still have significant research to complete. Applications will be reviewed according to the following criteria: clarity and completeness of submission packet, stage of research, evidence of research progress (such as published papers and/or technical reports), assessment of contribution to and benefit from participating in the DC, and advisor's input.
</p>

<h3 class="text2">At the Conference</h3>
<p>
All participants selected to present their work at the Doctoral Consortium are expected to be present throughout the consortium. Our experience has been that participants gain almost as much by interacting with their peers as by having their presentations critiqued by the faculty panel. As such, we expect a commitment from participating students to attend the entire DC.
</p>

<p>
In addition, all students attending AAAI are invited to observe the Doctoral Consortium, whether or not they apply to present their work. It can be a tremendous learning experience to watch peers make presentations, as well as listen to invited speakers or panel discussions.
</p>

<h3 class="text2">International Participation</h3>
<p>
Significant effort has been made in recent years to expand AAAI participation internationally. In the past, the DC has had limited ability to provide support for students from foreign institutions (outside the US). This year, we are expanding the sources of funding and hope to provide support for more non-US students than in past years.
</p>

<h3 class="text2">Acknowledgements</h3>
<p>
Support for the 2016 Doctoral Consortium is provided by AAAI, with additional support traditionally coming from ACM's SIGAI, the National Science Foundation (NSF), the Artificial Intelligence journal, and various industry supporters.
</p>

