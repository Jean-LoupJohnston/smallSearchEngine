<h3 class="text2">Questions and Suggestions</h3>
<p>


Please contact the SMPT cochairs for any questions or suggestions. Specific questions are answered below.
</p>

<p>
Q: How can I know whether I am considered senior enough?<br />
A: As a general rule this track is designed for researchers who are well established in their research area. They should be a significant number of years away from their Ph.D and have published a significant number of papers in the top AI conferences and journals. However, exceptions are possible and will be considered on a per case basis.
</p>

<p>
Q: Can submissions be coauthored?<br />
A: Naturally, the talks include one presenter only. However, the papers submitted might include other authors but the first author must be the talk presenter.
</p>

<p>
Q: Does submitting a 6-page paper increase the chances of my talk being accepted when compared to a shorter paper?<br />
A: Not at all. All submissions will be reviewed/considered along the same review criteria.
</p>

<p>
Q: Is there any relation between this track and the status of Senior Member of AAAI.<br />
A: Not at all. Anyone who meets the criteria above can submit. You do not have to hold the status of a Senior Member of AAAI nor be a AAAI Fellow.
</p>

