<h3 class="text2">Description</h3>

<h3 class="text2">Description</h3>
<p>
The Senior Member Presentation Track provides an opportunity for established researchers in the AI community to give a broad talk on a well-developed body of research, an important new research area, or a promising new topic. These presentations should provide a big picture view, in contrast to regular papers, which may focus on a specific contribution. Submissions include a proposal for a talk and a paper (in the AAAI format) covering the topic of the talk. Senior members of AI are researchers who are well established in their research area.
</p>

<p>
The track will be run in the same fashion as in AAAI 2016. In particular, the following features will be continued:
  Subtracks (summary talks and blue sky ideas talks)
  Talks come with a paper in the proceedings
  Three grants for the best three proposals in the blue sky ideas track, given by the Computing Community Consortium.
</p>

<h3 class="text2">Eligibility</h3>
<p>
This track is open to researchers who are a significant number of years away from their Ph.D (exceptions will be considered on a per case basis), have acquired an international recognition, and have established a significant publication record of AI-related research in AI conferences and journals.
</p>

<p>
It is important to note that you do not have to be a AAAI Fellow nor hold the official status of Senior-Member of AAAI in order to submit to the Senior Member Presentation Track.
</p>

<h3 class="text2">Subtracks</h3>
<p>
There are two subtracks for submissions of talk proposals for the SMPT:
</p>

<p>
<i>Summary talks:</i> broad talks on a well-developed body of research or an important new research area. They generally will also include results obtained by researchers other than the speaker.
</p>

<p>
<i>Blue Sky Ideas talks:</i> These presentations aim at presenting ideas and visions that can stimulate the research community to pursue new directions, e.g., new problems, new application domains, or new methodologies that are likely to stimulate significant new research. The presenter should find the right arguments to convince the audience that the topic is promising, and should relate the talk as much as possible to the existing literature. The Blue Sky Ideas track will be sponsored by the Computing Community Consortium, which will give three awards ($1000, $750 and $500), for the three best submissions in this category, to be used as travel grants for the presenters. See the Computing Community Consortium for more on blue sky ideas talks.
</p>

<h3 class="text2">Proposal / Paper Submission</h3>
<p>

A proposal for the SMPT includes two parts.
</p>

<p>
<i>Part 1: Talk Outline and CV</i><br />
The first part of the proposal should include the title, an abstract and an outline of the suggested talk. All these in 1 page (in any reasonable format). In addition it should also include a short CV (up to 2 pages in any reasonable format) for a total of 3 pages for part 1. The CV should also include evidence that the proposer is qualified to talk about the topic, such as specific contributions of the proposer to the proposed topic (in the case of a summary talk) or contributions of the proposer in related topics (in the case of a blue sky idea talk). To show the broadness of the topic, proposals are required to cite at least 3 papers in the suggested topic which were published by different authors in the last 3 years in a highly competitive venue. (This applies to summary talks only.)
</p>

<p>
<i>Part 2: Summary Paper</i><br />
Researchers who submit talk proposals must also submit a paper. Accepted papers will be included in the proceedings. Papers should cover the same topic as the talk and are intended to give a personal synthetic view of the topic, together with appropriate references. The papers should be self-contained and are intended to be accessible for anyone including people who did not attend the talk. The papers should be formatted as PDF files using the AAAI style. Submissions are not anonymous. They should include the names and affiliations of the authors. The length of the papers depends on the track: between 2 and 6 pages for the summary talks track, and between 2 and 4 pages for the blue sky ideas track. An additional page for references is also allowed (that is, 6 + 1 and 4 + 1, for each of the tracks, respectively).
</p>

<h3 class="text2">Submission Process</h3>
<p>

Easychair Senior Member Track Submission site.
Please select "AAAI-17 Senior Member Presentation Track" on the AAAI-17 submission site. Complete the online submission form and then submit all of the information required for the track in one PDF. Begin each section of your submission on a new page. Add your sub-track (i.e., Summary talks or Blue Sky Ideas talks) as a sub-title on the first page. Your submission should include no more 3 pages for the Talk outline plus CV and no more than 4 or 6 pages for the Blue Sky Idea or the Summary Talk papers, respectively.
</p>

<h3 class="text2">Reviewing Process</h3>
<p>

All senior member talk proposals and the corresponding papers will be reviewed by qualified members of the AAAI program committee. The reviewers of the paper will follow the main regulations of regular AAAI submission and will check the quality and clarity of the paper.
</p>

<p>
The final decisions on acceptance will be made by the track cochairs.
</p>

