<h2 class="backgrnd6"><span class="text1">Twenty-First AAAI/SIGAI Doctoral Consortium</span></h2>
<p><i>Collocated with the Thirty-First Conference on Artificial Intelligence</i><br />
February 4&ndash;5, 2017, San Francisco, California USA</p>
<h4 class="text1">Call for Applications</h4>
<div class="hr1">
<hr />
</div>
<h3 class="text2"><a href="https://easychair.org/conferences/?conf=aaai17" rel="nofollow">AAAI-17 Doctoral Consortium Submission Site</a></h3>
<div class="hr1">
<hr />
</div>
