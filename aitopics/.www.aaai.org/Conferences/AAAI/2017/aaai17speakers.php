<h3 class="text2"><i><a name="Aha">IAAI-17 Engelmore Award Lecture</a></i><br />
Goal Reasoning: Emerging Applications, a Foundation, and Prospects </h3>
<p><b>David W. Aha (Naval Research Laboratory, USA)</b></p>

<p>Goal reasoning (GR) has a bright future as a foundation for research on intelligent agents and as a methodology for developing applications. GR is the study of agents that can deliberate on and self-select their goals/objectives, which is a desirable capability for deliberative autonomy. While it has been studied for several decades in multiple disciplines, GR has only recently been the focus of intense interest. In this presentation, I will introduce GR, relate it to other AI topics, summarize some emerging applications, describe our foundation for GR, and discuss some current and future research directions.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2017/Photos/aha.jpg" alt="David W. Aha" width="62" height="75" class="img1" />David W. Aha (UCI 1990) is a member of NRL's Navy Center for Applied Research on AI. His group conducts basic and applied research on intelligent agents, ML, case-based reasoning, and related topics; their current projects concern goal reasoning or deep learning. He has mentored 12 postdocs, served on 20 PhD committees, was a AAAI Councilor, cocreated the AAAI AI Video Competition, and created the UCI Repository for ML Databases. David has coorganized 30+ events (including AAA-17 DC, ICCBR-17, IJCAI-17 Workshop on XAI), serves on several PCs, and led or leads the evaluation team for four DARPA or ONR Programs.
</p><p><a href="http://www.aaai.org/Conferences/AAAI/2017/aaai17speakers-Aha.pdf" rel="nofollow">Slides</a></p>

