<h3 class="text2"><i><a name="Howard">EAAI-17 Invited Talk</a></i><br />
Designing Assistive Robots and Technologies for Pediatric Care</h3>
<p><b>Ayanna Howard (Georgia Institute of Technology, USA)</b></p>


<p>In recent months, there has been an upsurge in the attention given to robots and artificial intelligence and their inevitable destruction of the human race if we are not watchful. Whether your opinion sits on one side or the other, the fact remains; robots have already become a part of our society. In particular, with recent advances in robotics, therapeutic interventions using robots is now ideally positioned to make an impact. There are numerous challenges though that must still be addressed. In this talk, Howard will discuss the role of robotics and related technologies for pediatric therapy.  She will present her approaches in which these technologies address real-life therapy goals for children with special needs.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2017/Photos/howard.jpg" alt=" Ayanna Howard" width="66" height="75" class="img1" /> Ayanna Howard, Ph.D. is the Linda Smith Endowed Chair Professor in the School of ECE at the Georgia Institute of Technology and chief technology officer of Zyrobotics. She received her B.S. in engineering from Brown University and her PhD in electrical engineering from the University of Southern California. Her area of research has a specific emphasis on developing assistive robotics and technologies for children with special needs. To date, her unique accomplishments have been highlighted through a number of awards and articles, including highlights in <i>USA Today, Upscale,</i> and <i>TIME Magazine,</i> as well as being recognized as one of the 23 most powerful women engineers in the world by <i>Business Insider. </i> 
</p>

