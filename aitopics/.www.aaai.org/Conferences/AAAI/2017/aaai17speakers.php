<h3 class="text2"><i><a name="Ethics">EAAI-17 Invited Panel</a></i><br />
Ethics Education</h3>
<p><b>Moderator: Sven Koenig (University of Southern California)</b></p>
<p><b>Panelists: Benjamin Kuipers (University of Michigan), Judy Goldsmith (University of Kentucky), Illah R. Nourbakhsh (Carnegie Mellon University CREATE Lab)</b></p>
<p>The panel will discuss how, as educators, we can incorporate ethical issues into undergraduate or graduate AI classes, such as the following questions: Do we need to worry about the reliability, robustness, and safety of AI systems?  Do we need to provide oversight of their operation? How do we guarantee that their behavior is consistent with social norms and human values? Who is liable for incorrect AI decisions?  How will AI technology impact standard of living, distribution and quality of work, and other social and economic aspects?
</p><p>
Benjamin Kuipers is a professor of computer science and engineering at the University of Michigan.  He received his B.A. from Swarthmore College, and his PhD from MIT. He investigates the representation of commonsense and expert knowledge, with particular emphasis on the effective use of incomplete knowledge. His research accomplishments include developing the TOUR model of spatial knowledge in the cognitive map, the QSIM algorithm for qualitative simulation, the Algernon system for knowledge representation, and the Spatial Semantic Hierarchy models of knowledge for robot exploration and mapping. He has served as department chair at UT Austin, and is a Fellow of AAAI, IEEE, and AAAS.
</p>
<p>Judy Goldsmith received her degrees in mathematics from Princeton University and the University of Wisconsin-Madison. She held post-doc at Dartmouth College and Boston University, an assistant professorship at the University of Manitoba, and has been in the Computer Science Department of the University of Kentucky since 1993. She is a full professor. Her research areas include many aspects of decision making, including decision making under uncertainty; computational social choice; preference elicitation, representation, and aggregation; computational learning theory, and computational complexity. Goldsmith was the recipient of the The First Annual IJCAI-JAIR Best Paper Prize, Honorable Mention, 2003. She was recognized in 2014 as a Senior Member of AAAI, the Association for the Advancement of Artificial Intelligence. In 2015, Goldsmith received an Undergraduate Research Mentor award from the Computing Research Association. In 1998, Goldsmith was recognized by the AAAS for her mentoring of members of underrepresented groups in the STEM disciplines. She has been on the editorial board of <i>JAIR</i> since 2008, and on the editorial board of <i>Artificial Intelligence</i> since 2015. 
</p>
<p>
Illah R. Nourbakhsh is a professor of robotics, director of the Community Robotics, Education and Technology Empowerment (CREATE) lab, and associate director for robotics faculty at Carnegie Mellon University. In 2009, the National Academy of Sciences named him a Kavli Fellow. In 2013 he was inducted into the June Harless West Virginia Hall of Fame. He is coauthor of <i>Introduction to Autonomous Mobile Robots,</i> author of <i>Robot Futures and Parenting for Technology Futures.</i> He is a trustee of the Claude Worthington Benedum Foundation, and chairman of the board of directors of the Southwestern Pennsylvania Environmental Health Project. He is a member of the Global Future Council on the future of ai and robotics for the World Economic Forum and senior advisor to The Future Society, Harvard Kennedy School.
</p>

