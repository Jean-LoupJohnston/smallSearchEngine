<h3 class="text2"><i><a name="Education">EAAI-17 Invited Panel</a></i><br />
AI for Education</h3>
<p><b>Moderator: Sheila Tejada (University of Southern California)</b></p>
<p><b>Panelists: Yolanda Gil (USC Information Sciences Institute), Ayanna Howard (Georgia Tech), Peter Norvig (Google), Mehran Salami (Stanford University), Sebastian Thrun (Udacity, KittyHawk, Stanford, Georgia Tech)</b></p>
<p>The panel will discuss how we can apply AI techniques in the domain of education to improve teaching, student evaluation, and learning. The panelists will talk about which aspects of the education problem they were able to address with AI and give insight into the education space, suggesting where other educators and researchers can apply AI tools to improve their own teaching or student learning.</p>



