</ul>


<p>
A New York Times Magazine article recently announced "The Great A.I. Awakening": The rise of Recognition Systems based on machine learning methods and neural networks, and their use by commercial firms. This view predominates inside and outside of the AI community. Yet this view neglects AI's previous awakenings. 
</p>

<p>
In 1985, Allen Newell, AAAI's first President, wrote: "There is no doubt, as far as I am concerned, that the development of expert systems is the major advance in the field during the last decade.... The emergence of expert systems has transformed the enterprise of AI." Further, <i>Computerworld</i> reported that, "Recent advances in expert systems are putting society at the brink of a massive application of artificial intelligence."
</p>

<p>
From, roughly, 1970 to 1993, Expert Systems captivated the AI community and public as do today's Recognition Systems. Expert Systems technology was absorbed by the software industry, omnipresent but unnoted in contemporary software.
</p>

<p>
What is the history of Expert Systems, and how does it inform the development of machine learning and neural networks today? Join a panel of former AAAI presidents &mdash; all major contributors to the expert systems story &mdash; for insights and perspectives.
</p>

<div class="hr1"><hr /></div>

<h3 class="text2"><i><a name="Poker">AAAI-17 Panel</a></i><br />
Advances in AI in Poker: Two Mini-Talks on Recent Breakthroughs and a Panel</h3>
<p><b>Moderator: Kevin Leyton-Brown (University of British Columbia, Canada)</b></p>

<p><b>Panelists: Michael Bowling (University of Alberta, Canada) and Tuomas Sandholm
(Carnegie Mellon University, USA)</b></p>

<p>Poker has been a challenge problem in AI and game theory for decades. As a game of imperfect information it involves obstacles not present in classic board games like chess and go, but which are present in many real-world applications, such as negotiation, security, and auctions. However, no programs had beaten professional players in large poker games. Until now! New breakthrough domain-independent advances have seen computer programs surpass human experts in the game of heads-up no-limit Texas hold'em, which has over 10<sup>160</sup> decision points. Michael Bowling (who led the team that beat top poker pros in limit Texas hold'em in 2008) will talk about DeepStack, which late last year defeated a collection of professional poker players in heads-up no-limit. Tuomas Sandholm (who led the team that is the current two-time champion of the Annual Computer Poker Competition in heads-up no-limit) will talk about Libratus, which last month defeated a team of top heads-up no-limit specialist professionals. These two short talks will be followed by a panel discussion. Come learn about the latest scientific breakthroughs and milestones achieved in AI.</p>

