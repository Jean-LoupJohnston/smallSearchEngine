<h3 class="text2"><i><a name="Dolgov">AAAI-17/IAAI-17 Joint Invited Talk</a></i><br />
Self-Driving Cars and the Future of Mobility</h3>
<p><b>Dmitri Dolgov (Waymo)</b></p>


<p>In the US, more than 35,000 people die in car accidents every year; 100 million hours are wasted every day on people's commutes. We can do better. Self-driving cars offer a promise of higher safety, efficiency, and convenience in transportation. At Waymo (formerly Google's self-driving car project), we're working to bring self-driving technology to millions of people. In 2015, we took a major step towards that goal: we performed the world's first fully driverless ride on public roads in an uncontrolled setting. This talk will cover the technology behind fully self-driving cars and how they're able to safely share the roads with pedestrians, cyclists and other road users.</p>

