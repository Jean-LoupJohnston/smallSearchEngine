<h3 class="text2"><i><a name="Grauman">AAAI-17 Invited Talk</a></i><br />Learning How to Move and Where to Look from Unlabeled Video<br />
</h3>
<p><b>Kristen Grauman (University of Texas at Austin, USA)</b><br /><i>Sponsored by Capital One</i></p>


<p><img src="http://www.aaai.org/Conferences/AAAI/2017/Photos/grauman.jpg" alt="Kristen Grauman" width="54" height="75" class="img1" />The status quo in visual recognition is to learn from batches of unrelated Web photos labeled by human annotators. Yet cognitive science tells us that perception develops in the context of acting and moving in the world &mdash; and without intensive supervision. How can unlabeled video augment computational visual learning? I'll describe our recent work exploring how a system can learn effective representations by watching unlabeled video. Fist we consider how the ego-motion signals accompanying a video provide a valuable cue during learning, allowing the system to internalize the link between "how I move" and "what I see." Next, I explore how the temporal coherence of video permits new forms of invariant feature learning, whether by capturing how object-centric regions evolve over time or by representing higher order consistency in visual changes. Incorporating these ideas into various recognition tasks, we demonstrate the power in learning from ongoing, unlabeled visual observations &mdash; even overtaking traditional heavily supervised approaches in some cases. Finally, I examine how simply having seen unlabeled human-taken videos, a system can learn to mimic human videographer tendencies, automatically creating normal field of view video out of unedited 360 degree panoramas.</p>

<p>Kristen Grauman is an associate professor in the Department of Computer Science at the University of Texas at Austin. Her research in computer vision and machine learning focuses on visual search and object recognition. Before joining UT Austin in 2007, she received her Ph.D. from MIT. She is a Sloan Research Fellow and recipient of NSF CAREER, ONR Young Investigator, PAMI Young Researcher, and PECASE awards, and the 2013 IJCAI Computers and Thought award.  She and her collaborators were recognized with the CVPR Best Student Paper Award in 2008 for their work on hashing algorithms for large-scale image retrieval, the Marr Best Paper Prize at ICCV in 2011 for their work on modeling relative visual attributes, and the Best Application Paper Award at ACCV in 2016 for their work on automatic cinematography in 360 degree video. She serves as an associate editor in chief for <i>Transactions on Pattern Analysis and Machine Intelligence</i> (TPAMI) and served as a program chair of CVPR 2015 in Boston.</p>
