<h3 class="text2"><i><a name="Young">AAAI-17 Invited Talk</a></i><br />
Statistical Spoken Dialogue Systems and the Challenges for Machine Learning</h3>
<p><b>Steve Young (Cambridge University Engineering Department, UK)</b></p>


<p>
This talk will review the principal components of a spoken dialogue system and then discuss the opportunities for applying machine learning for building robust high performance open-domain systems. The talk will be illustrated by recent work at Cambridge University using machine learning for belief tracking, reward estimation, multi-domain policy learning and natural language generation. The talk will conclude by discussing some of the key challenges in scaling these solutions to work in practical systems.
</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2017/Photos/young.jpg" alt="Steve Young" width="61" height="75" class="img1" /> Steve Young is a professor of information engineering at Cambridge University and a member of the Apple Siri Development team based in Cambridge, UK. His main research interests lie in the area of statistical spoken language systems including speech recognition, speech synthesis and dialogue management. He is the recipient of a number of awards including an IEEE Signal Processing Society Technical Achievement Award, an ISCA Medal for Scientific Achievement and an IEEE James L Flanagan Speech and Audio Processing Award. He is a Fellow of the Royal Academy of Engineering and the Institute of Electrical and Electronics Engineers (IEEE).
</p>




