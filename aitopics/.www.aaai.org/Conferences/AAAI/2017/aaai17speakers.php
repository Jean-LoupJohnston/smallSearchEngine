</h3>
<p><b>Lynne Parker (University of Tennessee and National Science Foundation, USA)</b></p>

<p>Released by the White House in October 2016, the <a href="https://www.nitrd.gov/news/national_ai_rd_strategic_plan.aspx" rel="nofollow">National AI R&amp;D Strategic Plan</a> outlines a set of AI research priorities for the U.S. Federal Government. In this talk, I will discuss the development of the report from my perspective as cochair of the interagency Task Force that generated the report. I will discuss the rationale behind the content of the Plan, as well as insights on how multiagency collaboration led to the creation of the report. I will also discuss possible future benefits that might result from the Plan. Ample time for Q&amp;A will be provided. </p> 

