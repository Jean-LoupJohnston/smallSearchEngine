<h3 class="text2"><i><a name="Picard">AAAI-17 Invited Talk</a> <br /></i>Adventures in Building Emotional Intelligence Technologies</h3>
<p><b>Rosalind Picard (MIT and Empatica, USA)</b></p>

<p>
Years ago, I set out to create technology with emotional intelligence, demonstrating the ability to sense, recognize, and respond intelligently to human emotion. At MIT we designed studies, gathered data, and developed signal processing and machine learning techniques to see what insights could be reliably obtained. In this talk I will highlight the most surprising findings during this adventure. These include new insights about the "true smile of happiness," discovering new ways cameras (and your smartphone, even in your handbag) can compute your biosignals, finding electrical signals on the wrist that reveal insight into deep brain activity, and learning surprising implications of wearable sensing for autism, anxiety, sleep, memory, epilepsy, and more. What is the biggest challenge for AI to solve next?
</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2017/Photos/picard.jpg" alt="Rosalind Picard, Photo by Andy Ryan" width="75" height="75" class="img1" />
Rosalind Picard, ScD, FIEEE is founder and director of the Affective Computing Research Group at the MIT Media Laboratory, cofounder of Affectiva, providing emotional intelligence technology used by 1/3 of the Global Fortune 100, and cofounder and chief scientist of Empatica, improving lives with clinical-quality wearable sensors and analytics. Picard is the author of over 250 articles in computer vision, machine learning, signal processing, affective computing, and human-computer interaction. She is known internationally for her book, <i>Affective Computing,</i> which helped launch the field by that name. Picard holds bachelors in electrical engineering (EE) from Georgia Tech and masters and a doctorate degrees in electrical engineering and computer science from MIT. Picard's inventions have been twice named to "top ten" lists, including the <i>New York Times Magazine's</i> Best Ideas of 2006 for the Social Cue Reader, and 2011's Popular Science Top Ten Inventions for a Mirror that Monitors Vital Signs. 
</p>

