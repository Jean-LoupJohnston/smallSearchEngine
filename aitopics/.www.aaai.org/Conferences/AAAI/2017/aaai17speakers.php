<h3 class="text2"><i><a name="Frank">IAAI-17 Invited Talk</a></i><br />Enabling Autonomous Space Mission Operations with Artificial Intelligence<br />
</h3>
<p><b>Jeremy Frank (Intelligent Systems Division, NASA Ames Research Center)</b></p>
<p>
For over 50 years, NASA's crewed missions have been confined to the Earth-Moon system, where speed-of-light communications delays between crew and ground are practically nonexistent. This ground-centered mode of operations, with a large, ground-based support team, is not sustainable for NASA's future human exploration missions to Mars. Future astronauts will need smarter tools to make decisions on their own, without assistance from ground-based mission control. In this talk, we will describe several demonstrations of astronaut decision support tools using AI techniques including automated planning, fault diagnosis, automated reasoning and machine learning. These demonstrations show how developments in AI will enable humanity's journey to Mars. 
</p>

<p>
<img src="http://www.aaai.org/Conferences/AAAI/2017/Photos/frank.jpg" alt="Jeremy Frank" width="67" height="75" class="img1" />Dr. Jeremy Frank is the group lead of the Planning and Scheduling Group, in the Intelligent Systems Division, at NASA Ames Research Center. He received his Ph.D. from the Department of Computer Science, at the University of California at Davis, in June 1997. Frank's work involves the development of space mission operations planning tools; the integration of technologies for planning, plan execution, and fault detection for space applications; and the development of technology to enable astronauts to autonomously operate spacecraft. Frank has published over 50 conference papers, nine journal papers, and three book chapters, and received over 40 NASA awards, including two Exceptional Achievement Medals, the Silver Snoopy, and the NASA Engineering and Safety Center Award.
</p>


