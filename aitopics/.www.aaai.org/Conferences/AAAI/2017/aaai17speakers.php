<h3 class="text2"><i><a name="Thrun">AAAI/EAAI Outstanding Educator Award Lecture</a></i><br />
Democratizing Education &mdash; Why Not?</h3>
<p><b>Sebastian Thrun (Udacity, KittyHawk, Stanford, Georgia Tech)</b></p>
<p>When Thrun and Norvig made Stanford's graduate-level AI course public (as the first global MOOC), 160,000 students from 195 countries signed up. This started a global wave of Massive Open Online Courses, through which tens of millions of students were able to receive world-class education. Thrun will report on progress at Udacity, the start-up company that grew out of this experiment. Udacity has become a dominant educator in areas as advanced as self-driving cars or virtual reality. Its hundreds of industrial partners willingly hire Udacity graduates, sometimes even without job interview. Thrun will lay out his vision for global education, arguing that the democratization of higher education might one day double the world's GDP.</p><p>Prof. Dr. Dr.h.c. Dr.h.c Sebastian Thrun pursues research on robotics, artificial intelligence, education, and human computer interaction. He founded Google's self driving car team, after winning the DARPA Grand Challenge. Together with Peter Norvig, he also developed the very first global MOOC with 160,000 students enrolled. His company, Udacity, has educated over 5 million students, and has been valued at more than one billion dollars. Google Scholar ranks Thrun's publication h-index #14 worldwide in all of computer science. Thrun also founded Google X, where he founded Google Glass among many other projects. He was elected into the National Academy of Engineering and the German Academy of Sciences at age 39. Fast Company named Thrun the fifth most creative person in business, and Foreign Policy touted him Global Thinker #4. He won numerous awards, including the prestigious Max Planck Research Award. </p>




