<h3 class="text2"><i><a name="Tedrake">AAAI-17 Invited Talk</a></i><br />
Convex and Combinatorial Optimization for Dynamic Robots in the Real World</h3>
<p><b>
Russ Tedrake (MIT Computer Science and Artificial Intelligence Laboratory and Toyota Research Institute, USA)</b></p>


<p>
Humanoid robots walking across intermittent terrain, robotic arms grasping multifaceted objects, or UAVs darting left or right around a tree ... many of the dynamics and control problems we face today have both rich nonlinear dynamics and an inherently combinatorial structure. In this talk, Tedrake will review some recent work on planning and control methods which address these two challenges simultaneously. He will present our explorations with mixed-integer convex-, semidefinite-programming-relaxations, and satisfiability-modulo-theory(SMT)-based methods applied to hard problems in legged locomotion over rough terrain, grasp optimization, and UAVs flying through highly cluttered environments.
</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2017/Photos/tedrake.jpg" alt="Russ Tedrake" width="66" height="75" class="img1" />Russ Tedrake is a professor of electrical engineering and computer science, aeronautics and astronautics, and mechanical engineering at MIT, the Director of the Center for Robotics at the Computer Science and Artificial Intelligence Lab, and was the leader of Team MIT's entry in the DARPA Robotics Challenge. TEDRAKE is also the director of simulation and control at the new Toyota Research Institute. He is a recipient of the NSF CAREER Award, the MIT Jerome Saltzer Award for undergraduate teaching, the DARPA Young Faculty Award in Mathematics, the 2012 Ruth and Joel Spira Teaching Award, and was named a Microsoft Research New Faculty Fellow.
</p>

