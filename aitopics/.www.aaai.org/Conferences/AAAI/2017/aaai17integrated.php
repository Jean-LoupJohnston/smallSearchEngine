<h2 class="backgrnd6"><span class="text1">AAAI-17 Special Track on Integrated Systems</span></h2>
<p><i>The Thirty-First Conference on Artificial Intelligence</i><br />
February 4&ndash;9, 2017 &nbsp;
San Francisco, California USA
</p>
