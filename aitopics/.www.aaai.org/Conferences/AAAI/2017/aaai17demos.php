<p>
Submissions from everyone, including authors of paper submissions to AAAI, IAAI, and AAAI-16 workshops, are encouraged. Work submitted to other tracks (such as the Robotics Exhibition and the Videos Competition) can also be submitted to the Demonstrations Program in the format prescribed below, and it will be evaluated independently according to the Demonstrations review process.
</p>


<h4>Author Registration</h4>

Authors must register at the AAAI-17 submission site before submitting their expressions of interest. Authors should select "Demo Track." The software will assign a password, which will enable the author to log on and submit an expression of interest. Authors are strongly encouraged to register as soon as possible, well in advance of the September 14 deadline.

<ul>
	<li>Registration will close at 11:59 PM PDT on September 9, 2016</li>
	<li>Abstract (expression of interest) submission will close at 11:59 PM PDT on September 14, 2016</li>
	<li>Final 2-page paper submission will close at 11:59 PM PDT on October 21, 2016</li>
</ul>


<h4>Submission and Review Process</h4>
Submissions to the AAAI-17 Demonstrations program will undergo a two-stage evaluation process.

<p>
In Stage 1, researchers and practitioners interested in showcasing a demonstration are required to submit an "expression of interest" through the web-based conference management system in the "abstract" field. An expression of interest consists of a short description introducing the demonstration and describing its significance. The expression of interest must specify the list of authors and the material needed to perform the demonstration. It may optionally include supporting material, such as a video or a previously published paper, as long as these are specifically related to the proposed demonstration. Such additional items must be submitted as a zip file.
</p>

<p>
The expression of interest will be judged for clarity, the significance of the proposed demonstration, and its relevance to the AI community. Authors will receive high-level feedback on the expression of interest, and a decision on whether their work has been selected for Stage 2.
</p>

<p>
In Stage 2, authors will have to submit two items: (1) a 2-page short paper describing their system, formatted in AAAI two-column style, and (2) a video (of duration up to 10 minutes) of the proposed demonstration. The paper must present the technical details of the demonstration, discuss related work, and describe the significance of the demonstration. The paper must contain unpublished work. If accepted, authors will be required to transfer copyright of their paper to AAAI. For more information on the AAAI style, please download the AAAI Press author
kit.
</p>

<p>
Since the demonstration will ultimately be viewed and "experienced" by attendees, submissions will also be evaluated based on a "preview": a video showing the demonstration in the form it is planned to be exhibited. It is all right if some parts of the demonstration are not ready by the Stage 2 submission deadline. However, authors must use the video to convey the demonstration's overall "look and feel", which will be used to decide whether the submission should be accepted. The Demonstrations Program Chair reserves the right to reject submissions if the reviewers believe existing deficiencies would be difficult to fix by the authors within the given time frame. The Stage 2 reviews can be expected to be more detailed; they will also convey the final decision on accepting the demonstration for showcasing at AAAI-17.
</p>

<p>
Demonstration participants are welcome to exhibit and distribute additional material, such as videos running on a dedicated console, scientific papers, pamphlets, and noncommercial brochures. Such ancillary materials need not be submitted for review.
</p>

<p>
At least one member of every demonstration must register for and attend AAAI-17. The conference registrant will be responsible for building the exhibit.
</p>

-->

<h4>Award</h4>
<p>
To foster the presentation of exciting demonstrations, AAAI-17 will present an award for the Best Demonstration. The award will be granted on the the basis of audience evaluation through a ballot given to all attendees.
</p>

<h4>Services</h4>

<p>
Researchers and practitioners showing a demonstration are required to bring all necessary equipment and material for the successful operation of their exhibit. AAAI will provide shipping instructions, if needed, for the delivery of material to the conference site. Each demo program participant will be provided with one 6-foot draped table, one monitor, and one poster board, as well as electrical and internet access. Special electrical connections must be arranged through the Demonstrations Program Chair no later than 45 days prior to the start of the conference.
</p>

