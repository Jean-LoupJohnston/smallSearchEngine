<p>
The AAAI-17 Demonstrations Program is intended to foster discussion and exchange of ideas among researchers and practitioners from academia and industry by presenting software and hardware systems and research prototypes of such systems, including their capabilities and workings. Accepted demonstrations will be allocated one time slot of 1.5 or 2 hours during one of the main conference evening poster programs, and will have a short paper included in the proceedings.
</p>
