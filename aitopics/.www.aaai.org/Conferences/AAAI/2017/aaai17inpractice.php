<h3 class="text2"><i><a name="Kurzweil">Slot 7: Fireside Chat</a> <br /></i>The Future Capability and Impact of AI</h3>
<p><b>Fireside Chat with Ray Kurzweil</b></p>

<p>I'll briefly recount my fifty year experience in AI. It began with my meeting Marvin Minsky in 1962 when I was fourteen which began a 54 year mentorship until his passing a year ago. That same month I also met Frank Rosenblatt, then leader of the nascent connectionist school. He shared an intuition with me which would be proven correct decades after his passing in 1971.</p>

<p>
Deep Neural Nets and Long-Short Temporal Memory techniques, along with the ongoing progression of what I call the "Law of Accelerating Returns" (the exponential growth of the price-performance and capacity of information technologies, which is a much broader phenomenon than Moore's Law) is fueling a wave of optimism in AI. But DNNs and LSTMs have a limitation characterized by the motto "life  begins at a billion examples."
</p>

<p>
I'll share an alternative model based on self-organizing hierarchies of sequential models and explain why I believe that this is how the human neocortex works, and why this approach has the potential to overcome the apparent limitations of big DNNs.
</p>

<p>
I would then like to explore these ideas and get questions from the insightful AAAI audience.
</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2017/Photos/Kurzweil.jpg" alt="In Practice Speaker" width="64" height="75" class="img1" />Ray Kurzweil was the principal inventor of the first CCD flat-bed
scanner, the first omni-font optical character recognition, the first
print-to- speech reading machine for the blind, the first text-to-speech
synthesizer, the first music synthesizer capable of recreating the grand
piano and other orchestral instruments, and the first commercially marketed
large-vocabulary speech recognition.</p>

<p>Among Ray's many honors, he received the Grace Murray Hopper Award from the
Association for Computing Machinery, a Grammy award for outstanding
achievements in the field of music technology; he is the recipient of the
National Medal of Technology, was inducted into the National Inventors Hall
of Fame, holds twenty-one honorary Doctorates, and honors from three U.S. presidents.
Ray has written five national best-selling books, including New York Times
best sellers The Singularity Is Near (2005) and How To Create A Mind
(2012). He is cofounder and chancellor of Singularity University and a
director of engineering at Google heading up a team developing machine
intelligence and natural language understanding."</p>




