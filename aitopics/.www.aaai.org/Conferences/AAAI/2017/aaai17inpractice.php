<h3 class="text2"><i><a name="Smola">Slot 6: Invited Talk</a> <br /></i>Fast and Personal &mdash; Scaling Deep Learning with MxNet</h3>
<p><b>Alex Smola (Amazon)</b></p>

<p>In this talk I will address the challenges of building deep learning systems that are able to adjust to users for content recommendation and user engagement estimation. They rely on nonparametric latent variable models, such as LSTMs to deal with nonstationary time-series data. Going beyond models, I will discuss how scalable deep learning models can be implemented efficiently in MxNet, a parallel distributed high performance deep learning framework. In particular, I will discuss programming models, its execution engine and how to distribute computation efficiently over hundreds of GPUs with linear scaling.
</p><p><img src="http://www.aaai.org/Conferences/AAAI/2017/Photos/Smola.jpg" alt="In Practice Speaker" width="78" height="75" class="img1" />
Alex Smola studied physics in Munich at the University of Technology, Munich, at the Universita degli Studi di Pavia and at AT&amp;T Research in Holmdel. In 1996 he received the Master degree at the University of Technology, Munich and in 1998 the Doctoral Degree in computer science at the University of Technology Berlin. After that, he worked as a Researcher and Group Leader at the Australian National University. From 2004-08 he worked as program leader at the Statistical Machine Learning Program at NICTA. From 2008 to 2012 he worked at Yahoo Research and from 2012&ndash;2014 at Google Research. He joined the Carnegie Mello University faculty in 2013 as a professor. After cofounding Marianas Labs in 2015 he now works at Amazon Web Services as Director of Machine Learning. He has written over 200 papers and several books.</p>



