<h3 class="text2"><i><a name="Agarwal">Slot 2: Invited Talk</a> <br /></i>AI that Creates Professional Opportunities at Scale</h3>
<p><b>Deepak Agarwal (LinkedIn)</b></p>

<p>Professional opportunities can manifest itself in several ways like finding a new job, enhancing or learning a new skill through an online course, connecting with someone who can help with new professional opportunities in the future, finding insights about a lead to close a deal, sourcing the best candidate for a job opening, consuming the best professional news to stay informed, and many others. LinkedIn is the largest online professional social network that connects talent with opportunity at scale by leveraging and developing novel AI methods. In this talk, I will provide an overview of how AI is used across LinkedIn and the challenges thereof. The talk would mostly emphasize the principles required to bridge the gap between theory and practice of AI, with copious illustrations from the real world.
