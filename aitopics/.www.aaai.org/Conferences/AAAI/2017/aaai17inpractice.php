<h3 class="text2"><i><a name="Amatriain">Slot 9: Invited Talk</a> <br /></i>Lessons Learned from Building Practical AI Systems</h3>
<p><b>Xavier Amatriain (Quora)</b></p>

<p>There are many good textbooks and courses where you can be introduced to machine learning and maybe even learn some of the most intricate details about a particular approach or algorithm. While understanding that theory is a very important base and starting point, there are many other practical issues related to building real-life ML systems that you don't usually hear about. In this talk I will share some of the most important lessons learned in years of building the large-scale ML solutions that power products like Quora or Netflix to delight millions of users across the world. I will discuss issues such as model and feature complexity, sampling, regularization, distributing/parallelizing algorithms, the importance of metrics, or how to think about offline vs. online computation. I will also address how to combine supervised and non-supervised approaches, the deep learning "hype," or the role of ensembles in practical ML systems.</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2017/Photos/Amatriain.jpg" alt="In Practice Speaker" width="82" height="75" class="img1" />Xavier Amatriain is vice president of engineering at Quora, where he leads the team building the platform to share and grow knowledge in the Internet. With over 50 publications in different fields, Xavier is best known for his work on machine learning in general and recommender systems in particular. Before Quora, he was a research/engineering director at Netflix, where he led the team building the famous Netflix recommendation algorithms. Previously, Xavier was a research scientist at Telefonica Research and a research director at UCSB. He has lectured at different universities both in the US and Spain and is frequently invited as a speaker at conferences and companies.


</p>
