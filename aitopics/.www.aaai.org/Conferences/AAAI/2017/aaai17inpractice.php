<h3 class="text2"><i><a name="Candela">Slot 8: Invited Talk</a> <br /></i>Designing AI at Scale to Power Everyday Life</h3>
<p><b>Joaquin Quinonero Candela (Facebook)</b></p>
<p>
The majority of the experiences and interactions people have on Facebook today are made possible with AI. Well over 1 billion people enjoy unique, personalized experiences on Facebook that are powered by a wealth of AI and machine learning algorithms. AI is an incredibly fast-moving field: engineers and researchers across the company are turning the latest research breakthroughs into tools, platforms, and infrastructure that make it possible for anyone at Facebook to use AI in the experiences and products they build. This talk will look at how Facebook is conducting and applying industry-leading research to help drive advancements in AI disciplines like computer vision, language understanding, speech and video. We will also talk about building an infrastructure that anyone at Facebook can use to easily reuse algorithms in different products, scale to run thousands of simultaneous custom experiments, and give concrete examples of how employees across the company are able to leverage these platforms to build new AI products and services.
</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2017/Photos/Candela.jpg" alt="In Practice Speaker" width="71" height="75" class="img1" />
Joaquin Quinonero Candela, engineering director at Facebook, leads the Applied Machine Learning (AML) team, driving product impact at scale through applied research in machine learning, language technologies, computer vision, computational photography and other AI disciplines. Prior to Facebook, Joaquin worked at Microsoft Research in Cambridge, UK, building the click prediction and auction optimization teams at Microsoft AdCenter as well as co-creating and teaching a new ML class at the University of Cambridge. He was a postdoctoral researcher at the Fraunhofer Institute in Berlin, at the Technical University of Berlin and also at the Max Planck Institute for Biological Cybernetics in T&uuml;bingen, Germany. Joaquin received his PhD from the Technical University of Denmark in 2004.
</p>

