<h3 class="text2"><i><a name="Marcus">Slot 4: Invited Keynote</a> <br /></i>Artificial Intelligence, Local Minima, and the Quest for AGI</h3>
<p><b>Gary Marcus (Uber and NYU)</b></p>

<p>
AGI (or artificial general intelligence) is the quest for superhuman machines that have the flexibility and resourcefulness of human intelligence, but the computational power and resources of machines. Recently we have seen some impressive advances in artificial intelligence, but the <i>general</i> in artificial general intelligence is still lacking (despite some apparent counterexamples). By and large, machines still need to be programmed for particular tasks, or failing that to be provided with enormous numbers of supervised training examples that pertain largely to a single task. Most AI software remains narrowly engineered, to specific tasks.
</p>

<p>
In this talk I will try to assess why progress has been slower than many of us might have anticipated, and examine some possible obstacles to progress, and suggest that &mdash; all recent enthusiasm to the contrary &mdash; the field could be headed for a local minimum, solving some problems extremely well, yet still falling far short of what is theoretically possible. I will close by suggesting a few relatively unpopular avenues that might be worth exploring.
</p>

<p>
[Disclaimer: My comments represent my own views, not necessarily those of the institutions for which I work.]
</p>

<p><img src="http://www.aaai.org/Conferences/AAAI/2017/Photos/Marcus.jpg" alt="In Practice Speaker" width="75" height="75" class="img1" />
Gary Marcus is director of Uber AI Labs, and was a founder and CEO of Geometric Intelligence, a machine learning startup recently acquired by Uber.  Trained by Steven Pinker, he is also both an award-winning professor of psychology and neural science at New York University, and a bestselling author (Guitar Zero, Kluge) who frequently appears on radio and television.
</p>


