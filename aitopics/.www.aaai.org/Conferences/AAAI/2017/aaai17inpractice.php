<h3 class="text2"><i><a name="Wang">Slot 1: Invited Keynote</a> <br /></i>Natural Language Processing at Baidu</h3>
<p><b>Haifeng Wang (Baidu)</b></p>
<p>Language is a carrier of knowledge and thoughts. Natural language
processing (NLP) is one of the most important fields in artificial
intelligence. Baidu's NLP technologies support about 100 applications and
the modules are called up more than 100 billion times per day by search
engine, feeds, intelligent assistant, machine translation, internet finance
and others. In this talk, I will introduce Baidu's NLP technologies including
semantic understanding, sentiment analysis, deep question answering,
language generation, machine translation, dialogue system, knowledge graph,
and others.
</p>
