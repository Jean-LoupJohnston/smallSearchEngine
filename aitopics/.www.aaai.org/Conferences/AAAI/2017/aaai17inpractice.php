AI in Practice will showcase invited presentations of visionary AI practitioners that will reflect on key successes of AI in the commercial world and crystalize emerging technologies and promising new directions.</p>

<p>
AI in Practice is a special event of the <a href="../../../index.html">Association for the Advancement of Artificial Intelligence (AAAI)</a>. AAAI is the premier membership organization in artificial intelligence (AI). With several thousand members, the Association for the Advancement of Artificial Intelligence (formerly the American Association for Artificial Intelligence) is a nonprofit scientific society founded in 1979 devoted to advancing the scientific understanding of the mechanisms underlying thought and intelligent behavior and their embodiment in machines. AAAI aims to promote research in, and responsible use of, artificial intelligence. AAAI also aims to increase public understanding of artificial intelligence, improve the teaching and training of AI practitioners, and provide guidance for research planners and funders concerning the importance and potential of current AI developments and future directions. Major AAAI activities include organizing and sponsoring conferences, symposia, and workshops, publishing a quarterly magazine for all members, publishing books, proceedings, and reports, and awarding grants, scholarships, and other honors.
