<h3 class="text2"><i><a name="Vanhoucke">Slot 5: Invited Talk</a> <br /></i>"OK Google, fold my laundry s'il te pla&icirc;t"</h3>
<p><b>Vincent Vanhoucke (Google)</b></p>

<p>Deep learning has enabled computers to approach human-level performance on many practical perception and language understanding tasks, ranging from speech recognition to computer vision and machine translation. One of today's grand AI challenges is to bring these new capabilities into the physical world, and teach machines how to behave and make themselves useful in human-centered environments. In this talk, I'll argue how robotics may be on the cusp of its very own deep learning revolution, but that for this endeavor to succeed, machine learning practitioners have to break from the relative comfort of the large-scale supervised learning setting that has buoyed the field for the past decade and humbly face some thorny problems that have comparatively been neglected: data scarcity and skill transfer, active and lifelong learning, as well as safety and predictability. The good news is that tackling these problems is also one of the necessary next steps towards bridging the gap between mere learning and actual intelligence.
</p><p>
<img src="http://www.aaai.org/Conferences/AAAI/2017/Photos/Vanhouke.jpg" alt="In Practice Speaker" width="74" height="75" class="img1" />Vincent Vanhoucke is a principal scientist in the Google Brain Team, and leads Google's robotics research effort. His research has spanned many areas of machine learning, from speech recognition to deep learning, computer vision and robotics. He also chairs the upcoming 2017 Conference on Robot Learning.
</p>
<p><a href="http://www.aaai.org/Conferences/AAAI/2017/aaai17inpractice-Vanhoucke.pdf" rel="nofollow">Slides</a></p>
