<h3 class="text2"><i><a name="Witbrock">Slot 3: Invited Talk</a> <br /></i>AI for Complex Situations: Beyond Uniform Problem Solving</h3>
<p><b>Michael Witbrock (IBM)</b></p>

<p>The majority of recent technical advances in AI stem from problems that are structurally fairly uniform, but have complex and hard-to-describe patterns of variation within that structure. This structural uniformity characterizes, for example, speech signals up to transcription, text up to approximate translation, information extraction, video game play, lane-following, object labeling, and even the game of Go. However, many of the problems that we typically write programs for do not appear to be structurally uniform in this way: understanding a contract, or a regulation, and deciding how it affects a particular business process, is structurally complex: each detail of how the elements of the problem instance relate to one another is potentially critical. General reading comprehension, or automated programming seem similarly complex. If we are to produce AI systems that provide professional level assistance, we must address this complexity along with the variation. In this talk, I will discuss some of the complex, professional level problems we are attempting to address at IBM, and sketch some research paths, from both the past and possible future of AI.
</p><p><img src="http://www.aaai.org/Conferences/AAAI/2017/Photos/Witbrock.jpg" alt="In Practice Speaker" width="79" height="75" class="img1" />
Michael Witbrock is a Distinguished Research Staff Member at IBM Research where he leads the Learning to Reason department, and coordinates global research efforts in knowledge extraction and knowledge representation. Michael recently joined IBM after an extended period as Vice President for Research at Cycorp, where he directed research projects in automated reasoning (including speed-up learning), automated and interactive knowledge acquisition, and machine reading, in domains as varied as military operations planning, counter terrorism, NL medical records query, code analysis and vulnerability detection, video analysis and retrieval, and the molecular mechanisms underlying cancer. Before joining Cycorp, Michael was Principal Scientist at Terra Lycos, working on integrating statistical and knowledge based approaches to understanding web user behavior, and IP capture; a research scientist at JustSystems Pittsburgh Research Center, working on statistical summarization; and a systems scientist at Carnegie Mellon on the Informedia visual and spoken document information retrieval project. While maintaining a strong interest in knowledge representation and capture and in natural language understanding, his current research goals involve the development and use of quasi-logics, which retain approximations of the formal properties of logic while adding the learnability and flexibility of distributed representations, and the development and application of large, inferentially productive knowledge bases and inference systems across the resulting range of reasoning paradigms. He hopes to apply these representations and reasoners to the construction of "dense models" of domains, which are sufficiently complete support the full range of in-domain reasoning. He is author of numerous publications in areas ranging across computational linguistics, speech modeling and recognition, neural networks, automated inference, automated reading and multimedia information retrieval, and has dabbled in web browser design and implementation, genetic design and parallel computer architecture. As well as his technical work, Michael is very interested in entrepreneurship around AI and for social good, and in the social and economic outcomes of advances in AI. He has been pursuing the former interest as a member of the board of StartOut, and the latter interest, inter alia, as a co-founder of AI4Good.org. Michael has a PhD in Computer Science from Carnegie Mellon University and a BSc Hons in Psychology from Otago University in New Zealand.</p>
