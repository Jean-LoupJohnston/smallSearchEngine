<h2 class="backgrnd6"><span class="text1">AAAI-17 Tutorial Forum Call for Proposals</span></h2>
<p><i>The Thirty-First Conference on Artificial Intelligence</i><br />
February 4&ndash;5, 2017, San Francisco, California USA</p>
<p><i>Sponsored by the Association for the Advancement of Artificial Intelligence</i></p>
<div class="hr1">
<hr />
</div>

 
<h4 class="text2">Key Dates</h4>
<ul>
	<li>September 7, 2016: Tutorial Proposals due to tutorial chairs (aaai17tutorials@aaai.org) </li>
	<li>October 5, 2016: Tutorial Acceptances mailed </li>
	<li>October 19, 2016: Tutorial descriptions, autobiographical statements, and speaker photos due</li>
	<li>January 4, 2017: Completed course materials must be posted on speaker's website </li>
	<li>February 4-5, 2017: AAAI-17 Tutorial Forum </li>
</ul>

 
<p>
The AAAI-17 Program Committee invites proposals for the Tutorial Forum of the Thirty-First AAAI Conference on Artificial Intelligence (AAAI-17). The Tutorial Forum will be held February 4-5, 2017 in San Francisco, California, USA. Anyone interested in presenting a tutorial at AAAI-17 should submit a proposal to the 2017 Tutorial Forum Cochairs at &#97;&#97;&#97;&#105;&#49;&#55;&#116;&#117;&#116;&#111;&#114;&#105;&#97;&#108;&#115;&#64;&#97;&#97;&#97;&#105;&#46;&#111;&#114;&#103;.
</p>
 
<h4 class="text2">What Is the Tutorial Forum?</h4>
<p>
The Tutorial Forum provides an opportunity for researchers and practitioners to spend two days each year freely exploring exciting advances in disciplines outside their normal focus. We believe this type of forum is essential for the cross fertilization, cohesiveness, and vitality of the AI field. We all have a lot to learn from each other; the Tutorial Forum promotes the continuing education of each member of AAAI. To encourage full participation by technical conference registrants, no separate fee will be charged for admittance to the Tutorial Forum in 2017. 
</p>
 
<h4 class="text2">Topics</h4>
<p>
Our goal is to present a diverse program that includes core areas of AI, new techniques from allied disciplines that can inform research within AI, and emerging applications of AI techniques to new areas. In addition to tutorials that teach a core area within AI, we encourage proposals from areas that are closely related to AI such as core robotics, operations research techniques relevant for AI, linguistics research applicable to language processing, etc. We also very much welcome proposals that have a hands on component. We additionally solicit tutorials by organizers of various competitions within the sub-fields of AI (Robocup, planning competition, SAT competition, etc) describing the current state of the art and tools available for new students to enter a competition. Last but not the least, we also solicit proposals from applied researchers who may have used AI-techniques in real applications within industry. Novel proposals are highly encouraged. 
</p>
 
<h4 class="text2">Duration</h4>
<p>
The majority of tutorials in the past have been 3.5 hours, but we also very much welcome quarter-day or full-day formats as the topic dictates. 
</p>
 
<h4 class="text2">Submission Requirements</h4>
<p>
Each proposal should include at least the following:
</p>
<ul>
	<li><i>Goal of the tutorial:</i> Who is the target audience? What will the audience walk away with? What makes the topic innovative? </li>
	<li><i>History:</i> List of previous venues and approximate audience sizes, if the same or a similar tutorial has been given elsewhere; otherwise an estimate of the audience size. </li>
	<li><i>Content:</i> Detailed outline and list of additional materials, augmented with samples, such as past tutorial slides and survey articles, whenever possible. Be as complete as possible. </li>
	<li><i>Duration:</i> The target duration for the tutorial. Preferable durations are as follows: 1.75 hours (1 hour, 45 minutes); 3.5 hours, excluding 30-minute break; or 7 hours, excluding two 30-minute breaks and 60-minute lunch break </li>
	<li><i>Tutorial description:</i> A short paragraph summarizing the tutorial outline.</li>
	<li><i>Prerequisite knowledge: </i>What knowledge is assumed of the target audience. </li>
	<li>Please also submit the following information about the team of presenters: </li>
	<ul>
	<li>	name </li>
	<li>	mailing address </li>
	<li>	telephone number </li>
	<li>	email address</li>
	<li>	background in the tutorial area, including a list of publications and/or presentations</li>
	<li>	any available examples of work in the area (ideally, a published tutorial-level article or presentation materials on the subject)</li>
	<li>	evidence of teaching experience (courses taught or references)</li>
	<li>	evidence of scholarship in AI or computer science </li>
</ul>
</ul>


</p>
 
<h4 class="text2">Submission Procedure</h4>
<p>
Proposals should be submitted by September 7, 2016. Decisions about the tutorial program will be made by October 5, 2016. Speakers should be prepared to finalize their tutorial descriptions and bios by October 19, 2016, and to post completed course materials on their websites by January 4, 2017. 
</p>
 
<p>
Submissions must be in pdf format and sent to the Tutorial Program Cochairs at &#97;&#97;&#97;&#105;&#49;&#55;&#116;&#117;&#116;&#111;&#114;&#105;&#97;&#108;&#115;&#64;&#97;&#97;&#97;&#105;&#46;&#111;&#114;&#103;.
</p>
 
