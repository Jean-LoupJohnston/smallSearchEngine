<h3 class="text2">Video Format and Content</h3>

<p>
Either 1 minute (max) short video or a 5 minute (max) long video, with English narration (or English subtitles). Consider combining screen shots, interviews, and video of a system in action. Make the video self-contained, so that newcomers to AI can understand and learn from it. We encourage a good sense of humor, but will only accept submissions with serious AI content. For example, we welcome submissions of videos that accomplish the following: 
</p>

<ul>
	<li>Highlight a research topic &mdash; contemporary or historic, your own or from another group  </li>
	<li>Introduce viewers to an exciting new AI-related technology  </li>
	<li>Provide a window into the research activities of a laboratory and/or senior researcher  </li>
	<li>Attract prospective students to the field of AI  </li>
	<li>Explain AI concepts &mdash; your video could be used in the classroom </li>
</ul>

<p>
Please note that this list is not exhaustive. Novel ideas for AI-based videos, including those not necessarily based on a "system in action" are encouraged. No matter what your choice, creativity is encouraged! (Please note: The authors of previous, award-winning videos typically used humor, background music, and carefully selected movie clips to make their presentations come alive.) 
</p>

<p>
Please also note that videos should contain only material for which the authors own copyright. Clips from films or television, and music for soundtrack, should only be used if copyright permission has been granted by the copyright holders, and this written permission accompanies the video submission. 
</p>

<h3 class="text2">Submission Instructions</h3>

<p>
Submit your video by making it available for download on a (preferably password-protected) dropbox, ftp or website. Once you have done so, please fill out the <a href="http://www.aaaivideos.org/submission_form.txt" rel="nofollow">submission form</a> and send it to us by email submission@aaaivideos.org. All submissions are due no later than November 15, 2016. 
</p>

<h3 class="text2">Review and Award Process</h3>

<p>
Submitted videos will be peer-reviewed by members of the programme committee according to the criteria below. Videos that receive positive reviews will be accepted for publication in the AAAI Video Competition proceedings. Select videos will also be uploaded to Youtube, promoted through social media, and featured on the <a href="http://www.aaaivideos.org/" rel="nofollow">dedicated website.</a> The best videos will be nominated for awards. Winners will be revealed at the award ceremony during AAAI-17. 
</p>

<p>
All authors of accepted videos will be required to sign a distribution license form. 
</p>

<p>
Review criteria includes the following:
</p>

<ol>
	<li>Relevance to AI (research or application) </li>
	<li>Excitement generated by the technology presented </li>
	<li>Educational content</li>
	<li>Entertainment value</li>
	<li>resentation (cinematography, narration, soundtrack, production values) </li>
</ol>


<h3 class="text2">Awards</h3>

<p>
Trophies ("Shakeys") will be awarded.
</p>

<p>
Award Categories include Best Video, Best Short Video, Best Student Video, People's Choice Award, Best Robot Video, and Most Entertaining Video. (Categories may be changed at the discretion of the cochairs.) 
</p>

