<h4>Opening Reception and Poster/Demo Session</h4>

<p>The AAAI-17 Opening Reception will be held on Sunday evening, February 5, in the Yosemite Room of the Hilton San Francisco Union Square Hotel, and the Poster/Demo Sessions will be held on Monday, Tuesday, and Wednesday evenings, February 6&ndash;8, in the Imperial Ballroom of the hotel. Admittance to the opening reception and the conference-wide poster/demo sessions is included in the AAAI-17 technical registration fee. Guests and children are welcome for the following fees:

</p>

<p><b>Opening Reception Guest</b><br />
  Adult &nbsp; $125.00<br />
  Child &nbsp; $20.00 (children 12 and under are welcome free of charge)</p>

<p><b>Poster/Demo Session Guest per night</b><br />
  Adult &nbsp; $65.00<br />
  Child &nbsp; $15.00 (children 12 and under are welcome free of charge)</p>


