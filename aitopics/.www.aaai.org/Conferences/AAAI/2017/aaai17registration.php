<h4>Student Newcomer Reception</h4>

<p>
AAAI will hold a Student Newcomer Reception on Saturday evening, February 4, at 7:00 PM. This event is designed to welcome first-time students to the conference and give them an opportunity to meet other participants before the launch of the main conference. If you are interested in joining us, please apply by checking the box on the registration form. All students are welcome!
</p>

<div class="hr1">
<hr />
</div>

<h4>EAAI-17 Educational Outreach Event</h4>

<p>
The EAAI-17 Outreach Event will be held Saturday, February 4, giving EAAI presenters a chance to teach other AAAI attendees a single outreach activity, which they in turn will perform with local K-12 students. If you plan to attend, please check the appropriate box on the registration form. Attendance is complimentary</p>

<div class="hr1">
<hr />
</div>

<h4>Breakfast with Champions: A Women's Mentoring Event</h4>

<p>
AAAI is holding its third annual women's mentoring event for student mentees, junior professional mentees, and senior mentors in CS/AI. Breakfast with Champions will be Tuesday morning, February 7. If you are interested in joining us, please apply by selecting the appropriate registration category on the registration form. Attendance is limited. 

<ul>
	<li>Student mentee: $15.00</li>
	<li>Junior professional mentee (last enrolled as a student no more than 5 years ago): $15.00</li>
	<li>Mentor (by invitation only) : Complimentary (please write to &#97;&#97;&#97;&#105;&#49;&#55;&#64;&#97;&#97;&#97;&#105;&#46;&#111;&#114;&#103;&#32;for code)</li>
</ul>
</p>


