<h3 class="text2">Technical Program Registration</h3>

<p>The AAAI-17/IAAI-17 technical program registration includes admission to all technical paper, and poster sessions, the Tutorial Forum, invited talks, exhibits, demos, competitions, the opening reception, and an electronic copy of the AAAI-17/IAAI-17 Conference Proceedings.</p>

<p><b>To register, please use the <a href="http://www.regonline.com/aaai17" rel="nofollow">AAAI-17 online registration form.</a></b></p>
<p><b>ALL FEES ARE QUOTED IN US DOLLARS</b></p>

<p><b>Deadlines</b><br />
<i>Early:</i> On or before December 20<br />
<i>Late:</i> On or before January 13<br />
<i>Onsite:</i> After January 13</p>
