<h4 class="text2">Important Dates</h4>
<ul>
	<li>September 14, 2016: Paper submission deadline</li>
	<li>November 9, 2016: Author notification of acceptance or rejection</li>
	<li>November 28, 2016: Camera-ready copy due at the AAAI office</li>
	<li>February 5-6, 2016: EAAI-17</li>
</ul>
<div class="hr1"><hr /></div>

<p>
EAAI-17 provides a venue for researchers and educators to discuss pedagogical issues and share resources related to teaching and using AI in education across a variety of curricular levels (K-12 through postgraduate training), with an emphasis on undergraduate and graduate teaching and learning. The symposium seeks contributions showing how to more effectively teach AI, as well as how themes from AI may be used to enhance education more broadly (for example, in introductory computing courses or as a means for teaching computational thinking). We encourage the sharing of innovative educational approaches that convey or leverage AI and its many subfields, including robotics, machine learning, natural language processing, computer vision, and others.
</p>


<ul>
	<li>EAAI-17 is expected to include two invited talks (including a talk of the recipient of the AAAI/EAAI Outstanding AI Educator Award 2016); a panel with AI education experts; special tracks on Providing Research Experiences for Undergraduate Students, Best Practices for Running an AI Research Group, and AI for Education and Outreach; Model AI Assignments; the EAAI NSG Challenge; and other exciting activities.</li>
</ul>


<h4 class="text2">Topics</h4>

<p>
EAAI-17 provides a Main Paper Track with full-length papers (6 pages + 1 page of references) and extended abstract/poster contributions (2 pages, including references) on all topics of relevance to EAAI-17. EAAI-17 also provides a variety of Special Tracks, each with their own submission requirements (see the section on <a href="http://www.aaai.org/Conferences/AAAI/2017/eaai17call.php#Special">Special Tracks</a> that follows).
</p>

<p>
EAAI-17 welcomes Main Track paper submissions on a variety of topics, including (but not limited to) the following:
</p>

<ul>
	<li>Educational resources, including pedagogical strategies, innovative assignments, and curricular development related to AI</li>
	<li>Multidisciplinary curriculum efforts highlighting the application of AI in other contexts (such as computational biology, algorithmic game theory, or computational economics) and/or foundational concepts of AI in other fields (such as philosophy, cognitive science, linguistics, or psychology)</li>
	<li>The use of robotics and other tangible media in both AI courses and elsewhere in the curriculum</li>
	<li>Software that assists the teaching/learning process, such as software to help visualize search spaces and search algorithms or software substrates that can be used by students to do projects</li>
	<li>Resources and strategies for teaching specific AI sub-areas or topics (such as machine learning, robotics, computer vision, natural language processing, or game playing)</li>
	<li>Strategies for situating AI within a wider computer-science curriculum</li>
	<li>Ways to incorporate popular entertainment and media portrayal of AI into educational materials (such as in movies, news, or advertisements)</li>
	<li>Real-world examples of AI deployments, described in sufficient detail to provide case studies and/or serve as useful springboards for other educators</li>
	<li>Innovative means for integrating research as part of coursework in AI</li>
	<li>Material for teaching ethical considerations with regard to AI technology</li>
</ul>

<p>
We also encourage submissions to the Educational Video Track within the AAAI-17 Video Program.
</p>
<div class="hr1"><hr /></div>


<h4 class="text2"><a name="Special">Special Tracks</a></h4>

<h5>Special Paper Track: Providing Research Experiences for Undergraduate Students (REU)</h5>
<p><i>Organizer:</i> Matthew E. Taylor, Washington State University
<br />
The special track is about best practices about mentoring undergraduate students and involving them in research, including (but not limited to) NSF REU experiences. The submission requirements are the same as for the main track.
</p>

<h5>Special Paper Track: Best Practices for Running an AI Research Group</h5>
<p><i>Organizer:</i> Eric Eaton, University of Pennsylvania<br />
Part of an educator's job often involves running an effective research group, both for the production of high quality research artifacts and the education of junior researchers.  This special track will explore the best practices for running an effective research group, including techniques for managing numerous researchers and projects, running reading groups, ensuring high quality research and experiments, and tools for publication and software management.  Submissions to this track should focus on practical topics and techniques of use to early career scientists.  The submission requirements are the same as for the main track, with the addition that it will also accept shorter papers (2-4 pages, including references).</p>

<h5>Special Paper Track: AI for Education and Outreach</h5>
<p><i>Organizer:</i> Sheila Tejada, University of Southern California<br />
The special track is about using AI in applications for education to improve teaching and evaluation (for example, intelligent tutors or machine learning for MOOCs) or improve learning and retention of students (for example, educational robots, competitions, games, wearables, or K-12 outreach activities). The submission requirements are the same as for the main track.</p>

<h5><a href="http://www.aaai.org/Conferences/AAAI/2017/eaai17modelaicall.php" rel="nofollow">Model AI Assignments</a></h5>
<p><i>Organizer:</i> Todd Neller, Gettysburg College<br />
Good project assignments for AI classes are hard to come by. If you believe an assignment you have developed may be useful to other AI educators, we encourage you to prepare it for broad dissemination and submit it to the Model AI Assignments session.  If selected, the project will be made available to other AI educators as a Model AI Assignment (<a href="http://modelai.gettysburg.edu" rel="nofollow">modelai.gettysburg.edu</a>) and will be presented at EAAI (<a href="http://eaai.stanford.edu" rel="nofollow">eaai.stanford.edu</a>). The submission requirements are described in the <a href="http://modelai.gettysburg.edu" rel="nofollow">Call for Model AI Assignments</a> and at the separate <a href="http://eaai.stanford.edu" rel="nofollow">EAAI supplementary website.</a></p>

<h5><a href="http://www.aaai.org/Conferences/AAAI/2017/eaai17nsgcall.php" rel="nofollow">EAAI-17 NSG Challenge: Widely-Accessible AI Robotics Tasks</a></h5>
<p><i>Organizer:</i> Zach Dodds, Harvey Mudd College<br />
It is difficult to share curricula that include both AI and Robotics. Despite the difficulty, EAAI supports the development - and sharing - of compelling AI Robotics curricula. The 2017 EAAI NSG[1] challenge invites compelling and accessible AI robotics tasks designed for educational use. In a sense, this is a meta-robotics challenge, inviting AI robotics challenges that maximize both curricular worth and easy deployment. The submission requirements are described in the Call for Model AI Assignments and at the <a href="http://www.cs.hmc.edu/~dodds/nsgc17/" rel="nofollow">NSG Challenge website.</a></p>

<h4 class="text2">Submission Content and Formatting</h4>

<p>
Full-length submissions to the main or special paper tracks of EAAI-17 should describe well-developed ideas and/or pedagogical examples. Submissions are expected to provide in-depth arguments for the advantages of the proposed ideas. For example, a proposed curriculum could be evaluated by comparing it against existing ones or by presenting feedback from students obtained via questionnaires. Formal evaluations are welcome but not required. Extended abstract/poster submissions may highlight preliminary or ongoing work.
</p>


<p>
Papers submitted to the Main Track must be formatted in AAAI two-column, camera-ready style. Special Tracks may have their own submission requirements, detailed above.  EAAI submissions should be anonymous whenever possible, and should be formatted for double-blind review. Full-length submissions may have up to 7 pages with page 7 containing nothing but references. Shorter submissions of 2-4 pages are permitted, depending on the track; those page limits include references. The AAAI copyright block is not required on submissions, but must be included on final versions.
</p>


<h4 class="text2">Policy Concerning Submissions to Other Conferences or Journals</h4>

<p>
EAAI-17 will not consider any paper that, at the time of submission, is under review for or has already been published or accepted for publication in a journal or another conference. Once submitted to EAAI-17, authors may not submit the paper elsewhere during EAAI/AAAI's review period. These restrictions apply only to refereed journals and conferences, not to unrefereed forums or workshops with a limited audience and without archival proceedings. Authors must confirm that their submissions conform to these requirements at the time of submission.
</p>


