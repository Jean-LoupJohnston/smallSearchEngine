<h2 class="backgrnd6"><span class="text1">The Seventh Symposium on Educational Advances in Artificial Intelligence</span></h2>

<div class="content">
<p><i>Sponsored by the Association for the Advancement of Artificial Intelligence and Colocated with AAAI-17</i></p>
