<h2 class="backgrnd12"><span class="text1">AAAI-13 Trading Agent Competition</span></h2>

<p>The AAAI-13 Trading Agent Competition will be held 
July 15&ndash;16, 2013 in Bellevue, Washington USA</p>


<p>The Trading Agent Competition is an international forum aiming to encourage and promote high quality research in the technology underlying trading agents. The competition has been held annually since 2000 and has attracted participants from multiple institutions worldwide. This year TAC is collocated with AAAI and its workshop TADA (the Workshop on Trading Agents Design and Analysis) will be held on Monday July 15. The semifinals and finals of TAC will take place on July 15 and 16. The competition will involve two scenarios:</p>

<p><b>Power TAC:</b>  Sustainable energy systems of the future will need more than efficient, clean, low-cost, renewable energy sources; they will also need efficient price signals that motivate sustainable energy consumption as well as a better real-time alignment of energy demand and supply. In Power TAC, agents act as retail brokers in a local power distribution region, purchasing power from a wholesale market as well as from local sources, such as homes and businesses with solar panels, and selling power to local customers and into the wholesale market. Retail brokers must solve a supply-chain problem in which the product is infinitely perishable, and supply and demand must be exactly balanced at all times.</p>

<p><b>TAC Ad Auctions:</b> In the TAC/AA game, agents representing Internet advertisers bid for search-engine ad placement over a range of interrelated keyword combinations. A back-end search-user model translates placement over each simulated day to impressions, clicks, and sale conversions, yielding revenue for the advertiser. Advertiser strategies combining online data analysis and bidding tactics compete to maximize profit over the simulated campaign horizon.</p>

<p>The overall organizer of TAC is the <a href="http://tradingagents.org/organisation/" rel="nofollow">Association for Trading Agent Research</a>.</p>

<p>General Chair, TAC 2013<br />
Ioannis Vetsikas<br />
National Center for Scientific Research,<br />
Demokritos&acirc;, Greece
</p>




