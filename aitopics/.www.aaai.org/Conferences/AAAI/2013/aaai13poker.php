<h2 class="backgrnd12"><span class="text1">AAAI-13 Computer Poker Competition</span></h2>
<div class="hr1">
<hr />
</div>

<p>The AAAI-13 Computer Poker Competition will be held 
July 14&ndash;18, 2013 in Bellevue, Washington USA</p>

<p>
In recent years, poker has emerged as an important challenge problem
for AI. Just as the development of world-class chess-playing programs
was considered an important milestone in the development of
intelligent computing, poker is increasingly being seen in the same
light. Several important features differentiate poker from other games
such as chess or Go including the presence of imperfect information (due
to hidden cards), and stochastic events.
</p>

<p>
The AAAI Annual Computer Poker Competition, now in its eighth year,
showcases state-of-the-art intelligent programs for playing poker.
This is the premiere venue for demonstrating poker-playing software
systems, as exemplified by the previous years' competitions. The 2012
competition featured 29 poker-playing programs submitted from ten
countries.
</p>

<h3 class="text2">Competition Formats</h3>

<p>
The games played in the 2013 competition will be variants of Texas
Hold'em. As in recent years, we will have divisions for two-player
limit, three-player limit and two-player no-limit. With many
interesting challenges in all three categories, we expect this year's
competition to continue to spur the development of new techniques.
</p>

<p>
If you have any questions or comments about the AAAI Computer Poker
Competition, we encourage you to address them to the competition
cochairs, Neil Burch and Eric Jackson, or to participate in the
competition's online discussion forum.
</p>

<p>
The source code, rules of the competition, and other information may
be found at the competition website.
</p>

<h3 class="text2">AAAI-13 Computer Poker Workshop</h3>

<p>
Following up on the successful Computer Poker Symposium from last year,
we will have a workshop this year focusing on computer poker. Dates and
details remain to be determined. Please check back later for more
information.
</p>

