<h4>Tutorial Forum Registration</h4>
<p>(For tutorial information, please see the <a href="aaai13tutorials.php">AAAI-13 Tutorials page.</a>)

<p>The AAAI-13 Tutorial Forum is open to all technical registrants. Please select up to four consecutive tutorials on Sunday, July 14 and Monday, July 15. Tutorial syllabi will be available for download at the individual speaker's websites in July.</p>

