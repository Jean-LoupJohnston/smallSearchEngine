<p>The AAAI-13/IAAI-13 technical program registration includes participation in EAAI-13 for invited participants and other interested individuals. For planning purposes, please notify us of your intention to attend on the online registration form. If you are attending the EAAI-13 sessions only, please contact AAAI at &#97;&#97;&#97;&#105;&#49;&#51;&#64;&#97;&#97;&#97;&#105;&#46;&#111;&#114;&#103; for further information.</p>

