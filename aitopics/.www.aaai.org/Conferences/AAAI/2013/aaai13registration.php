<h4>AAAI-13 Banquet</h4>
<p>The AAAI-13 Banquet will be held Wednesday evening, July 17, at the Bellevue Arts Museum (BAM), 510 Bellevue Way NE, Bellevue, WA 98004. BAM serves as the Pacific Northwest's center for art, craft and design, with an emphasis on Northwest artists. The banquet fee includes one complimentary beverage, and a three-course, sit-down dinner with wine. Access to the galleries will be available after dinner. Space is limited.</p>

<p><b>AAAI-13 Banquet</b><br />
<i>Early:</i> &nbsp; $115 per person<br />
<i>Late:</i> &nbsp; $125 per person<br />
<i>Onsite:</i> &nbsp; $150 per person</p>
