<h4>Opening Reception and Poster/Demo Session</h4>

<p>The Opening Reception will be held on Monday, July 15 at 6:30 PM, and the Poster/Demo Session will be held Tuesday, July 16 at 5:45 PM, both in the Hyatt Regency Bellevue Hotel. Admittance to the opening reception and the conference-wide poster/demo session are included in the AAAI-13 technical registration fee. Guests and children are welcome for the following fees:</p>

<p><b>Opening Reception Guest</b><br />
  Adult &nbsp; $60.00<br />
  Child &nbsp; $30.00</p>

<p><b>Poster/Demo Session Guest</b><br />
  Adult &nbsp; $50.00<br />
  Child &nbsp; $25.00</p>
<p><a href="http://www.regonline.com/aaai13" rel="nofollow"><i>Online Registration Form</i></a></p>

