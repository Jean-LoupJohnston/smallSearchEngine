<h4>AAAI-13 Hard Copy Proceedings</h4>

<p>Preregistrants may order a hard copy of the AAAI-13 proceedings, which will be mailed after the conference. The pre-conference discounted price is $95.00 per copy, plus shipping and handling. Individuals who select this option will be contacted regarding the shipping and handling fees after the conference. (Proceedings will be $220 after the conference.)</p>

<div class="hr1">
<hr />
</div>
<h4>Payment Information</h4>

<p>Payment of registration fees is required at the time of registration. Checks and bank transfers must be in US dollars. American Express, MasterCard, and VISA are also accepted. Registration applications must be submitted online by the registration deadlines to qualify for reduced rates. If you qualify for student rates, please submit your proof of student status to AAAI by email attachment to aaai13@aaai.org or by fax to +1-650-321-4457.</p>

<h5>Refund Requests</h5>

<p>The deadline for refund requests is June 21, 2013. All refund requests must be made in writing to &#97;&#97;&#97;&#105;&#49;&#51;&#64;&#97;&#97;&#97;&#105;&#46;&#111;&#114;&#103;. A $100.00 processing fee will be assessed for all refunds.</p>

