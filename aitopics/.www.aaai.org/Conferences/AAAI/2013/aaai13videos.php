			<h2 class="backgrnd12"><span class="text1">AAAI-13 AI Video Competition</span></h2>

<h4>Call for Video Submissions</h4>
<img src="http://www.aaai.org/Conferences/AAAI/2011/Photos/shakey.jpg" alt="AAAI Video Awards" width="258" height="272" class="img1" />
<h3 class="text2">Timetable for Submissions</h3>

<ul>
	<li>Submission Deadline: April 30, 2013</li>
	<li>Reviewing Decision Notifications and Award Nominations: May 31, 2013</li>
	<li>Final Version Due: June 16, 2013</li>
	<li>Screening and Award Presentations: During the AAAI Conference, July 14&ndash;15, 2013, Bellevue, Washington, USA</li>
</ul>


<p>
AAAI is pleased to announce the continuation of the AAAI Video Competition, now entering its seventh year. The video competition will be held in conjunction with the AAAI-13 conference in Bellevue, Washington, USA, July 14&ndash;18, 2013. At the award ceremony, authors of award-winning videos will be presented with "Shakeys," trophies named in honor of SRI's Shakey robot and its pioneering video. Award winning videos will be screened at this ceremony.
</p>

<p>
The goal of the competition is to show the world how much fun AI is by documenting
exciting artificial intelligence advances in research, education, and application. View
previous entries and award winners at the <a href="http://www.aaaivideos.org/past_competitions" rel="nofollow">Video Competition website.</a> 
</p>

<p>
The rules are simple: Compose a short video about an exciting AI project, and narrate it
so that it is accessible to a broad online audience. We strongly encourage student
participation.
</p>

<h3 class="text2">Video Format and Content</h3>
<p>
Either 1 minute (maximum) short video or a 5 minute (maximum) long video, with English narration (or English subtitles). Consider combining screen shots, interviews, and video of a system in action. Make the video self-contained, so that newcomers to AI can understand and learn from it. We encourage a good sense of humor, but will only accept submissions with serious AI content. For example, we welcome submissions of videos that accomplish the following:
</p>

<ul>
	<li>Highlight a research topic &mdash; contemporary or historic, your own or from another group</li>
	<li>Introduce viewers to an exciting new AI-related technology</li>
	<li>Provide a window into the research activities of a laboratory and/or senior researcher</li>
	<li>Attract prospective students to the field of AI</li>
	<li>Explain AI concepts &mdash; your video could be used in the classroom</li>
</ul>

<p>
Please note that this list is not exhaustive. Novel ideas for AI-based videos, including
those not necessarily based on a "system in action," are encouraged.  No matter what your
choice, creativity is encouraged! (Please note: The authors of previous, award-winning
videos typically used humor, background music, and carefully selected movie clips to make
their presentations come alive.)
</p>
<p>
Please also note that videos should contain only material for which the authors own copyright. Clips from films or television and music for soundtrack should only be used if copyright permission has been granted by the copyright holders, and this written permission accompanies the video submission.
</p>


<h3 class="text2">Submission Instructions</h3>
<p>
Submit your video by making it available for download on a (preferably password-protected)
ftp or web site. Once you have done so, please fill out the <a href="http://www.aaaivideos.org/aaaistatic/submission/aaai_video_comp_2013_submission_form.txt" rel="nofollow">submission form</a>
and send it to us by email (&#115;&#117;&#98;&#109;&#105;&#115;&#115;&#105;&#111;&#110;&#64;&#97;&#97;&#97;&#105;&#118;&#105;&#100;&#101;&#111;&#115;&#46;&#111;&#114;&#103;). All submissions are due no later
than April 30, 2013.
</p>


<h3 class="text2">Review and Award Process</h3>
<p>
Submitted videos will be peer-reviewed by members of the programme committee according to
the criteria below. Videos that receive positive reviews will be accepted for publication
in the AAAI Video Competition proceedings, published on the <a href="http://www.aaaivideos.org" rel="nofollow">AI Videos dedicated website.</a>
The best videos will be nominated for awards. Winners will be
revealed at the award ceremony during AAAI-13.
</p>

<p>
All authors of accepted videos will be asked to sign a distribution license form.
</p>


<p>
Review criteria:
</p>

<ol>
	<li>Relevance to AI (research or application)</li>
	<li>Excitement generated by the technology presented</li>
	<li>Educational content</li>
	<li>Entertainment value</li>
	<li>Presentation (cinematography, narration, soundtrack, production values)</li>
</ol>

<h3 class="text2">Award Categories</h3>
<p>
Best Video, Best Short Video, Best Student Video, Most Jaw-Dropping Technology,
Most Educational, Most Entertaining and Best Presentation.
(Categories may be changed at the discretion of the chairs.)
</p>


<h3 class="text2">Awards</h3>
<p>
Trophies ("Shakeys").
</p>

<h3 class="text2">For More Information</h3>
<p>
For more Information please visit the<a href="http://www.aaaivideos.org" rel="nofollow"> supplementary AI Video website.</a>
</p>

<p>
We look forward to your participation in this exciting event!
</p>

