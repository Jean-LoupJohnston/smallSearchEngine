<h3 class="text2">Timetable for Authors</h3>
<ul>
	<li>March 15, 2015 &ndash; April 9, 2013: Authors register on the AAAI web site</li>
	<li>April 9, 2013: Electronic papers due</li>
	<li>April 30, 2013: Notification of acceptance or rejection</li>
	<li>May 9, 2013: Camera-ready copy due at AAAI office</li>
</ul>

<p>AAAI-13 invites short papers on late-breaking developments in the 
field of artificial intelligence, to be presented at AAAI-13 in 
Bellevue, WA. The purpose of late-breaking papers is to provide 
conference attendees with information about research that was 
initiated, enhanced, improved, or completed after the original paper 
submission deadline in January 2013. All accepted papers will be 
presented in poster format during the conference.</p>

<h3 class="text2">Paper Submission</h3>
<p>Late-breaking papers are limited to two pages, formatted in AAAI 
two-column, camera-ready style. References may be included on a third 
page. Note that NO OTHER MATERIAL may be included on the third page. 
Because of the short turnaround time for reviewing, submissions that 
violate this restriction or that do not follow the formatting 
guidelines will be rejected without review.</p>

<p>Submissions to the late-breaking paper track are not anonymous, so 
author names and contact information should be included. Papers must 
be formatted in AAAI two-column, camera-ready style, and must be in 
trouble-free, high-resolution PDF format, formatted for
US Letter (8.5" x 11") paper, using Type 1 or TrueType fonts.
(See the <a href="http://www.aaai.org/Publications/Templates/AuthorKit.zip">AAAI Author Kit</a> for formatting details and instructions.)</p>

<p>When submitting their paper, authors should choose only the SINGLE 
TOPIC the best describes the area of their paper. This mechanism will 
let us assign papers to reviewers quickly and accurately. Please DO 
NOT select multiple topics.</p>

<p>
The submission website will close at 11:59 PM PDT on April 9, 2013.
</p>

<h3 class="text2">Policy Concerning Previously Published And Resubmitted Work</h3>
<p>
Late-breaking papers are intended to showcase novel, exciting, 
ongoing work. They are not meant to be short papers describing 
previously published work, nor should they be considered as simple 
"fallback positions" for work that was rejected from the main track. 
Therefore, on the submission page, authors will be asked whether any 
work in the area by the authors was previously submitted to any 
AAAI-13 track or previously published in another conference or 
journal. For such previously submitted work, authors will be required 
to explain why this work is appropriate for the late-breaking paper 
track. (Specifically, it should summarize improvements since these 
prior submissions.)
</p>

<h3 class="text2">Review Process</h3>
<p>
Authors will be asked to select a single most relevant topic from a 
list of possible topics, and papers will be assigned to reviewers 
(members of the AAAI-13 program committee) based on those topics. 
Each late-breaking paper submission will be reviewed by at least two 
reviewers. Review criteria include innovation, quality, and relevance 
to the AI community. Due to the rapid turnaround time, no author 
response phase will be included. Final decisions will be made by the 
program cochairs.
</p>

<h3 class="text2">Presentation and Publication</h3>
<p>
Accepted late-breaking papers will be presented as posters at the 
conference and will also receive a short oral "lightning 
presentation" in a special session during the conference. As with 
papers in the main track, at least one author must attend the 
conference to present any accepted late-breaking paper. Each accepted 
paper will be allocated two pages, plus an additional page for 
references, in a technical report published by AAAI Press. Final 
papers found to exceed page limits and or otherwise violating the 
instructions to authors will not be included in the proceedings. 
Authors will retain copyright of their work but will be required to 
sign a distribution license so that the paper can be distributed in 
AAAI's digital library. Since late-breaking papers will not be 
published in the proceedings volume, they (or longer submissions that 
include the paper) can be submitted to a future AAAI conference or 
elsewhere without violating the dual-submission policy.
</p>


