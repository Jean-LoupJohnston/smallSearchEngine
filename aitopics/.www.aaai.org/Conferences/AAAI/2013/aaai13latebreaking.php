<h2 class="backgrnd12"><span class="text1">AAAI-13 Late-Breaking Papers</span></h2>
<div class="hr1">
<hr />
</div>

<h3 class="text2"><a href="https://www.easychair.org/conferences/?conf=aaai13lbptrack" rel="nofollow">Submission Site</a></h3>

<div class="hr1">
<hr />
</div>

<p><a href="http://www.aaai.org/Publications/Templates/AuthorKit.zip">Author Kit</a></p>
<div class="hr1">
