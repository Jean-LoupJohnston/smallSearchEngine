Single/Double Occupancy: $179.00<br />
Triple Occupancy: $204.00 CAD<br />
Quadruple Occupancy: $229.00 CAD<br />
Check-in time: 3:00 pm<br />
Check-out time: 12:00 pm<br />
Cut-off date for conference rate reservations: 5:00 PM local hotel time (PDT), Wednesday, June 19, 2013</p>

<p>
Reservation requests received after the cut-off date will be based on availability at the Hotel's prevailing rates. Reservations should be made directly with the Hyatt Regency Bellevue through one of the methods listed above. Rate is per room, per night, excluding applicable state and local taxes (currently 14.4%). Rates include complimentary wireless Internet access in guest sleeping rooms and hotel's lobby public spaces.
</p>

<p>
Rooms must be guaranteed by a major credit card. Cancellations made within 24 hours of arrival will forfeit one night's room and tax.
</p>

<h3 class="text2">Parking</h3>
<p>
Self parking is available at $25 per day, Sunday - Thursday nights. Friday and Saturday nights are complimentary (8pm Friday until 12am Sunday). Valet Parking is available for $28 per day.
</p>

<h3 class="text2">Additional Hotel Information</h3>
<p>
The Hyatt Regency Bellevue on Seattle's Eastside is 20 minutes from Sea-Tac International Airport and nine miles east of Seattle. It serves as an anchor within the trendy Bellevue Collection. Stroll through connecting sky bridges and discover more than 250 shops, 45 restaurants and lounges and plenty of entertainment options directly adjacent to the <a href="http://www.bellevue.hyatt.com/" rel="nofollow">hotel</a>.
</p>
