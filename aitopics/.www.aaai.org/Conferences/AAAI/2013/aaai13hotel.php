<h3 class="text2">Getting to Bellevue</h3>
<p>The Hyatt Regency Bellevue is accessible from the Sea-Tac International Airport (SEA). The Hyatt is about 30 minutes by taxi and will cost approximately $45.00&ndash;50.00 one way.</p>

<h3 class="text2">Airport Transportation</h3>
<p>For more information about ground transportation options between Sea-Tac International Airport and the Hyatt, please visit  the <a href="http://www.portseattle.org/Sea-Tac/Parking-and-Transportation/Ground-Transportation" rel="nofollow">Sea-Tac Parking,</a><a href="http://www.visitbellevuewashington.com/gettinghere/buses-shuttles.shtml"> Visit Bellevue Washington,</a> or service provider websites.</p>

<h3 class="text2">Shuttle Express</h3>
<p><a href="http://www.shuttleexpress.com/" rel="nofollow">www.shuttleexpress.com</a><br />
(425) 981-7000</p>
<p>Shuttle Express provides service to and from Sea-Tac Airport: Passengers are picked up at the inner drive curb on the third floor of the airport garage. The Shuttle Express booth is located between elevator banks 3 and 4, next to the Ground Transportation booth. Call 425-981-7000 for reservations. Passengers are dropped off at their airline. Shared ride, door-to-door service to Bellevue and other Puget Sound communities. Typical cost between Bellevue and Sea-Tac Airport is $19&ndash;$22 per person.</p>

