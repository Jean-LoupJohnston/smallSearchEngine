<h2 class="backgrnd12"><span class="text1">AAAI-13 Hotel Information</span></h2>
<p>AAAI has reserved a block of rooms at the Hyatt Regency Bellevue at reduced conference rates. Space is limited. (Information about discounted student accommodations at the Hyatt Regency Bellevue is available by writing to &#97;&#97;&#97;&#105;&#49;&#51;&#115;&#116;&#117;&#100;&#101;&#110;&#116;&#115;&#64;&#97;&#97;&#97;&#105;&#46;&#111;&#114;&#103; Proof of full-time student status required.)</p>


<p>Hyatt Regency Bellevue Hotel<br />
900 Bellevue Way NE<br />
Bellevue, WA 98004 USA<br />
www.bellevue.hyatt.com<br />
Hotel Phone: +1 425 462 1234</p>

<p><a href="https://resweb.passkey.com/Resweb.do?mode=welcome_ei_new&eventID=9724847">AAAI-13 Online Reservations Link</a><br />
Reservations by telephone: 1-888-421-1442<br />
