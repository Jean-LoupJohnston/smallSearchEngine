<h2 class="backgrnd12"><span class="text1">AAAI-13 Sponsorship Program</span></h2>

<h3 class="text2"><a href="http://www.aaai.org/Conferences/AAAI/2013/aaai13sponsorship.pdf">Sponsorship Brochure and Contract</a></h3>
<div class="hr1">
<hr />
</div>


<p>Bellevue, Washington, USA<br />
July 14-18, 2013</p>

<p>
On behalf of the Conference Committee of the Twenty-Seventh AAAI Conference on Artificial Intelligence, we invite you to participate in the sponsor program for AAAI-13. The conference will be held July 14-18, 2013 in Bellevue, Washington, USA, and is sponsored by the Association for the Advancement of Artificial Intelligence (AAAI). The site of the conference will be the Hyatt Regency Bellevue Hotel, located on Seattle's Eastside. The greater Seattle area is a thriving AI center &mdash; home to both Microsoft and the University of Washington, among several other AI centers. AAAI is pleased to announce its partnership with the local AI community to showcase local research at AAAI-13.
</p>

<p>
AAAI is the annual North American artificial intelligence conference, attracting over 1,000 AI researchers and practitioners, representing government, university and corporate research facilities from throughout the world. Your participation in the AAAI-13 Sponsor Program will give you instant visibility to this diverse group of AI professionals, representing a host of research areas such as robotics, AI and the web, cognitive systems, AI applications, computational sustainability and AI, human computation, interactive digital entertainment, education, and recommender systems.
</p>

<p>
As part of most levels of sponsorship, you will be provided with a table-top exhibit throughout the technical portion of the conference. This heightened visibility will provide an opportunity for one-on-one exchange with conference participants. Whether you want to showcase your intelligent technology to the AI community, or tap into this incredible source of talent, AAAI-13 is the premier venue to accomplish these goals.
</p>

<h3 class="text2">Named Events</h3>

<p>
The conference includes several major events that can be named in honor of major contributors. These naming opportunities bring very high visibility but are limited in number. We urge companies considering this level of support to contact AAAI as soon as possible. Preference will be given to companies who commit early and/or commit to support the event for multiple years. See the next section for the additional benefits of Platinum, Gold, and Silver sponsorships.
</p>

    <ul>
	<li>Conference Banquet: $25,000. Includes a complimentary Platinum sponsorship.</li>
	<li> Opening Reception: $25,000. Includes a complimentary Platinum sponsorship.</li>
	<li>Poster Session Reception: $15,000. Includes a complimentary Gold sponsorship.</li>
	<li>Distinguished Lecture: $10,000 (per lecture).<br />
	One of the lectures by a distinguished invited speaker will be named after your organization. Includes a complimentary Silver sponsorship.</li>
	<li> WiFi Internet Access: $10,000. Includes a complimentary Silver sponsorship.</li>
</ul>

<h3 class="text2">Sponsorship Levels and Benefits</h3>
<p>
Each AAAI-13 Sponsor will receive their company name and/or logo prominently displayed on the following:
</p>

   <ul>
	<li> Printed materials, as schedule permits</li>
	<li>    AAAI-13 website</li>
	<li>    AAAI-13 registration area sign</li>
	<li>    Signs at appropriate conference events</li>
</ul>

<p>
Certain levels of sponsorship also include the following:
</p>

    <ul>
	<li>Complimentary conference registration(s) and banquet ticket(s)</li>
	<li>    Exhibit tabletop(s) in conference center foyer</li>
	<li>    Sponsor-supplied inserts in conference registration packets</li>
</ul>

<h3 class="text2">Platinum ($25,000)</h3>
<p>
4 Complimentary registrations; 2 exhibit table-tops; a registration packet insert of up to 4 pages
</p>

<h3 class="text2">Gold ($15,000)</h3>
<p>
3 Complimentary registrations; 2 exhibit table-tops; a registration packet insert of up to 2 pages
</p>

<h3 class="text2">Silver ($10,000)</h3>
<p>
2 Complimentary registrations; 1 exhibit table-top; a registration packet insert of up to 1 page
</p>

<h3 class="text2">Bronze ($5,000)</h3>
<p>
1 Complimentary registration; 1 exhibit table-top
</p>

<p>
