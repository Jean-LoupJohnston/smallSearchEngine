<h2 class="backgrnd12"><span class="text1">AAAI-13 Tutorial Forum</span></h2>

<p><i>Call for Proposals</i></p>

<h3 class="text2">Timetable for Proposals</h3>
<ul>
	<li>November 15, 2012: Tutorial Proposals due to tutorial chairs</li>
	<li>December 14, 2012: Tutorial Acceptances mailed</li>
	<li>January 4, 2013: Tutorial descriptions, autobiographical statements,
and speaker photos due</li>
	<li>June 15, 2013: Completed course materials must be posted on speaker's website</li>
	<li>July 14&ndash;15, 2013: AAAI-13 Tutorial Forum</li>
</ul>
<p>The AAAI-13 Program Committee invites proposals for the Tutorial Forum of the Twenty-Seventh AAAI Conference on Artificial Intelligence (AAAI-13). The Tutorial Forum will be held July 14&ndash;15, 2013 in Bellevue, Washington, USA. Anyone interested in presenting a tutorial at AAAI-13 should submit a proposal to the 2013 Tutorial Forum Cochairs via <a href="https://www.easychair.org/conferences/?conf=aaai13tf" rel="nofollow">EasyChair.</a>
<h3 class="text2">What Is the Tutorial Forum?</h3>
<p>
The Tutorial Forum provides an opportunity for researchers and practitioners to spend two days each year freely exploring exciting advances in disciplines outside their normal focus. We believe this type of forum is essential for the cross fertilization, cohesiveness, and vitality of the AI field. We all have a lot to learn from each other and the Tutorial Forum promotes the continuing education of each member of AAAI. To encourage full participation by technical conference registrants, no separate fee will be charged for admittance to the Tutorial Forum in 2013.
</p>
<h3 class="text2">Topics</h3>
<p>
Our goal is to present a diverse program that includes core areas of AI, new techniques from allied disciplines that can inform research within AI, and conversely emerging applications of AI techniques to new areas. Previous tutorial programs provide an indication of the scope and variety of possible topics. The list is not exclusive; indeed, we are expressly interested in topics that we would not have imagined to mention. Finally, note that we very much welcome proposals for educational approaches that go beyond the traditional format of four-hour tutorials, exploiting the flexibility that the open format program offers. Although the majority of tutorials in the past have been four hours, we also encourage quarter-day or full-day formats as the topic dictates.
</p>
<h3 class="text2">Submission Requirements</h3>
Each proposal should include at least the following:
<p><i>Goal of the tutorial:</i> Who is the target audience? What will the audience walk away with? What makes the topic innovative?</p>
<p><i>History:</i> List of previous venues and approximate audience sizes, if the same or a similar tutorial has been given elsewhere; otherwise an estimate of the audience size.
<p><i>Content:</i> Detailed outline and list of additional materials, augmented with samples, such as past tutorial slides and survey articles, whenever possible. Be as complete as possible.</p>
<p><i>Tutorial description:</i> A short paragraph summarizing the tutorial outline, and the intended duration of the symposium (default is four hours).</p>
<p><i>Prerequisite knowledge:</i> What knowledge is assumed of the target audience.</p>
<p><i>Please also submit the following information about the team of presenters:</i> name, mailing address, phone number, email address; background in the tutorial area, including a list of publications and/or presentations; any available examples of work in the area (ideally, a published tutorial-level article or presentation materials on the subject); evidence of teaching experience (courses taught or references); and evidence of scholarship in AI or computer science.
</p>

<h3 class="text2">Submission Deadline</h3>
<p>
Proposals must be received by November 15, 2012. Decisions about the tutorial program will be made by December 14, 2012. Speakers should be prepared to submit their tutorial descriptions and bios by January 4, 2013, and to post completed course materials on their websites by June 15, 2013.
</p>


