<h2 class="backgrnd12"><span class="text1">Please Join Us for the Thirty-First AAAI Conference (AAAI-17)!</span></h2>

<h2><a href="https://www.regonline.com/builder/site/Default.aspx?EventID=1900653">Register!</a>&nbsp;&nbsp;4 &ndash; 9 February &nbsp;&ndash;&nbsp; San Francisco, California  USA</h2>
<p><b></b></p>
<div class="hr1"><hr /></div>
<ul>
	<li><a href="https://guidebook.com/g/aaai17" "rel="nofollow">AAAI Guidebook Schedule</a>
<br /></li>
</ul>
<div class="hr1">
<hr /></div>
<p><a href="http://videolectures.net/aaai2017_sanfrancisco/" rel="nofollow">Selected lectures and programs from this conference have been posted at VideoLectures.Net.</a></p>

<p>The Thirty-First AAAI Conference on Artificial Intelligence (AAAI-17) will be held February 4&ndash;9 at the Hilton San Francisco, San Francisco, California, USA. The workshop, tutorial, and doctoral consortium programs will be held Saturday and Sunday, February 4 and 5, followed by the technical program, Monday through Thursday, February 6&ndash;9.
</p>


<p>
The chairs of AAAI-17 are Satinder Singh (University of Michigan) and Shaul Markovitch (Technion-Israel Institute of Technology).
</p>

<p>The purpose of the AAAI conference is to promote research in artificial intelligence (AI) and scientific exchange among AI researchers, practitioners, scientists, and engineers in affiliated disciplines. AAAI-17 will have a diverse technical track, student abstracts, poster sessions, invited speakers, tutorials, workshops, and exhibit and competition programs, all selected according to the highest reviewing standards. AAAI-17 welcomes submissions on mainstream AI topics as well as novel crosscutting work in related areas.</p>

