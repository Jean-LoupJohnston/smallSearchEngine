<h3>AAAI Code of Conduct for Events and Conferences</h3>
<ul>
<li>All persons, organizations and entities that attend AAAI conferences and events are subject to the <a href="https://aaai.org/Conferences/code-of-conduct.php">standards of conduct set forth here</a>. AAAI expects all community members to formally endorse this code of conduct, and to actively prevent and discourage any undesired behaviors. Everyone should feel empowered to politely engage when themselves or others are disrespected, and to raise awareness and understanding of this code of conduct. AAAI event participants asked to stop their unacceptable behavior are expected to comply immediately. Sponsors are also subject to this code of conduct in their participation in AAAI events.</li>
<li>For inquiries about this code, please contact <a href="mailto:aaaiethics@aaai.org">aaaiethics@aaai.org</a>.</li>

</ul>

