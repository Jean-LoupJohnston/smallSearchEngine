<h2 class="backgrnd12"><span class="text1">The Fourth International AAAI Conference on Weblogs and Social Media</span></h2>
<div class="content">
<p><i>Sponsored by the Association for the Advancement of Artificial Intelligence</i></p> <p>The Fourth International AAAI Conference on Weblogs and Social Media will be held in May 23&ndash;26, 2010, in Washington, DC USA. This international venue brings together researchers from the disciplines of natural language, social psychology, data mining, sociology, and visualization to increase our understanding of social media in all its incarnations. Research that blends social science and technology is especially encouraged.</p>

<p>The 2010 meeting will be held in Washington DC, where government innovators are experimenting with the use of social media to increase transparency and better engage with the citizenry. The conference will take advantage of this venue to invite leaders from "the goverati" to share their experiences in the use of social media.</p>
