<h3>Keynote Speakers And Tutorial Speakers</h3>
<p>The keynote speakers for ICWSM-10 will be Bob Kraut (Carnegie Mellon University) and Michael Kearns (University of Pennsylvania). Macon Phillips (U.S. White House, Head of New Media), 
James Pennebaker (Department of Psychology, University of Texas), 
Nicole Ellison (Department of Telecomm, Michigan State University), 
S. Craig Watkins (Department of Radio, TV, and Film, University of Texas)
Don Burke (CIA, Intellipedia), and
Haym Hirsh (NSF IIS) will all present invited talks.</p>

