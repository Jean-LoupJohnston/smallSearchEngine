<h2 class="backgrnd12"><span class="text1">The Fifth International AAAI Conference on Weblogs and Social Media</span></h2>
<div class="content">
<p><i>Sponsored by the Association for the Advancement of Artificial Intelligence</i></p> <p>The Fifth International AAAI Conference on Weblogs and Social Media was held July 17&ndash;21, 2011 in Barcelona (Spain) and was be collocated with IJCAI-11. The International Conference on Weblogs and Social Media is a unique forum that brings together researchers from the disciplines in computer science, linguistics, communication, and the social sciences. The broad goal of ICWSM is to increase understanding of social media in all its incarnations. Submissions describing research that blends social science and technology are especially encouraged.</p><p>

Though this conference is relatively new, it has become one of the premier venues for social scientists and technologists to gather and discuss cutting-edge research in Social Media. This is largely due to a typical acceptance rate of 20 percent for full-length research papers and support from Association for the Advancement of Artificial Intelligence (AAAI).
</p><p>
For ICWSM-11, in addition to the usual program of contributed technical talks, tutorials and invited presentations, the main conference included a selection of keynote talks from prominent social scientists and technologists. This year, for the first time, the workshop program was expanded to an additional day, in addition to the usual tutorial and main conference. There were also opportunities to connect with people in the IJCAI community both formally and informally (a joint social event was held).

