<h2 class="backgrnd12"><span class="text1">The Third International AAAI Conference on Weblogs and Social Media</span></h2>
<div class="content">
<p><i>Sponsored by the Association for the Advancement of Artificial Intelligence</i></p> <p>ICWSM is an interdisciplinary conference that brings together researchers and industry leaders interested in creating and analyzing social media. Past conferences have included technical papers from areas such as computer science, linguistics, psychology, statistics, sociology, multimedia and semantic web technologies. The Third International AAAI Conference on Weblogs and Social Media will be held in May 17&ndash;20, 2009, in San Jose, California USA.</p>
<p>The social and community driven aspects of our digital lives continue to rapidly increase, resulting in transformative behaviours and, significantly, publishing and distributing huge amounts of fascinating data. The International Conference on Weblogs and Social Media will meet once more in 2009 to discuss the latest research analyzing and leveraging this resource. As with previous meetings, we will bring together a wide range of researchers and industry practitioners from many disciplines providing a unique opportunity for sharing ideas and collaboration in this space.</p>

<p>
Disciplines which are relevant to our meeting include computer science, linguistics, psychology, statistics, sociology, multimedia and semantic web technologies. The following are key areas of interest:</p>

<ol>
	<li>Psychological, personality-based and ethnographic studies of social media</li>
	<li>Analyzing relationship between social media and mainstream media</li>
	<li>Centrality/influence of social media publications and authors both within genres and between genres of data</li>
	<li>Ranking/relevance of blogs; web page ranking based on blogs</li>
	<li>Data acquisition: crawling/spidering and indexing</li>
	<li>Human computer interaction; social media tools; navigation and visualization</li>
	<li>Multimedia: tools and techniques for distribution, sharing, and analysis of social activity with/around multimedia.</li>
	<li>Semantic analysis; cross-system and cross-media name tracking; named relations and fact extraction; discourse analysis; summarization</li>
	<li>Semantic web; unstructured knowledge management; collaborative creation of structured knowledge</li>
	<li>Subjectivity in textual data; sentiment analysis; polarity/opinion identification and extraction</li>
	<li>Social network analysis; communities identification; expertise and authority discovery; collaborative filtering</li>
	<li>Text categorization; topic recognition; demographic/gender/age identification</li>
	<li>Trend identification and tracking; time series forecasting; measuring predictability of phenomena based on social media</li>
	<li>New social media applications; interfaces; interaction techniques</li>
<li>Trust; reputation; recommendation systems</li>
</ol>
