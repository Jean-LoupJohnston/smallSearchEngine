</div>

<h3>Data Challenge</h3>
<p>ICWSM-09 is planning to release a large blog dataset in conjunction with the conference. This data will include the full content and markup of the blog post as well as extracted text. The conference invites researchers to explore the dataset and submit their findings as technical papers. <a href="http://www.icwsm.org/2009/data/" rel="nofollow">Information on obtaining the dataset is available</a>.</p>
