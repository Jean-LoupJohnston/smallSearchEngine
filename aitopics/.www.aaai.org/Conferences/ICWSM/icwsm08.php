<h2 class="backgrnd12"><span class="text1">The Second International Conference on Weblogs and Social Media</i></span></h2>
<div class="content">
<p>The Second International Conference on Weblogs and Social Media was held March 30&ndash;April 2, 2008 at the Hilton Seattle Downtown, Seattle, Washington.</p>
<p>The rapid creation and consumption of social media content continues to drive the evolution of the Internet and the Web. Social media content now accounts for the majority of content published daily on the web.</p>
<p>As the space evolves, researcher and industrial practitioners find themselves at a key point for collaborating on research, implementation and deployment of a wide range of analyses and applications. The International Conference on Weblogs and Social Media invited researchers in the broad field of social media analysis to submit papers for its second meeting. Following in the tradition of earlier workshops and the first meeting in Boulder, USA in 2007, ICWSM was exciting, high quality event that brought together academic and industrial practitioners to present and to discuss new research, applications, thoughts and ideas that are shaping the future of social media analysis.</p>
