	<li>Promoting greater awareness of AI and its potential among the chapter's local community at large;</li>

	<li>Fostering greater interactions between the chapter AI community and the international AI community; and</li>

	<li>Promoting greater participation of and membership in AAAI by chapter members.</li>
</ol>

