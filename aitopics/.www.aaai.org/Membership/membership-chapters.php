<h2 class="text2">AAAI Chapter Guidelines</h2>
<p>(also available in <a href="http://www.aaai.org/Membership/AAAI-Chapter-Guidelines">PDF</a> format)</p>

<p> The Association for the Advancement of Artificial Intelligence (AAAI), a U.S. Section 501(c)(3) public charity, offers these guidelines that serve to govern the establishment, maintenance, and dissolution of AAAI chapters. AAAI chapters correspond to discrete geographical regions. AAAI consults with chapter representatives to define the appropriate region of a Chapter. AAAI reserves the right to modify these guidelines as needed. </p>

<h3 class="text2">1. PURPOSE OF CHAPTERS</h3>
<p> AAAI local chapters must be organized and operated for charitable, educational, and scientific purposes to promote the nonprofit mission of AAAI, including: </p>
<ol type="a">

<li>Increasing knowledge of, and greater interest in, artificial intelligence (AI);</li>

<li>romoting greater awareness of AI and its potential among the chapter's local community at large;</li>

<li>Fostering greater interactions between the chapter AI community and the international AI community; and</li>

<li>Promoting greater participation of and membership in AAAI by chapter members.</li>
</ol>

<h3 class="text2">2. CHAPTER ACTIVITIES</h3>
<p> Chapters should sponsor activities that advance the purposes listed in Section 1. Examples include: </p>
<ol type="a">

<li>Hosting periodic speakers on AI, such as local researchers or speakers recommended by AAAI;</li>

<li>Organizing annual meetings concerning AI;</li>

<li>Holding technical workshops/symposia on AI topics;</li>

<li>Maintaining a database of resumes and/or local AI experts;</li>

<li>Organizing professional-development seminars concerning AI;</li>

<li>Providing scholarships and travel support to AI conferences (particularly for students); and</li>

<li>Holding educational presentations on AI for the general public, including at educational institutions at all levels.</li>
</ol>

<h3 class="text2">3. LEGAL RELATIONSHIP TO AAAI</h3>
<ol type="a">

<li>Chapters are independent organizations (whether unincorporated associations, nonprofit corporations, or other), which are affiliated with AAAI. Chapters are not controlled by, subsidiaries of, or otherwise part of AAAI, and chapters themselves have no legal role as members or otherwise in the corporate governance of AAAI.</li>

<li>AAAI is not liable for the debts or obligations of any chapter.</li>

<li>Chapter is not authorized to act on behalf of AAAI. Chapter's representatives are not agents of AAAI and have no authority to bind AAAI.</li>

<li>The tax-exempt status of a Chapter, if any, will be separate from that of AAAI.</li>
</ol>

<h3 class="text2">4. OBLIGATIONS OF AAAI</h3>
<p>AAAI will provide local chapters with the following support: </p>
<ol type="a">

<li>Listing of AAAI chapter on the AAAI website, with pointer to local chapter website;</li>
 
<li>Contact information for local AAAI members (email and postal addresses);</li>

<li>Announcements of chapter events in the AI Magazine and on the AAAI website;</li>

<li>Limited organizational assistance, such as a list of available speakers; and</li>

<li>During the term of a chapter's affiliation, AAAI will grant to the chapter a license to use the AAAI name and logo and other intellectual property owned by AAAI, as defined by AAAI.</li>
</ol>

<h3 class="text2">5. OBLIGATIONS OF CHAPTERS</h3>

<p>
 Chapters will provide AAAI with: </p>
<ol type="a">

<li>Contact information for all chapter officers;</li>

<li>Yearly reports summarizing finances and membership statistics;</li>

<li>A well updated local chapter website with reports on chapter activities, so AAAI can publicize or report in the <i>AI Magazine</i> and on the AAAI website.</li>
</ol>

<p>Chapters should at all times conduct a sufficient level of activity, as described in Section 2 above. </p>
<h3 class="text2">6. CHAPTER ESTABLISHMENT AND ORGANIZATION</h3>
<p>Chapters may be formed by either of two methods: (1) the affiliation with AAAI of a pre-existing organization (including one that may have been established solely for the purpose of becoming a AAAI Chapter) or (2) by the acceptance of these Guidelines by an initial set of members proposing to form a AAAI Chapter. In both cases, organizers should adhere to the following guidelines for the establishment of a AAAI chapter. </p>
<ol type="a">

<li>AAAI will accept proposals to form new chapters only upon submission of a petition from chapter organizers. The following information is required at the time of petition:</li>

	<ul>
<li>Chapter name and geographic definition (proposed geographic region should be both in accordance with other existing chapters (check the AAAI Chapters list) and with local activities and members).</li>
<li>Chapter liaison name and contact information, including affiliation, postal address, email, phone number, and URL.</li>
<li>To help indicate a broad base of support, names of three individuals to serve as Chair, Co-Chair and Secretary/Treasurer, preferably from different institutions. These individuals must be AAAI regular members.</li>
<li>The names of at least ten (10) additional individuals, who must be AAAI regular members, willing to carry out the chapter's mission and participate in activities.  To be considered active and viable by AAAI, chapters must have a minimum of 10 members.</li>
<li>Once the petition has been received, it will be reviewed AAAI and notification will be sent to the petitioners within 60 days.</li>
</ul>
<li>AAAI may request the chapter proponents to redefine the chapter's geographical operating area to avoid overlap with another existing chapter.</li>

<li>Chapters will use the following format for their names: "AAAI Chapter of [Location]."</li>

<li>Chapter officers must at all times be members in good standing of AAAI, and encourage all chapter members to join AAAI. New chapters are required to have a minimum of 13 AAAI members, including all organizers or officers of the chapter, and are expected to recruit a minimum of 10% additional members each year.</li>

<li>A designated officer of a chapter will be that chapter's liaison with AAAI.</li>

<li>Chapters may support their activities through annual local membership fees, individual or organizational donations, general fundraising activities, or per-activity fees. In no case may the Chapter license the use of AAAI intellectual property (e.g., name, logo, etc.) to another organization in return for funds or any other consideration. AAAI reserves the right to review and approve (or prohibit) any fundraising materials, plans, or activity of a chapter.</li>

<li>Chapters must conduct themselves in a manner that is in no way inconsistent with AAAI bylaws (http://www.aaai.org/Organization/bylaws.php) or the Chapter Affiliation Agreement.</li>

</ol>
<h3 class="text2">7. CHAPTER REPORTING</h3>
<p> Chapters are required to submit an annual report of chapter activities, as well as maintaining an updated local website or responding to any specific requests for information from AAAI. Chapters may be dissolved if they do not submit an annual report for two consecutive years or maintain a sufficient level of activity. AAAI's fiscal year corresponds to the calendar year, January 1 - December 31. Every chapter should submit an annual report no later than March 31 following the end of the fiscal year. The report should include the following information: </p>
<ul>
<li>List of Officers and their contact information</li>
<li>Name of Designated Liaison and contact information</li>
<li>Membership Roster, including names of active AAAI members</li>
<li>List of new AAAI members for fiscal year</li>
<li>Chapter activities, including meetings, conferences, workshops, or outreach</li>
</ul>

<h3 class="text2">8. PROBATION AND DISSOLUTION OF AAAI CHAPTER AFFILIATE</h3>
<p> If a chapter fails to submit an annual report for two consecutive years, the following steps will be taken: </p>
<ol type="a">
<li>AAAI will make every attempt to contact the last known chapter liaison. If successful, AAAI will determine the reasons for the missed reporting, the status of the chapter, and the viability of the chapter.</li>

<li>If the chapter is unable to produce a report because of inactivity (supporting AAAI's goals of public education and awareness of AI), AAAI will make an attempt to help the chapter identify methods for revitalizing the chapter by providing the information and services outlined in Section 4 of this document.</li>

<li>If efforts as described in a. and b. are not successful, the chapter will be placed on probationary status. The Chapter will no longer be authorized to use the AAAI name and logo, as well as any other intellectual property that AAAI has approved for its use.</li>

<li>AAAI will remove the name of Chapters on probationary status from its active Chapters list, but will make every attempt to contact chapter representatives in an attempt to further encourage local members to reactivate the chapter. At the end of one year, if no further activity or interest is established, the Chapter will be dissolved.</li>
</ol>

<p>If AAAI determines that at any time a chapter has not operated within the guidelines outlined in this document, or AAAI determines that continued affiliation is no longer in the best interests of AAAI or the scientific field of AI, AAAI reserves the right to terminate the Chapter's affiliation with AAAI immediately with no probationary period. The Chapter will no longer be authorized to use the AAAI name and logo, as well as any other intellectual property that AAAI has approved for its use. Before an immediate termination action is taken, AAAI will contact the local chapter with its concerns and intentions. An opportunity will be provided to the local chapter to respond. </p>
<p>A former chapter that wishes to be reinstated must submit a new petition for consideration. </p>

<h3 class="text2">9. AAAI GOVERNING LAW</h3>

<p> AAAI is governed by the laws of the State of California, United States of America, regardless of where a chapter may be located. </p>
