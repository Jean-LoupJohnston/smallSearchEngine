<h3><a name="Rates">Membership/Subscription Rates</a></h3>

<p><i>All rates are subject to change without notice.</i></p>                 
<p>
Thank you for your interest in AAAI and  <i>AI Magazine.</i> Membership in AAAI is open to anyone with an interest in artificial intelligence research and development. The <i>AI Magazine</i> is published quarterly and is mailed to all current AAAI members. New memberships are accepted at any time, and commence at the time payment is received and the membership has been processed. Renewal dates fall on the date the membership is activated, and are not necessarily based on the calendar year. Cancellations are accepted at any time; however, unused subscription balances for the <i>AI Magazine</i> are nonrefundable.
</p>
<h4>Regular Individual</h4>
<ul>
	<li>One-Year Rate:     $145.00      </li>
	<li>Three-Year Rate:   $435.00      </li>
	<li>Five Year Rate:    $725.00      </li>
	<li>Life Rate:        $2030.00</li>
</ul>

<h4>*Full-Time Student</h4>
<ul>
	<li>One-Year Rate:     $75.00</li>
</ul>

<h4>Institution/Library</h4>
<ul>
	<li>One-Year Rate:     $285.00      </li>
	<li>Three-Year Rate:   $855.00      </li>
	<li>Five Year Rate:   $1425.00      </li>
</ul>

<ul>
	<li>**Institution/Library Online Supplement</li>
</ul>
<ul>
	<li>One-Year Rate:     $290.00      </li>
	<li>Three-Year Rate:   $870.00      </li>
	<li>Five Year Rate:   $1450.00      </li>
</ul>

<p>
Fees listed above include all shipping and handling charges to both domestic and international destinations. No agency discounts are given.
</p>
<p>
*Student applicants must send legible proof of full-time student status in a degree-bearing program, such as a letter from their faculty advisor or a copy of their registration.
</p>
<p>
**Institutional Online Fee is available only as a supplement to and in conjunction with the regular institutional fee. Note restrictions under Access to the Online Digital Library.
</p>
