<h3>Access to the Online Digital Library</h3>
<p>
Effective January 1, 2009, access to the AAAI Digital Library is now available to all individual members of AAAI, as well as institutions and libraries. Institutions and libraries should include the $290.00 online access option in their membership application to qualify for this access. Institutional access is tied to an IP address, and is limited to one IP address per subscription.
</p>

